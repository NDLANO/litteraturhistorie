  p De symbolistiske lyrikerne la stor vekt på musikalske virkemidler som klanger, rim og rytme, men de brukte ikke nødvendigvis faste rimmønstre. Den ytre formen skulle sammen med ordene skape mening i teksten. Både i «Fandango» og «Der skreg en fugl» viser Krag hvor dyktig han er til å utnytte form, klang og rytme slik at de både utdyper og forsterker det som blir sagt med ord. 

  h3 Sørlandsforfatteren Krag 
  p Etter at Krag hadde bodd flere år i hovedstaden og i København, søkte han etter hvert mer mot det hjemlige i skrivingen sin, og det var landsdelen, naturen og folket som nå var motiv for diktningen. Personene han tegnet, levde i harmoni både med seg selv og med naturen de var omgitt av. 

  p Krag skaper en forestilling om Sørlandet som en egen landsdel, og han prøver å gi folket som bor der, en identitet. I en artikkel i Morgenbladet fra 1902 foreslår han å kalle landsdelen med kyststripe fra Risør til Flekkefjord for Sørlandet. Forslaget fikk raskt gjennomslag. 

