  p Dei symbolistiske lyrikarane la stor vekt på musikalske verkemiddel som klangar, rim og rytme, men dei brukte ikkje nødvendigvis faste rimmønster. Den ytre forma skulle saman med orda skape meining i teksten. Både i «Fandango» og «Der skreg en fugl» viser Krag kor dyktig han er til å utnytte form, klang og rytme slik at dei både utdjupar og forsterkar det som blir sagt med ord. 

  h3 Sørlandsforfattaren Krag 
  p Etter at Krag hadde budd fleire år i hovudstaden og i København, søkte han etter kvart meir mot det heimlege i skrivinga si, og det var landsdelen, naturen og folket som no var motiv for diktinga. Personane han teikna, levde i harmoni både med seg sjølve og med naturen dei var omgitt av. 

  p Krag skaper ei førestelling om Sørlandet som ein eigen landsdel, og han prøver å gi folket som bur der, ein identitet. I ein artikkel i Morgenbladet frå 1902 foreslår han å kalle landsdelen med kyststripe frå Risør til Flekkefjord for Sørlandet. Forslaget fekk raskt gjennomslag.

