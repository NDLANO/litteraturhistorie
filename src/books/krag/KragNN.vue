  p Ordet 
    em dekadanse 
    | tyder forfall, nedgang, særleg i kulturell forstand. Tida rett før og etter 1900 opplevde mange som sluttfasen av ein kulturell periode. Ein kunne sjå at ei ny tid var i emning, men samfunnsendringane gjorde menneska utrygge. I Frankrike brukte dei omgrepet «fin de siècle», det vil seie slutten på hundreåret, om den melankolske og urolege stemninga som følgde med denne overgangsfasen. «Fin de siècle»-stemninga prega verka til mange forfattarar og kunstnarar i Europa.

