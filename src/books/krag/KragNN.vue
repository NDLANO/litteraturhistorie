  h3 Heimstaddikting 
  p Nyromantikken skapte også ei interesse for 
    em heimstaddikting
    | . Mange menneske opplevde urbaniseringa og moderniseringa av samfunnet som eit trugsmål mot det bygdenorske, og heimstaddiktinga utgjorde ei motvekt mot den nye og til dels skremmande utviklinga. Heimstaddiktarane skreiv varmt om heimemiljøet. Slik dei såg det, levde menneska det gode livet der dei hadde sine røter og sitt opphav.

  p Vilhelm Krag sat i Kristiania og skreiv om Sørlandet, og han lengta heim. I 1917 skaffa vennene han bustaden Havbukta i Ny-Hellesund. Plassen var eit gammalt fiskebruk og bustad for losen. For diktaren vart Ny-Hellesund og Havbukta ein fristad og ein inspirasjon til å skrive. 

