  h3 Mannen i krise 

  figure.col.col6.right-image.mt5
    img(src='./loe_Tatt_av_kvinnen1.jpg' alt='Mann med ryggsekk sitter ved siden av kvinne som holder hendene foran ansiktet. De sitter på en teaterscene. Foto')
    figcaption Gard B. Eidsvold og Helle Haugen i en scene fra teaterstykket «Tatt av kvinnen». Foto: Erlend Aas / NTB / Scanpix

  p I Loes debutroman 
    em Tatt av kvinnen 
    | møter vi for første gang den typiske Loe-karakteren: en mann i 20–30-årene som sliter med å finne mening i tilværelsen, og som har problemer med kravene som stilles til han som moderne mann. Den navnløse hovedpersonen opplever å bli invadert av en kvinne – Marianne. Marianne vet akkurat hva hun vil, og hvordan hun skal oppnå målene sine. Den mannlige hovedpersonen blir derimot framstilt som en nærmest viljeløs tøffelhelt som ukritisk aksepterer Mariannes påfunn og føringer. I møte med den sterke postmoderne kvinnen blir mannen rådløs og ettergivende. 

  p Mens den navnløse hovedpersonen i 
    em Tatt av kvinnen 
    | kan leses som en typisk representant for 90-tallets ungkar, en mann som sliter med langvarige og seriøse forhold og forpliktelser, står Andreas Doppler i romanen 
    em Doppler 
    | (2004) fram som en representant for mannen som sliter etter å ha innrettet seg etter samfunnets krav om å etablere seg i et A4-familieliv. Etter at faren dør, opplever Andreas Doppler en eksistensiell krise. Han innser at livet hans kun består av overflatiske forhold og prosjekter. Sine egne barn kjenner han knapt, og samtalene med kona handler om oppussing og baderomsfliser.

  figure.col.col6.right-image.mt5
    img(src='./loe_elg.jpg' alt='En enslig elg i snødekket landskap. Foto')
    figcaption Foto: Roy Mangersnes / Samfoto / Scanpix
    
  p For å komme mer i kontakt med seg selv flytter han ut i skogen. Her får han en ny bestevenn – elgen Bongo. Det eneste Doppler savner fra den moderne sivilisasjonen, er lettmelk og sjokolade. Han bryter seg derfor inn i huset til en mann som heter Düsseldorf – han er nemlig «en hund etter sjokolade». Düsseldorf og Andreas Doppler utvikler etter hvert et vennskap, og særlig snakker de om forholdet til fedrene sine. Düsseldorf har nemlig et stort prosjekt: Han rekonstruerer et slag fra andre verdenskrig der faren hans falt. Dette inspirerer Doppler til å lage en totempåle som skal hedre hans egen fars minne. 
  
  h3 Regresjon som kritikk 
  p Tekstene til Loe kan leses som en kritikk av det postmoderne mennesket og samfunnet. I møte med en kompleks, uoversiktlig og til tider skremmende verden, reagerer Loes karakterer med avmakt, og ofte tilbaketrekking til en enklere tilstand eller verden. Dette ser vi både i 
    em Doppler 
    | og i den halvdokumentariske reiseromanen 
    em L 
    | (1999). Mens Doppler flytter ut i skogen, reiser Erlend Loe og vennene hans til en sydhavsøy for å slippe unna samfunnets krav og forpliktelser – særlig Statens lånekasse skremmer! Hovedkarakterene trekker seg tilbake til en enklere naturverden der kampen for å overleve er det eneste de må forholde seg til. Kritikken av den postmoderne sivilisasjonen kan tolkes ut fra denne såkalte regresjonen til en enklere måte å leve på. 

