  h3 Erlend Loe – ironigenerasjonens litterære ikon

  figure.col.col4.left-image.mt5
    img(src='./loe_Portrett1.jpg' alt='Portrett av skallet mann med skjegg. Foto')
    figcaption Erlend Loe. Foto: Lise Åserud / NTB / Scanpix

  p Erlend Loe ble født i Trondheim i 1969. I 1993 debuterte han med romanen 
    em Tatt av kvinnen
    | . Med denne romanen og den etterfølgende suksessen 
    em Naiv.Super 
    | (1996) ble Loe en av 90-tallets store litterære suksesser – omfavnet av både publikum, kritikere og litteraturvitere. 

  p Mange pekte også ut Loe som en talsmann for en hel generasjon ungdom – da spesielt menn. Den nye ungdomskulturen på 90-tallet var preget av en ironisk distanse til verden og en bevisst naiv holdning til både samfunn og menneskelige relasjoner – en generasjon unge som var uten opprørstrang eller higen etter voksenlivet. De ønsket å holde på barndom og ungdom lengst mulig uten å måtte forholde seg til voksenlivets alvor og forpliktelse. Disse unge, ofte omtalt som generasjon x – ironigenerasjonen, levde også med en visshet om at de i liten grad hadde bidratt til å utvikle det samfunnet og den velstanden de levde i og opplevde som naturlig. 

  img.col.col4.right-image.mt5(src='./loe_Kurt_blir_grusom1.jpg' alt='To bøker med forsiden opp. Foto ')
  p Erlend Loe har skrevet en rekke ­romaner. Han har også utgitt flere barnebøker i samarbeid med illustratør Kim Hiorthøy (1973– ) og oversatt dikt av den amerikanske lyrikeren Hal Sirowitz til norsk. I 2000 debuterte han som manusforfatter med filmen 
    em Detektor
    | . Seinere har han også vært involvert i filmer som 
    em Folk flest bor i Kina 
    | (2002), 
    em Nord 
    | (2009) og 
    em En helt vanlig dag på jobben 
    | (2010). Debutromanen 
    em Tatt av kvinnen 
    | ble filmatisert i 2007 med Petter Næss (1960– ) som regissør, mens 
    em Kurt blir grusom
    | , bok nummer to i barnebokserien om truckfører Kurt, ble animasjonsfilm i 2008. 

