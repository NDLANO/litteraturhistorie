  h3 Hvordan skape mening i en meningsløs tilværelse? 

  figure.col.col6.left-image.mt5
    img(src='./loe_Portrett4.jpg' alt='Mann leker med et bankebrett. Foto')
    figcaption Erlend Loe med bankebrett.Foto: Bjørn Reese / VG / Scanpix
    
  p Selv om handlingen tilsynelatende er enkel og litt «overflatisk», behandler 
    em Naiv.Super store eksistensielle spørsmål. Hovedpersonen leter etter en mening i tilværelsen, og han forsøker å finne strategier som fungerer i en kompleks og usikker postmoderne verden. For å takle hverdagen trekker han seg tilbake til det private og det barnlige – altså tilbake til å sparke ball og hamre på bankebrett. Bankingen skal hjelpe han med å tømme hodet for tanker – for mange tanker blir ifølge hovedpersonen bare masete. Hovedpersonen har stor tro på at bankingen skal hjelpe han med å finne mening i livet: «Hvis jeg banker tilstrekkelig lenge, vil jeg kanskje oppnå en følelse av mening på så vel globalt som personlig plan.» I forsøket på å skape mening og sammenheng skriver han også lister – blant annet lister over hvilke dyr han har sett, og ting han likte å gjøre da han var barn.

  p Hovedpersonen i 
    em Naiv.Super 
    | og i andre romaner av Loe, som 
    em Tatt av kvinnen 
    | og 
    em Doppler
    | , forsøker alle å finne måter å takle den postmoderne virkeligheten på. Det postmoderne samfunnet preges av enorme valgmuligheter og en stor grad av frihet – du står fritt til å forme ditt liv, din identitet og virkeligheten din som du selv vil! Denne friheten kan virke lammende. Hvordan skal jeg kunne skape mening i en flytende og skiftende verden? For å takle en total frihet, og det medfølgende ansvaret, velger Loes karakterer altså en slags regresjon – en tilbakevending til et tidligere stadium. Loes karakterer velger det enkle og det barnlige i møte med den store, åpne og til tider skremmende verden. 

  p Et annet sentralt spørsmål Loes forfatterskap stiller, er hvorvidt vi kan se en forflatning av livet og virkeligheten: Når hvem som vant et brettspill i barndommen, blir like viktig som et samlivsbrudd, hva blir da stående igjen som det virkelig viktige og betydningsfulle i eget liv? Og hva gjør en slik forflatning av både følelsesliv og virkelighetsforståelse med menneskene? Dette er spørsmål Loes forfatterskap reiser, og dermed plasserer han seg godt innenfor en postmodernistisk tradisjon der nettopp de ustabile menneskelige relasjonene og kampen om virkeligheten undersøkes.  
  h3 Den «loeske» humor og stil 
  p Erlend Loes romaner preges av en særegen og bevisst naivisme – merkelappen 
    em nynaivisme 
    | har blitt brukt til å karakterisere tekstene hans. Underdrivelser og ironisk distanse er med på å understreke karakterenes syn på seg selv og sitt forhold til omgivelsene. Den ironiske distansen til alt og alle gjenspeiles i karakterenes manglende evne til å la seg begeistre eller gå inn i et prosjekt med hud og hår. Loes karakterer lar seg sjelden rive med eller provosere. De beholder en nærmest likegyldig ro midt i dramatiske hendelser som samlivsbrudd eller flykrasj. 

  p Erlend Loes tekster er alle preget av en språklig enkelhet – helt fra setningsnivå til overordnet komposisjon. Han bruker ofte helsetninger med et muntlig preg. Som barnespråket er også Loes språk preget av mye gjentakelser, og karakterene har en tendens til å sortere komplekse følelser og tanker i enkle kategorier og kontraster. Dette ser vi allerede i starten av Naiv.Super når hovedpersonen forteller at han har «[…] to venner. En god og en dårlig». Også listene han skriver, har et barnlig preg. Hovedpersonen blir uvel av å ha for mange tanker i hodet samtidig, og dette barnlige blikket på en komplisert verden er med på å forenkle livet hans. Språket er derfor et viktig virkemiddel forfatteren bruker for å underbygge hovedpersonens prosjekt – å forsøke å forenkle sitt eget liv og rydde opp i komplekse tanker.   

