  p Hoel blir først og fremst rekna for ein tradisjonell og realistisk forfattar, men tidvis finn vi spor av dei tidlegare eksperimentelle tekstane hans. En dag i oktober frå 1931, ein av dei beste og mest kjende romanane til Hoel, er eit godt eksempel i så måte.

  p Som tittelen tilseier, skildrar romanen ein dag i månaden oktober. Ei kvinne tek livet sitt ved å kaste seg ut frå eit vindauge i ein bygard. Denne hendinga får vi presentert av mange og svært ulike karakterar: dei andre som budde i denne bygarden, naboane til kvinna. En dag i oktober inneheld mange små forteljingar, og boka bryt på den måten med dragnaden i realismen mot «den store forteljinga».

  p Tematisk er En dag i oktober ein typisk Hoel-tekst. Det er ein tydeleg psykologisk roman om mislykka samliv og kjærleikssvik. Romanen er også kulturradikal: Det borgarlege ekteskapet blir framstilt i eit kritisk og negativt lys.
  
  h3 Psykoanalysen – eit nytt perspektiv

  figure.col.col4.left-image.mt5
   img(src='./hoel_psykoanalysen1.jpg' alt='')
   figcaption Psykologen Wilhelm Reich (1897-1957). Foto: Scanpix

  p Psykoanalysen vart allment kjend i Noreg i 1920-åra. Frå om lag 1930 prega psykoanalysen debattar om oppseding, skule, kristendom, seksualmoral, biletkunst og litteratur. Det førte til ei ny interesse for barndommen og ei ny forståing av dei underbevisste og fortrengde kjenslene, røynslene og drivkreftene til menneska.

  p Sigurd Hoel kombinerte sitt politiske engasjement med interesse for psykoanalysen. Han var kanskje meir påverka av Wilhelm Reich enn av Sigmund Freud. Reich var elev av Freud og kommunist, og han vidareutvikla psykoanalysen i ei meir politisk retning.

  p Reich såg ein grunnleggjande samanheng mellom fascistiske ideologiar som nazismen og den autoritære, undertrykkjande oppsedinga som barn vart utsette for. Denne oppsedinga skapte etter hans syn menneske som lettare underkasta seg totalitære politiske system.

  p Sigurd Hoel var ingen marxist, men tankane til Reich appellerte til han fordi dei gav nye reiskapar til å analysere dei rådande samfunnstilhøva. Særleg okkupasjonsromanen Møte ved milepelen ber preg av Reichs forklaringsmodell.
  
  h3 Politikk og psykologi
  p Kombinasjonen av politisk og psykologisk tematikk går att i forfattarskapen til Hoel. Vi ser han kanskje tydelegast i Møte ved milepelen, ein sentral okkupasjonsroman frå 1947. Her undersøkjer han nazismens problem, og han tek føre seg både samfunnet og enkeltmennesket.

