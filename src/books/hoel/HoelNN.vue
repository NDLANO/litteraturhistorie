  p Romanen drøftar det vanskelege spørsmålet om korleis såkalla vanlege menneske kan bli forrædarar, tystarar og torturistar. Den psykoanalytiske tenkinga blir her ein reiskap til å forstå nazismens problem. Vi ser korleis kjærleikssvik i barneåra i verste fall kan føre heile livet inn på eit feilspor, slik at vi sjølve endar opp som svikarar.

  p Møte ved milepelen var også eit viktig innlegg i debatten om skuld og soning som kom i samband med rettsoppgjeret etter krigen. Bodskapen i boka er at vi alle ber på ei bør av skuld – ingen av oss er feilfrie. Dette er ein roman om plikt og ansvar, ikkje om fordømming av «dei andre». Slik sett kan boka også kallast ein erkjenningsroman.
  
  h3 Veien til verdens ende
  p Veien til verdens ende frå 1933 kan kallast både ein oppvekstroman og ein psykologisk utviklingsroman. Av alle Hoels tekstar viser kanskje denne romanen best korleis han bruker psykoanalysen litterært, og romanen har vorte ståande som ein av dei fremste i sin sjanger. Boka prøver å klarleggje korleis dei tidlege røynslene til barnet blir avgjerande for karakterutviklinga.

  p Handlinga går føre seg i eit bygdesamfunn likt det Hoel sjølv voks opp i. Vi følgjer hovudpersonen Anders, frå han som fireåring har fast lyttepost under kjøkkenbordet, til han forlèt heimen og bygda etter konfirmasjonen.

