  p Sigurd Hoel (1890–1960) voks opp i Nord-Odal i Hedmark, men levde storparten av livet sitt i Oslo. Litt misvisande vart han derfor kalla ein hovudstadsintellektuell. Dei litterære karakterane hans høyrer gjerne til i eit liberalt og intellektuelt hovudstadsmiljø, men som forfattaren sjølv har dei ofte røtene sine i eit bygdesamfunn.

  p Hoel var ein skuleflink og kunnskapstørst gut, men han kunne ikkje heilt bestemme seg for kva han ville bli. Han byrja på ulike studium utan å fullføre nokon av dei. Til sist var det den litterære og intellektuelle interessa som tok overhand.

  p I mellomkrigstida var Hoel ein av dei mest framståande personane i det norske litterære miljøet. I tillegg til å vere skjønnlitterær forfattar var han litteraturkritikar, forlagskonsulent og redaktør for fleire tidsskrift og aviser. Dessutan var han dominerande i dei kulturelle og politiske debattane i samtida. Han omtalte seg sjølv som ein uavhengig liberal radikalar.

  p Livshistoria til Hoel streifar forfattarskapen hans på fleire område. Han var gift og skild to gonger, og skreiv om fallitten til det borgarlege ekteskapet i vår tid. Han gjekk òg i psykoanalyse hos Wilhelm Reich (1897–1957), elev av Sigmund Freud, og mange av bøkene til Hoel viser at han var inspirert av psykoanalysen og ideane til Reich.
  
  h3 Kulturradikalaren
  p Sigurd Hoel var ein av dei fremste kulturradikalarane i Noreg i samtida. Saman med Arnulf Øverland (1889−1968) og Helge Krog (1889−1962) utgjorde han det vi gjerne kallar det radikale trekløveret blant forfattarane i mellomkrigstida. Kulturradikalarane var venstreorienterte og samfunnskritiske, men samstundes ofte partiuavhengige.

  figure.col.col6.right-image.mt5
   img(src='./hoel_kulturradikalen2.jpg' alt='')
   figcaption Foto: Morten Holm / NTB / Scanpix
   
  p Det var Hoel sjølv som brukte omgrepet kulturradikalisme for første gong på norsk, i ein artikkel han skreiv i 1955. Her argumenterte han for at det finst ein «kulturradikal» tradisjon i Norden, som byrja allereie med Georg Brandes i 1870- åra, og som vart ført vidare mellom anna av Bjørnson, Ibsen, Øverland, Krog og han sjølv.

  p I norsk litteraturhistorie er det likevel vanleg å knyte omgrepet kulturradikalisme spesielt til mellomkrigstida. Dei kulturradikale forfattarane i denne perioden sette søkjelyset på den rådande seksualmoralen og på konservativ kristen morallære. Dei søkte frigjering for enkeltmennesket og ynskte å motarbeide fordommar og hykleri.

  p Denne politiske radikalismen var særleg knytt til organisasjonen og tidsskriftet Mot Dag, som var ein viktig faktor i samfunnsdebatten. Motdagistane ynskte å utgjere ein revolusjonær elite og hadde dei sovjetiske bolsjevikane som føredøme. Mange av forfattarane i mellomkrigstida var knytte til Mot Dag-rørsla.

  p Hoel var i ein periode redaktør i Mot Dag, men han var ingen typisk representant for motdagistane. Han kalla seg verken marxist eller kommunist, men ein uavhengig og liberal radikalar. Politisk høyrde han altså til venstresida, men i somme saker tok han eit meir konservativt standpunkt. Han var for eksempel sterk motstandar av samnorskpolitikken og var i ein periode leiar av Riksmålsforbundet.
  
  h3 Modernist og tradisjonalist

  figure.col.col6.right-image.mt5
