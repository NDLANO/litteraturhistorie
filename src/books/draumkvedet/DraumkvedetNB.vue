  h3 Opphav 
  img.col.col12(src="./Draumkvedet_Opphav.jpg")
  p Det var Jørgen Moe og Magnus B. Landstad som i 1840-årene gjorde de første nedskriftene av «Draumkvedet». 

  p Det finnes over hundre nedskrivninger av «Draumkvedet», og alle er funnet i ­Telemark. Noen av dem er bare små bruddstykker, og ingen av dem er helt fullstendige. Dessuten skiller de seg til dels sterkt fra hverandre. Den viktigste nedskrivningen er etter Maren Ramskeid fra Bronkeberg. 

  p I 1890-årene laget professor Moltke Moe en restituert (gjenopprettet) versjon av «Draumkvedet». Han tok utgangspunkt i opptegnelsen til Maren Ramskeid og supplerte med strofer fra flere andre kilder for å få en mer helhetlig tekst. Det er denne versjonen som er best kjent i dag. 

  p Det er vanskelig å fastslå nøyaktig når «Draumkvedet» ble skapt. Innholdet samsvarer i stor grad med katolsk lære (for eksempel finner vi forestillingen om skjærsilden), så det er trolig at verket ble til før reformasjonen. Vi finner også navn og begreper fra norrøn mytologi. For eksempel er Gjallarbrua opprinnelig brua over til Hel, det norrøne dødsriket. I «Draumkvedet» går brua over til det kristne dødsriket. Dette peker mot at kvedet kan stamme fra tidlig middelalder, en tid da både kristen og norrøn mytologi var levende i samfunnet. 
    
  h3 Visjonslitteratur 
  p Visjonslitteratur er diktning som prøver å skildre det som skjer med menneskene etter døden. En kan ikke vite noe om dette gjennom vanlig sanseerfaring, derfor må slik kunnskap komme til forfatteren gjennom et syn (en visjon). 

  p Vi møter en del visjonslitteratur allerede i antikken, men i Europa var den ikke særlig utbredt før middelalderen. 

  p Formålet med den kristne visjonslitteraturen var gjerne å omvende folk og få dem til å leve et gudfryktig liv. Mange av visjonene inneholdt utførlige og til dels groteske skildringer av pinslene som sjelene i helvete måtte gjennomgå. 

  p Forfatteren av «Draumkvedet» kjente nok til denne typen fortellinger, men i «Draumkvedet» er tonen mer menneskelig og forsonende. Samme tematikk som i «Draumkvedet» finner vi også i Edda-diktet «Solarljod» og i Dantes «Divina Commedia». Dantes verk blir for øvrig regnet for å være høydepunktet i visjonslitteraturen. 

