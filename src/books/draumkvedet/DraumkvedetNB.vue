  p «Draumkvedet» er et norsk visjonsdikt, mest sannsynlig fra slutten av middelalderen. Diktet skildrer et syn (en visjon) av hva som skjer etter døden. Mange regner «Draumkvedet» for høydepunktet i norsk balladediktning. Diktet har vært en kilde til inspirasjon for mange norske kunstnere. 

  h3 Handling 
  p Fortelleren i diktet, Olav Åsteson, faller i søvn på julaften og våkner ikke før trettende dag jul. Da rir han til kirke, setter seg på kirketrappa og forteller hva han har drømt. 

  p I drømmen har han gått «yver vigde vatne og yver djupe dalar» til han kommer til Gjallarbrua, som skiller vår egen verden frå dødsriket. Her er det ville dyr som ikke slipper gjennom den som «feller domane vrange». På ferden gjennom dødsriket får han se glimt av både skjærsild, helvete og paradis, og han møter jomfru Maria. Hun ber han dra til Brokksvalinn, plassen der «domen skal stande». 

  p Fortelleren skildrer hva han ser på veien dit: Her er det sjeler som blir straffet for gjerrighet, trolldom, incest og andre onde gjerninger. Så får han oppleve kampen mellom de gode og de onde maktene. Satan med sin hær kommer farende fra nord, mens St. Mikael og Jesus står i spissen for englehæren. Til slutt får Olav Åsteson se hva som skjer med de angrende sjelene på dommedag. De blir veid på vektskåla, og alle blir de vist til Jesus Kristus. Ingen sjeler som angrer, er fortapt. 

  h3 Form 
  p Strofene i «Draumkvedet» er bygd opp etter mønster av balladestrofen på fire linjer: Andre og fjerde linje rimer på hverandre, første og tredje linje rimer ikke. Hver strofe slutter med et omkved. 

  p <strong>Strofe:</strong> Han la seg ned um joleftan og sterkan svevnen fekk; vakna ̛kje fyrr um trettandagjen, då folkje at kjyrkjunn gjekk. 
  p <strong>Omkved:</strong> Og det var Olav Åsteson, som heve sovi so lengje. 

