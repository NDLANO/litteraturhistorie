  p Det finst over hundre nedskrivingar av «Draumkvedet», og alle er funne i Telemark. Somme av dei er berre små brotstykke, og ingen av dei er heilt fullstendige. Dessutan skil dei seg til dels sterkt frå kvarandre. Den viktigaste nedskrivinga er etter Maren Ramskeid frå Bronkeberg.

  p I 1890-åra laga professor Moltke Moe ein restituert versjon av «Draumkvedet». Han tok utgangspunkt i nedskrivinga til Maren Ramskeid og supplerte med strofer frå fleire andre kjelder for å få ein meir fullstendig tekst. Det er denne versjonen som er best kjend i dag.

  p Det er vanskeleg å slå fast nøyaktig når «Draumkvedet» vart til. Innhaldet samsvarar i stor grad med katolsk lære (for eksempel finn vi oppfatninga om skjærselden). Det er derfor truleg at verket vart skapt før reformasjonen. Vi finn også namn og omgrep frå norrøn mytologi. For eksempel er Gjallarbrua eigentleg brua over til Hel, det norrøne dødsriket. I «Draumkvedet» går brua over til det kristne dødsriket. Dette peiker mot at kvedet kan stamme frå tidleg mellomalder, ei tid da både kristen og norrøn mytologi var levande i samfunnet.

  h3 Visjonslitteratur
  p Visjonslitteratur er dikting som prøver å skildre det som skjer med menneska etter døden. Vi kan ikkje få kjennskap til dette gjennom vanlege sanseinntrykk, derfor må slik kunnskap komme til forfattaren gjennom eit syn (ein visjon). 

  p Vi møter ein del visjonslitteratur alt i antikken, men i Europa var han ikkje særleg utbreidd før i mellomalderen. 

  p Formålet med den kristne visjonslitteraturen var gjerne å omvende folk og få dei til å leve eit gudfryktig liv. Mange av visjonane inneheldt utførlege og til dels groteske skildringar av pinslene som sjelene i helvete måtte gjennomgå.

  p Forfattaren av «Draumkvedet» kjende nok til denne typen forteljingar, men i «Draumkvedet» er tonen meir menneskeleg og forsonande. Den same tematikken som i «Draumkvedet» finn vi også i Edda-diktet «Solarljod» og i Dantes «Divina Commedia». Dantes verk blir elles rekna for å vere høgdepunktet i visjonslitteraturen.

