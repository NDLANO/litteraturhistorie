  h3 Form
  p Strofene i «Draumkvedet» følgjer mønsteret i den firelinja balladestrofa: Andre og fjerde linje rimar på kvarandre, første og tredje linje rimar ikkje. Kvar strofe sluttar med eit omkved.

  p <strong>Strofe:</strong> Han la seg ned um joleftan og sterkan svevnen fekk; vakna ̛kje fyrr um trettandagjen, då folkje at kjyrkjunn gjekk. 

  p <strong>Omkved:</strong> Og det var Olav Åsteson, som heve sovi so lengje. 

