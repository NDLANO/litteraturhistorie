  p «Draumkvedet» er eit norsk visjonsdikt frå slutten av mellomalderen. Diktet skildrar eit syn (ein visjon) av kva som skjer etter døden. Mange reknar «Draumkvedet» for høgdepunktet i norsk balladedikting. Diktet har vore ei kjelde til inspirasjon for mange norske kunstnarar.

  h3 Handlinga
  p Forteljaren i diktet, Olav Åsteson, fell i søvn på julaftan og vaknar ikkje før trettande dag jul. Da rir han til kyrkje, set seg på kyrkjetrappa og fortel kva han har drøymt.

  p I draumen har han gått «yver vigde vatne og yver djupe dalar» til han kjem til Gjallarbrua, som skil vår eiga verd frå dødsriket. Her er det ville dyr som ikkje slepp gjennom den som «feller domane vrange». På ferda gjennom dødsriket får han sjå glimt av både skjærseld, helvete og paradis, og han møter jomfru Maria. Ho ber han dra til Brokksvalinn, plassen der «domen skal stande».

  p Forteljaren skildrar kva han ser på vegen dit: Her er det sjeler som blir straffa for gjerrigskap, trolldom, incest og andre vonde gjerningar. Så får han oppleve kampen mellom dei gode og dei vonde maktene. Satan med hæren sin kjem setjande frå nord, mens St. Mikael og Jesus står i spissen for englehæren. Til slutt får Olav Åsteson sjå kva som skjer med dei angrande sjelene på dommedag. Dei blir vegne på vektskåla, og alle slepp fram til Jesus Krist. Ingen sjeler som angrar, er fortapte.

