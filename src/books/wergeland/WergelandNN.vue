    | Like før han døyr, i mai 1845, skriv Wergeland diktet «Til Foraaret». Wergeland ligg sjuk og veit at han skal døy. Diktet hans er ei fortvilt og grenselaus bønn til naturen om at han må få leve vidare.

  p Wergeland tilber våren – og plantane og dyra som høyrer våren til: det første graset, symra, løvetanna, hestehoven, svalene som er komne tilbake, og det gamle treet som har fått nye knoppar. Han ber dei vitne om kjærleiken han føler for dei.

  p Diktaren seier at våren er den årstida han set høgast. Den smålåtne symra er viktigare for han enn rosene om sommaren, sjølv om dei er flottare å sjå på. Kjærleiken hans er så sterk at han sjølv ønskjer å vere ein del av naturen, han vil vere eit lønnetre som kan stå saman med det gamle treet. Han hevdar at treet og symrene faktisk ber for han, før han kjem med ei siste bønn til naturen om å redde han.

  p Det er ikkje tilfeldig at Wergeland vel å skrive om våren. For det første er diktet skrive i mai månad, og Wergeland blir nok heilt enkelt inspirert av det han kan sjå rett utanfor vindauget sitt. For det andre representerer våren sjølve livskrafta, den tida på året da alt liv blir skapt på nytt. Wergeland er glad i livet, han vil ikkje døy. Våren er rett og slett den årstida som best formidlar ønsket hans om å få leve. For det tredje liknar fornyinga som skjer i naturen kvar vår, på den kunstnarlege skapingsprosessen. Wergeland skriv i diktet at våren er den årstida han liker best. Når vi ser på alt han har greidd å skrive og utrette i løpet av sitt korte liv, forstår vi kvifor han kjenner størst slektskap med nettopp denne årstida.

