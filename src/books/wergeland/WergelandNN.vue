    em Digte. Anden Ring
    | . Med desse tre bøkene sette Wergeland ein ny standard for versekunst. Men han bryt også alt som finst av reglar for lyrikk, og det gjer han til ein kontroversiell diktar. I tillegg er han ein person som lett kjem i konflikt med andre. Han skaffar seg mange uvenner og eit dårleg rykte. Det går ut over boksalet.

  h3 Wergeland som språkreformator
  p I 1830-åra er det stadig fleire som tek til orde for at Noreg må få sitt eige skriftspråk. Dei er påverka av tankane til den tyske filosofen Herder, som hevda at språket formidlar folkeånda: Det speglar historia, kulturen og tradisjonane til eit folk.

  p Også Wergeland meiner at Noreg treng eit eige skriftspråk. Han ønskjer ei gradvis fornorsking av det danske skriftspråket. Danske ord og uttrykk må avløysast av norske, og skrivemåten bør bli meir tilpassa norsk uttale.

  figure.col.col6.right-image
   img(src='./Sprogreformation_Peter_Andreas_Munch.jpg' alt='Portrett i svart og kvitt av mann med pelskrage og stor, stripete sløyfe i halsen. Illustrasjon')
   figcaption Peter Andreas Munch
  p Ein av dei få på denne tida som har omfattande kunnskap om språk, er professor Peter Andreas Munch. I 1832 skriv han artikkelen «Norsk Sprogreformation», der han går imot ideen om ei gradvis fornorsking av skriftspråket. I artikkelen gjer han narr av tanken om å lage eit skriftspråk utan å ta omsyn til at språket er eit system som heng saman. Munch argumenterer for at ein anten bør halde fram med å skrive dansk, eller basere eit norsk skriftspråk på ein av dei dialektane som best tek best vare på det norrøne systemet.

