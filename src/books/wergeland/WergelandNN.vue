    | kjem ut i 1830. I dette storslåtte visjonsdiktet formulerer han eit program for religiøs og politisk fridom: Menneska treng kunnskap og opplysning for å kunne søkje fred med kvarandre, og skilnadene mellom folk må bli mindre. Dette prøver Wergeland å vise med ein gjennomgang av verdshistoria frå skapinga og fram til dei siste dagar.

