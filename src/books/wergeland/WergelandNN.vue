  h3 Striden mellom Wergeland og Welhaven
  p Etter at Wergeland i 1830 har publisert 
    em Skabelsen, Mennesket og Messias
    | , går Johan Sebastian Welhaven ut og kritiserer offentleg Wergelands måte å skrive på. Dette er opptakten til ein livslang konflikt. Wergeland og Welhaven er ikkje berre usamde om kva som er god lyrikk. Striden mellom dei handlar like mykje om ulike syn på spørsmålet om nasjonsbygging. Konflikten har derfor fått nemninga kulturstriden.

  p Welhaven og meiningsfellane hans meiner at ein må byggje vidare på dansknorsk embetsmannskultur og halde kontakten med Danmark. Wergeland og tilhengarane hans meiner derimot at grunnlaget for den nye norske nasjonen må vere norsk bondekultur. Utover i 1840-åra stilnar debattenav, og striden ebbar ut.

  h3 Wergeland som folkeopplysar og samfunnsdebattant

  img.col.col5.right-image(src='./Sprogreformation_For_Arbeidsklassen.jpg' alt='Bokside med trykt gotisk skrift og illustrasjon. Foto')
  p Wergeland er oppteken av folkeopplysning. Han har tru på at kunnskap kan lyfte vanlege menneske opp og fram i samfunnet, og ivrar for å få i gang bibliotek der alle kan ha tilgang til bøker og tidsskrift. Wergeland gir sjølv ut opplysningstidsskrifta «For Almuen» og «For Arbeidsklassen».

  p Han er også ein ivrig samfunnsdebattant. Ei av kampsakene hans er å få bort den såkalla jødeparagrafen i Grunnlova. Denne paragrafen i lova nektar jødar å busetje seg i Noreg. Wergeland gir aldri opp kampen, men lovendringa kjem først i 1851, seks år etter hans død. 

  p Wergeland har sterke synspunkt på mykje, og han greier å fornærme mange folk med den polemiske stilen sin. Derfor hamnar han i fleire rettssaker, som han taper kvar gong. Rettssakene påfører han store utgifter, og mot slutten av livet sitt er han nesten ruinert økonomisk.

  h3 Giftarmål og siste leveår

  img.col.col3.left-image(src='./Om-norsk-Sprogreformation_Bilde-av-Amalie-Sofie-Bekkevold.jpg' alt='Portrett av kvinne med blå kjole og brunt hår med krøller. Måleri')
  p I 1838 blir Wergeland trulova med Amalie Sofie Bekkevold. Han søkjer fleire prestekall, men får aldri noka stilling. Kong Karl Johan bergar han til slutt og gir han diktargasje.

  p I 1839 gifter Wergeland seg med Amalie Sofie. Året etter ordnar kongen med at Wergeland får ei stilling som byråsjef ved Riksarkivet. Denne hjelpa frå kongen gjer at han får enda fleire uvenner. Mange meiner at han får fordelar framfor andre, og dei synest ikkje det er rett at Wergeland som republikanar tek imot økonomisk stønad frå kongen.

  p Med stilling i embetsverket og diktargasje har Wergeland råd til å byggje diktarbustaden Grotten i 1841. Men i 1844 får han ein lungebetennelse som utviklar seg til tuberkulose. Han kjem dessutan i eit økonomisk uføre og må flytte til eit lite hus i Pilestredet.

