    | Like før han døde, i mai 1845, skrev Wergeland diktet «Til Foraaret». Wergeland lå syk og visste at han skulle dø. Diktet hans er en fortvilt og lidenskapelig bønn til naturen om at han må få leve videre. 

  p Wergeland påkaller våren – og plantene og dyra som hører våren til: det første gresset, symra, løvetanna, hestehoven, svalene som er kommet tilbake, og det gamle treet som har fått nye knopper. Han ber dem vitne om kjærligheten han føler for dem. 

  p Dikteren sier at våren er den årstida han setter høyest. Den beskjedne symra betyr mer for han enn sommerens roser, selv om de er flottere å se på. Kjærligheten hans er så sterk at han selv ønsker å være en del av naturen, han vil være et lønnetre som kan stå sammen med det gamle treet. Han hevder at treet og symrene faktisk ber for han, før han kommer med en siste bønn til naturen om å redde han. 

  p Det er ikke tilfeldig at Wergeland velger å skrive om våren. For det første er diktet skrevet i mai måned, og Wergeland ble nok helt enkelt inspirert av det han kunne se rett utenfor vinduet sitt. For det andre representerer våren selve livskraften, den tida på året da alt liv blir gjenskapt. Wergeland er glad i livet, han vil ikke dø. Våren er rett og slett den årstida som best gjenspeiler ønsket hans om å få leve. For det tredje likner fornyelsen som skjer i naturen hver vår, på den kunstneriske skapingsprosessen. Wergeland skriver i diktet at våren er den årstida han liker best. Når vi ser på alt han har klart å skrive og utrette i løpet av sitt korte liv, forstår vi at han føler størst slektskap med nettopp denne årstida. 

