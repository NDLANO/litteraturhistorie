  h5 Humorserier og spenningsserier 
  p I 1930-årene begynte forlagene å gi ut egne tegneserieblad eller julehefter med tegneseriestriper. Det er vanlig å dele tegneseriene inn i to hovedgrupper: humorserier og spenningsserier. «Nr. 91 Stomperud» er en typisk humorserie, mens «Ingeniør Knut Berg på eventyr» er en spenningsserie med Knut Berg som superhelt. Best kjent i denne sjangeren er uten tvil den amerikanske tegneserien «Supermann». 

  p Tegneserien «Donald Duck» ble utgitt for første gang i 1934. Bladet er en representant for den typen humorserier der hovedfigurene er morsomme dyr med menneskelige egenskaper. Til samme sjanger hører serier som «Tom og Jerry», «Pusur» og den norske tegneserien «Bamse Brakar». 
  
