  p Det eldste norske ukebladet, Allers, kom ut første gang i 1877. Ukebladet Hjemmet ble etablert i 1904, og fra 1933 utgav forlaget Ernst G. Mortensen Norsk Ukeblad. Den viktigste målgruppa for slike familieblad var voksne kvinner. Bladene presenterte lettere opplysningsstoff om mat, håndarbeid, moter og kjente personer, men også fiksjonsstoff som noveller og tegneseriestriper. Fra 1937 har tegneseriestripa «Nr. 91 Stomperud» vært et fast innslag i Norsk Ukeblad. 

  p Det eneste ukebladet på nynorsk, Nynorsk Vekeblad, kom ut regelmessig i perioden 1934–1954. Her ble det trykt tekster av kjente nynorskforfattere som Johannes Heggland, Anders Hovden og Ragnvald Vaage. Nynorsk Vekeblad trykte også de første tegneseriestripene om «Vangsgutane» og «Ingeniør Knut Berg på eventyr». 

  p Norsk Barneblad ble grunnlagt i 1887 og er et av de aller eldste barnebladene i verden. Bladet har all tekst på nynorsk og kommer ut den dag i dag. De første årene gikk bladet under navnet Sysvorti (svarttrost). Norsk Barneblad inneholdt mange litterære tekster for barn, også kjente utenlandske barnebøker i nynorsk oversettelse var med som framhaldsfortellinger. Fra 1938 har tegneserien «Smørbukk» hatt fast plass på baksida av bladet. 
  
