  p I 1930- og 1940-åra voks det fram eit rikt utval av vekeblad og teikneseriar. Vekeblad er publikasjonar som kjem ut fast kvar veke, men nemninga blir også brukt om blad som berre kjem ut ein gong i månaden.

  p Det eldste norske vekebladet, Allers, kom ut første gongen i 1877. Vekebladet Hjemmet vart etablert i 1904, og frå 1933 gav forlaget Ernst G. Mortensen ut Norsk Ukeblad. Den viktigaste målgruppa for slike familieblad var vaksne kvinner. Blada presenterte lettare opplysningsstoff om mat, handarbeid, motar og kjende personar, men også fiksjonsstoff som noveller og teikneseriestriper. Frå 1937 har teikneseriestripa «Nr. 91 Stomperud» vore eit fast innslag i Norsk Ukeblad.

  p Det einaste vekebladet på nynorsk, Nynorsk Vekeblad, kom ut jamt i perioden 1934–1954. Her vart det trykt tekstar av kjende nynorskforfattarar som Johannes Heggland, Anders Hovden og Ragnvald Vaage. Nynorsk Vekeblad trykte også dei første teikneseriestripene om «Vangsgutane» og «Ingeniør Knut Berg på eventyr».

  p Norsk Barneblad vart grunnlagt i 1887 og er eit av dei aller eldste barneblada i verda. Bladet har all tekst på nynorsk og kjem ut den dag i dag. Dei første åra gjekk bladet under namnet Sysvorti (svarttrost). Norsk Barneblad inneheldt mange litterære tekstar for barn, også kjende utanlandske barnebøker i nynorsk omsetjing var med som framhaldsforteljingar. Frå 1938 har teikneserien «Smørbukk» hatt fast plass på baksida av bladet.
  
