  p I 2007 gav Fonna forlag ut serien i ein moderne versjon, skriven av Jon Bing og Tor Åge Bringsværd og illustrert av Knut Westad, som også teiknar serien «Fantomet».

  p Serien om ingeniør Knut Berg voks fram som eit norsk motstykke til den amerikanske teikneserien «Lyn Gordon». I dei første seriane utfører den staute, lyshåra nordmannen heltegjerningane sine i det ukjende landet Kalimanko i Det fjerne austen. Her kjempar han mot den vonde diktatoren Dr. Panga, som vil gifte seg med prinsesse Aima og leggje heile riket under seg. Eventyraren Knut Berg syner seg som ein sann humanist og bergar prinsessa og folket hennar frå nød og undertrykking.

  p Etter kvart som serien utviklar seg, står kampen mellom skurken Dr. Panga og helten Knut Berg ikkje berre om jordkloden, men om heile verdsrommet. Ved hjelp av ingeniørkunstane sine legg Knut Berg under seg fjerne galaksar. Slik sett kan vi seie at ingeniør Knut Berg var den første nordmannen i verdsrommet!

