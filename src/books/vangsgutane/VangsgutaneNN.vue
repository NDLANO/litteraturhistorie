  blockquote Det må ha vore i året 1944 eller 1945, og heltane frå romferdene eller fjella kring Todalen erobra hjartet og trollbatt meg fordi dei førte meg inn i eventyret. For slik var det. Orda bergtok meg, det nynorske språket var fullt av trolldom, skrifta og talen spegla ikkje av den gjevne røyndomen, men skapte ein ny og mykje meir fascinerande ein. Den fyrste gongen ein opplever eit språk, set det råmene for det som kjem etter, og for meg vart det nynorske språket for alltid knytt til romferdene til ingeniør Knut Berg og den vakre kjærasten hans, prinsessa frå landet Kalimanko. Dei to, helten og heltinna, kjempa tappert mot skurken doktor Panga og freista å hindra at han tok makta i heimlandet til prinsessa. Og dei vann til sist, gjorde dei ikkje?  

