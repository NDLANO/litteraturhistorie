  h5 Humorseriar og spenningsseriar
  p I 1930-åra byrja forlaga å gi ut eigne teikneserieblad eller julehefte med teikneseriestriper. Det er vanleg å dele teikneseriane inn i to hovudgrupper: humorseriar og spenningsseriar. «Nr. 91 Stomperud» er ein typisk humorserie, mens «Ingeniør Knut Berg på eventyr» er ein spenningsserie med Knut Berg som superhelt. Best kjend i denne sjangeren er utan tvil den amerikanske teikneserien «Supermann».

  p Teikneserien «Donald Duck» vart utgitt for første gong i 1934. Bladet er ein representant for den typen humorseriar der hovudfigurane er morosame dyr med menneskelege eigenskapar. Til same sjangeren høyrer seriar som «Tom og Jerry», «Pusur» og den norske teikneserien «Bamse Brakar».
  
