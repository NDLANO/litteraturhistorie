  p Sigrid Undset var ikke historiker av fag, men hun hadde lest mye og hadde evnen til å leve seg inn i mennesker som få andre. Hun høstet mange lovord for kunnskapen om norsk middelalder. Framstillingene hennes er fulle av små detaljer som vitner om grundig kjennskap til klær, redskaper, skikker, bygninger og dagligliv i middelalderen.  

  h3 Verdikonservativt grunnsyn  

  img.col.col4.left-image.mt5(src='./undset_Portrett2.jpg' alt='')
  p Sigrid Undset var kritisk til normoppløsningen i samtida. Individualismen og kapitalismen var ødeleggende og nedbrytende prosesser, hevdet hun. Hun mente de førte til fremmedgjøring. Som en kontrast til denne utviklingen så hun den katolske middelalderen, der det ikke fantes noen selvhevdende individualisme. 

  p Det verdikonservative menneskesynet kommer til uttrykk i mesteparten av det hun skrev. Etter at hun hadde konvertert  til katolisismen, ble hun også en viktig debattant for den katolske kirken i trosspørsmål og i polemikk med lutherske prester og biskoper. 

  p Undset kastet seg ut i avisdebatter, hun skrev artikler om ulike emner og var hele tida i opposisjon til dem som stod for moderniseringsprosjektet. Hun så på kvinnesaksforkjemperne som fiender av familien og fellesskapstanken. Plikten mot andre mennesker og mot Gud var viktigere enn lysten til å gå egne veier, mente hun. Riktignok forstod hun og argumenterte for at det var viktig for kvinner å frigjøre seg både økonomisk og sosialt, men hun mente den viktigste oppgaven likevel var å være mor og verne om familien. 

