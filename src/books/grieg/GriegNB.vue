  h3 Å gå inn i si tid 
  p Nordahl Grieg hadde i større grad en politisk agenda. Han hadde vært i Spania og opplevd fascismens frammarsj på nært hold, og han så det som en viktig oppgave å skape en antifascistisk front. Grieg så det slik at den eneste kraften som kunne redde Europa fra fascismen, var en folkelig bevegelse. Det måtte handling til, mente Nordahl Grieg, og han oppfordret de intellektuelle til å være med i kampen: 


