  p Grieg var stadig på reise, i Tyskland, Italia og England. I 1937 var han i Spania og arbeidet som reporter under borgerkrigen. Oppholdet der gjorde sterkt inntrykk på han, og bekymringen over fascismens framvekst ble forsterket. 

  p Også når han var i hjemlandet, var han på reise. Han var spesielt fascinert av Finnmark og reiste nordover flere ganger. Her fant han ro og inspirasjon til å skrive. Et av de mest kjente diktene hans er «Morgen over Finnmarksvidda»:

