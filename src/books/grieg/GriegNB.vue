  p I Finnmark fant han harmoni og fred i sjela, og her var det naturen og dyrelivet, ikke krig og sosial urett, som gav inspirasjon.

  h3 Til ungdommen
  p Nordahl Grieg var en politisk omstridt person fordi han forsvarte kommunismen. Samtidig var han klar i advarselen mot nazismen og krigstrusselen. I flere år før Hitler invaderte Polen i 1939, hevdet Nordahl Grieg at krigen ville komme. 

