  h3 Å gå inn i si tid
  p Nordahl Grieg hadde i større grad ein politisk agenda. Han hadde vore i Spania og opplevd fascismens frammarsj på nært hald, og han såg det som ei viktig oppgåve å skape ein antifascistisk front. Grieg såg det slik at den einaste krafta som kunne redde Europa frå fascismen, var ei folkeleg rørsle. Det måtte handling til, meinte Nordahl Grieg, og han oppmoda dei intellektuelle til å vere med i kampen:

