  h3 «Til ungdommen»
  p Nordahl Grieg var ein politisk omstridd person fordi han forsvara kommunismen. Samstundes var han klar i åtvaringa mot nazismen og trugsmålet om krig. I fleire år før Hitler invaderte Polen i 1939, hevda Nordahl Grieg at krigen ville kome.

