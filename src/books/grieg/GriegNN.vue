  p Grieg var stadig på reise, i Tyskland, Italia og England. I 1937 var han i Spania og arbeidde som reporter under borgarkrigen. Opphaldet der gjorde eit sterkt inntrykk på han, og uroa over fascismens framvekst vart forsterka. 

  p Også når han var i heimlandet, var han på reise. Han var spesielt fascinert av Finnmark og reiste nordover fleire gonger. Her fann han ro og inspirasjon til å skrive. Eit av dei mest kjende dikta hans er «Morgen over Finnmarksvidda»:

