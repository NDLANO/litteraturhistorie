  h3 Striden mellom Welhaven og Wergeland 
  p Henrik Wergeland og Johan Sebastian Welhaven var de  to sentrale dikterne i Norge i første halvdel av 1800-tallet. Begge var romantikere, men de var uenige i flere viktige spørsmål. I tillegg var både Welhaven og Wergeland taleføre og sterke personligheter, med hver sin tilhengerflokk. Krangelen mellom de to dikterne foregikk i full offentlighet, og debattinnleggene var stort sett nokså usaklige. 
  
