  h5 Usemje om diktekunsten
  p Kva er god dikting, og kva er formålet med diktinga? Også desse spørsmåla er Welhaven og Wergeland usamde om.

  p Welhaven meiner at tankar og kjensler må ordnast og foredlast før dei kan bli til dikting. Forma må spegle denne foredlinga, derfor må diktaren underkaste seg strenge reglar for strofebygnad og språklege uttrykk. Wergeland eksperimenterer derimot med versemål og rim, han prøver å finne den forma som står til det han vil uttrykkje.

  p For Welhaven er dikta til Wergeland merkte med «alle poesiens dødssynder». Han hevdar mellom anna at Wergelands dikt manglar klarleik, og at dei er skjemde av altfor sterke kjensler og drifter.

  p Både Welhaven og Wergeland er i grunnen samde om at diktarevna er ei spesiell gåve, men Welhaven meiner at diktinga først og fremst skal medverke til eit rikare indre liv for lesaren. Wergeland vil derimot ikkje skape «sofalitteratur», som han kalla den danske, upolitiske litteraturen som Welhaven sette høgt. For han har diktarane også ei politisk oppgåve: Dei skal vere «idéanførere og folkenes lærere».
  
