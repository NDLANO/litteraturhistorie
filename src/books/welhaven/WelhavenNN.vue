  h3 Striden mellom Welhaven og Wergeland
  p Henrik Wergeland og Johan Sebastian Welhaven er dei to sentrale diktarane i Noreg i første halvdel av 1800-talet. Begge er romantikarar, men dei er usamde i fleire viktige spørsmål. I tillegg er både Welhaven og Wergeland taleføre og sterke personlegdommar, med kvar sin tilhengarflokk. Krangelen mellom dei to diktarane går føre seg i fullt rampelys, og debattinnlegga er stort sett nokså usaklege.
  
