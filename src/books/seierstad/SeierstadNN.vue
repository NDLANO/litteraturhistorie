  p Men først og fremst handlar boka om kvardagen til familien Khan heime i leilegheita i Kabul. Familien Khan er ingen typisk afghansk familie. Familien tilhøyrer det ein kan kalle ein middelklasse i eit land som elles er prega av store skilnader mellom rike og fattige – dei fleste afghanarar er fattige analfabetar. Familien Khan er relativt velståande, og fleire i familien har utdanning. Seierstad skriv sjølv at årsaka til at ho valde denne familien, er at han inspirerte henne. Familien gjorde også forteljinga mogleg gjennom at dei tok imot henne og lét henne få leve tett innpå seg. Dette gav forfattaren eit unikt innblikk i kvardagen og røyndommen til familien.

  p Men i tillegg til å late seg inspirere har Seierstad som frigjord vestleg kvinne også late seg provosere i møtet med eit undertrykkjande kvinnesyn. Afghanske kvinner lever som offer i ei verd der menn har all makt. Også i samfunnet elles, og i lovverket, blir kjønna behandla ulikt. Da for eksempel overhovudet i familien, Sultan Khan, ønskte seg ei kone til – ei ung og vakker ei – måtte den første kona hans akseptere å dele heim med rivalen sin.

  h3 «Ei litterær form»

  figure.col.col6.right-image.mt5
    img(src='./seierstad_Bokhandleren_i_Kabul5.jpg' alt='Mann omgitt av familie med barn i ulike aldre. To kvinner held kvart sitt barn. Foto')
    figcaption Shah Mohammad Rais med familien i Kabul.  Foto: Kjell Odegard / TT / Scanpix
    
  p Researchmetoden Seierstad har brukt, kan kallast «deltakande observerande intervju» – ein metode som gir grundig innblikk i – og innsikt i – tankane og kjenslene til intervjuobjektet. Seierstad har levd tett på, observert og delteke i liva til og intervjua medlemmene i familien Khan. Deretter har ho valt å skrive forteljinga i det ho omtaler som ei 
    em litterær form 
    | som blandar element frå både sakprosaen og skjønnlitteraturen, fakta og fiksjon. Dette kan kallast ein 
    em faksjonstekst
    | . Litt forenkla kan ein seie at innhaldet i 
    em Bokhandleren i Kabul 
    | er basert på fakta. Den språklege utforminga, vinkling og komposisjon, er derimot prega av skjønnlitterære grep.

  p I forordet gjer forfattaren greie for prosjektet sitt, så trekkjer ho seg tilbake frå forteljinga. Ein kan seie at boka har ein 
    em allvitande 
    | forteljar, med vekslande synsvinkel. Forfattaren lèt personane sjølve fortelje historiene sine, men forteljarrøyst og synsvinkel varierer frå kapittel til kapittel. Tankar og kjensler blir ofte formidla i form av indre monologar. «Hun savner Sultan forferdelig når han er borte. De andre i familien er ikke så hyggelige mot henne når mannen ikke er der», tenkjer for eksempel den yngste kona.

  h3 Å konstruere røyndom
  p Det Seierstad gjer, å krysse grenser mellom fakta- og fiksjonstekst, er typisk for mange tekstar, i ulike medium, i samtida. I reality-tv blir røyndommen arrangert – vi kan snakke om ein konstruert røyndom eller pseudorøyndom. Den prislønte amerikanske regissøren Michael Moore (1954– ) regisserer også røyndommen, med eit stramt og klart politisk forteljargrep, i dokumentarfilmane sine 
    em Bowling for Columbine 
    | (2002) og 
    em Fahrenheit 9/11 
    | (2004). Og Karl Ove Knausgård har skapt debatt med romanane sine – da verkelege personar har kritisert han fordi dei opplever at dei har vorte karakterar i fiksjonsuniverset hans. Seierstad sjølv er inspirert av den polske journalisten Ryszard Kapuściński (1932–2007), som i reportasjebøkene sine skriv personleg, og i utstrekt grad bruker litterære grep og verkemiddel.

  p To utfordringar oppstår som følgje av Seierstads sjangerval – eller sjangerblanding. For det første: Er teksten truverdig? Når ho går «inn i» hovudet på karakterane sine, kan vi stole på at ho har klart å gjengi akkurat det dei har tenkt og følt? For det andre: Kva etiske implikasjonar har ei slik sjangerblanding? Kor går grensa for 
    em kva 
    | ein kan skrive om? Og 
    em korleis 
    | kan ein skrive om reelle personar og verkelege hendingar? 

