  h3 Krigen og krigslagnadene

  figure.col.col6.right-image.mt5
    img(src='./seierstad_Bokhandleren_i_Kabul2.jpg' alt='To kvinner handhelsar. Den eine kvinna held eit trofe i venstre hand. Foto')
    figcaption Åsne Seierstad mottek Bokhandlerprisen for 2002 for boka «Bokhandleren i Kabul». Kulturminister Valgerd Svarstad Haugland gratulerer. Foto: Terje Bendiksby / NTB / Scanpix

  p Åsne Seierstad (1970– ) er journalist og forfattar. Ho er utdanna ved Universitetet i Oslo og har studert faga idéhistorie, spansk og russisk. Seierstad har jobba som journalist i Arbeidarbladet og var stasjonert i Russland frå 1993 til 1996 og i Kina i 1997. Mest kjend er ho nok som journalist for NRK Dagsrevyen der ho har dekt ulike krigar – mellom anna i Kosovo, Afghanistan og Irak.

  p I tillegg til å rapportere som journalist har Seierstad skrive fleire sakprosaverk om erfaringane sine frå – og opplevingane i – krigsherja område. Dei 14 portretta i 
    em Med ryggen mot verden: Portretter fra Serbia 
    | (2000) – er portrett av menneske frå ei omstridd folkegruppe under krigen i det tidlegare Jugoslavia. Lagnadene blir følgde vidare i oppfølgjaren 
    em Med ryggen mot verden – fremdeles 
    | (2005). 
    em Hundre og én dag 
    | (2003) er ei reportasjebok frå Irak. Seierstad var i landet då USA gjekk til åtak i 2003. Ho skriv om det som skjedde før, under og rett etter invasjonen. I De krenkede – 
    em Fortellinger fra Tsjetsjenia 
    | (2007) er det krigslagnadar frå Tsjetsjenia Seierstad skriv om.

  p Seierstad har vunne fleire prisar for det modige arbeidet ho gjer som journalist og forfattar, mellom anna Fritt Ords Honnør (2001), Bokhandlerprisen (2002) og svenske Årets Frihetspenna (2010). I grunngivinga for tildelinga av den siste prisen står det at ho får prisen for si «grunnleggjande og urokkelege tru på trykkje- og ytringsfridomen».

  h3 Bokhandleren i Kabul – eit familiedrama

  figure.col.col6.right-image.mt5
    img(src='./seierstad_Bokhandleren_i_Kabul3.jpg' alt='Mann med flippskjegg og bart sit i ein stol omgitt av hyller med bøker. Foto')
    figcaption Shah Mohammad Rais i bokhandelen sin i Kabul. Foto: Tor Arne Andreassen / Aftenposten / Scanpix

  p Taliban, ei radikal islamistisk rørsle, styrte Afghanistan med jernhand i perioden 1996 til 2001. Etter terroråtaket 11. september 2001 invaderte amerikanske og britiske styrkar landet, og Taliban-regimet mista makta vinteren 2001. Våren etter bur Åsne Seierstad hos ein bokhandlar og familien hans, som ho i boka kallar Khan, i Kabul. Familien består av bokhandlaren sjølv – kalla Sultan Khan – dei to konene hans, barna deira, Sultans systrer, broren, mora og nokre fetrar.

  p Seierstad skildrar eit Afghanistan, og ein familie, i ein overgangsperiode – i ein bokstaveleg talt ny vår. For gradvis blir no Afghanistan modernisert. Den strenge kontrollen, frå tida før og under Talibantida, blir mjuka opp. Seierstad følgjer familien i kvardagen: i bryllaup, på skulen og i hammamen – det offentlege badet. Ho følgjer Sultan Khan på forretningsreisene hans, og sonen hans på ei pilegrimsreise.

  p Det er eit brutalt og herja samfunn Seierstad skriv om. Forfattaren tek somme tilbakeblikk til tida da landet var okkupert av Sovjetunionen, og til tida under Mujahedins makt. Mujahedin var islamistiske motstandsrørsler som i kampen mot Sovjet vart støtta av USA. Men mest av alt er landet i 2002 prega av tida under Taliban. Krig, dødsstraff, pisking og steining – stadige truslar og redsler under skiftande fiendebilete og alliansar – har prega Afghanistans historie og folk.

