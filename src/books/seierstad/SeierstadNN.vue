  p Åsne Seierstads bok 
    em Bokhandleren i Kabul 
    | (2002) er ei mykje lesen og omtalt bok. Ho har seld i 250 000 eksemplar berre i Noreg. Boka er omsett til 40 språk, og ho låg i over 40 veker på bestseljarlista til The New York Times. Boka er også på fleire måtar omstridd. 
    em Bokhandleren i Kabul 
    | er ei dei mange bøkene i samtida som kryssar grenser mellom fakta og fiksjon, levd liv og dikta røyndom. Seierstad skriv om verkelege menneske og hendingar – i eit språk som nærmar seg skjønnlitteraturen.

  p Dette skapte furore. Kor går grensene mellom to grunnleggjande menneskerettslege prinsipp – ytringsfridom og vern mot krenkingar? Den verkelege bokhandlaren i Kabul gjekk til rettssak, ein sak forfattaren og forlaget tapte i 2010. Dommen vart rett nok omgjord av lagmannsretten i 2011.

  p I 2013 gav Seierstad ut boka 
    em En av oss 
    | om 22. juli og Anders Behring Breivik. I denne boka går ho tett inn på sentrale personar i familien og omgangskrinsen til Breivik. Også denne boka vart kritisert av kjelder som meinte seg feilsiterte og misbrukte av forfattaren.

