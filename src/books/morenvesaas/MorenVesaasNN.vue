  p Moren Vesaas skildrar både ungt og intenst begjær og korleis ein på eldre dagar kan oppleve forelskinga og det rusande begjæret på nytt. I diktet «Har vandra så langt» er det eit meir roleg og avklart bilete av forelskinga som blir teikna. Diktet fortel om eit levd liv – eit liv der ho har fått «plukke så mange» av «livsens blomar». Når ho så trur at det ikkje står att fleire forelskingar og kjærlege møte, blir ho overraska:

  blockquote
