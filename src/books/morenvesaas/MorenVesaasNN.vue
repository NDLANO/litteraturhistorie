    br
    | duvar framfor deg eit vell av blomar 
    br
    | som gløder av liv, av liv.

  p Også i den siste livsfasen kan ein møte kjærleiken, ei røyst som kviskrar: «Det er vi. Det er eg og du.»

  h3 Fellesskap og kvardagslykke

