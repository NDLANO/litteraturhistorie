  p Moren Vesaas skildrer også lykken som ligger i livet som husmor og mor. I motsetning til andre samtidige stemmer i litteraturen er hun ikke opptatt av å lete etter lykken et annet sted – for Moren Vesaas er det viktig å finne lykken i det livet en faktisk lever. Diktene hennes er en oppfordring til å ta vare på det som er verdt å ta vare på, og til å finne lykken i det nære og hverdagslige. Og kanskje er det nettopp her vi finner nøkkelen til å forstå Moren Vesaas’ popularitet som lyriker: Gjennom lyrikken inspirerer hun leseren til å omfavne livet i alle faser og finne lykken der vi er. 
  
  h3 Verken tradisjonalist eller modernist 
  p Halldis Moren Vesaas beveget seg fritt mellom en tradisjonell og en modernistisk uttrykksmåte. Enkelte forskere hevder at det ikke har noen hensikt å prøve å plassere Moren Vesaas innenfor en klar litterær retning – lyrikken hennes er sterkt preget av hennes personlige stemme, og publikum omfavnet diktene hennes uavhengig av formen de var skrevet i. 

  p Noe av det som kjennetegner Halldis Moren Vesaas’ diktning, er bruken av naturen som symbol og virkemiddel. Hun sammenlikner for eksempel livets siste fase med en islagt eng med visne blomster, mens begjæret koples til duvende enger og lukten av fruktbar jord. Et gjennomgangssymbol i lyrikken hennes er treet, og et av de mest kjente diktene er «No plantar kvinna» fra 1945. I dette diktet bruker hun treet som symbol på gjenreisningen etter krigen. 

