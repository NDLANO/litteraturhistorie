  p Moren Vesaas skildrer både ungt og intenst begjær og hvordan en på eldre dager kan oppleve forelskelsen og begjærets berusende kraft igjen. I diktet «Har vandra så langt» er det et mer rolig og avklart bilde av forelskelse som tegnes. Diktet forteller om et levd liv – et liv der hun har fått «plukke så mange» av «livsens blomar». Når hun så tror at det ikke er flere forelskelser og kjærlige møter igjen, blir hun overrasket: 

