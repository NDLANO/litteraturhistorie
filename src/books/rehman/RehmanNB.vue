  p Shabana Rehman (1976– ), gift Shabana Rehman Gaarder, er en  utradisjonell stemme i den norske samfunnsdebatten. Som norsk-pakistansk kvinne, stand-up-komiker, performanceartist, spaltist, foredragsholder og debattant har hun et blikk på samtida som ofte skaper debatt. Da hun under et åpent debattmøte løftet mulla Krekar, understreket hun hvem hun er – ei kvinne som med alle midler kjemper mot fordommer, dogmatisme og fundamentalisme. 

  p Rehmans mullaløft fikk delte reaksjoner – for hvor går grensen mellom handlingsfrihet og ærekrenkelse? Med hvilke midler kan man kjempe for ytringsfrihet og åpen dialog? Med hvilke metoder kan man jobbe for menneskerettigheter – for alle? 

  p Shabana Rehman ble født i Pakistan, men kom til Norge som ettåring. Hun har vokst opp på Holmlia i Oslo. Rehman har jobbet som fast spaltist i Dagbladet og Aftenposten, og hun har laget – og oppført – ulike show på norske og amerikanske scener. De siste årene har hun pendlet mellom Oslo og Brooklyn, New York. Både som skribent og komiker er hun alltid trofast i sin kamp for ytringsfrihet og menneskerettigheter, mot fremmedfrykt, homofobi og kvinnediskriminering. Hun har en egen evne til å leke med fordommer – i forsøket på å bryte ned tabuer og flytte grenser. 
  
  h3 Bunad og burka 

