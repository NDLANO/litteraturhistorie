  h3 Språk og form
  p Diktsamlinga er delt inn i seks delar. Kvar del inneheld frå sju til 32 dikt. Vold bruker svært sjeldan fast rim og rytme; han varierer mellom å skrive korte dikt i fri form, dikt med to og to eller fire og fire verselinjer i kvar strofe, og lange – nærmast prosaaktige og forteljande parti. Språket er munnleg og lite høgstemt. Og med unntak av den delen som blir kalla Byen H, er tekstane veldig konkrete og inneheld få bilete som krev tolking. I fleire av dikta blir lesaren presentert for fleire versjonar, og forfattaren reviderer sitt eige dikt i diktet. Eit eksempel er «Tale for loffen»:

  blockquote
    | Jeg vil holde en tale 
    br
    | for de tykke og smale – nei 
    br
    | det vil jeg ikke, jeg vil snakke 
    br
    | om loffen, vår alles venn i brødveien (…). 

  h3 Mor Godhjertas glade versjon – ikkje den triste …
  p På første side i diktsamlinga står det skrive «Til mor fra Jan Erik», med handskrift, noko som gjer at lesaren i første omgang kan få assosiasjonar til at dette er ei gåve frå Jan Erik til mora. Ein nærliggjande tanke er da også at det er mor hans som er Mor Godhjerta. Kva tyder det da at dette er hennar glade versjon – og ikkje den triste?

  p Ved fleire høve har Vold uttrykt at diktsamlinga i stor grad handlar om faren hans, som døyde året før diktsamlinga kom ut. Er dette ein glad versjon, meint for trøyst? Ein versjon som viser at livet er mangfaldig og rikt? I alle fall – indirekte seier tittelen noko om at det finst ein annan versjon også, men dette er altså den glade versjonen. 

  h3 Døden, kjærleiken og pengemakta
  
  figure.col.col12
    img(src='./vold_Portrett3.jpg' alt='')
    figcaption Jan Erik Vold med diktsamlinga «Tolv meditasjoner».  Foto: Bjørn Aslaksen / VG / Scanpix

  p På 80-talet syner Vold seg som ein meir «moden» forfattar, oppteken av sentrallyriske emne som kjærleik, det forgjengelege, sorg og sakn. Sorgen. Sangen. Veien (1987) inneheld også politiske dikt. Vold er kritisk til pengemakt, maktpersonar og autoritære regime. I Elg (1989), som er ei samling med tidlegare skrivne tekstar gitt ut på plate eller trykt i aviser, ser ein meir av den politisk og sosialt engasjerte Vold. Her skriv han om politivald, hushaiar og Oslo byråd.

  p Den meir samfunnsengasjerte diktaren ser vi meir av på 90-talet. IKKE. Skillingstrykk fra nittitallet (1993) og Kalenderdikt (1995) inneheld mange politiske og samtidssatiriske dikt. En sirkel is (1994) er tekstar Jan Erik Vold skreiv til Norges Skøyteforbunds hundreårsjubileum i 1993, som i utvida versjon er gitte ut som ei lyrikksamling. I dei siste diktsamlingane hans, Tolv meditasjoner (2002), Drømmemakeren sa (2004) og Diktet minner om verden (2005) finn ein både politiske dikt, eksistensielle dikt og dikt om den konkrete og nære kvardagen og røyndommen vår.

  h3 Her. Her i denne verden 
  
  p Vold er kjent som ein særeigen essayist. I ulike tekstar om eigen og andres litteratur har han vore med å setje preg på norsk litteraturhistorie. I perioden 1970– 74 var han medredaktør for litteraturtidsskriftet Vinduet. Vold har mellom anna gjendikta forfattarar som Tomas Tranströmer, Samuel Beckett og Bob Dylan. I tillegg har han skrive bøker om forfattarar som Gunvor Hofmo, Cornelis Vreeswijk, Olav H. Hauge og Johan Borgen, og han har samla og redigert tekstar av forfattarar som har vore i ferd med å bli gløymde. For arbeid som formidlar av andres tekstar vart han utnemnd til æresdoktor ved Universitetet i Oslo i 2000.

  p Både som forfattar, litteraturformidlar og samfunnsdebattant har Vold ei karakteristisk og lett gjenkjenneleg røyst – alltid trufast mot tanken om at ein forfattar er eit menneske som er engasjert i verda. Det er ikkje noko tilfelle at ei essaysamling av Vold frå 1984 fekk tittelen Her. Her i denne verden (1984).

  p Han har skrive dikt om USA og Israel, komme med kritiske utsegner om norsk politikk – og norske politiske parti – og han har vore kritisk til korleis Noreg behandla jødane under andre verdskrigen. Politisk har han heile tida stått på venstresida, og han har kjempa mot byråkrati og maktelite.

  h3 Enkelt og kvardagsleg
  p Da Vold i 1968 gav ut Mor Godhjertas glade versjon. Ja var han ein del av den såkalla Profil-krinsen. Profil var eit litteraturtidsskrift som vart gitt ut av ei gruppe samfunnskritiske og radikale forfattarar og studentar ved Universitetet i Oslo frå midten av 1960-talet. Saman med andre Profil-forfattarar som Dag Solstad (1941– ), Espen Haavardsholm (1945– ), Liv Køltzow (1945– ) og Einar Økland (1940– ) var Vold med på å fornye synet på korleis litteratur skulle skrivast og kva han skal handle om.

