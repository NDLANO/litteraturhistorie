  p Felles for Vold og dei andre lyrikarane i Profil-krinsen var opposisjonen deira mot den tradisjonelle etterkrigsmodernismen. Dei meinte han var for innovervend og alvorleg, og tyngd av komplekse symbol og metaforar. Lyrikk skal ikkje vere for «eliten», hevda dei, men for folk flest. Og diktaren er ikkje eit visjonært geni, men eit menneske som er engasjert i det samfunnet han eller ho er ein del av.

  p Vold og dei andre Profil-forfattarane ønskte seg, og lanserte, ei ny form for modernisme – Profil-modernismen eller nyenkel dikting. Formmessig var dei inspirerte av mellom anna den strammeog ordknappe forma til haiku-dikta. I tema og motiv var dei påverka av eksistensialismen. Grunntanken om at det vi har, er livet her og no, utan forsikringar om eit evig liv etter dette, skapte dikt med kvardagslege og konkrete motiv. Språkleg lét dei seg inspirere av kvardagsspråk, munnleg sjargong, poptekstar og reklamespråk.
  
  h3 Tinga rundt deg – ikkje sorga inni deg
  p Vold skreiv dikt om trikkeskinner og loff, der lesaren ikkje må leite etter ei djupare meining bak dei språklege bileta i diktet. Faktisk treng ikkje eit dikt innehalde språklege bilete i det heile. Skriv ein ein «Tale for loffen», kan diktet faktisk handle om loffen – og korleis han luktar og smakar. Vold skjønnmålar eller opphøgjer ikkje røyndommen. Han skriv om røyndommen på ein jordnær og realistisk måte, slik han artar seg for dei fleste av oss.

  p Vold er kritisk til den tradisjonelle eg-dyrkinga i lyrikken. Han har sjølv sagt at han meiner lyrikarar bør interessere seg meir for tinga rundt seg enn for sorga inni seg. Eg-et er likevel tydeleg til stades også i mange av dikta til Vold, men da som observatør av tilsynelatande ganske trivielle motiv rundt han. 
  
