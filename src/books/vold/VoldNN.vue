  p Forfattaren er sjølv ein framifrå formidlar av eigen, og andres, lyrikk gjennom opplesingar eller framføring av dikt akkompagnert av musikk. Han har mellom anna samarbeidd med saksofonisten Jan Garbarek (1947– ). Med dette samarbeidet innførte han sjangeren jazz & poetry – jazz og poesi – i Noreg. Briskeby blues (1969) og ingentings bjeller (1977) er kritikarroste album der Vold samarbeider med nettopp Garbarek. Vold har også samarbeidd med trompetisten Chet Baker (1929–1988) og pianisten Egil Kapstad (1940–2017).

