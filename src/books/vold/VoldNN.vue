  p Med Mor Godhjertas glade versjon. Ja (1968) fekk Jan Erik Vold ei større gruppe lesarar enn dei fleste poetar kan drøyme om. Med over 20 000 selde bøker har samlinga treft eit stort publikum. Det er også her mange av dei mest kjende dikta til Vold står, for eksempel «Trikkeskinnediktet», «Funny», «Tale for loffen» og «Bildet stanser på Barkåker-diktet». Dikta er prega av leikelyst, overskott og openheit.

  p Samlinga har eit sjølvbiografisk preg. Det lyriske eg-et i samlinga er Jan Erik Vold. Han skriv om byen og bydelen han elskar – Oslo og Briskeby. Han namngir venner og kjende; Kjell (Askildsen), Halldis og Tarjei (Vesaas) og Georg (Johannesen) – og gir opp adressene og telefonnummera deira. Poeten nektar seg lite, da idealet er å skape eit truverdig og autentisk miljø.

  p Vold skriv også om kvinner han har møtt, bøker han har lest, reiser han har gjort, og han skriv om døden. Og ikkje minst – han skriv om loffen, trikken, vondt i ryggen, vinmonopolet, ein morgondukkert – ja, om det konkrete og nære livet i all si sanselege prakt. Det heile kan verke kvardagsleg, nesten barnleg konkret – men innimellom sparkar han til pengefolk og politikarmakt. Særlig kritisk er han til byutviklinga i Oslo.

