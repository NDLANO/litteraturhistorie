  h3 Jazz & poetry
  p I den nyenkle lyrikken ligg vekta i mindre grad på dei språklege bileta, og i større grad på dei klanglege og formelle sidene ved diktet. Vold leikar med lydar og ordklangar, og vektlegginga av rytmen, klangen og musikaliteten i diktet har ført til at mange av Volds dikt eignar seg godt til munnleg framføring.

