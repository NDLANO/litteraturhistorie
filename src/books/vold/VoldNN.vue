  h3 Mellom speil og speil

  figure.col.col6.right-image.mt5
    img(src='./vold_Mellom_speil_og_speil1.jpg' alt='')
    figcaption Jan Erik Vold mottek Tarjei Vesaas’ debutantpris i 1965. Foto: NTB / Scanpix

  p Jan Erik Vold er ein svært produktiv forfattar. Som lyrikar debuterte han i 1965 med diktsamlinga mellom speil og speil, ei diktsamling han fekk Tarjei Vesaas’ debutantpris for. Eit gjennomgåande tema i denne samlinga er at mennesket er innesperra i språket og tviler på om verda der ute eksisterer. Dikta er til tider klaustrofobiske. Den einaste redninga er kjærleiken til «den andre» – ein venn, ein elska person.

  p I 1966 kom diktsamlinga HEKT. Mange av dikta har eit konkret og kvardagsleg motiv, dei er knappe, språket er talemålsnært, og forfattaren er merksam på det grafiske oppsettet av dikta. I Blikket (1966) er eksperimenteringa med det grafiske oppsettet gjord til ei hovudsak.

  p I 1967 gav Vold ut si første prosabok, frå rom til rom SAD. Tekstane varslar ein ny modernisme, også i prosaen. Vold leikar og eksperimenterer, han skriv om tinga vi omgir oss med, og om menneske som prøver å orientere seg i ei verd «i tingenes vold». Denne kortprosaen fører han vidare i BusteR brenneR (1976).

  p Diktsamlingar som spor, snø (1970), Bok 8: LIV (1973), S (1978) og sirkel sirkel (1979) er tydeleg inspirerte både av haikudikting – ei tradisjonell japansk diktform – og zenbuddhisme. Dikta er no korte og enkle i forma, nesten meditative i utforming. Vold krinsar her rundt omgrep som «ingenting» og «intet» – og andre omgrep som gir assosiasjonar til austleg filosofi og religion.

