  h3 Bjørnson – et nasjonalt symbol 
  p Sammen med Ibsen, Kielland og Lie regner vi Bjørnson til en av «de fire store» i norsk litteratur – selv om flere mener at han var en bedre debattant, politiker, journalist og kritiker enn han var forfatter. 

  p Bjørnson var en del av det såkalte poetokratiet på slutten av 1800-tallet. Forfatterne – poetene – var nå så sentrale i norsk offentlig debatt at de var med på å prege utviklingen av samfunnet. Bjørnson ble på den måten et nasjonalt symbol – et symbol i kampen for et mer rettferdig Norge. 

  p Kjernen i den unike rollen Bjørnson hadde i norsk kulturhistorie, henger kanskje sammen med rollen både som samfunnsengasjert debattant og som produktiv forfatter? Han var en markant intellektuell i samtida, som gjennom sin fargerike framtoning skapte liv og debatt i det offentlige rom. 

  p Til tross for at det til tider stormet rundt han, var han en høyt elsket personlighet. Gravferden hans vitner om det – den var en konge verdig.            

