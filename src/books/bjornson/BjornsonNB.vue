  p En årsak til at Bjørnson hadde en norskfarget syntaks, var nok at han selv snakket bygdemål i oppveksten. Men  stilen i folkeeventyrene og sagaene virket også inn. Bjørnson bruker et muntlig norsk språk med korte setninger: «’Er det ellers noe?’ spurte presten, han så på Tord. – ’Ellers er det intet.’ – Tord gikk.» («Faderen» 1860) 

  p Vi finner særnorske uttrykk og norsk ordstilling i uttrykk som «Au, den gikk i stykker» og «Er den din, den bukken?» (En glad gutt). Bruk av påpekende pronomen og bestemt form av substantivet kaller vi dobbel bestemmelse («den bukken»). Dette er norsk uttrykksmåte og ikke dansk, som har enkel bestemmelse («den bukk»). 
  
  h3 «Faderen» og «lidelsens høyskole» 
  p «Faderen» er en kort og mesterlig skrevet novelle på bare to sider. Det mest slående ved denne novellen er at fortellingen er så konsentrert, og at Bjørnson fanger inn høydepunktene i et helt livsløp på så få linjer. 

  p I mer enn noe annet verk av Bjørnson kjenner vi her igjen fortellemåten fra sagastilen. Her er det scener og dialoger som dominerer, og fortelleren lar være å kommentere tanker og følelser hos personene. Han registrerer bare det som skjer, som i de islandske ættesagaene. 

  p «Faderen», fra 
    em Småstykker 
    | (1860), handler om Tord Øveraas – den mektigste og mest velstående bonden i bygda. Vi følger bonden og sønnen hans  gjennom sønnens livsløp. Fire ganger er Tord hos presten. De tre første er når sønnen skal døpes, når han skal konfirmeres, og før han skal gifte seg med den rikeste jenta i bygda. Hver gang betaler Tord for en markering som viser at han er en mektig mann. Men før bryllupet drukner sønnen, og faren må til presten en siste gang for å få sønnen gravlagt. 

  p Sentralt i teksten står den endringen faren gjennomgår. Hele livet har han vært en mann som er vant til å få det som han vil fordi han har makt og penger. Etter at sønnen dør, utvikler han seg til å bli en ydmyk mann, som verdsetter andre verdier enn penger og posisjon. 

  p Bjørnson hadde en pedagogisk idé med bondefortellingene sine. De var en del av et idealistisk dannelsesprosjekt. Bjørnson selv omtalte dette som «lidelsens høyskole». Hovedkarakterene i bondefortellingene er gjerne personer som må gjennomleve en vond tid. Ofte er de i konflikt med noe i seg selv. Men i kampen mot ulike hindringer gjennomgår de en forvandling. Denne forvandlingen er en prosess mot et mer ideelt og etisk godt liv. For Bjørnson var denne endringen oftest knyttet til et religiøst verdigrunnlag, og det kan vi se i «Faderen». 

  p Vi kan derfor si at «Faderen» har et romantisk verdigrunnlag – jakten på det gode, sanne og vakre livet. Men personene er plassert i mer realistiske omgivelser og må kjempe mot disse omgivelsene på veien. Litteraturhistorisk hører derfor bondefortellingene hjemme i den epoken vi kaller poetisk eller romantisk realisme. 

