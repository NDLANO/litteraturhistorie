  p Bjørnstjerne Bjørnson (1832–1910) ble født i Kvikne i Østerdalen. I ungdomstida flyttet familien til Romsdal, der faren fikk en prestestilling. Sytten år gammel sendte faren Bjørnstjerne til Kristiania for å gå på Heltberg studentfabrikk. Her tok han examen artium som tjueåring. På Heltberg ble Bjørnson kjent med Jonas Lie, Henrik Ibsen og Aasmund Olavsson Vinje. 

  p Opp gjennom livet skrev Bjørnson både romaner, noveller, dikt og skuespill. Han var også en aktiv samfunnsdebattant og avisskribent. Han skrev litteratur- og teateranmeldelser og ivret blant annet for en fornorsking av teaterspråket. Språkengasjementet hans førte til at Riksmålsforbundet ble stiftet i 1907. 

  p En annen hjertesak for Bjørnson var teateret. I 1857 ble han utnevnt av Ole Bull til sjef for teateret i Bergen. Her var han i to år og arbeidet både som teatersjef og som skribent. Seinere ble han leder for Christiania teater. 

  p I 1858 giftet Bjørnson seg med skuespilleren Karoline Reimers. Hun ble en viktig støtte for han. Ekteskapet varte livet ut, til tross for temperamentet hans og utallige sidesprang. Karoline og Bjørnstjerne fikk seks barn sammen. 

  p Som andre forfattere på den tida dro Bjørnson til byer som Roma, Paris og München for å finne inspirasjon til å skrive. Men det var på gården Aulestad, som Bjørnstjerne og Karoline kjøpte i 1874, at han fant ro og levde de siste årene av sitt liv. 

  p Bjørnson døde i Paris 26. april 1910. Kista ble fraktet til København med kong Haakons jernbanevogn, og videre hjem med panserskipet «Norge» til æresbegravelse. Begravelsen hans vitnet om at Bjørnson var en høyt elsket samfunnsborger. Det at han fikk nobelprisen i 1903, var også en bekreftelse på hvor høyt aktet han var. 

