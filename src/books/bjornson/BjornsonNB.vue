  p Bjørnstjerne Bjørnson skrev mange sanger og dikt. Mest kjent er nok nasjonalsangen «Ja, vi elsker» (1859). I 1870 kom samlingen 
    em Digte og Sange 
    | ut for første gang, og som tittelen vitner om, skrev Bjørnson gjerne melodiøs lyrikk. Flere komponister har satt toner til diktene hans, og på den måten har Bjørnson vært en viktig bidragsyter til den norske sangtradisjonen. 

  p Prestesønnen Bjørnson gjennomgikk en religiøs krise i 1870-årene. Han brøt med kristendommen og søkte et nytt ståsted i naturvitenskapen og tankene til Charles Darwin. I darwinismen fant Bjørnson en vitenskapelig bekreftelse på den optimistiske livstroen som alltid hadde vært en grunntone i diktningen hans. 

  p Det nye livssynet kommer til uttrykk i samlingen Salme II (1880). Her formidler Bjørnson de nye tankene han er påvirket av, blant annet at alle levende organismer har utviklet seg fra det enkle og primitive til det mer kompliserte og høytstående. 

