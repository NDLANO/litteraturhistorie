  p Ei årsak til at Bjørnson hadde ein norskfarga syntaks, var nok at han sjølv snakka bygdemål i oppveksten. Men stilen i folkeeventyra og sogene spela også inn. Bjørnson bruker eit munnleg norsk språk med korte setningar: «’Er det ellers noe?’ spurte presten, han så på Tord. – ’Ellers er det intet.’ –Tord gikk.» («Faderen» 1860)

  p Vi finn særnorske uttrykk og norsk ordstilling i uttrykk som «Au, den gikk i stykker» og «Er den din, den bukken?» (En glad gutt). Bruk av peikande pronomen og bunden form av substantivet kallar vi dobbel bestemming («den bukken»). Dette er norsk uttrykksmåte og ikkje dansk, som har enkel bestemming («den bukk»).
  
  h3 «Faderen» og «lidelsens høgskole»
  p «Faderen» er ei kort og meisterleg skriven novelle på berre to sider. Det mest slåande ved denne novella er at forteljinga er så konsentrert, og at Bjørnson fangar inn høgdepunkta i eit heilt livsløp på så få linjer.

  p I meir enn noko anna verk av Bjørnson kjenner vi her att forteljemåten frå sagastilen. Her er det scener og dialogar som dominerer, og forteljaren lèt vere å kommentere tankar og kjensler hos personane. Han registrerer berre det som skjer, som i dei islandske ættesogene.

  p «Faderen», frå 
    em Småstykker 
    | (1860), handlar om Tord Øveraas – den mektigaste og mest velståande bonden i bygda. Vi følgjer bonden og sonen hans gjennom livsløpet til sonen. Fire gonger er Tord hos presten. Dei tre første er når sonen skal døypast, når han skal konfirmerast, og før han skal gifte seg med den rikaste jenta i bygda. Kvar gong betaler Tord for ei markering som syner at han er ein mektig mann. Men før bryllaupet druknar sonen, og faren må til presten ein siste gong for å få sonen gravlagd.

  p Sentralt i teksten står den endringa faren går gjennom. Heile livet har han vore ein mann som er van med å få det som han vil fordi han har makt og pengar. Etter at sonen døyr, utviklar han seg til å bli ein audmjuk mann, som ser andre verdiar enn pengar og posisjon.

  p Bjørnson hadde ein pedagogisk idé med bondeforteljingane sine. Dei var ein del av eit idealistisk danningsprosjekt. Bjørnson sjølv omtalte dette som «lidelsens høgskole». Hovudkarakterane i bondeforteljingane er gjerne personar som må gjennomleve ei vond tid. Ofte er dei i konflikt med noko i seg sjølv. Men i kampen mot ulike hindringar gjennomgår dei ei forvandling. Denne forvandlinga er ein prosess mot eit meir ideelt og etisk godt liv. For Bjørnson var denne endringa oftast knytt til eit religiøst verdigrunnlag, og det kan vi sjå i «Faderen».

  p Vi kan derfor seie at «Faderen» har eit romantisk verdigrunnlag – jakta på det gode, sanne og vakre livet. Men personane er plasserte i meir realistiske omgivnader og må kjempe mot desse omgivnadene på vegen. Litteraturhistorisk høyrer derfor bondeforteljingane heime i den epoken vi kallar poetisk eller romantisk realisme.

