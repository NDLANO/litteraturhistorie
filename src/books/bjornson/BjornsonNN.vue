  h3 Bjørnson – eit nasjonalt symbol
  p Saman med Ibsen, Kielland og Lie reknar vi Bjørnson til ein av «dei fire store» i norsk litteratur – trass i at fleire meiner han var ein betre debattant, politikar, journalist og kritikar enn han var forfattar.

  p Bjørnson var ein del av det såkalla poetokratiet på slutten av 1800-talet. Forfattarane – poetane – var no så sentrale i norsk offentleg debatt at dei var med på å prege utviklinga av samfunnet. Bjørnson vart på den måten eit nasjonalt symbol – eit symbol i kampen for meir rettferd i Noreg.

  p Kjernen i den unike rolla Bjørnson hadde i norsk kulturhistorie, heng kanskje saman med rolla både som samfunnsengasjert debattant og som produktiv forfattar? Han var ein markant intellektuell i samtida, som gjennom sin fargerike veremåte skapte liv og debatt i det offentlege rom.

  p Trass i at det til tider storma rundt han, var han ein høgt elska personlegdom. Gravferda hans vitnar om det – ho var ein konge verdig.

