  p På 1700-talet kjem det ut mange bøker som spekulerer i kva menneska vil oppdage i jordas indre, på månen eller på andre uoppdaga stader. Holberg er heilt klart inspirert av romanen Gullivers reiser av Jonathan Swift, som kom ut allereie i 1726. Både Gullivers reiser og Niels Klims underjordiske rejse kan seiast å vere banebrytarar for sciencefictionsjangeren.
  
  h3 Språket på Holbergs tid
  img.col.col6.left-image(src='./NielsKlim_Holberg.jpg' alt='')
  p Ludvig Holberg skreiv romanen Niels Klims underjordiske rejse på latin. Det var vanleg blant lærde menn på 1700-talet. Dansk og norsk var lågstatusspråk. Ved hoffet snakka dei berre tysk og fransk, og fransk vart også brukt i skrift og tale av den norske overklassen.

  p Etter kvart tok Holberg til å gjere narr av denne pompøse stilen. Han skreiv på dansk og var dermed med på å utvikle eit moderne dansk skriftspråk. Holberg ville avgrense bruken av tyske, franske og latinske ord i det danske språket, men slike ord kunne òg gjere det danske språket rikare.

  p Holbergs Epistler (1748–1754) er skriven på dansk. Her gjer Holberg greie for meiningane sine om så mangt, frå parykkens historie til nestekjærleik. Ein av epistlane heiter «Hvorfor jeg foretrekker fruentimmer-selskab».

