  h3 Sjanger
  p Niels Klims underjordiske rejse er ein satirisk roman. Satire er kritikk blanda med humor og latterleggjering, eit sterkt våpen som ofte blir brukt for å ramme dei mektige i samfunnet.  Mange av dei fantasisamfunna som er skildra i boka, har fellestrekk med land som Holberg sjølv hadde reist i, og som han no kritiserer og driv ap med. Men kritikken er indirekte: Lesaren må sjølv tenkje seg denne samanhengen mellom forteljing og røyndom. Vi kan derfor seie at delar av romanen skal oppfattast allegorisk, som eit bilete på noko anna.

