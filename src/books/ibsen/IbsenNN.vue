  p Forfattarskapen til Ibsen kan delast i fire fasar. I førstninga skreiv Ibsen 
    em historiske drama 
    | i nasjonalromantisk ånd. Motiva var henta frå norsk historie, og handling og konfliktar går føre seg i ei fjern fortid. Eit sentralt tema i fleire av desse drama er tanken om samkjensle og nasjonalt sjølvstende, og heltane er staute nordmenn som kjempar for land og folk.

  p Men frå og med 
    em Brand 
    | (1866) skreiv Ibsen det vi kallar 
    em idédrama 
    | – tekstar der han drøftar eksistensielle emne og spørsmål. Han skreiv mellom anna om tru og tvil, om identitet og identitetsløyse. Deretter vart han ein del av det moderne gjennombrotet i norsk litteratur og samfunnsdebatt med sine realistiske samtidsdrama. Frå midten av 1880-åra ser vi at karakterane hos Ibsen blir meir samansette og nyanserte. Han gjer ein djupdykk i livsvilkåra til den moderne kvinna og mannen, og med dei såkalla symboldrama introduserer han 
    em symbolismen 
    | og 
    em modernismen 
    | i norsk dramatikk.

