    em Peer Gynt 
    | er eit av dei mest framførte stykka til Ibsen, trass i at persongalleriet er svært omfattande og handlinga går føre seg både i Noreg og i utlandet, til havs og på land, i den verkelege verda og i draumeverda. 

  p Med 
    em Peer Gynt 
    | tok Ibsen eit oppgjer med det han såg på som typisk norske karaktertrekk og norsk veremåte. Ibsen meinte at nordmenn var «seg selv nok». Peer Gynt lyg, snik seg unna problem, driv med tvilsame forretningar og sviktar menneske rundt seg. I kontrast til Peer står Solveig. Ho ventar tolmodig på kjærasten sin heime i Noreg. I henne såg Ibsen mange av dei kvalitetane han meinte det var verdt å ta vare på, og det er kjærleiken til Solveig som bergar Peer frå å gå under.

