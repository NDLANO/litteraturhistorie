  h3 Ibsens symbolisme: tidleg modernisme 
  p 
    | Frå og med 
    a(href="https://www.nb.no/items/ac782e5c8d19e74132fd2cb625ea4138?page=0&searchText=et dukkehjem" target="_blank") 
      em Vildanden
    | &nbsp;(1884) skjer det noko nytt i Ibsens forfattarskap. Han riv seg laus frå det realistiske teateret og skriv psykologiske og symbolladde drama. Vi finn att drag i tekstane som vi har sett før, for eksempel konflikten mellom ideal og røyndom og mellom løgn og sanning. Men Ibsen går no djupare inn i karakterane sine og gjer dei meir komplekse. Han nyttar dessutan i større grad symbol i tekstane sine, symbol som ikkje er uttømmande, men kan tolkast på fleire måtar.

  p I 
    em Vildanden 
    | er det mykje mystikk knytt til mørkeloftet hos familien Ekdal. På dette loftet gøymer dei ei skadeskoten villand. Denne villanda blir sjølve symbolet på den vesle familien, der alle er skadeskotne på kvar sin måte. Hjalmar Ekdal får vite at Hedvig eigentleg ikkje er dottera hans, og vender henne ryggen. Hedvig blir knust og tek til sist livet sitt.

  p I 
    em Vildanden 
    | peiker Ibsen enda ein gong på kva det kan koste å leve i sanning. I stykket står dei to personane Gregers Werle og Dr. Relling som kontrastar i synet på nettopp dette. Gregers Werle meiner ein må leve i «full sanning», og han medverkar sterkt til at Hedvigs eigentlege far blir kjend. Dr. Relling er av ei anna oppfatning. Mot slutten av skodespelet summerer han opp bodskapen sin slik: «Tar De livsløgnen fra et gjennomsnittsmenneske, tar De lykken fra ham med det samme.»

  p Andre kjende drama frå denne perioden er 
    em Fruen fra havet 
    | (1888), 
    em Hedda Gabler 
    | (1890), 
    em Byggmester Solness 
    | (1892) og 
    em Når vi døde vågner 
    | (1899). 

