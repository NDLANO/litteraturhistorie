  p 
    em Peer Gynt 
    | er eit stykke som handlar om å søkje etter eigen identitet. Peer prøver å finne ut kven han er, men i staden for ein fast kjerne og stabil personlegdom finn han fleire identitetslag. Og nettopp spørsmålet om identitet gjer stykket moderne og evig aktuelt. På nytt står Solveig som kontrast til identitetsløysa, for i kjærleiken, trua og håpet hennar er Peer eit heilt menneske.
  
  h3 Kritisk realisme og det moderne gjennombrotet 
  p Det moderne gjennombrotet og kritisk realisme er for norsk litteratur nærast synonymt med Ibsen og hans samtidsdrama: 
    em Samfundets Støtter 
    | (1877), 
    em Et Dukkehjem 
    | (1879), 
    em Gengangere 
    | (1881) og 
    em En Folkefiende 
    | (1882). I desse tekstane utfordrar Ibsen tradisjonelle verdiar og normer som han meinte var undertrykkjande og hyklerske. Tanken var at menneska – både mann og kvinne – måtte få leve i fridom og sanning. 

  h5 Et Dukkehjem 
  p 
    | I  
    a(href="https://www.nb.no/items/ac782e5c8d19e74132fd2cb625ea4138?page=0&searchText=et dukkehjem" target="_blank") 
      em Et Dukkehjem 
    | &nbsp;er det Nora som må ta eit oppgjer med sitt eige liv. Ektemannen omtaler Nora som lerkefuglen sin, han ser på henne som ei leikedokke, ikkje som eit vakse og likeverdig menneske. Men også Nora sjølv spelar rolla som lerkefugl etter beste evne, heilt til det går opp for henne at ho ikkje er meir for mannen sin enn nettopp det. Da forlèt ho mann og barn for å finne seg sjølv. 
    em Et Dukkehjem 
    | vekte sterke reaksjonar da stykket vart framført. På tyske scener nekta dei å setje opp stykket om ikkje Ibsen endra slutten. At Nora gjekk frå både mann og barn, vart for mykje å svelgje for publikum.

  p 
    em Et Dukkehjem 
    | var òg eit innlegg i samtidsdebatten om stillinga til gifte kvinner. Gifte kvinner var juridisk umyndige, det var mannen som rådde i økonomiske spørsmål. Han hadde også rett til å avgjere korleis barna skulle oppsedast, og kvar familien skulle bu. Lova stadfesta at kvinner vart rekna som store barn, ikkje som vaksne. 

