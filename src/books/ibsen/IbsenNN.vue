  p Løvborg kjem på besøk til Tesmans, og det oppstår spenningar mellom han og Hedda. Hedda er sjalu på forholdet mellom Løvborg og fru Elvsted og på samarbeidet deira om manuskriptet. Ho provoserer Løvborg til å slå følgje med Tesman og nokre andre som skal ut på byen.

  p Løvborg blir full og mister manuskriptet sitt på veg heim frå denne byturen. Han trur manuskriptet har gått tapt, men Tesman har funne det og gitt det til Hedda. Han vil at Løvborg skal få att manuskriptet sitt. Når Løvborg kjem, er han skamfull og fortvilt. For å skåne Thea Elvsted fortel han til henne og Hedda at han har rive sund manuset. Hedda røper ikkje at det er ho som har det. I staden oppmodar ho han til å ta livet sitt og «dø i skjønnhet» framfor å leve med skamma og skandalen, og ho gir han ein av dei to pistolane sine. Deretter brenner ho manuskriptet. Overfor Tesman forsvarar ho seg med at ho har gjort det for å sikre han stillinga på universitetet.

  p Kort tid seinare kjem assessor Brack med nyheita om at Løvborg er funnen død på eit horehus, med ein avfyrt pistol i lomma. Pistolen hadde gått av ved eit uhell. Men Brack har kjent att pistolen som Løvborg bar på seg, og han gjennomskodar kva Hedda har gjort. Han prøver å presse Hedda til å bli elskarinna hans. Hedda orkar ikkje tanken på at Brack skal ha makt over henne resten av livet. Ho tek den eine pistolen ho har att, og skyt seg.

  h5 Eit framandgjort og einsamt liv 
  p Hedda er ei kvinne som er oppteken av fasade og konvensjonar – og ho har høge ideal. Ho stiller store krav til seg sjølv, til menneska rundt seg og til livet generelt. Fallhøgda blir derfor stor når ho opplever at ingen av mennene rundt henne lever opp til ideala hennar.

  p Hedda finn heller ikkje noka meining i rolla som hustru i eit borgarleg fornuftsekteskap. Ho vil noko anna, noko meir, men samtidas kjønnsroller og samfunnsnormer gjer det umogleg for henne å finne ein sjølvstendig identitet og skape seg eit meiningsfullt liv. 

  p Samstundes som Hedda ønskjer seg menneskeleg nærleik, held ho alle på avstand. Ho vil gjerne få oppleve å vere til nytte, men ingen treng henne eigentleg. Ein stad i teksten seier ho at det einaste ho duger til, er å kjede seg til døde. Mangelen på meining i eige liv, kombinert med sjalusien på forholdet mellom Ejlert Løvborg og Thea Elvsted, gjer henne manipulerende og destruktiv.

  p Både i samtida og seinare har ein fokusert mykje på Ibsen som ein realistisk forfattar som sette problema under debatt. Men med 
    em Hedda Gabler 
    | og dei andre stykka frå 1890-åra peiker Ibsen i større grad fram mot eit meir modernistisk teater. Han set mellom anna søkjelyset på fleire av problemstillingane ved modernismen, som opplevinga av meiningstap og isolasjon, av framandgjering og identitetsløyse.

  h3 Ibsen i dag

  p Nest etter Shakespeare er Henrik Ibsen den mest spela dramatikaren i verda. Nora, Hedda, Peer Gynt og Hedvig blir tolka av kinesiske, brasilianske, russiske og indiske skodespelarar, for å nemne nokon. Men kvifor alt engasjementet rundt Ibsen? Kva er det som gjer at han framleis er så aktuell? 

  p For det første tok Ibsen opp tema som er allmennmenneskelege. For det andre handlar Ibsens samtidsdrama om spørsmål som framleis er aktuelle og til dels kontroversielle, trass i at handlinga i desse stykka er lagd til overklassemiljø i Kristiania på slutten av 1800-talet. Nora i 
    em Et Dukkehjem 
    | inspirerer kvinner i ulike kulturar i dag, kulturar der jamstillinga for kvinner ikkje er komen like langt som i Noreg. Gengangere handlar om tema som utruskap, kjønnssjukdommar og aktiv dødshjelp – tema som også i dag er kontroversielle. Og i Hedda Gabler tek Ibsen opp forholdet mellom kjønna og søkinga etter identitet, i tillegg til framandgjering og meiningstap. Dette er emne som aldri går ut på dato. 

