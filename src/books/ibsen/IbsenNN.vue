  p Henrik Johan Ibsen var fødd i Skien i 1828. Faren var ein rik forretningsmann, og mora kom òg frå ein velståande handelsfamilie. Nederlaget og prestisjetapet var derfor stort da faren gjekk konkurs, og familien måtte flytte ut av byen. Fleire biografar har lagt vekt på denne hendinga som ei drivkraft for Ibsen seinare i livet.

  p I 1843 flytte Ibsen til Grimstad. Her arbeidde han som apotekarlærling, samstundes som han for alvor tok til å skrive. I 1850 drog han til Kristiania for å studere ved «Heltbergs studentfabrikk». Her møtte han både Vinje, Bjørnson og Lie, og han byrja å publisere eigne tekstar.

  p I perioden 1850–1862 arbeidde Ibsen som sceneinstruktør, dramatisk forfattar og teaterleiar, først i Bergen, seinare i Oslo. I Bergen vart han kjend med SuFoto: Scanpix zannah Thoresen, som han gifte seg med i 1858. Suzannah var ei sterk og kunnskapsrik kvinne som skulle bli viktig for Ibsen. Ho kom med innspel til tekstane hans og var både inspirator og kritikar.

  p Ibsen hadde vore leiar for Det Norske Theateret i Kristiania, men i 1862 gjekk teateret konkurs, og Ibsen opplevde nokre vanskelege år både økonomisk, kunstnarleg og privat. I 1864 forlét han og kona Noreg og busette seg så i Italia og Tyskland. Fedrelandet besøkte dei berre nokre få gonger i denne utanlandsperioden, som skulle vare i 27 år. Men det var i desse åra Ibsen fekk det store kunstnarlege gjennombrotet sitt, både i Noreg og ute i Europa. 

  p Først i 1891 flytte Ibsen og kona attende til heimlandet, og dei busette seg i Arbins gate like ved Slottet i Oslo. Ibsen var no ein kjend diktar, ja ein kjendis. Han hadde ein dagleg spasertur på same tid kvar dag, frå heimen sin og ned til stamkafeen Grand Café, og mange menneske kom berre for å sjå han spasere. Ibsen døydde i 1906, etter fleire års sjukdom. Han hadde da vorte eit nasjonalt ikon og ein folkehelt, og han vart gravlagd i Æreslunden på Vår Frelsers gravlund i Oslo. 

