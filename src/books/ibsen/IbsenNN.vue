  h5 Personane rundt Hedda 
  p Far til Hedda var general, og rang og status er viktig for henne. Mora får vi ikkje vite noko om, men det er tydeleg at Hedda har vakse opp i eit maskulint miljø. Mellom anna har ho to pistolar etter far sin som ho har lært å bruke. Faren har ikkje etterlate Hedda nokon arv, og sidan ho er ei kvinne frå overklassen, er vegen til ein yrkeskarriere stengd. Ho gifter seg med Jørgen Tesman for å sikre seg den posisjonen og levestandarden ho vil ha. Planen er at Tesman skal gjere karriere på universitetet. Han er ein snill og veik mann – ein tøffelhelt, synest Hedda, som ikkje elskar mannen sin. To andre menn er òg sentrale i stykket. Den eine er Ejlert Løvborg, ein tidlegare flamme hos Hedda og kanskje den ho kjenner størst indre slektskap med. Den andre mannen er husvennen, assessor Brack.
  
  h5 Handlinga
  p Hedda og Jørgen Tesman er heime att etter ei lang bryllaupsreise. Hedda kjedar seg, for Tesman er berre oppteken av oppgåva han skriv på. Denne oppgåva kan gi Tesman ei stilling på universitetet. Men så får ekteparet Tesman vite at Ejlert Løvborg er ein sterk kandidat og konkurrent til stillinga, og Tesman blir uroleg. Løvborg har tidlegare hatt eit alkoholproblem, men dei siste åra har han budd utanbys og greidd å halde seg unna alkoholen. Han har dessutan gitt ut ei bok som har vorte godt motteken, og eit nytt manuskript er alt ferdig. I arbeidet sitt har han fått hjelp av Thea Elvsted, som er forelska i han og reiser etter han til byen. 

  figure.col.col6.right-image.mt5
   img(src='./ibsen_Hedda_Gabler2.jpg' alt='Personar i kostyme på ei scene. Scenelyset gir eit raudleg skjær. Foto')
