  p Salmene er skrevet til bruk hjemme og under gudstjenester i menigheten. De gjenspeiler høytidene i kirkeåret og den kirkelige liturgien. 
  p De fleste salmene er bygd opp etter et fast mønster som menigheten kan gjenkjenne. Formålet er å formidle et religiøst budskap. Gjennomgangsmotivet i barokksalmene er at alt skal forgå, men ved Guds nåde  kan mennesket få del i et liv etter døden. 

  p Kingos salmebok var i vanlig bruk i Norge fram til 1869. Da overtok Landstads salmebok, som inneholder Landstads egne salmer og eldre salmer som ble oversatt til norsk. 

  h3 Språk og litterære virkemidler i salmediktningen 
  p Etter reformasjonen ble latin avløst av dansk som kirkespråk i Danmark-Norge. Den første danske bibeloversettelsen kom i 1550. Dansk bibeltekst og danske salmer ble brukt i alle norske menigheter. Slik fikk det danske språket innpass hos alle nordmenn. 

  p Kingo bruker bilder med sterke kontraster – sorg og glede, lykke og ulykke, synd og nåde, himmel og jord. Språket er også fullt av velkjente klisjeer som 
