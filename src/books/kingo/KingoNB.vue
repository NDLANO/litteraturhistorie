    | . Salmene skal ikke nødvendigvis være originale, de skal først og fremst vekke følelser og få tilhørerne til å leve seg inn i budskapet. 

  p Barokkdikterne var opptatt av at en skulle gripe de jordiske gledene, men samtidig huske at det jordiske livet snart ville ta slutt. Derfor finner vi mange dødsmotiver i salmene. 

