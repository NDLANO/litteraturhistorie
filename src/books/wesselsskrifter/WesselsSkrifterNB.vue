  p Johan Herman Wessel (1742–1785) kom fra Vestby i Akershus. Han var prestesønn og gikk på latinskole i Oslo. Nitten år gammel reiste han til København for å studere videre på universitetet, men han var ingen flittig student og tok aldri noen eksamen. Derimot var han aktiv i Norske Selskab, en litterær og patriotisk klubb for nordmenn i København. Her var han i godt selskap med Johan Nordahl Brun, Johan Vibe, Jens Zetlitz og flere andre som skrev dikt og annen skjønnlitteratur. 
  
  h3 Kierlighed uden Strømper 
  p Wessel er berømt for de elegante versene og de komiske fortellingene sine. Blant annet skrev han en parodi på de franske klassisistiske tragediene: Kierlighed uden Strømper. Her følger han reglene for dramaturgi og høytidelig språkbruk som kjennetegner denne typen tragedier, men karakterene i stykket hans er enkle håndverkere, atferden deres er latterlig og konflikten helt triviell. Stykket kom på trykk i 1772 og ble oppført på Det Kongelige Theater i København i 1773. Det ble en stor suksess. 

  p Handlingen går i korte trekk ut på at heltinnen Grete får et varsel om at hun må gifte seg innen dagen er omme. Mannen hun elsker, Johan von Ehrenpreis, er skredder og har reist bort for å bøte buksene til en major. Hun vender seg derfor til den tidligere kjæresten Mads. Johan kommer tilbake, men mangler strømper og kan derfor ikke gifte seg. Han stjeler da strømpene til Mads, men Mads oppdager det og skjeller ut Johan. Deretter tar Johan livet sitt, og til slutt tar for sikkerhets skyld alle livet av seg. 
  
  h3 Andre skrifter av Wessel 
  p Wessel skrev flere teaterstykker, men ingen av dem ble noen suksess. Derimot utgav han en rekke humoristiske fortellinger på verseform som ble populære, for eksempel «Herremanden», «Hundemordet», «Mosters Pølser», «Gaffelen» og «Smeden og Bageren». Også småversene og improvisasjonene hans ble raskt allemannseie. 

  p En av grunnene til at Wessel er blitt stående i ettertid, er at han var slagferdig og humoristisk. Han deklamerte egne tekster i Norske Selskab og var en mester i å improvisere, til stor begeistring for alle. 

