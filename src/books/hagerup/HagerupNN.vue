  p Dette er den modernistiske opplevinga av meiningsløyse i etterkant av røynslene frå krigen.
  
  h3 Ein ny type barnepoesi

  figure.col.col6.right-image.mt5
   img(src='./hagerup_Barnepoesi1.jpg' alt='')
   figcaption Inger Hagerup og Jan-Magnus fekk Kyrkjedepartementet sine prisar for beste barnebøker i 1961. Foto: Arne Tjensvold / Aftenposten / Scanpix

  p Ser vi nærare på formspråket til Inger Hagerup, oppdagar vi meir enn tradisjonelle rim og taktfaste rytmar. Tydelegast finn vi det i barnedikta. Her vel Hagerup å bryte med tradisjonen, ho skaper si eiga form. Slik vart Hagerups barnedikt eit gjennombrot for ein ny type barnepoesi.

  p Da diktsamlinga Så rart kom ut i 1950, var norsk barnelitteratur prega av realisme og moralisme. Hagerup gjer snarare det motsette: Ho snur realitetane på hovudet, og i staden for å skildre pedagogisk korleis ting er, set ho ord på små, rare spørsmål som alle ein gong har grubla på. Den lyriske røysta har noko djupt uskuldig og tillitsfullt ved seg, til liks med den spontane barnlege undringa, og står i sterk kontrast til Margrethe Munthe-tradisjonen. Stiltonen i dikta varierer frå det groteske til det naivistiske. Somme av dikta kan òg kallast nonsensdikt, det vil seie tekstar utan logisk innhald, der poenget er leiken med ord, rytme og klang. 
  
  h3 Forløpar for 60-talslyrikken

  figure.col.col4.left-image.mt5
   img(src='./hagerup_Portrett2.jpg' alt='')
   figcaption Foto: Erik Thorberg / NTB / Scanpix

  p Hagerups lyrikk følgjer ikkje berre tradisjonen, men peiker framover mot det som skulle kome i 1960-åra. Dei symboltunge modernistane i femtiåra forkasta Hagerups lyrikk, men sekstitalsmodernistane hadde ei heilt ny og open haldning til han.

  p Desse sekstitalsmodernistane sokna til ei retning som vi gjerne kallar nyenkel eller konkretistisk. Dei ville bort frå symbolmetta lyrikk og ville «ikke gi kaffekjelen vinger», som ein av forfattarane i denne krinsen, Dag Solstad (1941– ), formulerte det.

  p Vi ser at Hagerups lyrikk i ein viss forstand kan lesast som ein forløpar for denne nyenkle retninga. Inger Hagerup sa sjølv at ho ikkje skreiv dikt. Ho berre sa det som det var. 

