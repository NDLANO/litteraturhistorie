  h3 Folkekjær og kontroversiell
  p Inger Johanne Hagerup (1905–1985), født Halsør, er en av våre mest folkekjære diktere gjennom tidene. Mange av hennes dikt er blitt stående som høydepunkter i vår lyrikkarv.

  p Men Hagerup var også kontroversiell – og en radikal kritiker av det bestående samfunn. I hennes kulturelle virke finner vi noe av den samme allsidigheten og utadrettetheten som hos de store folkekjære og kontroversielle poetene Henrik Wergeland og Bjørnstjerne Bjørnson. De representerer alle tre, på hvert sitt vis, en kritisk, intellektuell kunstnertype: dikteren som lever et offentlig liv, engasjert og involvert i samtidas stridstemaer. 

  p Inger Hagerup var journalist i kommunistpartiets avis Friheten, hun var litteratur- og teateranmelder i tidsskriftet Kvinnen og Tiden, hun var eneste kvinnelige medlem av Forfatterforeningen, hun tilhørte den venstreradikale Mot Dag-gruppa, og hun var redaktør i barnebladet Maurtua. 

  p Under andre verdenskrig flyktet hun til Sverige, der hun levde i en koloni av framstående norske forfattere. I sine siste leveår leste hun dikt i sympati med sultestreikende samer og holdt appell mot den sovjetiske invasjonen i Afghanistan. Som politisk menneske var hun likevel en fritenker. Inger Hagerup var aldri medlem av noe politisk parti.

  h3 Barnelitteratur og kvinneperspektiv 
  p Allsidighet preget også forfatterskapet hennes. Hun er best kjent for en lyrikk med fascinerende spennvidde, men hun skrev også noveller, hørespill, skuespill, barnebøker og tre selvbiografiske erindringsbøker. Dessuten gjendiktet og oversatte hun store forfattere som Johann Wolfgang von Goethe, William Shakespeare, Emily Dickinson og Samuel Beckett. 

  p Inger Hagerup var en viktig kvinnelig stemme i et litterært landskap dominert av menn. Hun nektet å innordne seg en tradisjonell kvinnerolle. Hun ville leve et fullt liv, kunstnerisk og erotisk, på tvers av de rådende konvensjonene for kvinner. Derfor ble hun også en viktig fornyer i norsk lyrikk. 

  h3 «Det ene diktet om krigen som vil leve om hundre år» 
  p Inger Hagerup debuterte med diktsamlingen Jeg gikk meg vill i skogene i 1939. Hun fortsatte med samlingen Flukten til Amerika i 1942, en ganske dristig tittel akkurat da. Krigen var naturlig nok ikke til stede som åpent motiv i denne samlingen, men den fantes der for den som ville se. 

  p I 1942 hadde hun også skrevet et dikt hun ikke kunne utgi, «Aust-Vågøy. Mars 1941». Dette diktet distribuerte hun til fem mennesker hun stolte på. Derfra nådde det de frie norske sendingene fra BBC, det prydet i sin helhet forsiden til en svensk avis, og det ble trykt i antologier som kom ut i Sverige. 

  p Dette lille diktet gir oss det enkleste og samtidig sterkeste uttrykk for følelsene som preget folk i Norge gjennom krigsårene. Forfatteren Sigurd Hoel (1890– 1960) sier om diktet: «Det falt i Inger Hagerups lodd å skrive det mest berømte diktet fra denne krigen – det ene diktet vi alle vet vil leve om hundre år.» 

  p Det er ikke vanskelig å skjønne hvorfor «Aust-Vågøy. Mars 1941» ble så populært. Tankeinnholdet er enkelt, følelsene er sterke og samlende. Den lyriske stemmen er et vi, noe som var typisk for krigslyrikken. Slik ble fellesskapet og samholdet understreket. I tillegg finner vi et tydelig samsvar mellom rytme og motiv. Den korthogde rytmen «hamrer med harde, vonde slag» inn verselinjene: 

  blockquote
    | De brente våre gårder. 
