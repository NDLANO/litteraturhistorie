  p Både Arnakke og Torson er mordere. Arnakke er likevel det egentlige offeret, ødelagt av et jantesamfunn og av maktmennesker. Han drives av et kjærlighetshat og preges av disharmoni og ambivalens. Torson på sin side har fortrengt sine ugjerninger. Han prøver intenst å oppklare et mord han selv har begått, men altså ikke husker. Fremmedfølelsen og det indre kaoset er her altoverskyggende. 

  p Erling Vik er også en spaltet karakter med motstridende drivkrefter, og han tviler på at han overhodet har en identitet: «Var det en kalte personlighet, kanskje ikke så fast forankret som en gjerne ville tro? […] at en levde på en illusjon, en svindel. […] Han kunne kjenne det slik at han slett ikke var, aldri hadde vært til, som annet enn skalkeskjul for et skalkeskjul, et speilkabinett av skalkeskjul. […] Et blendverk, en uvirkelighet holdt sammen av en annen uvirkelighet.» 
  
  h3 «Sannheten er skrevet i vann» 
  p Det eksisterer mange historier og anekdoter om Sandemose, både som forfatter og privatperson. En av de mest utbredte, og mest usanne, er at han skal ha uttalt at mord og kjærlighet er det eneste det er verdt å skrive om. Dette er en replikk hentet fra karakteren Gunder Gundersen i Det svundne er en drøm. Selv kalte Sandemose denne historien for sitatfusk. 

  p Skulle vi funnet et sitat som fanger noe bærende i forfatterskapet, kunne det kanskje heller være dette: «Verden blir en annen mens du blunker. […] Sannheten er skrevet i vann» (fra forordet i En flyktning krysser sitt spor, andre utgave 1955). Enhver erkjennelse er altså flyktig og upålitelig, det er umulig å komme fram til en sannhet om virkeligheten eller seg selv. 

  p I forfatterskapet til Sandemose kommer dette synet til uttrykk på flere nivåer. For det første er synsvinkelen i de sentrale romanene personal og fortelleren upålitelig. Fortellingen formidler derfor ingen allmenngyldige sannheter. 

  p For det andre finner vi innslag av metafiksjon i tekstene, det vil si forskjellige grep som tydeliggjør at romanverdenen er fiksjon. Tekstene kan for eksempel kommentere seg selv, eller de kan diskutere i hvilken grad språket er i stand til å bringe fram eller formidle erkjennelser. 

  p I En flyktning krysser sitt spor og Det svundne er en drøm prøver fortellerne å oppnå livsviktig erkjennelse ved å rekonstruere sine liv gjennom skrift eller fortelling. Skrivingen er derfor en strategi for å forstå og gjenvinne kontroll over livet.  

  p I Varulven består metafiksjonen av en lek med andre litterære sjangrer. I romanen er det vevd inn en mordgåte, men den forblir uløst. I pakt med tanken om at sann erkjennelse om verden er umulig, ender mange av romanene åpent og gåtefullt. Vi får ingen svar. 

  p Mange av Sandemoses romaner er dessuten ikke romaner i tradisjonell forstand. Sandemose selv likte ikke å kalle tekstene sine romaner. Han framstiller først og fremst et indre sjeleliv av kaotiske og selvmotsigende krefter, ikke en forståelig virkelighet med et logisk ytre hendingsforløp. 

  p Slik er han en videreutvikler av James Joyces bevissthetsstrøm-teknikk («stream of consciousness»). Han fornyer denne teknikken ved å skape en hybrid (blanding) mellom romanen og essayet, sakprosaens subjektive og åpne sjanger. I sine beste romaner kombinerer han et ytre hendingsforløp med lange essayistiske partier. 

  p Både upålitelige fortellere, løst komponerte romaner med lengre essayistiske partier og tekster uten svar ble av samtidas litteraturforskere vurdert som svakheter ved Sandemoses forfatterskap. I dag tenker vi i stedet at dette forklarer hvorfor Sandemose som en av våre få nyere diktere har fått en plass i verdenslitteraturen. 

