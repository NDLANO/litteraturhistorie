  h3 Litt meir om Snorre Sturluson
  p Snorre kom frå ei mektig islandsk ætt og levde i første halvdel av 1200-talet. Snorre var høvding og vart den rikaste mannen på Island da han gifte seg med ei velståande enkje.  Snorre var svært ambisiøs og prøvde å bli den norske kongens representant på Island. For å nå dette målet søkte han støtte hos andre mektige menn på Island. Men planane hans slo feil, og Snorre vart drepen på garden sin av ein leigemordar, utsend av kong Håkon Håkonsson i 1241.

  h3 «Soga om Olav den heilage»
  p «Soga om Olav den heilage» er henta frå Snorres kongesoger. Ho fyller om lag ein tredel av sogene og fortel om Olav Haraldssons bragder i ungdommen, om kampen hans for å bli konge og for å innføre kristendommen i landet. 

