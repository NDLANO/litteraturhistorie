  h3 Jord og jern (1933)

  figure.col.col6.right-image.mt5
   img(src='./jacobsen_Jord_og_jern1.jpg' alt='')
   figcaption Foto: Scanpix
  p Debutsamlinga til Jacobsen, Jord og jern, har to delar. Den første delen omfattar fleire utradisjonelle naturdikt, mens dikta i andre delen handlar om den moderne bykulturen. Underjordiske kloakksystem, telefonleidningar, kraftstasjonar, raske bilar og fly er like naturlege motiv for Jacobsen som skog, fjell, vatn, fuglar og måneskin.

  p I desse dikta står ikkje natur og moderne sivilisasjon i direkte motsetning til kvarandre, det tyder også tittelen Jord og jern på. Begge verdene finst, dei eksisterer ved sida av kvarandre, og menneska høyrer heime begge stader.

  p Somme dikt i Jord og jern formidlar ein sterk fascinasjon for den teknologiske utviklinga, men stort sett er Jacobsen meir tvisynt i skildringa av den moderne sivilisasjonen: Trass i at teknologien kan begeistre og gjere livet meir makeleg for oss, gir han ikkje noko svar på dei eksistensielle spørsmåla våre. Framsteget gjer oss heller ikkje meir lykkelege.

  h3 Grøn poesi

  figure.col.col6.left-image.mt5
   img(src='./jacobsen_Groenn_Poesi1.jpg' alt='')
   figcaption Foto: Jon Hauge / Aftenposten / Scanpix

  p Det var først i byrjinga av 1960-åra at miljøproblema tok til å bli allment kjende og problematiserte, men Jacobsen var tidleg ute med å varsle om skuggesidene ved den teknologiske utviklinga. Diktet «Landskap med gravemaskiner» frå 1954 blir rekna som det første «grøne» diktet i Noreg.

  p I Jord og jern teiknar Jacobsen stort sett eit positivt bilete av den teknologiske utviklinga. Utviklinga står ikkje i kontrast til naturen, ho kjem i tillegg. Men i diktet «Landskap med gravemaskiner» skildrar Jacobsen ein natur som er truga av teknologien. Det er naturen som betaler prisen for framsteget.

  p Både natur og maskinar er menneskeleggjorde i «Landskap med gravemaskiner», og det er naturen sjølv som observerer og reflekterer over det som skjer. Maskinane er framstilte som menneskelege slavar: «Maskinene også er ofre, med blindede øyne og lenker om føttene.» Det er menneska som styrer maskinane, og det er menneska som er ansvarlege for den trugande utviklinga.

  p Men dette blir ikkje sagt direkte i diktet. Menneska er faktisk ikkje nemnde i det heile, dei er usynlege bak maskinane sine. Slik kan lesaren få inntrykk av at den øydeleggjande utviklinga har sin eigen dynamikk, uavhengig av alt og alle. Ho kan ikkje stansast. 

  h3 Politisk engasjement og samfunnskritikk
  p Jacobsen såg også tidleg faresignala ved medie- og forbrukssamfunnet som utvikla seg i etterkrigsåra. Påstanden hans var at kommersialiseringa av samfunnet førte folk bort frå kvarandre og bort frå seg sjølve.

