  h3 Store kontrastar og stille samtalar
  p Gjennom heile forfattarskapen var Jacobsen oppteken av motsetninga – og samanhengen – mellom natur og kultur. Men haldninga hans endra seg over tid. I diktsamlingane han gav ut før krigen, ser vi innimellom stor fascinasjon for teknologi og maskinar. I samlinga Fjerntog, som kom ut i 1951, fokuserer han meir på naturen enn kulturen, og mot slutten av forfattarskapen er han meir kulturkritisk – her tematiserer han øydeleggingane som maskinar og sivilisasjon påfører naturen.

  p Ein annen sentral tematikk i Jacobsens forfattarskap er kontrasten mellom samtale og togn. I dikta sine fører han ofte filosofiske samtalar med både fysiske og abstrakte gjenstandar. Han samtaler med jorda, med bussen, med antenner og gravemaskinar. Men samstundes etterlyser Jacobsen det tagale, og det er to typar stille han skriv om. På den eine sida har vi den gode stilla der vi lyttar til naturen rundt oss, stilla der vi høyrer at «myrstrå vipper». Og så er det den vonde stilla. Det er den stilla det moderne samfunnet har ført med seg. I mylderet av alt og alle som snakkar til oss, har vi menneske mist evna til å snakke med kvarandre.

  p Det finst også andre tematiske perspektiv hos Jacobsen. Han har skrive fleire såkalla metadikt – tekstar som tematiserer dikt og dikting. Jacobsen har også gitt ut fleire reisetekstar og dikt om kjærleik, samliv og sorg. I den siste utgivinga hans, Nattåpent frå 1985, skriv han mykje om kjærleiken til den avlidne kona si. I desse sorgdikta minnest og lengtar han tilbake til ei tid som ikkje lenger eksisterer.

