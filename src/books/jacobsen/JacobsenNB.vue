  h3 Bruken av kontraster og bilder 

  figure.col.col6.right-image.mt5
   img(src='./jacobsen_Bruken_av_kontraster.jpg' alt='En gravemaskin på et veianlegg i fjellandskap. Foto')
   figcaption Foto: Helge Sunde / Samfoto / Scanpix

  p Jacobsen bruker ofte kontraster i diktene sine – det er natur mot kultur, jord mot jern. Men de poetiske bildene bygger bru over motsetningene og skaper sammenheng. Når Jacobsen for eksempel sammenlikner undergrunnsbanen med en meitemark som åler seg rundt under jorda, setter han sammen natur og kultur – han viser oss at både mennesker, dyr og maskiner er del av den samme virkeligheten, den samme verdenen. 

  p Metaforene og symbolene Jacobsen velger, er enkle, tydelige og hverdagslige. Mange av tekstene hans er dessuten komponert på en måte som minner om en fortrolig samtale. Det er denne åpne, inkluderende og likeframme stilen som har gjort Jacobsen til en så folkekjær lyriker. 

  h3 Store kontraster og stille samtaler 

  p Gjennom hele forfatterskapet var Jacobsen opptatt av motsetningen – og sammenhengen – mellom natur og kultur. Men holdningen hans endret seg over tid. I diktsamlingene han gav ut før krigen, ser vi innimellom stor fascinasjon for teknologi og maskiner. I samlingen 
    em Fjerntog
    | , som kom ut i 1951, fokuserer han mer på naturen enn kulturen, og mot slutten av forfatterskapet er han mer kulturkritisk – her tematiserer han ødeleggelsene som maskiner og sivilisasjon påfører naturen. 

  p En annen sentral tematikk i Jacobsens forfatterskap er kontrasten mellom 
    em samtale 
    | og 
    em taushet
    | . I diktene sine fører han ofte filosofiske samtaler med både fysiske og abstrakte gjenstander. Han samtaler med jorda, med bussen, med antenner og gravemaskiner. Men samtidig etterlyser Jacobsen taushet, og det er to typer taushet han skriver om. På den ene siden har vi den gode tausheten der vi lytter til naturen rundt oss, tausheten der vi hører at «myrstrå vipper». Og så finnes den vonde tausheten. Dette er tausheten det moderne samfunnet har ført med seg. I mylderet av alt og alle som snakker til oss, har vi mennesker mistet evnen til å snakke 
    me med 
    | hverandre. 

  p Det finnes også andre tematiske perspektiv hos Jacobsen. Han har skrevet flere såkalte metadikt – tekster som tematiserer dikt og diktning. Jacobsen har også utgitt flere reisetekster og dikt om kjærlighet, samliv og sorg. I den siste utgivelsen hans, 
    em Nattåpent 
    | fra 1985, skriver han mye om kjærligheten til sin avdøde kone. I disse sorgdiktene erindrer og lengter han tilbake til en tid som ikke lenger eksisterer.   


