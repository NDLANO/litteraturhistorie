  h3 Grønn poesi

  figure.col.col6.left-image.mt5
   img(src='./jacobsen_Groenn_Poesi1.jpg' alt='Eldre mann holder i et tre. I bakgrunnen en gravemaskin. Foto')
   figcaption Foto: Jon Hauge / Aftenposten / Scanpix
   
  p Det var først i begynnelsen av 1960-årene at miljøproblemer begynte å bli allment kjent og problematisert, men Jacobsen var tidlig ute med å varsle om skyggesidene ved den teknologiske utviklingen. Diktet «Landskap med gravemaskiner» fra 1954 regnes som Norges første «grønne» dikt. 

  p I 
    em Jord og jern 
    | tegner Jacobsen stort sett et positivt bilde av den teknologiske utviklingen. Den framstår ikke som en kontrast til naturen, den kommer i tillegg. Men i diktet «Landskap med gravemaskiner» skildres en natur som er truet av teknologien. Det er naturen som betaler prisen for framskrittet. 

  p Både natur og maskiner er menneskeliggjort i «Landskap med gravemaskiner», og det er naturen selv som observerer og reflekterer over det som skjer. Maskinene framstilles som menneskenes slaver: «Maskinene også er ofre, med blindede øyne og lenker om føttene.» Det er mennesker som styrer maskinene, og det er menneskene som er ansvarlige for den truende utviklingen. 

  p Men dette sies ikke direkte i diktet. Menneskene nevnes faktisk ikke i det hele tatt, de er usynlige bak maskinene sine. Slik kan leseren få inntrykk av at den ødeleggende utviklingen har sin egen dynamikk, uavhengig av alt og alle. Den kan ikke stoppes. 

