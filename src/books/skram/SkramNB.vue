  h3 Et vanskelig liv 

  img.col.col6.left-image.mt5(src='./Skram_Et_vanskelig_liv.jpg' alt='Kvinne og mann sitter på en sofa i et interiør fra 1800-tallet. Kvinnen holder begge hender på mannens kne. Illustrasjon')
  p Amalie Skram ble født inn i en handelsfamilie fra Bergen. Atten år gammel ble hun giftet bort til en sjøkaptein som var ni år eldre enn henne. Ekteskapet var vanskelig, og i 1877 fikk Amalie mentale problemer og ble innlagt på Gaustad sykehus. Hun flyttet aldri tilbake til mannen sin, og de to ble skilt noen år seinere. 

  p Fra 1881 bodde hun hos broren Wilhelm i Kristiania. Her fikk hun kontakt med forfattere som Bjørnstjerne Bjørnson og Arne Garborg, og hun ble inspirert til å skrive. På samme tid ble hun kjent med den danske forfatteren Erik Skram. De giftet seg i 1884 og flyttet til København, der Amalie begynte å skrive for alvor. 

  p Amalie Skram hadde et vanskelig liv. Hun slet med dårlig mental helse og var flere ganger pasient ved psykiatriske sykehus, både i Kristiania og i København. Disse oppholdene har hun skildret med kritikk og harme i to romaner med selvbiografisk form, 
    em Professor Hieronimus 
    | og 
    em Paa Sct. Jørgen 
    | (begge fra 1895). Etter det siste oppholdet på psykiatrisk sykehus tok ekteskapet med Erik Skram slutt. Fra 1901 fikk hun diktergasje av den danske staten og bodde de siste årene i København med dattera. 



