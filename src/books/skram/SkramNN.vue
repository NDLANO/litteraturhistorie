  h3 Eit vanskeleg liv

  img.col.col6.left-image.mt5(src='./Skram_Et_vanskelig_liv.jpg' alt='')
  p Amalie Skram var fødd inn i ein handelsfamilie frå Bergen. Atten år gammal vart ho gift bort til ein sjøkaptein som var ni år eldre enn ho. Ekteskapet var vanskeleg, og i 1877 fekk Amalie mentale problem og vart innlagd på Gaustad sjukehus. Ho flytte aldri tilbake til mannen sin, og dei to vart skilde nokre år seinare.

  p Frå 1881 budde ho hos broren Wilhelm i Kristiania. Her fekk ho kontakt med forfattarar som Bjørnstjerne Bjørnson og Arne Garborg, og ho vart inspirert til å skrive. På same tid vart ho kjend med den danske forfattaren Erik Skram. Dei gifte seg i 1884 og flytte til København, der Amalie tok til å skrive for alvor.

  p Amalie Skram hadde eit vanskeleg liv. Ho sleit med dårleg mental helse og var fleire gonger pasient ved psykiatriske sjukehus, både i Kristiania og i København. Desse opphalda har ho skildra med kritikk og harme i to romanar med sjølvbiografisk form, Professor Hieronimus og Paa Sct. Jørgen (begge frå 1895). Etter det siste opphaldet på psykiatrisk sjukehus tok ekteskapet med Erik Skram slutt. Frå 1901 fekk ho diktargasje av den danske staten og budde dei siste åra i København med dotter si.   

