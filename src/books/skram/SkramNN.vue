  p Den fjerde og siste ekteskapsromanen var Forraadt frå 1892. Her møter vi den sytten år gamle Ory som blir gift bort til ein sjøkaptein som er femten år eldre. Uvitande om det meste som har med ekteskap og samliv å gjere, blir Ory plassert om bord på ei seglskute med ektemann og mannskap på veg til England. Det endar tragisk også her. 

