  h3 Ekteskap som tema
  p Amalie Skram skildrar det borgarlege miljøet frå siste del av 1800-talet i romanane sine. Ho ser på det borgarlege ekteskapet som ein handel med seksualiteten til unge jenter. Mangelen på både kjærleik og personleg val gjer at ekteskapet i beste fall blir ein måte å livberge seg på, i verste fall eit mareritt. 

