  p 
    em Det norske Folkesprogs Grammatikk 
    | er Aasens første store vitenskapelige verk. Med denne boka får han sitt offentlige gjennombrudd som språkforsker. Grammatikken forklarer lydverk, bøyning, ordlaging og syntaks i de forskjellige norske dialektene. 

  p To år seinere, i 1850, utgir Aasen 
    em Ordbog over det norske Folkesprog
    | . Boka har ca. 25 000 oppslagsord og er den første ordboka over norsk talemål som bygger på systematisk innsamlet materiale.

  p Ordbøker og grammatikker er gjerne 
    em normative 
    | (forklarende), det vil si at de forteller hva som er rett stavemåte, bøyning, setningsbygning osv. i et skriftspråk. De to bøkene til Aasen er derimot først og fremst 
    em deskriptive 
    | (beskrivende). De forteller hva folk faktisk sier. I Aasens ordbok blir hvert ord først forklart på dansk. Dersom ordet har forskjellige former i ulike dialekter, står alle formene oppført etter forklaringen. 

  p For Aasen er grammatikken og ordboka et skritt på veien mot et nytt skriftspråk. Med utgangspunkt i de to bøkene begynner han arbeidet med å skape et 
    em landsmål 
    | – et skriftspråk for hele landet som bygger på de norske dialektene. 

  h3 Landsmålet 
  p Etter 1850 konsentrerer Aasen seg om utformingen av landsmålet. Han arbeider blant annet etter disse prinsippene: 

  ul
    li Fellestrekk skal komme fram: Skriftspråket skal synliggjøre det felles systemet i dialektene så langt som mulig. 
    li Gammelt framfor nytt: Dersom det finnes flere former, velger han den formen som ligger nærmest norrønt. Han bruker også den norrøne formen som rettesnor når han skal lage en skriftform av et ord der ingen av dialektformene er noe  opplagt valg. 
    li Norsk framfor utenlandsk: Aasen er purist, det vil si språkrenser. Han vil ha et språk med færrest mulig utenlandske innslag. Blant annet avviser han de såkalte an-be-het-else-ordene. Dette er ord med bestemte for- og endestavinger som vi har lånt fra nedertysk, for eksempel ansette, beskrive, kjærlighet, overraskelse. 
    li Fast norm: Aasen vil ha en fast norm for landsmålet sitt. Han mener at alle må gi avkall på noen av dialektformene sine for å få til en klar skriftspråknorm. 

  p I 1853 lanserer Aasen et forslag til ny skriftspråknormal i boka 
    em Prøver av Landsmaalet i Norge
    | . Seinere omarbeider han både grammatikken og ordboka. I 
    em Norsk Grammatik
    | , som kommer ut i 1864, har han tatt med sitt eget utkast til skriftspråknormal og redegjør grundig for valgene sine. Den nye versjonen er dermed mer normativ enn deskriptiv. 

  p Det vi i dag kjenner som nynorsk, er en videreføring av landsmålet til Ivar Aasen. På samme måte som bokmål har også nynorsk endret seg en del på 150 år. 

  h3 Aasen som dikter 
  p Ivar Aasen er også en dyktig dikter. Med diktene og skuespillene sine er han med på å legge grunnlaget for den nynorske skjønnlitteraturen. 

  p De mest kjente diktene hans står i samlingen 
    em Symra 
    | (1863), blant annet «Gamle Norig». Det er et hyllingsdikt til fedrelandet, der Aasen skildrer den norske naturen med hav, fjord, fjell, fosser og daler. 

  p Han framhever at Norge er landet som nordmennene har arvet etter forfedrene sine, og han hedrer den norske bondestanden. Bøndene er «born av deim som bygde landet», de er bindeleddet mellom fortid og nåtid i Norge.

  p Alt dette er i utgangspunktet typiske nasjonalromantiske tanker. Men Aasen kjenner til bondelivet han skriver om, han underslår ikke at det også er ­slitsomt: Bonden er «van til møda meir enn til kjæla». 

  p «Millom Bakkar og Berg» (også kalt «Nordmanden»), «De gamle fjelli» og «Gamle grendi» er tre andre kjente dikt der det samme synet på Norge og det norske kommer til uttrykk. Diktene er enkle og folkelige og en hyllest til det norske bondesamfunnet og norsk natur. 

  p Men Aasen skriver også dikt om helt andre emner. Diktet «Saknad» handler om noe helt personlig, om lengselen etter nærhet og menneskelig fellesskap. Tredje og siste strofe i diktet lyder slik: 

