  p Ivar Aasen (1813–1896) var fra ­Ørsta på Sunnmøre. Han vokste opp på et småbruk og hadde dermed en helt annen sosial bakgrunn enn de fleste andre framtredende kulturfolk på 1800-tallet. 

  p Som ung gutt viser Aasen uvanlig gode evner, og allerede i 18-årsalderen blir han omgangsskolelærer i heimbygda. ­Seinere får han tilbud om universitetsutdanning, men velger i stedet å studere på egen hånd. Aasen opplever det som sårende at samfunnseliten ser ned på bondekulturen og bondespråket. Han vil ikke glemme hvor han kommer fra, men  heller arbeide for å styrke bondestandens kulturelle og språklige selvtillit. 

  p Som 22-åring tar han seg post som huslærer og bruker fritida til selvstudium. Han skaffer seg blant annet kunnskaper i tysk, latin og norrønt på egen hånd. Dessuten interesserer han seg for botanikk. Han er en systematiker som liker å granske og kategorisere både planter og språk. I de sju årene han arbeider som huslærer, lager han blant annet en omfattende plantesamling og en grammatikk over sunnmørsmålet. 

  h3 Aasen og språkdebatten i 1830-årene 
  p Det offisielle skriftspråket på 1800- tallet er fortsatt dansk, og embetsstanden snakker et forfinet dansk-norsk. Folk ellers snakker norske dialekter omtrent som de gjør i dag. Avstanden mellom skrift og tale er stor for de aller fleste. 

  p Språkdebatten i 1830-årene handler om hvordan en kan skape et nasjonalt skriftspråk i Norge. Henrik Wergeland går inn for en gradvis fornorsking av det danske skriftspråket, mens Johan Sebastian Welhaven mener at en foreløpig må holde fast ved dansk språk og kultur. 

  p Historikeren og språkforskeren Peter Andreas Munch deler Welhavens syn. Skal skriftspråket endres, kan det ikke gjøres stykkevis og delt slik Wergeland foreslår, mener han. Hele språksystemet må endres. 

  p Munch vil dessuten ha et skriftspråk basert på de dialektene som best ivaretar det norrøne systemet. Men han tenker seg at et slikt språk skal være et vitenskapelig studieobjekt. Han kan ikke se for seg noe reelt alternativ til dansk skriftspråk med det første. 

  p Ivar Aasen deltar ikke i den offentlige språkdebatten i 1830-årene, men i den upubliserte artikkelen «Om vort Skriftsprog» (1836) skisserer han sin egen plan for et norsk skriftspråk. Han avviser fornorskingstanken til Wergeland og ønsker seg et helt nytt skriftspråk som bygger på de norske bygdedialektene. Men Aasen vil ikke skape noe museumsspråk. Han ønsker seg et skriftspråk som setter hele folket i stand til å delta i samfunnslivet. 

