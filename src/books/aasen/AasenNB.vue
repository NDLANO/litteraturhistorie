  h3 Gamle former i Aasens landsmål
  p Aasen bruker det norrøne språket som rettesnor når han velger ut ordformene som skal være en del av landsmålet. Her følger noen eksempler:

  p I Aasens dikt finner vi former som 
    em (i) grendom, lidom, dalom, syningom, bygdom, (aat) endom
    | . Dette er gamle flertallsformer i dativ, og Aasen bruker dem fordi de har rot i norrønt. Norrøne substantiv fekk nemlig endingen -um i dativ flertall.

  p På Aasens tid er det fortsatt mange bygdedialekter som har rester av den norrøne kasusbøyningen. I dag finner vi dativformer bare i noen få dialekter, for eksempel i Gudbrandsdalen og på Ytre Sunnmøre. 

  p Substantivet visa har flertallsformen visur eller visor i norrønt. Begge formene er i bruk i enkelte dialekter, og Aasen velger formen visor. Her tar han hensyn til svensk, der flertallsformen er visor. Han vil ikke gjøre landsmålet mer ulikt svensk og dansk enn nødvendig. 

