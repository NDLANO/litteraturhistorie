  h3 Det norske Folkesprogs Grammatik (1848)
  p Det norske Folkesprogs Grammatik er det første store vitskaplege verket til Aasen. Med denne boka får han sitt offentlege gjennombrot som språkforskar. Grammatikken forklarer lydverk, bøying, ordlaging og syntaks i dei ulike norske dialektane.

  p To år seinare, i 1850, gir Aasen ut Ordbog over det norske Folkesprog. Boka har rundt 25 000 oppslagsord og er den første ordboka over norsk talemål som byggjer på systematisk innsamla materiale.

  p Ordbøker og grammatikkar er oftast normative (føreskrivande), det vil seie at dei fortel kva som er rett stavemåte, bøying, setningsbygnad osv. i eit skriftspråk. Dei to bøkene til Aasen er derimot først og fremst deskriptive (forklarande). Dei fortel kva folk faktisk seier. I ordboka til Aasen blir kvart ord først forklart på dansk. Dersom ordet har ulike former i ulike dialektar, står alle desse formene oppførte etter forklaringa.

  p For Aasen er grammatikken og ordboka eit steg på vegen mot eit nytt skriftspråk. Med utgangspunkt i dei to bøkene tek han fatt på arbeidet med å skape eit landsmål – eit skriftspråk for heile landet som byggjer på dei norske dialektane.

  h3 Landsmålet
  p Etter 1850 konsentrerer Aasen seg om utforminga av landsmålet. Han arbeider mellom anna etter desse prinsippa:

  ul
    li Fellestrekka skal kome fram: Skriftspråket skal synleggjere det felles systemet i dialektane så langt råd er.
    li Gammalt framfor nytt: Dersom det finst fleire former, vel han den forma som ligg nærast norrønt. Han bruker også den norrøne forma som rettesnor når han skal lage ei skriftform av eit ord der ingen av dialektformene er noko opplagt val.
    li Norsk framfor utanlandsk: Aasen er purist, det vil seie språkreinsar. Han vil ha eit språk med færrast mogleg utanlandske innslag. Mellom anna avviser han dei såkalla an-be-het-else-orda. Dette er ord med bestemte for- og endestavingar som vi har lånt frå lågtysk, for eksempel ansette, beskrive, kjærlighet, overraskelse.
    li Fast norm: Aasen vil ha ei fast norm for landsmålet sitt. Han synest at alle må gi avkall på nokre av dialektformene sine for å få til ei klar skriftspråknorm.

  p I 1853 lanserer Aasen eit forslag til ny skriftspråknormal i boka Prøver av Landsmaalet i Norge. Seinare omarbeider han både grammatikken og ordboka. I Norsk Grammatik, som kjem ut i 1864, har han teke med sitt eige utkast til skriftspråknormal og gjer grundig greie for vala sine. Den omarbeidde versjonen er dermed meir normativ enn deskriptiv.

  p Det vi i dag kjenner som nynorsk, er ei vidareføring av landsmålet til Ivar Aasen. På same måten som bokmål har også nynorsk endra seg ein del på 150 år.

  h3 Aasen som diktar
  p Ivar Aasen er også ein dugande diktar. Med dikta og skodespela sine er han med på å leggje grunnlaget for den nynorske skjønnlitteraturen.

  p Dei mest kjende dikta hans står i samlinga Symra (1863), mellom anna «Gamle Norig». Det er eit hyllingsdikt til fedrelandet, der Aasen skildrar den norske naturen med hav, fjord, fjell, fossar og dalar.

  p Han framhevar at Noreg er landet som nordmennene har arva etter forfedrane sine, og han heidrar den norske bondestanden. Bøndene er «born av deim som bygde landet», dei er bindeleddet mellom fortid og notid i Noreg.

  p Alt dette er i utgangspunktet typiske nasjonalromantiske tankar. Men Aasen kjenner til bondelivet han skriv om, han underslår ikkje at det også er strevsamt: Bonden er «van til møda meir enn til kjæla».

  p «Millom Bakkar og Berg» (også kalla «Nordmanden»), «Dei gamle fjelli» og «Gamle grendi» er tre andre kjende dikt der det same synet på Noreg og det norske kjem til uttrykk. Dikta er enkle og folkelege og ei hylling til det norske bondesamfunnet og norsk natur.

  p Men Aasen skriv også dikt om heilt andre emne. Diktet «Saknad» handlar om noko heilt personleg, om lengten etter nærleik og menneskeleg fellesskap. Tredje og siste strofe i diktet lyder slik:

