  p Aasen bruker det norrøne språket som rettesnor når han vel ut ordformene som skal vere ein del av landsmålet. Her følgjer nokre eksempel:

  p I dikta til Aasen finn vi former som (i) grendom, lidom, dalom, syningom, bygdom, (aat) endom. Dette er gamle fleirtalsformer i dativ, og Aasen bruker dei fordi dei har rot i norrønt. Norrøne substantiv fekk nemleg endinga -um i dativ fleirtal.

  p På Aasens tid er det framleis mange bygdedialektar som har restar av den norrøne kasusbøyinga. I dag finn vi dativformer berre i nokre få dialektar, for eksempel i Gudbrandsdalen og på Ytre Sunnmøre.

  p Substantivet visa har fleirtalsforma visur eller visor i norrønt. Begge formene er i bruk i somme dialektar, og Aasen vel forma visor. Her tek han omsyn til svensk, der fleirtalsforma er visor. Han vil ikkje gjere landsmålet meir ulikt svensk og dansk enn nødvendig.

  p Norrøne inkjekjønnsord hadde endinga -in i bunden form fleirtal. Orda landa og fjella heitte derfor  lǫndin og fjellin på norrønt. Men allereie på Aasens tid er den vanlegaste forma i dialektane landa og fjella. Aasen vel likevel londi og fjelli fordi i-formene ligg nærast norrønt. Desse i-formene finst i dag i dialektane i Indre Sogn, vest i Telemark og øvst i Setesdal.

