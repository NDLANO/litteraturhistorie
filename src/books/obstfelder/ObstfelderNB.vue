  h3 Nyskapning og sjangerblanding 

  img.col.col6.right-image.mt5(src='./Obstfelder_Melodier.jpg' alt='Bokforside med tittelen Melodier. Blomstermotiv sentralt på siden. Foto')
  p Da Obstfelder gav ut samlingen 
    em Digte 
    | i 1893, fikk den blandet mottakelse. Mens Dagbladets anmelder skrev at «denne lille bog blir årets største begivenhed i Norge», mente journalisten i Aftenposten at disse diktene bare var «nonsens». Den blandete mottakelsen vitner om at folk var usikre på hvordan de skulle møte Obstfelders lyrikk. Han brøt nemlig med det de var vant til å lese. Var dette bruddet spennende nyskapning, eller var det bare dårlig lyrikk? 

  p I dag ser vi tydelig at Obstfelder var en nyskaper innenfor den tradisjonelle lyrikken. Som andre modernister brøt han med tradisjonelle strofeformer og valgte friere rimmønstre. I stedet for enderim tok han i bruk bokstavrim, assonanser, lydhermende ord og varierende verselengde for å skape klanger og rytme i diktene sine. Denne omfattende bruken av musikalske virkemidler viser at Obstfelder var svært interessert i musikk. 

  p Et annet typisk trekk ved mange av Obstfelders dikt er utrop, spørsmål, pauser, avbrutte setninger og andre virkemidler som gjør at leseren identifiserer seg med jeg-personens ufiltrerte sansing og tanker. Obstfelders skrivemåte viser at formen er viktig for tolkingen av teksten – enten forfatteren støtter seg til kjente rimmønstre eller ikke. 

