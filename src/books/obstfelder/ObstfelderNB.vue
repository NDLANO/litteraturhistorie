  p Vi kan lese «Byen» som et uttrykk for den modernistiske livsfølelsen. På 1800-tallet flyttet mange mennesker inn til byene, men de følte seg ofte hjemløse og savnet de nære sosiale båndene i bondesamfunnet. Jeg-personen i Obstfelders tekst mener han forstår hvorfor bymennesker alltid har hast, de «piskes af sin egen skygge». Dette kan vi tolke som at de innerst inne er misfornøyde med hvem de er og hva de gjør, og at de haster slik for å slippe å tenke på dette. «Byen» er en subjektiv og sterk observasjon av hva urbaniseringen gjør med menneskene og forholdet mellom dem. 

  p I «Byen» finner vi også en kontrast mellom livet på fjellet og livet i byen, i alle fall på overflata. Jeg-personen i teksten opplever slett ikke det guddommelige nærværet i naturen som de romantiske dikterne elsket å skildre, tvert imot. Fjellet har ikke noe hjerte og maner fram angst hos jeg-personen. Derfor setter han sin lit til at byen kan være stedet han føler seg hjemme i. Men også byen skaper bare angst og fremmedgjøring. Jeg-personen blir hjemløs og ensom overalt i verden, han finner ikke ro noe sted.

