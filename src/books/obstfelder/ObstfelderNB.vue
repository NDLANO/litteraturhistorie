  p Sigbjørn Obstfelder (1866–1900) regnes for den første norske modernistiske lyrikeren. Få har skildret ensomheten og fremmedgjøringen til det moderne mennesket bedre enn han i tekster som «Byen» og «Jeg ser». 

  p Som ung reiste Obstfelder til USA og arbeidet som ingeniør. Her så han den moderne verden vokse fram – bylivet og store byggkonstruksjoner gjorde inntrykk, men han fant ikke ro og mistrivdes. Da han kom hjem til Norge, fikk han nervesammenbrudd og ble en kort stund liggende på sykehus. 

  p Obstfelder var stadig på flyttefot. Som en moderne nomade reiste han fra by til by. Han følte seg fremmed i de europeiske storbyene og lengtet hjem. Men hjemstedet gav han heller ikke ro i sjela. Han var rastløs og grublende og så på seg selv som et barn av sin tid, «som lever et nerveliv; anskuelser og livssyn er ikke for meg en hjernesag, de er også en rygmarvssag, en blodåresag». I Kristiania søkte han selskap med andre likesinnete i bohemog kunstnerkretser. Kunstnervenner som Edvard Munch uttrykte også en eksistensiell angst og lengsel i kunsten sin.  

