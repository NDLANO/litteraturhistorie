  p I 
    em Ute av verden 
    | (1998) møter vi for første gong Henrik Vankel. I ein alder av 26 år reiser han frå heimtraktene på Sørlandet til eit lite lokalsamfunn i Nord-Noreg. Her skal han arbeide som lærarvikar mens han freistar å realisere draumen om å bli forfattar. Romanen tek ei drastisk vending når Henrik Vankel forelskar seg i ein av elevane sine, den 13 år gamle Miriam. Det heile kulminerer når Vankel – i fylla – har seksuell omgang med jenta. Etter denne hendinga nærmast flyktar han frå staden. Romanen tematiserer ein ung manns drift – ei drift vekselvis bort frå og inn mot seg sjølv. Identitet og strev etter å fylle ei uklar mannsrolle er også sentrale motiv i denne romanen.

  p I 
    em En tid for alt 
    | (2004) møter vi igjen Henrik Vankel. Denne gongen sit han på ei øy og gjenfortel bibelhistorier. Han går djupare inn i bibelske karakterar som Noah, Kain og Abel, og deira tankar, sorger og gleder blir dermed frigjorde frå den knappe bibelske framstillinga. Gjennom ei nærast essayistisk utgreiing om arbeida til den fiktive italienske engleforskaren Antinous Belloris, utfordrar Knausgård også det klassiske skiljet mellom vitskap og tru. Men kva med Henrik Vankel? Historia hans blir først henta opp igjen etter 500 sider om englar og bibelhistorie. På øya Sandholmen sit han nesten like ribba, utstøytt og forlaten som ein fallen engel. Flukta frå det nordnorske bygdesamfunnet som blir skildra i 
    em Ute av verden
    | , har sett djupe spor.
  
