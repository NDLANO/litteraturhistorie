  h3 Karl Ove Knausgård – og kampen for fiksjonen

  figure.col.col6.left-image.mt5
    img(src='./knausgaard_Portrett1.jpg' alt='Portrett av middelaldrande mann med halvlangt hår og skjegg. Foto')
    figcaption Foto: Daniel Nilsson / TT / Scanpix

  p Karl Ove Knausgård (1968– ) debuterte i 1998 med romanen 
    em Ute av verden
    | . For denne boka fekk han som første debutant nokon gong den prestisjetunge Kritikarprisen. Ni år seinare kom 
    em En tid for alt
    | . Romanane har same hovudkarakter – Henrik Vankel, og da Knausgård i 2009 gav ut første bind i Min kampserien, såg fleire på dette som den tredje boka i serien om Henrik Vankel. Skilnaden var at forfattaren Karl Ove Knausgård kom inn i staden for Henrik Vankel. Grensene mellom fiksjon og røyndom vart utydelege – og med eitt var Karl Ove Knausgård den mest debatterte og omtalte norske samtidsforfattaren.

  p I det ambisiøse seksbindsverket 
    em Min kamp 
    | (2009–2011) skriv Karl Ove Knausgård svært utleverande og ærleg om sitt eige liv. Han fortel om hendingar frå oppveksten, han brettar ut dei to ekteskapa sine, og han skriv nærgåande om tilhøvet til barna sine og den avlidne faren. Knausgård insisterer på å kalle utgivingane romanar, sjølv om dei heilt klart har ein biografisk innfallsvinkel.

  p Romanserien vekte sterke reaksjonar i den norske offentlegheita. Knausgårds slekt og ekskona hans har reagert på å bli gjorde om til romanfigurar – figurar heilt prisgitte Knausgårds blikk og penn. Min kamp-prosjektet har vorte stempla som «uetisk judaslitteratur», noko som synleggjer kva som kan skje når skiljet mellom fiksjon og røyndom blir utfordra.

  p Knausgård vaks opp i Arendal og Kristiansand, men bur no i Malmö, Sverige. Han har skrive ei rekkje fagessays om litteratur og var i ein periode medredaktør i litteraturtidsskriftet 
    em Vagant
    | . Knausgård har fått fleire prisar for forfattarskapen sin. I tillegg til Kritikarprisen i 1998 fekk han i 2009 Brageprisen for 
    em Min kamp 1
    | . To gonger har han også fått både P2-lyttaranes romanpris og Sørlandets litteraturpris.

