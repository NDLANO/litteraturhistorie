  p Knausgårds prosjekt kan også plasserast i ein postmoderne medierøyndom som er prega av leik med røyndom og reality. TV-seriar som 
    em The Hills 
    | og 
    em Paradise Hotel 
    | er døme på at ein kan leike med, og setje i scene, røyndommen. Dei sprengjer den tradisjonelle sjangerforståinga og skaper ein 
    em pseudorøyndom
    | . Dette er noko den moderne mediebrukaren er godt van med å takle – så kvifor da ikkje i ein skjønnlitterær samanheng også?

  p Debatten i kjølvatnet av 
    em Min kamp
    | -serien hyllar på den eine sida prosjektet som banebrytande, nettopp som ein del av ein slags pseudorøyndom. På den andre sida er mange svært kritiske til at han trekkjer inn – med eller utan samtykkje – verkelege personar i romanprosjektet sitt. Og for dei involverte partane, mellom anna Knausgårds ekskone, blir ikkje romanprosjektet opplevd som ein pseudorøyndom, men som ei utlevering av intime kjensler og hendingar.
  
  h3 Ein problematisk sjanger
  p Men korleis skal offentlegheita møte eit romanprosjekt og ein sjanger som medvite blandar røyndom og fiksjon? Noko av spenninga i prosjektet ligg nettopp i at vi ikkje veit når Karl Ove Knausgård utleverer seg heilt, og når han poserer og set seg sjølv i scene – altså når han fortel sanning, og når han diktar. Samstundes utfordrar sjangeren heilt klart etiske og juridiske spørsmål. Kan ein skrive kva ein vil om andre menneske under dekkje av at det er fiksjon?

  p Romanprosjektet til Knausgård tematiserer på mange måtar kva som kan vere følgjene av at «dei store forteljingane er døde», og at vi menneske er overlatne til oss sjølve i forsøket på å skape meining i tilværet. Korleis kan vi finne vår eigen identitet i eit mylder av valmoglegheiter? Kor går grensene for kor mykje ein kan seie og skrive om andre menneske i eit forsøk på å forstå seg sjølv?

  p Sjølv hevdar Knausgård at han har rett til å formidle si historie, si sanning – med trykk på si. For å skape seg ein identitet er han nøydt til å fortelje historia om sitt eige liv. På denne måten konstruerer han si eiga sanning og sin eigen røyndom.

