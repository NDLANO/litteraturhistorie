  h3 Detaljrik impresjonisme

  p Somme kritikarar har samanlikna skrivestilen til Knausgård med barokkens ideal om detaljrikdom – alt skal skildrast så detaljert og nøyaktig som mogleg. I 
    em Min Kamp 
    | er det lange skildringar av korleis Karl Ove vaskar opp, lagar middag, traktar kaffi eller opplever eit barneselskap. Detaljrikdommen er stor, og sjølv om det ikkje skjer så mye, driv språket og forteljeteknikken historia framover.

  p Skrivestilen til Knausgård har mykje felles med den retninga som blir kalla 
    em impresjonismen 
    | i målarkunsten. Forfattaren målar stemningar og inntrykk med språket. Til liks med dei impresjonistiske 
    em målarane 
    | er det ikkje ei nøyaktig avbilding av røyndommen som er interessant, men kva stemningar og kjensler ei hending vekkjer. Denne måten å skrive på kan samanliknast med Marcel Prousts 
    em På sporet av den tapte tid 
    | (1913–1927). Som Proust er Knausgård oppteken av korleis kjensler og refleksjonar startar med sansing og oppleving. Det er ikkje tingen i seg sjølv som er interessant, men kjensla han vekkjer hos karakteren – og hos lesaren. 

  h3 Den inste kjernen
  p Knausgård har ytra at drivkrafta i det litterære prosjektet hans er å nå inn til kjernen i det å vere menneske. For å lykkast med det må han utlevere seg sjølv og dei inste tankane og kjenslene sine – heilt ærleg, blottstilt og nådelaust. Gjennom å gå så tett inn på det private og individuelle tematiserer og granskar romanane samstundes det som er allment menneskeleg. Kva er det som driv oss? Korleis ter vi oss i høve til andre menneske? Kvifor handlar vi som vi gjer? For Knausgård er ein viktig del av denne prosessen å prøve å forstå faren sin – og dermed seg sjølv. Kven var denne skremmande farsfiguren eigentleg? Og kvifor har han sjølv så mange av dei destruktive og negative sidene som han forakta hos faren?
  
