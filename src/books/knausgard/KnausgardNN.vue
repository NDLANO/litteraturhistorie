  p Knausgårds lengsel etter å skrive er eit gjennomgangstema i store delar av romanprosjektet. Vi møter han som elev på Skrivekunstakademiet i Bergen. Han søkjer inspirasjon hos store forfattarar som Hamsun, Ulven og Larsson, men opplever at forsøka hans på å skrive blir sette på som klisjéfylte og lite originale. Det er først når han opplever å kunne skrive om sitt indre, at det losnar for han. 
