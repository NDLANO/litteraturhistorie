  h3 Notid og fortid

  figure.col.col6.right-image.mt5
    img(src='./knausgaard_Portrett3.jpg' alt='Portrett av middelaldrande mann med halvlangt hår og skjegg. Held hendene i pyramideform framfor ansiktet. Foto')
