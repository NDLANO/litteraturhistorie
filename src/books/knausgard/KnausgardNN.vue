  p Romanprosjektet 
    em Min Kamp 
    | tek for seg Karl Ove Knausgårds liv fram til den dagen prosjektet blir avslutta – altså eit forløp på 42 år. Lesarane får eit innblikk i barne- og ungdomsår, og korleis Knausgård i dag lever eit tilnærma A4-familieliv med kone og barn i Malmö i Sverige, eit samliv som i periodar er truga av psykiske lidingar. Navet som forteljinga dreier seg rundt, er likevel farens død, eit dødsfall som fører hovudpersonen Karl Ove inn i ei krise. Forholdet til faren blir skildra som kaldt og prega av avstand, angst og frykt, men dødsfallet går likevel sterkt inn på sonen. Den kjensleborne ambivalensen til hovudpersonen pregar heile romanserien. 

