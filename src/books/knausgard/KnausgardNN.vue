  p Knausgård problematiserer og granskar tilhøvet mellom notid og fortid. I 
    em Ute av verden 
    | skriv han dette om dødsfallet til faren:

  blockquote Femten år senere var han død. Og så voldsomme var omstendighetene rundt dødsfallet hans, at han med det ikke bare forandret framtiden vår, men også fortiden. […] En slags kulde har spredd seg ut i den, noe høytidelig som vi ikke visste om mens vi var der, men som nå preger alt som fant sted, selv det mest trivielle og hverdagslige av det vi gjorde. Og det er en urovekkende tanke, at heller ikke fortiden er avsluttet, at også den fortsetter å endre seg, som om det i virkeligheten bare finnes én tid, for alt.

  p Den kulda som legg seg over alle minna han har av faren, illustrerer korleis vi som menneske heile tida konstruerer vår eiga livshistorie og forteljing. Det finst ikkje éi fortid, men mange! Fortida er ikkje ei konkret hending eller oppleving, men ei forteljing om – og ei kjensle av – kva denne fortida var. Romankarakteren Karl Oves kjensler og tankar omkring faren er heile tida prega av korleis forfattaren Karl Ove Knausgård sjølv har det 
    em her og no 
    | – i den notidige skriveprosessen.
  
  h3 Å skrive sitt liv

  figure.col.col6.left-image.mt5.mb5
    img(src='./knausgaard_Sangen_om_den_roede_rubin.jpg' alt='To menn sit med ryggen til. Framfor dei ei gruppe pressefotografar. Foto')
    figcaption Rettssaka om boka «Sangen om den røde rubin» av Agnar Mykle vekte stor interesse i mediene. Foto: Aktuell / Scanpix

  p Knausgård skriv seg inn i ein tradisjon der forholdet mellom fiksjon og røyndom blir problematisert. Allereie på 1890-talet hadde forfattarar knytte til Kristianiabohemen som mål «å skrive sitt liv». På 1950-talet vart Agnar Mykles (1915–1994) roman 
    em Sangen om den røde rubin 
    | (1956) trekt for retten. Sjølv om det i første rekkje var «usedelig» innhald som  førte til rettssak, var også eit av ankepunkta Mykles bruk av «levende modeller». Fleire personar i Mykles omgangskrins reagerte på at romankarakterane hans var lett forkledde utgåver av dei sjølve.

  p Fleire romanforfattarar på 2000-talet tek utgangspunkt i eller nyttar medvite biografisk materiale i dei litterære verka sine. I Noreg har mellom anna Nikolaj Frobenius (1965– ) skrive ein «løgnaktig selvbiografi» – 
    em Teori og praksis 
    | (2004) – om oppveksten sin på Rykkin – illustrert med bilete og kart frå stadene han skriv om. I Sverige har Åsa Lindeborg (1968– ) skrive 
    em Meg eier ingen 
    | (2007), om oppveksten sin med ein alkoholisert far, og våren 2011 vart danske Claus Beck-Nielsen (1963– ) frikjend for injurierande omtale av ein tidlegare ven i romanen 
    em Suverenen 
    | (2008).
  
  h3 Pseudorøyndom

  figure.col.col4.left-image.mt5
    img(src='./knausgaard_Portrett4.jpg' alt='Portrett av middelaldrande mann med halvlangt hår og skjegg sett gjennom eit vindauge med dugg på. Foto')
