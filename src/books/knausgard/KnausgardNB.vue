    em Ute av verden 
    | (1998) møter vi for første gang Henrik Vankel. I en alder av 26 år reiser han fra hjemtraktene på Sørlandet til et lite lokalsamfunn i Nord-Norge. Her skal han arbeide som lærervikar mens han forsøker å realisere sin drøm om å bli forfatter. Romanen tar en drastisk vending når Henrik Vankel forelsker seg i en av elevene sine, den 13 år gamle Miriam. Det hele kulminerer når Vankel – i fylla – har seksuell omgang med jenta. Etter denne hendelsen nærmest flykter han fra stedet. Romanen tematiserer en ung manns drift – en drift vekselvis bort fra og inn mot seg selv. Identitet og streben etter å fylle en uklar mannsrolle er også sentrale motiv i denne romanen. 

  p I En 
    em tid for alt 
    | (2004) møter vi igjen Henrik Vankel. Denne gangen sitter han på ei øy og gjenforteller bibelhistorier. Han går dypere inn i bibelske karakterer som Noah, Kain og Abel, og deres tanker, sorger og gleder blir dermed frigjort fra den knappe bibelske framstillingen. Gjennom en nærmest essayistisk redegjørelse av den fiktive italienske engleforskeren Antinous Belloris arbeider, utfordrer Knausgård også det klassiske skillet mellom vitenskap og tro. Men hva med Henrik Vankel? Hans historie hentes først opp igjen etter 500 sider om engler og bibelhistorie. På øya Sandholmen sitter han nesten like ribbet, utstøtt og forlatt som en fallen engel. Flukten fra det nordnorske bygdesamfunnet som skildres i 
    em Ute av verden
    | , har satt dype spor. 

