  h3 Nåtid og fortid 
   
  p Knausgård problematiserer og undersøker forholdet mellom nåtid og fortid. I 
    em Ute av verden 
    | skriver han dette om farens dødsfall:  

  blockquote Femten år senere var han død. Og så voldsomme var omstendighetene rundt dødsfallet hans, at han med det ikke bare forandret framtiden vår, men også fortiden. […] En slags kulde har spredd seg ut i den, noe høytidelig som vi ikke visste om mens vi var  der, men som nå preger alt som fant sted, selv det mest trivielle og hverdagslige av det vi gjorde. Og det er en urovekkende tanke, at heller ikke fortiden er avsluttet, at også den fortsetter å endre seg, som om det i virkeligheten bare finnes én tid, for alt.  

  p Den kulden som legger seg over alle minner han har av faren, illustrerer hvordan vi som mennesker hele tiden konstruerer vår egen livshistorie og fortelling. Det finnes ikke én fortid, men mange! Fortiden er ikke en konkret hendelse eller opplevelse, men en fortelling om – og en følelse av – hva denne fortiden var. Romankarakteren Karl Oves følelser og tanker omkring faren er hele tiden preget av hvordan forfatteren Karl Ove Knausgård selv har det 
    em her og nå 
    | – i den nåtidige skriveprosessen.  

  h3 Å skrive sitt liv 

  figure.col.col6.left-image.mt5.mb5
    img(src='./knausgaard_Sangen_om_den_roede_rubin.jpg' alt='To menn sitter med ryggen til. Foran dem en gruppe pressefotografer. Foto')
    figcaption Rettssaken om boka «Sangen om den røde rubin» av  Agnar Mykle vekket stor interesse i mediene. Foto: Aktuell / Scanpix

  p Knausgård skriver seg inn i en tradisjon der forholdet mellom fiksjon og virkelighet problematiseres. Allerede på 1890-tallet hadde forfattere tilknyttet Kristianiabohemen som mål «å skrive sitt liv». På 1950-tallet ble Agnar Mykles (1915–1994) roman 
    em Sangen om den røde rubin 
    | (1956) trukket for retten. Selv om det i første rekke var «usedelig» innhold som førte til rettssak, var også et av ankepunktene Mykles bruk av «levende modeller». Flere personer i Mykles omgangskrets reagerte på at romankarakterene hans var lett forkledde utgaver av dem selv. 

  p Flere romanforfattere på 2000-tallet tar utgangspunkt i eller bruker bevisst biografisk materiale i sine litterære verk. I Norge har blant annet Nikolaj Frobenius (1965– ) skrevet en «løgnaktig selvbiografi» – 
    em Teori og praksis 
    | (2004) – om sin oppvekst på Rykkin – illustrert med bilder og kart fra stedene han skriver om. I Sverige har Åsa Lindeborg (1968– ) skrevet 
    em Meg eier ingen 
    | (2007), om sin oppvekst med en alkoholisert far, og våren 2011 ble danske Claus Beck-Nielsen (1963– ) frikjent for injurierende omtale av en tidligere venn i romanen 
    em Suverenen 
    | (2008). 
  
