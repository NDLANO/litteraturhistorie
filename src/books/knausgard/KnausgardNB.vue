  p Knausgårds lengsel etter å skrive er et gjennomgangstema i store deler av romanprosjektet. Vi møter han som elev på Skrivekunstakademiet i Bergen. Han  søker inspirasjon hos store forfattere som Hamsun, Ulven og Larsson, men opplever at hans forsøk på å skrive blir sett på som klisjéfylte og lite originale. Det er først når han opplever å kunne skrive om sitt indre, at det løsner for han. 
