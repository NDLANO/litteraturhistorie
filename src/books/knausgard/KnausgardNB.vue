  h3 Detaljrik impresjonisme 

  p Enkelte kritikere har sammenliknet Knausgårds skrivestil med barokkens idealer om detaljrikdom – alt skal skildres så detaljert og nøyaktig som mulig. I 
    em Min Kamp 
    | er det lange beskrivelser av hvordan Karl Ove vasker opp, lager middag, trakter kaffe eller opplever et barneselskap. Detaljrikdommen er stor, og selv om det ikke skjer så mye, driver språket og fortelleteknikken historien framover. 

  p Skrivestilen til Knausgård har mye felles med den retningen som kalles 
    em impresjonismen 
    | i malekunsten. Forfatteren maler stemninger og inntrykk med språket. I likhet med de impresjonistiske 
    em malerne 
    | er det ikke en nøyaktig avbildning av virkeligheten som er interessant, men hvilke stemninger og følelser en hendelse vekker. Denne måten å skrive på kan sammenliknes med Marcel Prousts 
    em På sporet av den tapte tid 
    | (1913–1927). Som Proust er Knausgård opptatt av hvordan følelser og refleksjoner starter med sansing og opplevelse. Det er ikke tingen i seg selv som er interessant, men følelsen den vekker hos karakteren – og hos leseren. 

  h3 Den innerste kjernen 
  p Knausgård har uttalt at drivkraften i hans litterære prosjekt er å nå inn til kjernen i det å være menneske. For å lykkes med det må han utlevere seg selv og sine innerste tanker og følelser – helt ærlig, blottstilt og nådeløst. Gjennom å gå så tett inn på det private og individuelle tematiserer og undersøker romanene samtidig det som er allment menneskelig. Hva er det som driver oss? Hvordan forholder vi oss til andre mennesker? Hvorfor handler vi som vi gjør? For Knausgård er en viktig del av denne prosessen å prøve å forstå faren sin – og dermed seg selv. Hvem var denne skremmende farsfiguren egentlig? Og hvorfor har han selv så mange av de destruktive og negative sidene som han foraktet hos faren? 
  
