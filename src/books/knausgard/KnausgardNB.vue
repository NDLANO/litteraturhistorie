    | . Romanene har samme hovedkarakter – Henrik Vankel, og da Knausgård i 2009 gav ut første bind i Min kamp-serien, så flere på dette som den tredje boka i serien om Henrik Vankel. Forskjellen var at forfatteren Karl Ove Knausgård trådte inn i Henrik Vankels sted. Grensene mellom fiksjon og virkelighet ble utydelige – og med ett var Karl Ove Knausgård den mest debatterte og omtalte norske samtidsforfatteren. 

  p I det ambisiøse seksbindsverket 
    em Min kamp 
    | (2009–2011) skriver Karl Ove Knausgård svært utleverende og ærlig om sitt eget liv. Han forteller om hendinger fra oppveksten, han bretter ut sine to ekteskap, og han skriver nærgående om forholdet til sine barn og sin avdøde far. Knausgård insisterer på å kalle utgivelsene romaner, selv om de helt klart har en biografisk innfallsvinkel. 

  p Romanserien vakte sterke reaksjoner i den norske offentligheten. Knausgårds slekt og hans ekskone har reagert på å bli gjort om til romanfigurer – figurer helt prisgitt Knausgårds blikk og penn. Min kamp-prosjektet har blitt stemplet som «uetisk judaslitteratur», noe som synliggjør hva som kan skje når skillet mellom fiksjon og virkelighet utfordres. 

  p Knausgård vokste opp i Arendal og Kristiansand, men bor nå i Malmö, Sverige. Han har skrevet en rekke fagessays om litteratur og var i en periode medredaktør i litteraturtidsskriftet 
    em Vagant
    | . Knausgård har mottatt flere priser for forfatterskapet sitt. I tillegg til Kritikerprisen i 1998 fikk han i 2009 Brageprisen for 
    em Min kamp 1
    | . To ganger har han også mottatt både P2-lytternes romanpris og Sørlandets litteraturpris. 

