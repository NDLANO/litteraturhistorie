  p I Vibekes tilfelle gjennomsyrer reklame og kvinnemagasin både måten ho tenkjer på, kva ho drøymer om, og måten ho snakkar på. Ho kunne «trenge et nytt antrekk, det har hun virkelig fortjent […]», og ho ser for seg at ho «er på en endeløs strand» saman med ein mann, «det er vinter og de er alene der, hun småløper i vannkanten og han ser på henne, han ser alt i henne og er klok og varm». Klisjeane Vibeke fyller livet sitt med, gjer henne så sjølvoppteken at det grensar til narsissisme – altså ein grenselaus eigenkjærleik som gjer henne ute av stand til å sjå andre menneske, til og med sin eigen son.

  h3 Indre og ytre farar

  figure.col.col6.right-image.mt5
    img(src='./orstavik_Portrett2.jpg' alt='Ung kvinne står ved ein talerstol og les i ei bok. Publikum i forgrunnen. Foto')
    figcaption Foto: Håkon Mosvold Larsen / NTB / Scanpix

  p Tematisk krinsar 
    em Kjærlighet 
    | rundt kva farar vi møter i den postmoderne verda. Sosiologar som Ulrich Beck (1944– ) og Anthony Giddens (1938– ) omtaler vår tids samfunn som eit risikosamfunn. Den teknologiske og industrielle utviklinga har skapt globale truslar som menneske i dag må leve med vissa om og ikkje kan flykte frå. Samstundes er «dei store forteljingane» som gir tilværet meining, i ferd med å smuldre bort.

  p Når dei store forteljingane ikkje lenger kan skape meining for oss, og vi sjølve må skape meining, blir dei nære relasjonane ekstra viktige. Og i ei verd der katastrofar som krig og miljøtruslar ikkje lenger berre er lokale truslar, men globale, blir heimen og familien eit viktig mentalt vern for å skape meining i tilværet. Når familien så sviktar – og det er uro i reiret, får dette katastrofale følgjer for karakterane.

  figure.col.col4.left-image.mt5
    img(src='./orstavik_Portrett3.jpg' alt='Portrett av kvinne med bleika hår. Lener seg mot ein okergul vegg. Foto')
