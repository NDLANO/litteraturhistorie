  p I romanen 
    em Kjærlighet 
    | møter vi «eit reir i uro» – altså ein heim og ein familie som ikkje fungerer. Dette «vesle reiret i uro» kan lesast som ein allegori – ei forteljing i overført tyding – om det «store reiret i uro», altså samfunnet og verda generelt. Mens Jon i romanen blir utsett for det vi ser på som ytre truslar – han set seg inn i bilen til ei framand kvinne, han blir med ein ukjend, eldre mann ned i kjellaren, og han vandrar einsam rundt kveldstid, er det ikkje desse farane Jon blir offer for. Det er nemleg dei 
    em indre farane
    | , omsorgssvikt og mangel på kjærleik og kommunikasjon, som til slutt fører til at Jon blir liggjande aleine ute i snøen – noko som mest sannsynleg fører til at han døyr.

  p I 
    em Tiden det tar 
    | (2000) møter vi Signe som for første gong skal feire jul aleine saman med mann og barn. Det som skulle bli ei hyggjeleg julehøgtid, blir vanskeleg når Signes foreldre dukkar opp.

  h3 Utestengd og innestengd
  p Familiar som har slutta å fungere, er eit gjennomgangstema i Ørstaviks forfattarskap. I 
    em Kjærlighet 
    | blir Jon stengd ute frå heimen av ei åndsfråverande mor, mens i 
    em Like sant som jeg er virkelig 
    | (1999) skjer det motsette – Johanne blir låst inne på rommet sitt av ei altfor påpasseleg mor. Mora vil hindre Johanne i å dra til USA med kjærasten sin Ivar. Som Vibeke har også Johannes mor store problem med å skape ekte relasjonar. Men der Vibeke ikkje vørder Jon, blir Johannes mor grenselaus i si invadering av livet og kroppen til dottera – dottera kjenner det mellom anna som ho sviktar mora «ved å tenke på noe hun ikke visste om».

  p Johannes store draum er at ho etter psykologistudia skal kjøpe eit lite gardsbruk med eit tun – på dette tunet skal ho og mora ha kvar sitt hus slik at dei framleis kan vere saman. Tunet blir eit bilete på korleis dottera ønskjer å skape eit trygt reir saman med mora. Men undervegs i romanen blir det tydeleg for lesaren at dette tunet er moras draum, ikkje Johannes. Tunet blir dermed nok eit bevis på korleis mora invaderer dottera med sine ønske og behov.

