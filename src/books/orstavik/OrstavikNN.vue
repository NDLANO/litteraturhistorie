  h3 Ein ordknapp forfattar

  figure.col.col4.left-image.mt5
    img(src='./orstavik_Portrett1.jpg' alt='Portrett av ung kvinne med lyst hår. Lyskjelde i bakgrunnen forsvinn bak personen. Foto')
    figcaption Foto: Berit Roald / NTB / Scanpix

  p Hanne Ørstavik vart fødd i Tana i 1969, men flytte til Oslo da ho var 16 år. Bakgrunnen frå Nord-Noreg speglar seg ofte att i litteraturen hennar – i fleire av romanane er handlinga lagd til eit nordnorsk lokalsamfunn. Tematisk dreier mange av romanane hennar seg om vilkåra for familiane i det postmoderne samfunnet og korleis dette påverkar kommunikasjonen og relasjonane mellom nære familiemedlemmer. Spørsmåla ho stiller, er eksistensielle: Korleis lever vi med andre og oss sjølve?

  p Ho debuterte som forfattar i 1994 med 
    em Hakk
    | . Både denne romanen og 
    em Entropi 
    | (1995) er karakteristiske for Ørstaviks minimalistiske og ordknappe stil. Forteljemåten i romanane er prega av eit enkelt og nærmast strippa språk, der kvardagshendingar og detaljar får stor merksemd. Samtalane mellom dei litterære karakterane har ofte preg av å vere parallelle monologar – karakterane når ikkje fram til kvarandre med språket sitt, orda strekkjer ikkje til. Denne språklege minimalismen minner om den vi finn i forfattarskapane til Kjell Askildsen (1929– ) og Trude Marstein (1973– ).

  h3 Kjærlighet (1997)

  img.col.col4.right-image.mt5(src='./orstavik_Kjaerlighet1.jpg' alt='Bomslag, silhuett av hus med eit opplyst vindauge mot mørkeblå nattehimmel. Foto')
  p I gjennombrotsromanen 
    em Kjærlighet 
    | (1997) møter vi Vibeke og den ni år gamle sonen hennar Jon. Dei har nettopp flytt til ein liten tettstad nord i Noreg. Vibeke har fått jobb i kulturetaten i kommunen. Vi møter den vesle familien kvelden før Jons bursdag. Vibeke blir framstilt som ei kvinne som sluker bøker, og som elles er oppteken av utsjånaden sin og jakta på den perfekte mannen. Jon drøymer om samhald og lengtar etter mor si. Han gjer mykje for å fange merksemda hennar, men ho gir han få tilbakemeldingar.

  p Forteljeteknikken illustrerer avstanden mellom mor og son. Romanen er konstruert som ei parallellforteljing der mor og son nesten ikkje har kontakt med kvarandre – trass i at dei ofte er på same staden. Jon prøver heile tida å nærme seg mora, han tenkjer på henne heile tida og prøver å vere slik han trur ho vil han skal vere. Eit av dei få kontaktpunkta mellom mor og son byrjar med at mora stryk han gjennom håret, men merksemda hennar glir fort over til fargen på neglelakken og kva som vil matche «en mørk, brunøyd mann».

  h5 Kommersialiseringas pris
  p Vibeke lever eit liv sterkt inspirert av dei «glossy» kvinneblada ho les. Ho er oppteken av det ytre, og mennene ho jaktar på, oppfyller alle ein popkulturell klisjé – anten det er snakk om den høge, mørke ingeniøren eller den spennande omreisande tivolimannen. Vibekes liv og tankeverd avslører det Ørstavik ser på som problemet med den postmoderne verda: Det gjennomkommersialiserte samfunnet skaper avstand mellom menneska, og ei forflating av både kjensler og evna til å skape sunne relasjonar.

