  p Romanane 
    em Kjærlighet
    | , 
    em Like sant som jeg er virkelig 
    | og 
    em Tiden det tar 
    | blir ofte omtalte som ein tematisk trilogi. Dei utgjer den andre fasen i forfattarskapen og rettar, som vi har sett, mellom anna søkjelyset mot familieliv og sviktande kommunikasjon mellom nære menneske.

  p Den tredje fasen handlar om det å finne eit språk som er ekte og gir meining. I 
    em Uke 43 
    | (2002) og 
    em Presten 
    | (2004) møter vi sjølvstendige og einslege yrkeskvinner som meir eller mindre isolerer seg frå omgivnadene sine. Dei slit med å finne eit språk som kommuniserer. I 
    em Uke 43 
    | dreier det seg om det litterære språket, mens presten Liv i 
    em Presten 
    | ikkje greier å formidle tankar om tru, tvil og kall. I 2006 gav Ørstavik ut sin foreløpig siste roman, nemleg 
    em kallet – romanen
    | . Denne romanen har eit klart metaperspektiv – den tematiserer nemleg skrive- og leseprosessar og korleis lesaren også har eit ansvar for at kommunikasjonen mellom verk og lesar skal fungere.

  p I seinare verk som 
    em I morgen skal det være åpent for alle 
    | (2007) ser vi ei ny dreiing i forfattarskapen til Ørstavik – ein fjerde fase. Her eksperimenterer ho både med sjanger og forteljarperspektivet. I morgen skal det være åpent for alle er eit såkalla lesedrama der Ørstavik konstruerer ein samtale mellom to forteljarar som også inkluderer lesaren som ein slags medforteljar i teksten. 
    em Der alt er klart 
    | (2009) er eit samarbeid med den franske biletbokkunstnaren Pierre Duba. I denne illustrerte romanen er handlinga lagd til Svalbard. Lesaren møter ei kvinne som har teke seg jobb i kullgruvene der i håp om å lære den døde faren sin betre å kjenne. Faren omkom under Kings Bayulykka i 1962.

  p Felles for dei ulike fasane i Ørstaviks forfattarskap er korleis ho filosoferer over kva språk er, og kva det kan brukast til. Dette ser vi også i Ørstaviks førebels siste roman 
    em 48 rue Defacqz 
    | (2009) der Ørstavik mellom anna utforskar tilhøvet mellom kjønn og språk. Eit språk som er gjennomsyra av den postmoderne medieverdas formuleringar og perspektiv, er eit falskt språk – og eit falskt språk fortrengjer både den personlege stemmen og den  eigne tanken.

