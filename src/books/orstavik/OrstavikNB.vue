    em kallet – romanen
    | . Denne romanen har et klart metaperspektiv – den tematiserer nemlig skrive- og leseprosesser og hvordan leseren også har et ansvar for at kommunikasjonen mellom verk og leser skal fungere. 

  p I seinere verk som 
    em I morgen skal det være åpent for alle 
    | (2007) ser vi en ny dreiing i forfatterskapet til Ørstavik – en fjerde fase. Her eksperimenterer hun både med sjanger og fortellerperspektivet. I morgen skal det være åpent for alle er et såkalt lesedrama der Ørstavik konstruerer en samtale mellom to fortellere som også inkluderer leseren som en slags medforteller i teksten. 
    em Der alt er klart 
    | (2009) er et samarbeid med den franske bildebok-kunstneren Pierre Duba. I denne illustrerte romanen er handlingen lagt til Svalbard. Leseren møter en kvinne som har tatt seg jobb i kullgruvene der i håp om å lære sin avdøde far bedre å kjenne. Faren omkom under Kings Bay-ulykken i 1962. 

  p Felles for de ulike fasene i Ørstaviks forfatterskap er hvordan hun filosoferer over hva språk er, og hva det kan brukes til. Dette ser vi også i Ørstaviks roman 
    em 48 rue Defacqz 
    | (2009) der Ørstavik blant annet utforsker forholdet mellom kjønn og språk. Et språk som er gjennomsyret av den postmoderne medieverdens formuleringer og perspektiver, er et falskt språk – og et falskt språk fortrenger både den personlige stemmen og den selvstendige tanken. 

