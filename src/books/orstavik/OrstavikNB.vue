  p Tematisk kretser 
    em Kjærlighet 
    | rundt hvilke farer vi møter i den postmoderne verden. Sosiologer som Ulrich Beck (1944–2015 ) og Anthony Giddens (1938– ) omtaler vår tids samfunn som et risikosamfunn. Den teknologiske og industrielle utviklingen har skapt globale trusler som mennesker i dag må leve med vissheten om og ikke kan flykte fra. Samtidig er «de store fortellingene» som gir tilværelsen mening, i ferd med å smuldre bort. 

  p Når de store fortellingene ikke lenger kan skape mening for oss, og vi selv må skape mening, blir de nære relasjonene ekstra viktige. Og i en verden der katastrofer som krig og miljøtrusler ikke lenger kun er lokale trusler, men globale, blir hjemmet og familien en viktig mental beskyttelse for å skape mening i tilværelsen. Når familien så svikter – og det er uro i redet, får dette katastrofale følger for karakterene. 

