  p Den tredje fasen handler om det å finne et språk som er ekte og gir mening. I 
    em Uke 43 
    | (2002) og 
    em Presten 
    | (2004) møter vi selvstendige og enslige yrkeskvinner som mer eller mindre isolerer seg fra omgivelsene sine. De sliter med å finne et språk som kommuniserer. I 
