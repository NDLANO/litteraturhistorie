  p I romanen 
    em Kjærlighet 
    | møter vi «et rede i uro» – altså et hjem og en familie som ikke fungerer. Dette «lille redet i uro» kan leses som en allegori – en fortelling i overført betydning – om det «store redet i uro», altså samfunnet og verden generelt. Mens Jon i romanen blir utsatt for det vi ser på som ytre trusler – han setter seg inn i bilen til en fremmed kvinne, han blir med en ukjent, eldre mann ned i kjelleren, og han vandrer ensom rundt kveldstid, er det ikke disse farene Jon blir offer for. Det er nemlig de 
    em indre farene
    | , omsorgssvikt og mangel på kjærlighet og kommunikasjon, som til slutt fører til at Jon blir liggende alene ute i snøen – noe som mest sannsynlig fører til at han dør. 

  p I 
    em Tiden det tar 
    | (2000) møter vi Signe som for første gang skal feire jul alene sammen med mann og barn. Det som skulle bli en hyggelig julehøytid, blir vanskelig når Signes foreldre dukker opp.

  h3 Utestengt og innestengt 
  p Familier som har sluttet å fungere, er et gjennomgangstema i Ørstaviks forfatterskap. I 
    em Kjærlighet 
    | blir Jon stengt ute fra hjemmet sitt av ei uoppmerksom mor, mens i 
    em Like sant som jeg er virkelig 
    | (1999) skjer det motsatte – Johanne blir låst inne på rommet sitt av ei altfor oppmerksom mor. Mora vil hindre Johanne i å dra til USA med kjæresten sin Ivar. I likhet med Vibeke har også Johannes mor store problemer med å skape ekte relasjoner. Men der Vibeke ikke forholder seg til Jon, blir Johannes mor grenseløs i sin invadering av datterens liv og kropp – datteren føler blant annet at hun svikter mora «ved å tenke på noe hun ikke visste om». 

  p Johannes store drøm er at hun etter psykologistudiene skal kjøpe et lite gårdsbruk med et tun – på dette tunet skal hun og mora ha hvert sitt hus slik at de fremdeles kan være sammen. Tunet blir et bilde på hvordan datteren ønsker å skape et trygt rede sammen med mora. Men underveis i romanen blir det tydelig for leseren at dette tunet er moras drøm, ikke Johannes. Tunet blir dermed nok et bevis på hvordan mora invaderer datteren med sine ønsker og behov.  

