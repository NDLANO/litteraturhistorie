  h3 «Revolusjonens røst» 
  p Allerede som gymnasiast var Rudolf Nilsen politisk interessert. I 1919 meldte han seg inn i Norges Socialdemokratiske Ungdomsforbund. Da arbeiderbevegelsen ble politisk splittet i 1923, valgte Rudolf Nilsen den revolusjonære linja som Norges Kommunistiske Parti stod for. 

  p Våpenet hans var pennen. Han skrev både artikler, petiter og dikt, og i en periode var han ansatt som journalist i NKP-organet Norges Kommunistblad. 

  p Alt han skrev, både som journalist og forfatter, er farget av innsikt i de vilkårene arbeiderklassen levde under, og av en revolusjonær glød inspirert av den russiske revolusjonen i 1917. De russiske arbeiderne hadde styrtet tsarveldet og skapt en ny samfunnsorden. Dette ble forbildet for den radikale delen av arbeiderbevegelsen i Norge. Mer enn noen annen norsk forfatter kan Rudolf Nilsen derfor kalles «revolusjonens røst». 

  p I slutten av diktet «Nr. 13» møter vi dem som skal endre arbeidsfolks kår i Norge, inspirert av marxistisk tankegods: 

