  h3 Arbeiderbevegelsen i framvekst
  p I Norge skjedde det en industriell og politisk omveltning i første halvdel av 1900-tallet. Store industribedrifter vokste fram. De gav arbeid til mange, men skapte også en ny samfunnsklasse av rotløse arbeidere bosatt i byenes slumkvarter. Her var det stor sosial nød og lite hjelp å få. 

  p Framtidsoptimismen som preget tida rundt århundreskiftet, fikk en alvorlig knekk da første verdenskrig brøt ut i 1914. Industrielle nyvinninger som skulle gi menneskene et bedre liv, ble til masseødeleggelsesvåpen på slagmarka. Gamle europeiske kulturnasjoner som Tyskland og Frankrike førte en skyttergravskrig der tusenvis av soldater på begge sider ble slaktet ned for å erobre noen få kvadratmeter jord. 

  p I lys av det som hendte på slagmarka, fikk ideene til den tyske filosofen Nietzsche vind i seilene. Han hevdet at Gud var død, og at menneskene fra nå av måtte tenke nytt om hva som var sant og ikke sant, og hvilke verdier det nye samfunnet skulle bygge på. 

  p For mange framstod arbeiderbevegelsen som en internasjonal bevegelse som hevet seg over særinteressene til stormakter og kapitaleiere. Klasseforskjellene mellom rike og fattige gav næring til sosial uro. Deler av arbeiderbevegelsen ble radikalisert, både ute i Europa og i Norge. Forbildet var den russiske revolusjonen i 1917, tuftet på marxistisk ideologi. I tråd med dette krevde arbeiderne eiendomsretten til jord og produksjonsmidler.  
  
