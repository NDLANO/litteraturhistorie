  h3 «Revolusjonens røst»
  p Alt som gymnasiast var Rudolf Nilsen politisk interessert. I 1919 melde han seg inn i Norges Socialdemokratiske Ungdomsforbund. Da arbeidarrørsla vart politisk splitta i 1923, valde Rudolf Nilsen den revolusjonære linja som Norges Kommunistiske Parti stod for.

  p Våpenet hans var pennen. Han skreiv både artiklar, petitar og dikt, og i ein periode var han tilsett som journalist i NKP-organet Norges Kommunistblad.

  p Alt han skreiv, både som journalist og forfattar, er farga av innsyn i dei vilkåra arbeidarklassen levde under, og av ein revolusjonær glød inspirert av den russiske revolusjonen i 1917. Dei russiske arbeidarane hadde styrta tsarveldet og skapt ein ny samfunnsorden. Dette vart føredømet for den radikale delen av arbeidarrørsla i Noreg. Meir enn nokon annan norsk forfattar kan Rudolf Nilsen derfor kallast «revolusjonens røst». 

