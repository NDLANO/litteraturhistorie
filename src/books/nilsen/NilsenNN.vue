  h3 Arbeidarrørsla i framvekst
  p I Noreg skjedde det ei industriell og politisk omvelting i første halvdel av 1900-talet. Store industriverksemder voks fram. Dei gav arbeid til mange, men skapte også ein ny samfunnsklasse av rotlause arbeidarar busette i slumkvartera i byane. Her var det stor sosial nød og lite hjelp å få.

