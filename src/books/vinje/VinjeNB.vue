  h3 Språket til Vinje 
  p På midten av 1800-tallet var språket i avisene preget av den kansellistilen som var vanlig i akademiske utredninger og i lovtekster. Setningene var lange og innfløkte, med mange vanskelige ord. Et slikt språk var ikke lett å forstå for folk flest. Vinje var kritisk til denne måten å skrive på: 

  blockquote Og det er Mange, som hava kjært seg for, at Stilen er tung og fuld av Kunstord. Eg er ikki blind for Nytten af ein slik lærd Stil; den agar eller imponerar mangein Lesar; for han trur, at det maa vera grovt til lærde Menn, som skriva slikt eit lærd Spraak. So skriv Futen og Skrivaren og so skriv Professoren og sjølve Kongsraaden. 
    br 
    br
    | (Vort land 7.4.1867) 

  p Vinje brukte selv en mye enklere og konkret skrivemåte som var mer i tråd med det talemålet leserne hans kjente til. 

  p Før han startet Dølen i 1858, brukte Vinje dansk som skriftspråk. Etter at Aasen hadde laget de første tekstprøvene av landsmålet tidlig i 1850-årene, var Vinje en av de første til å ta språket i bruk. Men han holdt seg ikke til den landsmålsnormen som Aasen hadde satt opp. Han skrev et landsmål med talemålsformer fra sin egen landsdel, nemlig Vest-Telemark, og han var svært glad i å lage nye ord. 

  h3 Journalistikken vokser fram 
  p I den norske grunnloven av 1814 er ytringsfrihet nedfelt som en grunnleggende rett for alle borgere. Ytringsfriheten var en viktig forutsetning for framveksten av journalistikken. Fra midten av 1800-tallet var redaktører og journalister etablert som egne yrkesgrupper i Norge.

  p Av de avisene som ennå finnes i dag, er Adresseavisen i Trondheim den eldste (1767). Morgenbladet (1819) var den første norske dagsavisa. Den første samiske avisa, Muitalægje, kom ut i 1873. 

  p Fra midten av 1800-tallet hadde avisene funnet sin form som nyhetsmedier. Avisene var også viktige redskaper i folkeopplysningen og i den gryende politiske debatten. I 1880-årene ble de politiske partiene Høyre, Venstre og Arbeiderpartiet stiftet. Flere aviser etablerte seg som talerør for disse partiene, både nasjonalt og lokalt. I 1890 fantes det om lag 100 aviser i landet. 

  p I tida før vi fikk radio, tv og Internett, var det avisene som ga folk flest innsikt i norsk innenrikspolitikk og i det som hendte i hovedstaden. Som hovedstadskorrespondent for regionavisa Drammens Tidende hadde Vinje en viktig posisjon: 

  blockquote Intet Provindsblad, der vil gjælde for velopdragent og «tidsvarende», tør længere mangle sin Kristiana Korrespondentartikel. Den er ligesaa nødvendig for Bladet, som en Dram paa Morgenkvisten for Sjoueren. 
    br 
    br
    | (Krydseren 19.3.1851) 

  h3 Kulturformidling og underholdning 
  p Fra midten av 1800-tallet kom det ut tidsskrifter og ukeblad som rettet seg mot folk flest. Bedre skolegang førte til at de fleste kunne lese, og etterspørselen etter lesestoff økte. Tidsskriftene, som for eksempel Dølen, inneholdt både populærvitenskapelige tekster, dikt og fortellinger. 

  p Det illustrerte bladet Skilling-Magazin kom ut i perioden 1835–1891. Undertittelen «Til Udbredelse af almennyttige Kundskaber» viser at bladet så på seg selv som et bidrag til folkeopplysningen. Illustrasjonene skulle motivere folk til å lese. 

  p Men ukebladene hadde også som mål å underholde leserne. Vikingen, fra 1862, var et vittighetsblad som inneholdt karikaturtegninger og drev harselas med aktuelle nyhetssaker. 

  p I 1877 ga den danske Allers-familien ut ukebladet Illustreret Familie-Journal, som seinere ble ukebladet Allers. Trykkvaliteten var så god at enkelte hengte bildene på stueveggen til pynt.           




