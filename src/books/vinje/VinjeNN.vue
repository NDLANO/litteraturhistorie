  h3 Språket til Vinje 
  p På midten av 1800-talet var språket i avisene prega av den kansellistilen som var vanleg i akademiske utgreiingar og i lovtekstar. Setningane var lange og innfløkte, med mange vanskelege ord. Eit slikt språk var ikkje lett å forstå for folk flest. Vinje var kritisk til denne måten å skrive på: 

  blockquote Og det er Mange, som hava kjært seg for, at Stilen er tung og fuld av Kunstord. Eg er ikki blind for Nytten af ein slik lærd Stil; den agar eller imponerar mangein Lesar; for han trur, at det maa vera grovt til lærde Menn, som skriva slikt eit lærd Spraak. So skriv Futen og Skrivaren og so skriv Professoren og sjølve Kongsraaden.
    br
    br
    | (Vort land 7.4.1867)

  p Vinje brukte sjølv ein mykje enklare og konkret skrivemåte som var meir i tråd med det talemålet lesarane hans kjende til. 

