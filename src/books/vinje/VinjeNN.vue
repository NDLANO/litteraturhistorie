  p Før han starta Dølen i 1858, brukte Vinje dansk som skriftspråk. Etter at Aasen hadde laga dei første tekstprøvene av landsmålet tidleg i 1850-åra, var Vinje ein av dei første til å ta språket i bruk. Men han heldt seg ikkje til den landsmålsnorma som Aasen hadde sett opp. Han skreiv eit landsmål med talemålsformer frå sin eigen landsdel, nemleg Vest-Telemark, og han var svært glad i å lage nye ord. 

  h3 Journalistikken veks fram
  p I den norske grunnlova av 1814 er ytringsfridom nedfelt som ein grunnleggjande rett for alle borgarar. Ytringsfridommen var ein viktig føresetnad for framveksten av journalistikken. Frå midten av 1800-talet var redaktørar og journalistar etablerte som eigne yrkesgrupper i Noreg.

  p Av dei avisene som framleis finst i dag, er Adresseavisen i Trondheim den eldste (1767). Morgenbladet (1819) var den første norske dagsavisa. Den første samiske avisa, Muitalægje, kom ut i 1873.

  p Frå midten av 1800-talet hadde avisene funne si form som nyheitsmedium. Avisene var også viktige reiskapar i folkeopplysninga og i den gryande politiske debatten. I 1880-åra vart dei politiske partia Høgre, Venstre og Arbeidarpartiet stifta. Fleire aviser etablerte seg som talerøyr for desse partia, både nasjonalt og lokalt. I 1890 fanst det om lag 100 aviser i landet. 

  p I tida før vi fekk radio, tv og Internett, var det avisene som gav folk flest innsyn i norsk innanrikspolitikk og i det som hende i hovudstaden. Som hovudstadskorrespondent for regionavisa Drammens Tidende hadde Vinje ein viktig posisjon: 

  blockquote Intet Provindsblad, der vil gjælde for velopdragent og «tidsvarende», tør længere mangle sin Kristiana Korrespondentartikel. Den er ligesaa nødvendig for Bladet, som en Dram paa Morgenkvisten for Sjoueren. 
    br
    br
    | (Krydseren 19.3.1851) 

  h3 Kulturformidling og underhaldning

  img.col.col5.left-image.mt5(src='./Vinje_Kulturformidling_og_underholdning.jpg' alt='')
  p Frå midten av 1800-talet kom det ut tidsskrift og vekeblad som retta seg mot folk flest. Betre skulegang førte til at dei fleste kunne lese, og etterspurnaden etter lesestoff auka. Tidsskrifta, som for eksempel Dølen, inneheldt både populærvitskaplege tekstar, dikt og forteljingar. 

  p Det illustrerte bladet Skilling-Magazin kom ut i perioden 1835–1891. Undertittelen «Til Udbredelse af almennyttige Kundskaber» viser at bladet såg på seg sjølv som eit tilskot til folkeopplysninga. Illustrasjonane skulle motivere folk til å lese. 

  p Men vekeblada hadde også som mål å underhalde lesarane. Vikingen, frå 1862, var eit satirisk skjemteblad som inneheldt karikaturteikningar og dreiv harselas med aktuelle nyheitssaker.

  p I 1877 gav den danske Allers-familien ut vekebladet Illustreret Familie-Journal, som seinare vart vekebladet Allers. Trykkvaliteten var så god at somme hengde bileta på stoveveggen til pynt. 

