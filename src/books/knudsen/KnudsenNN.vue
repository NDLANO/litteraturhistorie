  h5 1907-reforma
  p Knudsen fekk mykje kritikk for fornorskingsprosjektet sitt i samtida. Både dei danskvennlege og forkjemparane for landsmålet meinte at han heldt på å skape eit blandingsmål som verken var dansk eller norsk, og han fekk berre gjennomslag for nokre få av framlegga sine mens han levde. 

  p Først i 1907, tolv år etter Knud Knudsens død, kom ei ny rettskriving der dei avgjerande prinsippa frå arbeidet hans låg til grunn. Etter 1907-reforma kunne riksmålet endeleg oppfattast som meir norsk enn dansk. I 1929 vedtok Stortinget at riksmål skulle få nemninga bokmål, og at landsmål skulle endre namn til nynorsk.

  p I dag kan vi trygt seie at grunnideen til Knudsen heldt mål likevel, for ingen ville lenger finne på å karakterisere bokmålet som noko anna enn norsk.
  
  h3 Unorsk og norsk
  
  p Knudsen var i grunnen meir oppteken av ordtilfang enn av grammatikk og lydlære. Han var purist og sterkt imot ein ukritisk bruk av framandord i norsk. Han meinte framandorda gjorde det vanskelegare for folk å skaffe seg kunnskap. Aller helst burde vi bruke norske avløysarord, meinte han, men der det var naturleg å bruke framandord eller lånord, måtte dei ha ein norsk skrivemåte.
  
