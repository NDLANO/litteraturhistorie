  h3 Fornorskingslinja 
  p Knud Knudsen hadde det same målet for auget som Ivar Aasen, nemleg eit sjølvstendig norsk skriftspråk. Men i motsetning til Aasen ville Knudsen gå «Gradvishetens Vej, ikke Bråhastens». Han såg ei gradvis fornorsking av det danske skriftspråket som den beste løysinga. Her var han på linje med Henrik Wergeland.

  p I første omgang ville han ta utgangspunkt i den såkalla danna daglegtalen og bruke denne talemålsvarianten som rettesnor for fornorskinga av skriftspråket. Knudsen karakteriserte danna daglegtale som «den almindeligste Udtale af Ordene i de Dannedes Mund». Dette var talemålet til borgarskapet i byane rundt om i landet. Knudsen syntest det var ein fordel å ta utgangspunkt i eit talemål som ikkje var knytt til nokon konkret landsdel.

  p I tillegg til danna daglegtale fanst det eit høgtidsspråk som vart kalla klokkardansk. Klokkardansken var ein bokstavrett norsk uttale av det danske skriftspråket, som mellom anna vart brukt i gudstenesta, i teateret og i skulen. Knudsen ville ha klokkardansken ut av både skulen og teateret.

