  p I tillegg var Knudsen en pådriver i arbeidet med å utvikle latinskolen til en mer allmenndannende skole. Knudsen mente at de klassiske språkfagene latin og gresk hadde en altfor dominerende plass i den høyere allmennskolen. Han ønsket at denne skoletypen skulle være noe mer enn et forstadium til universitetet. Etter hans syn burde modersmålsfaget stå sentralt, med vekt på norsk språk, litteratur og kultur. På dette området fikk han tidlig gjennomslag for ideene sine. Allerede i 1857 ble latinstilen avskaffet til studenteksammen, og i 1869 ble realartium innført som et alternativ til latinlinja. 
  
  h3 Fornorskingslinja 
  p Knud Knudsen hadde det samme målet for øyet som Ivar Aasen, nemlig et selvstendig norsk skriftspråk. Men i motsetning til Aasen ville Knudsen gå «Gradvishetens Vej, ikke Bråhastens». Han så en gradvis fornorsking av det danske skriftspråket som den beste løsningen. Her var han på linje med Henrik Wergeland. 

  p I første omgang ville han ta utgangspunkt i den såkalte dannede dagligtale og bruke denne talemålsvarianten som rettesnor for fornorskingen av skriftspråket. Knudsen karakteriserte dannet dagligtale som «den almindeligste Udtale af Ordene i de Dannedes Mund». Dette var talemålet til borgerskapet i byene rundt omkring i landet. Knudsen syntes det var en fordel å ta utgangspunkt i et talemål som ikke var bundet til noen konkret landsdel. 

  p I tillegg til dannet dagligtale fantes det et høytidsspråk som ble kalt 
