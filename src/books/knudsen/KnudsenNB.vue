  h5 1907-reformen 
  p Knudsen fikk mye kritikk for fornorskingsprosjektet sitt i samtida. Både de danskvennlige og forkjemperne for landsmålet mente at han holdt på å skape et blandingsmål som verken var dansk eller norsk, og han fikk bare gjennomslag for noen få av forslagene sine mens han levde. 

  p Først i 1907, tolv år etter Knud Knudsens død, kom en ny rettskriving der de avgjørende prinsippene fra arbeidet hans lå til grunn. Etter 1907-reformen kunne riksmålet endelig oppfattes som mer norsk enn dansk. I 1929 vedtok Stortinget at riksmål skulle få navnet 
