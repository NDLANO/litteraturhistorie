  p Bakgrunnen for tittelen Kolbotnbrev var at Arne Garborg og kona Hulda i nokre år budde i ei lita stove i Kolbotn i Østerdalen. Garborg var svært produktiv i denne perioden, og Hulda var medprodusent og redaktør. Hulda Garborg (fødd Bergersen) var dessutan eit skrivande menneske, og ho gav mellom anna ut to romanar: Et frit Forhold (1892) og Kvinden skabt af Manden (1904). Hulda Garborg stod for eit radikalt kvinnesyn som nok påverka mannen ho var gift med.

  p Frampå 1890-talet flytte ekteparet til Jæren, og Garborg bygde ei diktarstove på Knudaheio. I Knudaheibrev (utgitt i bokform i 1904) utdjupar han tankar om barndomstida og oppveksten, og han set heile livet sitt inn i ein kulturhistorisk samanheng.

  h3 Målmannen Garborg
  p Ved sida av Aasmund Olavsson Vinje var Arne Garborg den som bidrog mest til å skape ein nynorsk skriftkultur. Både Vinje og Garborg var påverka av målarbeidet til Ivar Aasen, og Garborg var overtydd om at det var nynorsken som best representerte norsk uttrykksmåte og kultur.

  p Som journalist skreiv Garborg både dansk og landsmål (nynorsk). Romanen Trætte Mænd frå 1891 gav han ut på dansk, men det var nok for å nå ei dansk målgruppe. Elles var han trufast mot nynorsken, og han vart sterkare i trua på målsaka etter at han flytte heim til Jæren. Garborg levde fram til 1924, men det var lite han skreiv dei siste åra.

  p Arne Garborg var ein autoritet og pådrivar i arbeidet med å vidareutvikle det nynorske skriftspråket og den nynorske skriftkulturen. Samstundes var han heilt tydeleg på at ikkje noko land kan vere seg sjølv nok i kultursamanheng, ein levande nasjonal kultur treng jamleg fornying gjennom impulsar utanfrå.

