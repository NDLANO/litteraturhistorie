  h3 Målmannen Garborg 
  p Ved siden av Aasmund Olavsson Vinje var Arne Garborg den som bidro mest til å skape en nynorsk skriftkultur. Begge var påvirket av Ivar Aasens målarbeid, og Garborg var overbevist om at det var nynorsken som best representerte norsk uttrykksmåte og kultur. 

  p Som journalist skrev Garborg både dansk og landsmål (nynorsk). Romanen 
    em Trætte Mænd 
    | fra 1891 gav han ut på dansk, men det var nok for å nå ei dansk målgruppe. Elles var han trofast mot nynorsken, og han ble sterkere i troen på målsaka etter at han flyttet hjem til Jæren. Garborg levde fram til 1924, men det var lite han skrev de siste årene. 

  p Arne Garborg var en autoritet og pådriver i arbeidet med å videreutvikle det nynorske skriftspråket og den nynorske skriftkulturen. Samtidig var han helt tydelig på at ikke noe land kan være seg selv nok i kultursammenheng, en levende nasjonal kultur har behov for jevnlig fornyelse gjennom impulser utenfra. 

