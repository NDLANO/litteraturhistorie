  h3 Trætte Mænd – en dekadanseroman 
  p Rundt 1890 hadde Garborg fått et forfatternavn utenfor Norge, og i Tyskland var han en periode den mest leste av alle nordiske forfattere. 
    em Trætte Mænd 
    | (1891) skrev Garborg på dansk. Det er ein eksperimentell roman i dagbokform. Hovedpersonen Gabriel Gram mislykkes med alle kjærlighetsprosjektene sine. Han får heller aldri riktig bestemt seg for hva slags verdier og normer han vil leve etter. Til slutt ender han opp i en slags religiøs mystisisme. 

  p I 
    em Trætte Mænd 
    | tematiserte Garborg eksistensielle spørsmål som mange intellektuelle var opptatt av mot slutten av 1800-tallet. Troen på det guddommelige var borte, menneskene slet med å finne et nytt ståsted og forventningene til framtida sviktet. Et fransk uttrykk for denne følelsen av at en epoke ugjenkallelig var slutt, var «fin de siècle» (slutten av hundreåret). 

  p Begrepet 
    em dekadanse 
    | (forfall) er tett knyttet til «
    em fin de siècle
    | ». Mange tenkere og forfattere mente at samtida bar preg av moralsk og kulturelt forfall. Vi bruker begrepet dekadanseromaner om bøker som problematiserer det å være menneske i ei tid der tradisjonelle verdier og samfunnsstrukturer ser ut til å gå i oppløsning. 

  p 
    em Trætte Mænd 
    | representerte et vendepunkt for Garborg – bort fra naturalismen og over i en nyskapende fase med fokus på individ og modernitet. Den neste romanen, 
    em Fred 
    | (1892), var også en roman om oppløsning. Her skrev Garborg om bondesamfunnet og pietismen på Jæren, og han vendte tilbake til et mer naturalistisk grunnsyn. Boka har klare selvbiografiske trekk. 

  h3 Haugtussa
  p Den episk-lyriske diktsyklusen 
    em Haugtussa 
    | (1895) representerer en ny fase og et omslag i Garborgs diktning. Han er tilbake på Jæren og ganske sikkert påvirket av de nyromantiske strømningene i tida. Men i tillegg har omslaget å gjøre med en krise og en erkjennelse i Garborgs liv. 

  p 
    em Haugtussa 
    | handler om de utfordringene og prøvelsene mennesket blir utsatt for gjennom livet. Verket handler om irrasjonelle krefter som angst, kjærlighet, kampen mellom det onde og det gode i oss. Kort sagt gjelder det kampen mellom de konstruktive og de destruktive kreftene i menneskesinnet, og utfordringen med å kunne mestre dem. 

  p Garborg har gitt dikterverket en folkloristisk form, der hvert dikt står aleine. 
    em Haugtussa 
    | er blitt tonesatt av Edvard Grieg, det er oppført som drama flere ganger, og verket har inspirert mange bildekunstnere. Kjente tekster og melodier er blant annet «Småsporven gjeng i tunet» og «Det stig av hav eit alveland». 

  p Verket forteller om ungjenta Veslemøy, som er synsk. Hun ser huldrer, nisser og troll, og haugfolkene (de underjordiske) prøver å lokke henne til seg for å få makt over henne. Men hun står imot. Hun ser at det eneste som kan gi fred, er å gjennomleve sorgen og lidelsen over kjærligheten og sviket hun har opplevd. Bare slik kan hun være et menneske. Budskapet i 
    em Haugtussa 
    | er at mennesket ikke kan flykte fra sorgene og lidelsene det møter, tvert om må vi møte lidelsene og gjennomleve dem for at vi skal komme videre. 

