  p I 1883 kom romanen 
    em Bondestudentar
    | , som ble Garborgs gjennombrudd som forfatter. Hovedpersonen i boka, Daniel Braut, er bondesønn og skal studere i byen. Garborg viser hvordan fattigdom og lav selvfølelse ødelegger for Daniel. Han ender opp med å svikte røttene og idealene sine. 

  p Garborg så tidlig på seg selv som europeer og gjorde reiser til flere europeiske land. Der ble han påvirket av nye ideer og tanker. I essayet «Bondekultur og åndsfridom» gikk han kraftig imot forestillingen om at en hjemlig kultur skal være bygd på «heimleg grunn». Språket er nasjonalt og en forutsetning for kultur, sier han, men kulturen er grenseløs: 

  p Å laga ein serskilt nordmannskultur vilde ikkje vera å gjera oss meir norske, det vilde vera å gjera oss meir dumme, og me vilde mindre og mindre kunna vera med i det store kappstræv millom nasjonarne.

  p Garborg så individuell og nasjonal identitetsbygging som en gjensidig og kulturell dannelsesprosess. Her presenterte han moderne tankegods som lå langt forut for sin tid. 

  
  h3 Mannfolk 
  p Romanen 
    em Mannfolk 
    | (1886) er ei bok om seksualitet og ekteskap, og et innlegg i debatten om dobbeltmoral og hykleri på 1880-tallet. Framstillingen i boka er naturalistisk, det er arv og miljø som er med på å styre skjebnen til de fleste. Unge, fattige jenter ender opp i prostitusjon, og mannfolkene greier ikke å inngå forhold som bygger på kjærlighet. 

  p En av karakterene i romanen, Georg Jonathan, drømmer om et frivillig og likeverdig kjærlighetsforhold. Men han og kjæresten Julie Lindner forstår fort at et slikt forhold er umulig i et samfunn der kvinner ikke kan livnære seg selv, og der mennene må vente i mange år før de får råd til å forsørge ei kone. Dette systemet gir mannen makt i samfunnet og i familien, men det ødelegger samtidig følelseslivet hans og tvinger fram usedelighet. For kvinnene er det enda verre – de har verken selvstendige liv eller håp om fritt å kunne velge ektefelle.

  p Året før 
    em Mannfolk 
    | kom ut, hadde Christian Krohg gitt ut romanen 
    em Albertine
    | , der hovedpersonen er ei fattig jente som ender opp som prostituert. Boka til Krohg ble stoppet og beslaglagt, og Krohg ble dømt for usedelig virksomhet. Mannfolk ble ikke stoppet, men reaksjonene var kraftige fra både politisk og kulturelt hold, og Garborg mistet jobben som statsrevisor året etter på grunn av boka og debattinnleggene sine.

