  p For det andre har han heile tida vist lojalitet med industriarbeidarar – og med sjøfolk. Det er desse samfunnsgruppene han har ønskt å gi røyst til. Den fiktive industristaden Lovra, og sjømannslivet, er ramma for mange av romanane hans. Det er i desse miljøa han sjølv har røtene sine, det er forteljartradisjonen knytt til desse miljøa han har vakse opp med.

  p Det tredje perspektivet som er tydeleg til stades i både skjønnlitteraturen og sakprosaproduksjonen, er interessa for Latin-Amerika og kultur frå den spansktalande delen av verda. Fløgstad har gjendikta lyrikarar frå Cuba, spanske barokkdiktarar og chilenaren Pablo Neruda. Han har skrive reiseskildringar frå LatinAmerika, og skrive om norske emigrantar dit, og han har på ulike måtar late seg inspirere av den spanskspråklege litteraturen og kulturen.

  h3 Nyare norsk historie i romanform
  p 
    em Dalen Portland, Fyr og flamme 
    | (1980) og 
    em Kniven på strupen 
    | (1991) er døme på romanar om norsk historie der heile eller delar av handlinga er lagd til industristaden Lovra. Men som kritisk samfunnsanalytikar ser forfattaren ut over industrimiljøet. I Fyr og flamme går historia fram til byrjinga på oljeeventyret – og oljeindustrien – på slutten av 70-talet. I Kniven på strupen skriv Fløgstad om kjøpesenter og narkotikatrafikk, med ein internasjonal medie- og finansmarknad som bakteppe.

  p 
    em Det 7. klima 
    | (1986) er ein skarp satirisk analyse, i form av ein framtidsbiografi, av vårt moderne medie- og informasjonssamfunn. Og i 
    em Kron og mynt 
    | frå 1998 skildrar Fløgstad mellom anna rolla til kunsten i det postmoderne mediesamfunnet – og han skildrar eit sosialdemokrati i forfall. Fløgstad har generelt eit skeivt blikk på det norske velferdssamfunnet, og på eit kapitalistisk forbrukarsamfunn der alt blir redusert til underhaldning og forbruk.

  p 
    em Romanen Grense Jakobselv 
    | (2009) skapte debatt på ulike måtar. Romanens motiv og bodskap – at nazismen på mange måtar var eit eliteprosjekt, og at mange nazistar også etter krigen sat godt i ulike maktposisjonar, skapte ny debatt om krigen og oppgjeret etterpå. Men enda større debatt skapte Fløgstad med handteringa av kjelder, og Grense Jakobselv er ein av dei mange romanane i samtida som har skapt debatt om forholdet mellom fiksjon og røyndom. Korleis bør ein fiksjonsforfattar gå fram i høve til kjeldene sine? Kva er sant – og kva er oppdikta? Og korleis kan ein nytte fiksjonen til å seie noko om røyndommen? 
  
