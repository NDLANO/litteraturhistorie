  h5 Vekslande forteljarperspektiv
  p Det er fleire forteljarar i 
    em Dalen Portland
    | . Forteljinga om Arnold og Selmer har ein autoral, allvitande forteljar. Forteljaren har større innsikter i samfunnsmekanismane som pregar livet til og rundt dei to, og han kan sjå både bakover og framover – slik at lesaren kan sjå karakterane i perspektiv. Forteljinga om Rasmus, derimot, er stort sett fortald av Rasmus sjølv, ja forteljaren gir han ordet inne i forteljinga. Mot slutten av romanen får vi vite at Rasmus strevar med å skrive sjølvbiografien sin. Denne biografien opnar med den same setninga som romanen opnar med. 
    em Ein slik leik med vekslande forteljarperspektiv 
    | er typisk for mange av romanane til Fløgstad.

  p Eit anna grep som peikar ut over sosialrealismen, er dei mange fantastiske episodane i forteljinga om Rasmus. Han er einaste overlevande etter ein flystyrt, han blir sambuar – og ventar barn – med ei kvinne som skal vise seg å vere halvsøstera hans, og han er elles utstyrt med eigenskapar – og ein mengde flaks – som til tider verkar noko urealistiske.

  p 
    em Den magiske realismen 
    | er kjenneteikna av at overnaturlege – eller fantastiske – hendingar skjer i ei elles ganske så realistisk handling. I historia om og av Rasmus finn vi mange slike døme. Pikareskromanen er ein sjanger som oppstod i Spania på 1500-talet. Han er ofte skriven som ei eg-forteljing, som inneheld korte episodar og handlar om ein omreisande, eventyrlysten og skøyaraktig vagabond – ein pícaro. På mange måtar kan ein lese Rasmus som ein slags moderne pícaro. 

  h3 Mot eit postmoderne uttrykk

  figure.col.col4.left-image.mt5
    img(src='./flogstad_Portrett3.jpg' alt='Portrett av vaksen mann med bart, halve ansiktet i skugge mot flammegul bakgrunn. Foto')
    figcaption Kjartan Fløgstad. Foto: © Luca Kleve-Ruud / Samfoto / Scanpix
    
  p Denne forma for leik med sjangrar og forteljarnivå er typisk for eit postmoderne uttrykk. Fløgstad låner og blandar tekstar frå ulike tekstkulturar, og seier sjølv at det er tre ting som har hatt mykje å seie for forfattarskapen hans: forteljartradisjonen knytt til smelteverket i Sauda, spanskspråkleg litteratur og kinoen i Sauda. Her såg han mange krim- og cowboyfilmar.

  p I novella «Riksvegen vestover», frå 1972, møter vi ein drosjesjåfør som har lånt fleire karaktertrekk frå cowboylitteraturen. Hovudpersonen, drosjesjåføren, les Morgan Kane, da han får ein tur til Vestlandet. Undervegs utviklar han seg til å bli meir og meir lik Morgan Kane, tenkjer som han og handlar som han. Kvinnene i novella har også klare trekk frå det noko føreseielege og klisjéfylte kjønnsrollemønsteret i populærkulturen. Slike intertekstuelle grep er typisk for Fløgstad – og for det postmoderne uttrykket generelt.

  p Eit anna typisk postmoderne trekk ved fleire av tekstane til Fløgstad er at dei er 
    em metafiksjonelle
    | . På ulike måtar kommenterer teksten seg sjølv som tekst, forteljaren kommenterer til dømes at han skriv ei forteljing. Fløgstad bruker mellom anna slike forteljargrep i romanane 
    em Dalen Portland, 
    em Fyr og flamme 
    | og 
    em Det 7. klima
    | . Fleire har peikt på at 
    em Det 7. klima
    | , som er ein kompleks og sjølvmedviten tekst om det postmoderne informasjonssamfunnet, er det nærmaste vi kjem ein postmoderne roman i norsk 80-tals litteratur.

