  h5 Vekslende fortellerperspektiv
  p Det er flere fortellere i 
    em Dalen Portland
    | . Fortellingen om Arnold og Selmer har en autoral, allvitende forteller. Fortelleren har større innsikter i samfunnsmekanismene som preger livet til og rundt de to, og han kan se både bakover og framover – slik at leseren kan se karakterene i perspektiv. Fortellingen om Rasmus, derimot, er stort sett fortalt av Rasmus selv, ja fortelleren gir ham ordet inne i fortellingen. Mot slutten av romanen får vi vite at Rasmus strever med å skrive sin selvbiografi. Denne biografien åpner med den samme setningen som romanen åpner med. 
    em En slik lek med vekslende fortellerperspektiv 
    | er typisk for mange av Fløgstads romaner. 

  p Et annet grep som peker ut over sosialrealismen, er de mange fantastiske episodene i fortellingen om Rasmus. Han er eneste overlevende etter en flystyrt, han blir samboer – og venter barn – med ei kvinne som skal vise seg å være halvsøstera hans, og han er ellers utstyrt med egenskaper – og en mengde flaks – som til tider virker noe urealistiske. 

  p 
    em Den magiske realismen 
    | kjennetegnes av at overnaturlige – eller fantastiske – hendelser skjer i en ellers ganske så realistisk handling. I historien om og av Rasmus finner vi mange slike eksempler. Pikareskromanen er en sjanger som oppstod i Spania på 1500-tallet. Den er ofte skrevet som en jeg-fortelling, bestående av korte episoder, og handler om en omreisende, eventyrlysten og skøyeraktig vagabond – en pícaro. På mange måter kan man lese Rasmus som en slags moderne pícaro. 

  h3 Mot et postmoderne uttrykk 

  figure.col.col4.left-image.mt5
    img(src='./flogstad_Portrett3.jpg' alt='Portrett av voksen mann med bart, halve ansiktet i skygge mot flammegul bakgrunn. Foto')
    figcaption Kjartan Fløgstad. Foto: © Luca Kleve-Ruud / Samfoto / Scanpix
    
  p Denne formen for lek med sjangre og fortellernivåer er typisk for et postmoderne uttrykk. Fløgstad låner og blander tekster fra ulike tekstkulturer, og sier selv at det er tre ting som har betydd mye for forfatterskapet hans: fortellertradisjonen knyttet til smelteverket i Sauda, spanskspråklig litteratur og kinoen i Sauda. Her så han mange krim- og cowboyfilmer. 

  p I novellen «Riksvegen vestover», fra 1972, møter vi en drosjesjåfør som har lånt flere karaktertrekk fra cowboylitteraturen. Hovedpersonen, drosjesjåføren, leser Morgan Kane, da han får en tur til Vestlandet. Underveis utvikler han seg til å bli mer og mer lik Morgan Kane, tenker som han og handler som han. Kvinnene i novellen har også klare trekk fra populærkulturens noe forutsigbare og klisjéfylte kjønnsrollemønster. Slike intertekstuelle grep er typisk for Fløgstad – og for det postmoderne uttrykket generelt. 

  p Et annet typisk postmoderne trekk ved flere av Fløgstads tekster er at de er 
    em metafiksjonelle
    | . På ulike måter kommenterer teksten seg selv som tekst, fortelleren kommenterer for eksempel at han skriver en fortelling. Fløgstad bruker blant annet slike fortellergrep i romanene 
    em Dalen Portland
    | , 
    em Fyr og flamme 
    | og 
    em Det 7. klima
    | . Flere har pekt på at Det 
    em 7. klima
    | , som er en kompleks og selvbevisst tekst om det postmoderne informasjonssamfunnet, er det nærmeste vi kommer en postmoderne roman i norsk 80-talls litteratur.  


