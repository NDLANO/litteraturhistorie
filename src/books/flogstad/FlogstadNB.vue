  p For det andre har han hele tida vist lojalitet med industriarbeidere – og med sjøfolk. Det er disse samfunnsgruppene han har ønsket å gi stemme til. Det fiktive industristedet Lovra, og sjømannslivet, er rammen for mange av romanene hans. Det er i disse miljøene han selv har sine røtter, det er fortellertradisjonen knyttet til disse miljøene han har vokst opp med. 

  p Det tredje perspektivet som er tydelig til stede i både skjønnlitteraturen og sakprosaproduksjonen, er interessen for Latin-Amerika og kultur fra den spansktalende delen av verden. Fløgstad har gjendiktet lyrikere fra Cuba, spanske barokkdiktere og chileneren Pablo Neruda. Han har skrevet reiseskildringer fra, og skrevet om norske emigranter til, Latin-Amerika, og han har på ulike måter latt seg inspirere av den spanskspråklige litteraturen og kulturen. 

  h3 Nyere norsk historie i romanform 
  p 
    em Dalen Portland, Fyr og flamme 
    | (1980) og 
    em Kniven på strupen 
    | (1991) er eksempler på romaner om norsk historie der hele eller deler av handlingen er lagt til industristedet Lovra. Men som kritisk samfunnsanalytiker ser forfatteren ut over industrimiljøet. I Fyr og flamme går historien fram til begynnelsen på oljeeventyret – og oljeindustrien – på slutten av 70-tallet. I Kniven på strupen skriver Fløgstad om kjøpesentre og narkotikatrafikk, med et internasjonalt medie- og finansmarked som bakteppe. 

  p 
    em Det 7. klima 
    | (1986) er en skarp satirisk analyse, i form av en framtidsbiografi, av vårt moderne medie- og informasjonssamfunn. Og i 
    em Kron og mynt 
    | fra 1998 skildrer Fløgstad blant annet kunstens rolle i det postmoderne mediesamfunnet – og han skildrer et sosialdemokrati i forfall. Fløgstad har generelt et skjevt blikk på det norske velferdssamfunnet, og på et kapitalistisk forbrukersamfunn der alt reduseres til underholdning og forbruk. 

  p Romanen 
    em Grense Jakobselv 
    | (2009) skapte debatt på ulike måter. Romanens motiv og budskap – at nazismen på mange måter var et eliteprosjekt, og at mange nazister også etter krigen satt godt i ulike maktposisjoner, skapte ny debatt om krigen og oppgjøret etter den. Men enda større debatt skapte Fløgstad med sin håndtering av kilder, og Grense Jakobselv er en av de mange romanene i vår samtid som har skapt debatt om forholdet mellom fiksjon og virkelighet. Hvordan bør en fiksjonsforfatter forholde seg til sine kilder? Hva er sant – og hva er oppdiktet? Og hvordan kan man bruke fiksjonen til å si noe om virkeligheten? 

  h3 Dalen Portland

  figure.col.col12
    img(src='./flogstad_Dalen_Portland1.jpg' alt='Røyk fra industripiper fyller lufta i nærområdet. Foto')
    figcaption © Helge Sunde / Samfoto / Scanpix
  
  h5 Motiv og tema 
  p Dalen Portland er et stykke industrihistorie, og et stykke norgeshistorie, fra midten av 1930-tallet til midten av 70-årene. I romanen følger vi to generasjoner industriarbeidere på Fløgstads oppdiktede industristed – Lovra. 

  p Den første delen av romanen, kalt «Harde tak» er viet fortellingen om Selmer Høysand. Selmer er bondesønn, men i takt med tida får han seg jobb på fabrikken i bygda – som er eid av et stort amerikansk selskap. Selmer er førstegenerasjons industriarbeider, og han oppdager fort at han kan tjene mye mer som industriarbeider enn som bonde. Han er en lojal og arbeidsom kar, men etter hvert innser han at identiteten hans er tett knyttet til livet som bonde. Han begynner å jobbe på familiegården i fritida, stemmer på Bondepartiet, leser nynorskforfattere med røtter i bondekulturen – og deltar aktivt i bondeungdomslagets fritidstilbud. 

  p Resten av romanen handler om Selmers sønn, Arnold Høysand, og Rasmus Høysand – som muligens er søskenbarnet til Arnold. Arnold er en typisk representant for en industriarbeider i et voksende velferds- og forbrukersamfunn. Livet hans består av grå og rutinepregede hverdager – med rekkehus, salong og sydenferier. Arnold er på fornavn med programlederne på tv og venter ofte på at hallodamen skal si god natt før han går og legger seg. Rasmus, derimot, er på mange måter en utbryter. Han vokser opp med mora si, Durdei – uten å vite hvem faren er. Han er en skarp gutt, og han gjør det godt på skolen, men bryter opp og reiser til sjøs. Etter noen år på farten, og etter mange ganske eventyrlige opplevelser, kommer han tilbake til hjemlandet. 

