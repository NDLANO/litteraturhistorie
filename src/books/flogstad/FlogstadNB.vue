  p Fløgstad har vært en kritisk stemme i norsk litteratur- og samfunnsdebatt i over 40 år. På 70-tallet var han redaksjonsmedlem i litteraturtidsskriftet 
    em Basar
    | . Forfatterskapet hans spenner vidt – fra komplekse romaner til tradisjonell krim og sakprosa. Han er også kjent for å leke med – og blande – ulike sjangre i tekstene sine. 

  h3 Symbolisme, marxisme  og spanskspråklig kultur 

  figure.col.col6.right-image.mt5
    img(src='./flogstad_NRK1.jpg' alt='Ung mann med halvlangt hår og bart leser i ei bok. Foto')
    figcaption Se en ung Kjartan Fløgstad snakke om lyrikken sin på NRK Skole
    
  p De to første diktsamlingene til Kjartan Fløgstad er modernistiske. De kan plasseres i en symbolistisk tradisjon. Mange av diktene er rike på symboler, de er komplekse, bildetunge – og uten fast rim og rytme. Det er et godt stykke fra disse diktene til dikt Fløgstad har skrevet seinere i forfatterskapet. I flere av romanene hans finner man dikt – disse er tradisjonelle i  formen, og likner mer på sangbare viser. 

  p Fløgstads dikt har også blitt framført til musikk, av gruppa Vestlandsfanden. I 1993 gav Fløgstad ut 
    em Dikt og spelmannsmusikk 
    | 1968–1993 – dette er dikt og gjendiktninger av forfatteren. 

  p Som sakprosaforfatter har Fløgstad en allsidig produksjon. Han har skrevet  reiseskildringer og samfunnskritiske og litterære essays. 
    em Portrett av eit magisk liv
    | , fra 1988, er en biografi over dikteren Claes Gill, men også en historie om Gills dannelsesreise fra industristedet Odda til Oslos vestkant. Og både 
    em Arbeidets lys. Tungindustrien i Sauda gjennom 75 år 
    | (1990) og boka om arkitektfirmaet Snøhetta, 
    em Hus som vil meg hysa 
    | (2004), er fortellinger om et stykke norsk industri-, bedrifts-, og kulturhistorie. I essaysamlingen 
    em Loven vest for Pecos 
    | (1981) argumenterer Fløgstad for at det ikke går an å skape et klart skille mellom såkalt «høy-» og «lavkultur». 

  p At et skarpt skille mellom genuine kunstverk og kulturindustri for massen ikke finnes, er en sentral forutsetning for Fløgstad både som sakprosaforfatter og som skjønnlitterær forfatter. I de komplekse romanene sine, alle med et skrått blikk på Norge og norsk historie, har Fløgstad stort sett vært trofast mot et marxistisk grunnsyn på verden – og mot tre hovedinteresser: 

  p For det første har Fløgstad gjennom hele forfatterskapet kjempet for nynorsk skriftspråk og skriftkultur. Han skriver på nynorsk og har i ulike sammenhenger argumentert for å heve statusen til nynorsk skriftspråk og skriftkultur, noe han blant annet markerte gjennom å gi prispengene fra Nordisk Råds litteraturpris til nettopp Noregs Mållag. 

