  p Arnulf Øverland ble født i Kristiansund i 1889 og vokste opp i Bergen. I 1904 flyttet familien til Oslo. Etter artium ved katedralskolen begynte han med filologiske studier. Men det gikk tregt med studiene, for det var forfatter og maler han ville bli. 
  
  h5 De første utgivelsene 
  p Øverland debuterte i 1911 med diktsamlingen Den ensomme fest, og i tiårene fram mot andre verdenskrig gav han ut mange dikt- og novellesamlinger. Tematisk var han mest opptatt av sentrallyriske temaer som kjærlighet, lengsel, ensomhet og død. I diktsamlingen Brød og vin (1919) og i novellen «Ærens mark» fra samlingen Deilig er jorden (1923) uttrykker han sin protest mot de mange grusomme hendingene under første verdenskrig. 
  
  h5 Medlem i Mot Dag 
  p Alt i 1920-årene markerte Øverland seg som en radikaler. Han var blant annet medlem av den politiske organisasjonen Mot Dag. Motdagistene samlet seg om ideene til Karl Marx og hadde mange kjente medlemmer. De utgav også et tidsskrift med samme navn. 
  
  h5 En overbevist ateist 
  p Arnulf Øverland var alltid krass i kritikken når han gikk til angrep på forhold han ikke kunne godta. Han var overbevist ateist og reiste landet rundt med det polemiske foredraget 
    a(href='http://virksommeord.no/tale/387/' target='_blank') «Kristendommen – den tiende landeplage»
    | . I 1933 ble han tiltalt etter blasfemiparagrafen på grunn av dette foredraget. Øverland måtte møte i retten, men ble frifunnet med én stemmes overvekt. 
  
  h5 Riksmålsmann 
  p I 1938 kom det en omfattende rettskrivingsreform som skulle føre bokmål og nynorsk nærmere hverandre. Denne reformen var Øverland sterkt imot. Som formann i Riksmålsforbundet fra 1947 til 1956 var han blant de fremste i kampen mot den statlige språkpolitikken. Han kunne være en sterkt usaklig debattant. 
  
  h5 Antimodernist 
  p Øverland hadde heller ikke noe til overs for modernismen, verken i bildekunsten eller i litteraturen. Allerede i 1920-årene hadde han skrevet polemiske artikler om Picasso og andre kunstnere som prøvde ut et nytt formspråk. I 1953 dro han på turné landet rundt med foredraget «Tungetale fra parnasset», der han særlig polemiserer mot modernistisk lyrikk, som han kaller «rabbel og snikksnakk». 
  
  h5 Endret syn på kommunismen 
  p Øverland var en uredd mann med klare holdninger, som han gjerne stod inne for hele livet. Men han endret syn på kommunismen mot slutten av 1930-årene. Moskvaprosessene (1936–1938), en utrenskingsaksjon der Stalin brutalt fjernet mange framstående medlemmer av det sovjetiske kommunistpartiet, fikk Øverland til å melde seg ut av det norske kommunistpartiet i 1937. 
  
  h5 Mot fascisme og nazisme 

