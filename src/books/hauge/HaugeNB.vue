    | i tunet 
    br
    | når du kjem. 
    br
    | Snakk litt med katten. 
    br
    | Det er han som er varast i garden. 

  p Det er denne enkle uttrykksmåten som er Hauges kjennemerke. Men bak den minimalistiske formen ligger det en livsvisdom og en innsikt som Hauge har evnet å nå svært mange med: 

  blockquote
    strong Ljåen 
