  h3 Ein lågmælt diktar
  p I samlinga 
    em Dropar i austavind 
    | (1966) går Hauge enda eit steg vidare i diktinga si. Her finn vi korte, tilsynelatande svært enkle dikt om kvardagslege ting: 

  blockquote
    | Katten sit 
    br
    | i tunet 
    br
    | når du kjem. 
    br
    | Snakk litt med katten. 
    br
    | Det er han som er varast i garden. 

  Det er denne enkle seiemåten som er kjennemerket til Hauge. Men bak den minimalistiske forma ligg det ein livsvisdom og eit innsyn som Hauge har evna å nå svært mange med:

  blockquote
    strong Ljåen 
    br
    | Eg er so gamal 
    br
    | at eg held meg til ljå. 
    br
    | Stilt syng han i graset, 
    br
    | og tankane kan gå. 
    br
    | Det gjer ikkje vondt heller, 
    br
    | segjer graset, 
    br
    | å falla for ljå. 

  p Fleire av dikta i samlinga 
    em Dropar i austavind 
    | er inspirerte av japansk haikudikting. Haikudiktet er stutt og utan rim, og det skal samanfatte noko om ein situasjon eller ei stemning. Haikudiktinga har hatt stor innverknad på den modernistiske lyrikken. Olav H. Hauge eksperimenterte med denne forma tidleg i 1960-åra, og han inspirerte også andre norske lyrikarar som Jan Erik Vold, Eldrid Lunden og PaalHelge Haugen.

  h3 Rikt biletspråk
  p Biletspråket til Hauge tek utgangspunkt i bygdeliv og natur, og det er vestlandsnaturen som kjem til syne, både konkret og symbolsk.

  blockquote
    strong Eg dreg ifrå glaset 
    br
    | Eg dreg ifrå glaset fyrr eg legg meg, 
    br
    | eg vil sjå det levande myrkret når eg vaknar, 
    br
    | og skogen og himmelen. Eg veit ei grav 
    br
    | som ikkje har glugg mot stjernone. 
    br
    | No er Orion komen i vest, alltid jagande – 
    br
    | han er ikkje komen lenger enn eg. 
    br
    | Kirsebærtreet utanfor er nake og svart. 
    br
    | I den svimlande blå himmelklokka 
    br
    | ritar morgonmånen med hard nagl. 

  p Hauge bruker enkle metaforar. Alle er henta frå naturen eller nærmiljøet, og det er heile tida menneskeleg livsrøynsle han prøver å seie noko om. Her følgjer eit lite dikt om kommunikasjon, også tydeleg inspirert av haikutradisjonen: 

  blockquote
    strong Ord 
    br
    | Eit ord 
    br
    | – ein stein 
    br
    | i ei kald elv. 
    br
    | Ein stein til – 
    br
    | Eg må ha fleire steinar skal eg koma over. 

  p Vi kan sjå på elva som eit symbol på livet, mens steinane er symbol for ord og kommunikasjon. Da blir bodskapen at vi ikkje kan klare oss åleine i dette livet. Vi treng kontakt med andre.

