  h3 Frå bunden til ubunden stil
  p I 1950-åra tok Hauge til å eksperimentere med den forma som skulle bli særmerkt for han. Han la bort enderim og fast rytme og skreiv no kortare dikt med eit meir konkret innhald. Hauge tok gjerne utgangspunkt i noko kvardagsleg, som for eksempel ein situasjon, ein gjenstand eller eit dyr. Diktet «Gjer ein annan mann ei beine» handlar om ein mann som vil betale for ei teneste (ei beine) fordi han veit at han ikkje kan gjengjelde tenesta på anna vis:

  blockquote
    strong Gjer ein annan mann ei beine 
    br
    br
    | Han kom or fjellet, skulde heim, 
    br
    | fekk føring ifrå Osa 
    br
    | ut til Øydvinstò. 
    br
    | Og han var raust 
    br
    | og baud betal. 
    br
    br
    | Men Osamannen 
    br
    | var ikkje fal: 
    br
    | Eg vil betala; 
    br
    | eg kan ikkje nå deg 
    br
    | med ei beine att. 
    br
    br
    | So gjer ein annan mann 
    br
    | ei beine då, 
    br
    | sa Osamannen, 
    br
    | og skauv ifrå. 
    br
    br
    | Fra samlingen 
    em Seint rodnar skog i djuvet 
    | (1956) 

  p Den vesle historia fortel noko allment: Ein treng ikkje alltid betale for ei teneste, og dersom det ikkje er mogleg å få gjort ei motyting – så gjer ein annan mann ei beine då. Ein enkel leveregel – og samstundes god kvardagsfilosofi.

  p I same samlinga finn vi dette diktet med ein annan leveregel:

  blockquote
    | Syn oss åkeren din 
    br
    | Møt ikkje med: 
    br
    | gøyande hund, 
    br
    | hotande hand, 
    br
    | trakk ikkje i rugen! 
    br
    | Men syn oss åkeren din 
    br
    | ei morgonstund! 

  p Diktet kan tolkast slik at vi må vere audmjuke og opne i møte med andre, for da blir vi også sjølve møtt med velvilje. I tillegg får vi eit rikare liv når vi deler det vi gjer og tenkjer med andre. Hauge bruker enkle, konkrete bilete frå bondekvardagen. Nettopp fordi han skriv så knapt og biletleg, tvingar han lesaren til å reflektere og dra inn eigne røynsler for å utfylle teksten og gi han meining.

