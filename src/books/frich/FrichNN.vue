  h3 Øvre Richter Frich – ein bestseljar

  img.col.col4.left-image.mt5(src='./frich_Portrett1.jpg' alt='')
  p I 1920- åra var Øvre Richter Frich (1872– 1945) den forfattaren som selde mest her i landet, med eit samla opplag på vel to millionar bøker. Bøkene hans vart omsette til ni språk og vart kalla spenningsromanar. Det var vel nettopp opplevinga av spenning og tankeflukt som gjorde at litteraturen hans fascinerte så mange.

  p Richter Frich var også journalist i fleire aviser, og han fekk status som stjernereporter i Aftenposten etter å ha dekt bybrannen i Ålesund i 1904. Måten han skreiv på, kom i ettertid til å vere eit mønster for norsk reportasjejournalistikk.

  p I 1911 debuterte Richter Frich som forfattar med romanen De knyttede næver. Her blir lesarane for første gong kjende med gjennomgangsfiguren Jonas Fjeld.

  p Jonas Fjeld har fleire fellestrekk med forfattaren sjølv. Richter Frich var ein individualist som levde etter prinsippet om å ha minst mogleg ansvar. Han sa opp journalistjobben i Aftenposten da han skjønte at han kunne leve av å vere forfattar og globetrotter på heiltid. I tjue år reiste han ikring i verda og levde gode dagar, samstundes som han skreiv spenningsromanar. Han var rastlaus av natur og skaffa seg ikkje fast bustad før han var seksti år. 

