  p Det er kampen mellom det gode og det vonde som er essensen i alle dei 21 bøkene om Jonas Fjeld, som kom i perioden 1911–1920. Den blonde germanske legen stig fram som ein helt og kjempar mot alle uromoment i samfunnet. Han er alltid på jakt etter å knuse ikkje-germanarar av typen anarkistar, sosialistar og kommunistar. Helten er alltid eit godt menneske, og moralen er at det er den gode og rettferdige som vinn til sist.

  h3 Kritikk av ideologi og menneskesyn

  figure.col.col6.right-image.mt5
   img(src='./frich_Kritikk_av_ideologi_og_menneskesyn1.jpg' alt='')
   figcaption Omslagsbilete til De udødelige dverge; Lille Jonas Fjeld; Storfjellets herre Øvre Richter Frich utgitt av Norild
  p Det var få eller ingen i samtida som reagerte på menneskesynet som kom til uttrykk i bøkene til Richter Frich. Hovudpersonen Jonas Fjeld vert framstilt som ein blond, høgreist og breiskuldra mann av den ariske rasen. Han er ein heltefigur: lege av yrke, ein mann som meistrar det meste, er iskald i alle situasjonar og ein skarpskodd våpenbrukar. Fiendane hans derimot er alltid utlendingar, og dei blir framstilte som mindreverdige og nedbrytande element som driv med statskupp og anna undergravande verksemd. Ofte er dei av den såkalla slaviske rasen.

  p Litteraturforskaren Willy Dahl har omtalt Øvre Richter Frich som «den mest erklærte rasist og voldsromantiker» i norsk populærlitteratur. Men i mellomkrigsåra var ikkje menneskesynet til Richter Frich noko ekstremt eller spesielt. Det var samtidas syn på framande folk og kulturar som kom til uttrykk i populærlitteraturen frå denne tida, og det gjaldt ikkje minst den populærlitteraturen som vart utgitt under nemninga kristen litteratur.

  h3 Kriminalromanen

  img.col.col5.left-image.mt5(src='./frich_Kriminalromanen1.jpg' alt='')
  p I etterkant har det vore vanleg å bruke nemninga kriminalromanar om bøkene til Øvre Richter Frich. Som skaparen av denne sjangeren reknar vi amerikanaren Edgar Allan Poe, som skreiv ei rekkje skrekk- og kriminalnoveller i 1830- og 1840-åra. Det skulle gå over førti år før Poe fekk ein etterfølgjar: I 1887 gav engelskmannen Arthur Conan Doyle ut A Study in Scarlet – ei bok der etterforskaren Sherlock Holmes er hovudfigur. Denne etterforskaren, med lupe og intelligens, skulle bli prototypen på ein detektiv.

  p Mordet på Maskinbygger Roolfsen (1839) av Mauritz Hansen vert rekna som den første kriminalromanen i norsk litteratur. Boka kom faktisk ut to år før debutboka til Poe. Romanen har mange drag som er typiske for romantikken, og interessa for det mystiske er felles for både kriminallitteraturen og romantikken.

  p Sven Elvestad (1884–1934), under psevdonymet Stein Riverton, var den som introduserte det norske svaret på Sherlock Holmes, detektiven Asbjørn Krag. Den mest kjende boka er Jernvognen frå 1907. Sven Elvestad oppnådde stor suksess med bøkene sine, og dei vart omsette til sju språk.

  p Elvestad stod også bak det populære seriemagasinet Lys og Skygge, som gjennom seksti år leverte kriminalhistorier om meisterdetektiven Knut Gribb og storforbrytaren Thomas Ryer.

  h3 Ein populær sjanger

  figure.col.col7.right-image.mt5
   img(src='./frich_En_populaer_sjanger1.jpg' alt='')
   figcaption Skodespelar Henny Moan i filmen «De dødes tjern», basert på boka med same namn av Bernhard Borge (Andre Bjerke). Foto: Scanpix

  p Tida fram til andre verdskrigen var ein gullalder for krim- og spenningslitteratur. Nordahl Grieg (1902–1943) var ein lovande ung journalist og forfattar. Saman med Gyldendals redaktør Nils Lie (1902–1978) skreiv han i 1929 Bergenstoget plyndret i natt.

  p Boka gjorde stor suksess. Gyldendal forlag lanserte henne rett før påske, med tittelen utheva som ei avisoverskrift. Mange las og oppfatta annonsen for boka som ei nyheit og trudde at bergenstoget verkeleg var rana. Det høyrer med at dette var opptakten til det norske fenomenet påskekrim.

  p Arthur Omre (1887–1967) og Torolf Elster (1911–2006) var populære krimforfattarar i 1930-åra. André Bjerke (1918– 1985) skreiv kriminalromanar under psevdonymet Bernhard Borge. Dei mest kjende romanane hans er De dødes tjern (1942) og Døde menn går i land (1947). Bjerke var påverka av Sigmund Freuds teoriar om korleis den menneskelege psyken fungerer, og dette kjem til uttrykk i dei fleste bøkene hans. 

  p Etter 1970 fekk vi ei bølgje av samfunnskritisk kriminallitteratur. Frå Sverige kom det ein serie politiromanar av Sjöwall og Wahlöö som inneheldt sterk kritikk av det svenske samfunnet. Her i landet har mellom anna forfattarane Jon Michelet og Gunnar Staalesen litt av same ståstaden.

  p Den tradisjonelle kriminalromanen er framleis populær, men også den psykologiske thrilleren og andre typar spenningsromanar har mange lesarar. Til dei mest lesne norske kriminalforfattarane i dag høyrer Anne Holt, Jo Nesbø, Karin Fossum og Unni Lindell.

