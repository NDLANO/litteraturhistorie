  h3 Populærlitteraturen veks fram
  p Populærlitteratur blir også kalla for triviallitteratur, kiosklitteratur eller underhaldningslitteratur og har til oppgåve å underhalde eit stort publikum. Denne typen litteratur kjem gjerne i mykje større opplag enn skjønnlitteraturen, og derfor blir også nemningane kvantitetslitteratur eller masselitteratur nytta.

  p Populærlitteraturen omfattar mange sjangrar, for eksempel romanar av ymse slag, vekebladnoveller, føljetongar, teikneseriar, skillingsviser og kjendisbiografiar. Det er verd å merke seg at dei omgrepa vi bruker om populærlitteraturen i dag, først dukka opp i 1960-åra og seinare.

  p I somme land, særleg i Frankrike og Belgia, finn vi ein gjensidig påverknad mellom populærlitteraturen og «finlitteraturen». I desse landa hentar populærlitteraturen ofte stiltrekk frå «finlitteraturen» og omvendt. Den same tendensen ser vi eksempel på også her i landet.

  h3 Populærlitteraturen når nye lesargrupper

  img.col.col12(src='./frich_Populaerlitteraturen1.jpg' alt='')
  p Romantikken (1800–1850) var ei blømingstid for populærlitteraturen, både her til lands og i mange andre land. Mauritz Hansen (1794–1842) skreiv fleire spennings- og kjærleiksforteljingar i denne tidsperioden.

  p Men gjennombrotet for populærlitteraturen kom først i andre halvdel av 1800-talet. No lærte fleire seg å lese, og fleire fekk råd til å kjøpe litteratur. Byane var i vekst, og her hadde folk tilgang til elektrisk lys i seine kveldstimar. Dei mest kjende populærskribentane frå denne perioden er Jon Flatabø (1846–1930), Rudolf Muus (1862–1935) og Karen Sundt (1841–1924).

  p Rudolf Muus skreiv ein slags detektivforteljingar med romantisk tilsnitt, der skurkane kom frå underklassen og overklassefolka var heltar. Karen Sundt skilde seg ut fordi ho var kvinne, men også fordi ho skreiv om dei sosiale problema til underklassen. Dessutan var ho den første kvinnelege avisredaktøren i landet.

  p Alle tre skreiv føljetongar (framhaldsforteljingar) i store opplag, og vi kallar dei folkelitteratar fordi dei nådde mange lesarar og gav folket det dei ville ha. Føljetongane skapte spenning og forventningar hos lesarane, og dei gav både forfattarane og forlaga store inntekter. 

