  h3 Westernsjangeren
  
  img.col.col6.right-image.mt5(src='./frich_westernsjangeren.jpg' alt='')

  p Helten i westernlitteraturen er opphavleg grensespeidaren («the frontier man»). Cowboyen melder seg på som helt litt seinare, og det er først da sjangeren får sitt store gjennombrot. Cowboyen blir ein helt som lykkast både i kjærleik og i revolverduellar.

  p Forteljingane om alle bragdene som dei kvite gjorde i det ville Vesten, kom til å likne på det vi kallar mytar. Ein myte er ei historie eller ei segn kring historiske hendingar, som vil vise at det står høgare makter bak. Historiene om Buffalo Bill og Billy the Kid, som eigentleg var massemordarar, vart til ei slags heltesegner for den amerikanske nasjonen.

  p For amerikanarane var det viktig å ha ein felles myte fordi dei som innvandra kom frå mange ulike land. Westernmyten med felles verdiar og ideal kunne skape ei felles forhistorie og sveise desse menneska saman til ein nasjon.

  p Westernsjangeren har vore svært populær her i landet. Den mest framståande westernforfattaren var Kjell Hallbing (1934–2004), som gav ut bøkene sine under psevdonymet Louis Masterson. Han skapte westernhelten Morgan Kane, fødd i Santa Fe av irske emigrantar. Den første boka, Uten nåde, kom i 1966. Til saman har Hallbing gitt ut 83 bøker om helten, med eit totalopplag på elleve millionar eksemplar. I tillegg er det laga film og teikneseriar.

