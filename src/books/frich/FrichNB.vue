  h3 Westernsjangeren 

  img.col.col6.right-image.mt5(src='./frich_westernsjangeren.jpg' alt='')

  p Helten i westernlitteraturen er opprinnelig grensespeideren («the frontier man»). Cowboyen melder seg på som helt litt seinere, og det er først da sjangeren får sitt store gjennombrudd. Cowboyen blir en helt som lykkes både i kjærlighet og i revolverdueller. 

  p Fortellingene om alle bragder som de hvite gjorde i det ville Vesten, kom til å likne på det vi kaller myter. En myte er en historie eller et sagn rundt historiske hendinger, som vil vise at det står høyere makter bak. Historiene om Buffalo Bill og Billy the Kid, som egentlig var massemordere, ble til et slags heltesagn for den amerikanske nasjonen. 

  p For amerikanerne var det viktig å ha en felles myte fordi de som innvandret kom fra mange ulike land. Westernmyten med felles verdier og idealer kunne skape en felles forhistorie og sveise disse menneskene sammen til en nasjon. 

  p Westernsjangeren har vært svært populær her i landet. Den mest framtredende westernforfatteren var Kjell Hallbing (1934–2004), som gav ut bøkene sine under psevdonymet Louis Masterson. Han skapte westernhelten Morgan Kane, født i Santa Fe av irske emigranter. Den første boka, 
    em Uten nåde
    | , kom i 1966. Til sammen har Hallbing gitt ut 83 bøker om helten, med et totalopplag på elleve millioner eksemplarer. I tillegg er det laget film og tegneserier. 

