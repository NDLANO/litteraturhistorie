  p I enkelte land, særlig i Frankrike og Belgia, finner vi en gjensidig påvirkning mellom populærlitteraturen og «finlitteraturen». I disse landene henter populærlitteraturen ofte stiltrekk fra «finlitteraturen» og omvendt. Den samme tendensen ser vi eksempler på også her i landet. 

