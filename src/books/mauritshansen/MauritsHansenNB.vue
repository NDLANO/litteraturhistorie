  h3 Novellen «Luren»
  p 
    | Novellen 
    a(href="https://www.nb.no/items/b0f17c7819e26ec07d1ae1ca771e09cc?page=17&searchText=Luren" target="_blank") «Luren» 
    | &nbsp;av Maurits Hansen kom ut i 1819. Den omtales gjerne som den første bondefortellingen i norsk litteratur, og novellen har klare nasjonalromantiske trekk. 

  p Handlingen i novellen er lagt til Gudbrandsdalen. Fortelleren er Carl Møllmann, en ung akademiker på reise gjennom dalen. Han forteller begeistret om folkelivet og om sjeldne inntrykk og opplevelser på ferden. 

  p Hovedpersonen i novellen er en storbonde som tar imot den reisende og gir han husly. Dattera til bonden elsker en fattig gutt, men bonden er for stolt til å godta en svigersønn fra husmannsstanden. Ved en tilfeldighet finner gjesten fra byen ut at de to unge har et barn sammen, noe de har greid å holde skjult for alle. Da griper han inn og ordner opp slik at de unge kan få hverandre, selv om den sosiale avstanden mellom dem er stor. 

  h3 Den norske bonden sett med romantiske briller 
  p Maurits Hansen gir en idealisert fremstilling av både miljø og menneske i novellen “Luren”. Bonden Thord er for eksempel en stolt mann og direkte etterkommer av kong Harald Hårfagre. Mer norsk er det omtrent ikke mulig å bli. Hansen framhever også at bonden snakker dialekt, han går kledd i nasjonaldrakta si, og han er slett ikke uten danning og historiekunnskaper. Formålet med personskildringen i novellen er nok mer å gjøre leseren stolt over bonden og bondekulturen enn å skape realistiske og nyanserte personportretter.

  p Bak fortellingen ligger en tydelig moral. Forfatteren ønsker å skrive en tekst som er oppdragende. Det er verdiene til den dannede middelklassen som kommer til syne. Så er det også embetsfolk og andre velsituerte som er målgruppa. 

  p I dag smiler vi litt av den romantiserende fortellingen og måten Maurits Hansen forteller på, med overdreven bruk av adjektiv og moraliserende kommentarer underveis. Teksten er et godt eksempel på hvordan en representant for datidas embetsstand og bykultur så på den norske bonden og bondelivet. 

  p I 1869 skrev en forfatter dette om «Luren»: 

  blockquote En norsk storbonde, som i årevis ikke av sine egne kan overtales til å ta en husmannsgutt til svigersønn, lar seg sannelig ikke beseire ved en smule moralprediken av en fremmed reisende og ved oppdagelsen av sin datters vanære gjennom samme reisendes utilgivelige indiskresjon. Selv om han også mange ganger får Bibelen og Snorre Sturlesøn foran seg på bordet, hvor smukk og betydningsfull enn denne sammenstilling i seg selv kan være. 

  h3 Maurits Hansen som pedagog
  p Maurits Hansen var ikke bare dikter, men også lærer i Trondheim og seinere skolestyrer ved Kongsberg middelskole. Han skrev lærebøker i en rekke ulike fag, blant annet i fransk, tysk  og latin, og han gav ut en grammatikk i morsmålet som ble trykt i flere opplag. 

  p Hansen deltok ikke i den offentlige språkdebatten i 1830-årene. Men han var overbevist om at skriftspråket i løpet av noen få tiår ville være helt fornorsket. I den femte utgaven av morsmålsgrammatikken, som kom i 1837, brukte han ortofonisk (lydrett) stavemåte. Han sløyfet stumme bokstaver, sluttet å markere lang vokal med dobbeltvokal og gav enkelte ord en helt ny stavemåte: 

