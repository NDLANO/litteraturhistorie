  h3 Maurits Christopher Hansen (1794–1842)
  p Maurits Hansen er ein av dei få norske forfattarane i tida like etter 1814. Interessa hans for litteratur og skriving blir vekt allereie i ungdommen. Han skriv både dikt, noveller og romanar. I tillegg er han lærar og lærebokforfattar.

  p I 1819 kjem det første større verket av Maurits Hansen, romanen Othar af Bretagne. Det litterære førebiletet for boka er riddarromanane til den tyske romantiske forfattaren Friedrich de la Motte Fouqué (1777–1843).

  p I 1840 gir Hansen ut Mordet paa Maskinbygger Roolfsen. Kriminalanekdote fra Kongsberg. Denne boka blir rekna for å vere Noregs første detektivroman. Hansen skreiv også fleire andre kriminalforteljingar.

  p Maurits Hansen blir sett på som den største forfattaren i samtida si. Han er også den mest lesne og ei inspirasjonskjelde for mange forfattarar som kjem etter han, for eksempel Henrik Wergeland, Jørgen Moe, Peter Christen Asbjørnsen, Camilla Collett og Henrik Ibsen. 
  
  h3 Novella «Luren»
  p Novella «Luren» av Maurits Hansen kjem ut i 1819. Novella blir gjerne omtalt som den første bondeforteljinga i norsk litteratur, og ho har klare nasjonalromantiske trekk.

  p Handlinga er lagd til Gudbrandsdalen. Forteljaren, Carl Møllmann, er ein ung akademikar på reise gjennom dalen. Han fortel oppglødd om folkeliv, hendingar og naturopplevingar på ferda og karakteriserer landskapet som ”det egentlige Norge”.

  p Hovudpersonen i novella er ein storbonde som inviterer Møllmann til å overnatte på garden hans. Dottera på garden elskar ein fattig gut, men bonden er for stolt til å godta ein svigerson frå husmannsstanden. Ved eit tilfelle finn gjesten frå byen ut at dei to unge har eit barn saman, og det har dei greidd å skjule for alle. Da grip han inn og ordnar opp slik at dei unge kan få kvarandre trass i standsforskjellen.
  
  h3 Den norske bonden sett med romantiske briller
  p Maurits Hansen gir ei idealisert framstilling av både miljø og menneske i novella si. Bonden Thord er for eksempel ein stolt mann og direkte etterkommar av kong Harald Hårfagre. Meir norsk er det omtrent ikkje mogleg å bli. Hansen framhevar også at bonden taler dialekt, han går kledd i nasjonaldrakta si, og han er slett ikkje utan danning og historiekunnskapar. Formålet med personskildringa i novella er nok meir å gjere lesaren stolt over bonden og bondekulturen enn å skape realistiske og nyanserte personportrett.

  p Bak forteljinga ligg ein tydeleg moral. Forfattaren ønskjer å skrive ein tekst som er oppdragande. Det er verdiane til den danna middelklassen som kjem til syne. Så er det også embetsfolk og andre velsituerte som er målgruppa. 

  p I dag smiler vi litt av den romantiserande forteljinga og måten Maurits Hansen fortel på, med overdriven bruk av adjektiv og moraliserande kommentarar undervegs. Teksten er eit godt døme på korleis ein representant for datidas embetsstand og bykultur såg på den norske bonden og bondelivet. 

