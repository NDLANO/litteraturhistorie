  h3 Maurits Hansen som pedagog
  p Maurits Hansen er ikkje berre diktar, men også lærar i Trondheim og seinare skolestyrar ved Kongsberg middelskole. Han skriv lærebøker i mange ulike fag, mellom anna i fransk, tysk og latin, og han gir ut ein grammatikk i morsmålet som kjem i fleire opplag.

  p Hansen tek ikkje del i den offentlege språkdebatten i 1830-åra. Men han er overtydd om at skriftspråket om nokre få tiår vil vere heilt fornorska. I den femte utgåva av morsmålsgrammatikken, som kjem i 1837, bruker han ortofonisk eller lydrett stavemåte. Han sløyfar stumme bokstavar, sluttar å markere lang vokal med dobbeltvokal og fornorskar skrivemåten i ein del ord:

