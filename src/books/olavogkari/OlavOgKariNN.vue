  h5 Blodhemn og tilgiving 
  p Mens ættesamfunnet krev at urett skal hemnast, krev den kristne trua at ein skal tilgi fiendane sine. I «Olav og Kari» finn vi ei blanding av den gamle og den nye tenkjemåten.

  p Rett før Kari døyr, ber ho Olav sende den blodige serken hennar til mor hennar. I førkristen tid var dette eit teikn på at ein kravde blodhemn. Men når Kari kjem til himmelriket, ber ho jomfru Maria om tilgiving for både Olav og mor hans. Her står ho for ein tenkjemåte som er i tråd med den kristne trua.

  h5 Jomfru Maria 
  p Det er jomfru Maria som tek imot Kari i himmelen. Det tyder på at visa fann forma si før reformasjonen, for det er i den katolske kyrkja at Jesu mor blir dyrka som himmeldronning.

  h3 Balladesjangeren
  p Det finst mange slags folkeviser, for eksempel arbeidssongar, voggeviser, skillingsviser, skjemteviser, balladar og stev. «Olav og Kari» høyrer til dei folkevisene som vi kallar balladar.

  p Balladen var ein sjanger som frå 1200-talet og utover vart populær i heile Europa, også i Noreg. Vi reknar med at balladen opphavleg kjem frå Sør-Frankrike. Der kalla dei han «ballada» , som tyder å danse, og mange av dei europeiske balladane er danseviser. Vi veit ikkje sikkert om også dei norske balladane vart dansa til.

  p Eit viktig kjennemerke ved balladen er at han fortel ei historie. Vi kallar derfor balladen ein episk-lyrisk sjanger. Innhaldet er episk (forteljande), mens forma er lyrisk. Her finst enderim og rytme, og ei oppdeling i strofer som gjer at visa kan syngjast til ein fast melodi. Balladen har dessutan oftast eit omkved (refreng). Sjølve historia som balladen fortel, har til vanleg ein tragisk utgang. 

