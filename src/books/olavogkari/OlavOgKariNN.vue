  p Segna handlar om den frankiske prinsessa Klotilde, som i 526 e.Kr. blir gift med vestgotarkongen Amalarik. Ho er katolikk, mens Amalarik er arianar. Arianarane er kristne, men dei trur ikkje at Jesus er guddommeleg. Det gjer derimot katolikkane. Amalarik mishandlar Klotilde for trua hennar. Til sist sender ho ein blodflekket serk til bror sin, Childebert, som eit teikn på at ho vil hemne seg. Broren kjempar mot Amalarik og vinn. Så tek han med seg Klotilde, men ho døyr på veg tilbake til frankarriket.

  p Denne forteljinga var svært populær i Frankrike i mellomalderen, og ho spreidde seg til fleire land, mellom dei Noreg. Men som alltid når litteratur blir overført munnleg, endrar innhaldet seg over tid. På 1200- og 1300-talet var det knapt nokon som hugsa arianismen lenger, og den norske visa handlar no om motsetninga mellom kristen tru og heidenskap. I tillegg har balladen vorte ei forteljing om kjærleik og sjalusi.

  h3 Handlinga
  p Olav gjestar mor si for første gong på åtte år. Mora spør sonen korleis han liker kona si, og Olav fortel at han er svært lykkeleg med Kari. Da skuldar mora Kari for å vere trollkvinne og ha omgang med alvane (dei underjordiske). Olav avviser dette som løgn, men mora minner han om at ein son må tru mor si. Slik utnyttar ho respekten Olav pliktar å ha for henne som forelder. 

  p Olav reiser tilbake til Kari og legg fram skuldingane. Kari nektar for å vere trollkvinne, men no trur ikkje Olav på henne. Han er sint og mishandlar Kari. Til slutt angrar han på det han har gjort, men det er for seint. Kari seier til Olav at han skal sende den blodige serken hennar til mor hennar slik at mora kan vaske den rein. Deretter døyr ho.

  p Siste del av visa fortel korleis Kari kjem til himmelriket og blir teken imot av jomfru Maria. Maria byr Kari ein stol, ei handling som tyder på at Kari har fått martyrstatus. Det endar med at Kari går i forbønn for Olav og mor hans. Jomfru Maria svarar at Olav får komme til himmelriket, men ikkje mora. 

