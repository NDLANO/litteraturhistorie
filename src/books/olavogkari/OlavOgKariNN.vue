  h3 Balladeforma

  h5 To- eller firelinja strofeform
  p Balladane har tolinja eller firelinja strofer og omkved (refreng). Den firelinja strofa har berre eitt refreng, som er ettersett. Her er eit eksempel frå visa «Bendik og Årolilja»: 

  em
    p
      | Bendik ri åt Sølondo
      br 
      | ville han skòa møy;
      br 
      | Han var ’kje lagje til att’e koma,
      br 
      | difyr so laut han døy.
      br 
      | – Årolilja, kvi søv’e du so lengje? 

  p Når strofa er tolinja, er det vanleg med to omkved, eitt etter kvar linje. Det første omkvedet kallar vi da mellomsleng og det andre ettersleng. Eit eksempel på denne typen omkved finn vi i folkevisa «Olav og Kari»:

  em
    p
      | Olav sat heime i åtte år,
      br 
      | – Trø meg inkje for nære
      br 
      | fyrr han ville si mo’eri sjå. 
      br 
      | – På vollen dansar mi jomfru. 

  h5 Omkvedet 
  p Omkvedet kan ha ulike funksjonar. Det kan for eksempel formidle ei stemning, eller det kan binde saman handlinga. I mange viser finn vi også omkved som nemner dans og dansing. Omkveda i «Olav og Kari» er eksempel på denne siste typen. 

  h5 Rim
  p Balladane har enderim (
      em møy – døy
      | ). Det er tilstrekkeleg med vokalsamklang (assonans), konsonantane kan vere ulike (
      em år – sjå, renn – heng
      | ). I dei firelinja strofene er det berre linjene 2 og 4 som rimar, linjene 1 og 3 er utan rim.

  h5 Dialogar
  p Dialogar er vanlege i balladane. I «Olav og Kari» finn vi samtalar både mellom Olav og mora og mellom Olav og Kari. Som i eventyra er det alltid berre to personar som snakkar saman (totalslova). Dialogane kastar lys over konfliktane og aukar spenninga.

  h5 Parallellstrofer
  p Eit anna særtrekk ved balladane er parallellstrofer, det vil seie gjentakingar med ein aldri så liten variasjon. Når mora spør Olav korleis han liker kona si, svarar han: 

  em
    p
      | Sosso likar eg gifta mi,
      br 
      | som eg drikk’e bå’ mjø’ og vin.
      br 
      | Sosso likar eg gifta god, 
      br 
      | som kvòr den dagjen å drikke jol.

  p Parallellstrofer gjer det mellom anna lettare for tilhøyrarane å få med seg handlinga.
  
  h5 Faste vendingar
  p Balladetekstane inneheld mange faste poetiske vendingar som vi lett kan kjenne att. Slike vendingar gjer det òg lettare å hugse teksten. For eksempel finn vi faste skildrande adjektiv: «det raude gull», «stolt Margjit». 

  h3 Stev

  p Eit stev er eit einstrofa dikt som blir sunge. Stev kan formidle livsvisdom eller ei stemning. Dei kan òg skildre menneske, dyr eller natur. Eller dei kan vere humoristiske og litt på kanten.

