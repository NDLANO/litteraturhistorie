  h5 Legendeviser eller religiøse viser
  p er forteljingar der kristen tru og tankegang kjem fram. Det kan òg vere forteljingar om helgenar, som vart rekna som førebilete for vanlege folk. Konfliktane i legendevisene finn ei løysing som er i tråd med kristne krav og verdiar. «Olav og Kari» og «Draumkvedet» høyrer til denne gruppa.

