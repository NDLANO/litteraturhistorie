  h3 Bakgrunnen for visa
  p Folkevisa om Olav og Kari har røtter tilbake til middelalderen. Den bygger trolig på et gammelt fransk sagn fra folkevandringstida. 

  p Sagnet handler om den frankiske prinsessen Klotilde, som i 526 e.Kr. blir gift med vestgoterkongen Amalarik. Hun er katolikk, mens Amalarik er arianer. Arianerne er kristne, men de tror ikke at Jesus er guddommelig. Det gjør derimot katolikkene. Amalarik mishandler Klotilde for troen hennes. Til slutt sender hun en blodflekket serk til bror sin, Childebert, som et tegn på at hun vil hevne seg. Broren kjemper mot Amalarik og vinner. Så tar han med seg Klotilde, men hun dør på vei tilbake til frankerriket. 

  p Denne fortellingen var svært populær i Frankrike i middelalderen, og den spredde seg til flere land, blant annet Norge. Men som alltid når litteratur blir overført muntlig, endrer innholdet seg over tid. På 1200- og 1300-tallet var det knapt noen som husket arianismen lenger, og den norske visa handler nå om motsetningen mellom kristen tro og hedenskap. I tillegg er balladen blitt en fortelling om kjærlighet og sjalusi. 

  h3 Handlingen 
  p Olav besøker mor si for første gang på åtte år. Mora spør sønnen hvordan han liker kona si, og Olav forteller at han er svært lykkelig med Kari. Da beskylder mora Kari for å være trollkvinne og ha omgang med alvene (de underjordiske). Olav avviser dette som løgn, men mora minner om at en sønn må tro sin egen mor. Slik utnytter hun respekten Olav plikter å ha for henne som forelder. 

  p Olav reiser tilbake til Kari og gjentar beskyldningene. Kari nekter for å være trollkvinne, men nå tror ikke Olav på henne. Han er sint og mishandler Kari. Til slutt angrer han på det han har gjort, men det er for seint. Kari sier til Olav at han skal sende den blodige serken hennes til mor hennes, slik at mora kan vaske den rein. Deretter dør hun. 

  p Siste del av visa forteller hvordan Kari kommer til himmelriket og blir tatt imot av jomfru Maria. Maria tilbyr Kari en stol, en handling som tyder på at Kari har fått martyrstatus. Det ender med at Kari går i forbønn for Olav og mor hans. Jomfru Maria svarer at Olav får komme til himmelriket, men ikke mora. 

  h3 Konfliktene

  h5 Kristendom mot hedenskap
  p I denne balladen blir kristendom satt opp mot hedenskap. Middelalderen er en brytningsfase for kristendommen i Norge, og det er viktig å ta avstand fra alt som minner om hedenskap. Det forklarer Olavs sterke reaksjon på det mora sier om Kari. Samtidig viser reaksjonen hans at han faktisk ser på de underjordiske og på trolldom som en realitet.

  h5 Kjærlighet, sjalusi og maktkamp 
  p Personkonflikten i visa er bare antydet. Likevel er det den som driver handlingen framover. Hvorfor har ikke Olav besøkt mor si på åtte år? Hvorfor kommer han aleine på besøk til henne? Og hvorfor lyver Olavs mor og beskylder Kari for å være trollkvinne? Visa forteller ikke noe om personenes motiver, men det er godt mulig at mora er fornærmet og sjalu, og forholdet til svigerdattera er neppe særlig godt. 

  h3 Kristne og hedenske forestillinger i visa 

  h5 Onde makter 
  p Vi finner kristne og hedenske forestillinger side om side i «Olav og Kari». I folketroen representerte for eksempel alvene farlige makter som truet menneskene. I middelalderen var onde ånder, trollmenn og trollkvinner slett ikke bare fantasiskapninger for menneskene.

  h5 Blodhevn og tilgivelse 
  p Mens ættesamfunnet krevde at urett skulle hevnes, krevde den kristne troen at en skulle tilgi sine fiender. I «Olav og Kari» finner vi en blanding av den gamle og den nye tenkemåten. 

  p Like før Kari dør, ber hun Olav sende den blodige serken hennes til mor hennes. I førkristen tid var dette et tegn på at en krevde blodhevn. Men når Kari kommer til himmelriket, ber hun jomfru Maria om tilgivelse for både Olav og mor hans. Her står hun for en tenkemåte som er i tråd med den kristne troen. 

  h5 Jomfru Maria 
  p Det er jomfru Maria som tar imot Kari i himmelen. Det tyder på at visa fant sin form før reformasjonen, for det er i den katolske kirka at Jesu mor blir dyrket som himmeldronning.  

  h3 Balladesjangeren 
  p Det finnes mange slags folkeviser, for eksempel arbeidssanger, voggeviser, skillingsviser, skjemteviser, ballader og stev. «Olav og Kari» hører til de folkevisene vi kaller ballader.

  p Balladen er en sjanger som fra 1200-tallet og utover ble populær i hele Europa, også i Norge. Vi regner med at balladen opprinnelig kommer fra Sør-Frankrike. Der ble den kalt «ballada», som betyr å danse, og mange av de europeiske balladene er danseviser. Vi vet ikke sikkert om også de norske balladene ble danset til. 

  p Et viktig kjennetegn ved balladen er at den forteller en historie. Vi kaller derfor balladen en episk-lyrisk sjanger. Innholdet er episk (fortellende), mens formen er lyrisk. Her finnes enderim og rytme og en oppdeling i strofer som gjør at visa kan synges til en fast melodi. Balladen har dessuten oftest et omkved (refreng). Selve historien som balladen forteller, har vanligvis en tragisk utgang. 

  p Helt fram til 1800-tallet ble balladene overført muntlig i Norge. Det er derfor vanskelig å slå fast hvor gamle balladene som er bevart, virkelig er. I første halvdel av 1800-tallet samlet Olea Crøger og Magnus Brostrup Landstad inn norske folkeviser og skriv dem ned. Allerede den gang var mange viser i ferd med å bli glemt, så innsamlingsarbeidet skjedde i siste liten. I 1853 gav Landstad ut samlingen Norske Folkeviser.  

  h3 Ulike balladetyper 
  p På samme måte som vi deler inn eventyr og sagn i ulike grupper, skiller vi også mellom ulike typer ballader:

  h5 Riddervisene 
  p er den største gruppa. Her er det kvinnene som spiller hovedrollen, og kjærlighet og sjalusi står sentralt. Men vi kan også finne overnaturlige innslag i disse visene. Den mest kjente riddervisa er «Bendik og Årolilja». 

  h5 Legendeviser eller religiøse viser 
  p er fortellinger der kristen tro og tankegang kommer fram. Det kan også være fortellinger om helgener, som ble regnet som forbilder for vanlige folk. Konfliktene i legendevisene finner en løsning som er i tråd med kristne krav og verdier. «Olav og Kari» og «Draumkvedet» hører til denne gruppa. 

  h5 Naturmytiske folkeviser 
  p forteller om bergtaking og om folk som blir omskapt til bjørn, ulv og andre farlige dyr. Visene kan også handle om hvordan mennesket slåss mot slike lumske krefter, og hvordan en kan bli løst fra trolldommen.«Villemann og Magnill» og «Olav Liljekrans» er naturmytiske folkeviser.

  h5 Kjempe- og trollviser 
  p har mye til felles med de naturmytiske visene. De handler oftest om menn i kamp med hverandre, eller i kamp med troll og riser. «Torekall» er et eksempel på ei trollvise, mens «Roland og Magnus kongjen» er ei kjempevise. 

  h5 Historiske folkeviser 
  p er det ikke så mange av. Disse visene gjør krav på å være sanne, og det er personene mer enn den historiske ramma som er interessant. «Kong Håkons død» er ei slik historisk folkevise. 

  h5 Dyre- og skjemteviser 
  p er eventyr i strofeform. De gjør narr av balladesjangeren og er reine parodier. «Tor Brynjulv og fluga» og «Reven og bonden» er eksempler på slike viser. 

  h3 Balladeformen 
  h5 To- eller firelinjet strofeform 
  p Balladene har tolinjete eller firelinjete strofer og omkved (refreng). Den firelinjete strofen har bare ett refreng, som er etterstilt. Her er et eksempel fra visa «Bendik og Årolilja»: 

  em
    p 
      | Bendik ri åt Sølondo,
      br 
      | ville han skòa møy; 
      br 
      | Han var ’kje lagje til att’e koma, 
      br 
      | difyr so laut han døy. 
      br 
      | – Årolilja, kvi søv’e du so lengje? 


  p Når strofen er tolinjet, er det vanlig med to omkved, ett etter hver linje. Det første omkvedet kaller vi da mellomsleng og det andre ettersleng. Et eksempel på denne typen omkved finner vi i folkevisa «Olav og Kari»: 

  em
    p 
      | Olav sat heime i åtte år, 
      br 
      | – Trø meg inkje for nære 
      br 
      | fyrr han ville si mo’eri sjå. 
      br 
      | – På vollen dansar mi jomfru. 

  h5 Omkvedet 
  p Omkvedet kan ha ulike funksjoner. Det kan for eksempel formidle en stemning, eller det kan binde sammen handlingen. I mange viser finner vi også omkved som nevner dans og dansing. Omkvedene i «Olav og Kari» er eksempler på denne typen. 

  h5 Rim 
  p Balladene har enderim (møy – døy). Det er tilstrekkelig med vokalsamklang (assonans), for konsonantene kan være ulike (år – sjå, renn – heng). I de firelinjete strofene er det bare linjene 2 og 4 som rimer, linjene 1 og 3 er uten rim.  

  h5 Dialoger 
  p Dialoger er vanlig i balladene. I «Olav og Kari» finner vi samtaler både mellom Olav og mora og mellom Olav og Kari. Som i eventyrene er det alltid bare to personer som snakker sammen (totallsloven). Dialogene tydeliggjør konfliktene og øker spenningen. 

  h5 Parallellstrofer 
  p Et annet særtrekk ved balladene er parallellstrofer, det vil si gjentakelser med en aldri så liten variasjon. Når mora spør Olav hvordan han liker kona si, svarer han: 

  em
    p 
      | Sosso likar eg gifta mi, 
      br 
      | som eg drikk’e bå’ mjø’ og vin. 
      br 
      | Sosso likar eg gifta god, 
      br 
      | som kvòr den dagjen å drikke jol.

  p Parallellstrofer gjør det blant annet lettere for tilhørerne å få med seg handlingen. 

  h5 Faste vendinger 
  p Balladetekstene inneholder mange faste poetiske vendinger som vi lett kan kjenne igjen. Slike vendinger gjør det også lettere å huske teksten. For eksempel finner vi faste beskrivende adjektiv: «det raude gull», «stolt Margjit». 

  h3 Stev 
  p Et stev er et enstrofet dikt som blir sunget. Stev kan formidle livsvisdom eller en stemning. De kan også skildre mennesker, dyr eller natur. Eller de kan være humoristiske og litt på kanten. 

  h5 Gammelstev og nystev
  p Det finnes to slags stevstrofer. Begge har fire verselinjer, men rimmønsteret er ulikt. Gammelstevet er bygd opp slik at linjene 2 og 4 rimer, mens linjene 1 og 3 ikke har rim: 

  em
    p
      | Hanen stend på stabburshella
      br 
      | bonden gjeve honnom konn.
      br 
      | Rakkin skvakkar i berg o’ nord
      br 
      | og hjuringen blæs i honn.

  p Nystevet består av to parrim:

  em
    p 
      | Å vil du hava meg til kveda?
      br 
      | Det skal eg gjere med største gleda.
      br 
      | Og eg skal kveda så vent fyr deg
      br 
      | At du skal gløyma deg sjølv og meg.

  h5 En muntlig tradisjon
  p Å lage og improvisere stev har vært en levende tradisjon i enkelte landsdeler helt  fram til vår tid. Særlig i Vest-Telemark og i Setesdal er dette en tradisjon som fortsatt lever. Stevet blir brukt i ulike  sosiale sammenhenger. Når folk kommer sammen til vennelag eller til ei veitsle (et gjestebud), er det vanlig å kvede eller å stevjes. (Det heter å kvede et stev, men å stevjes når det er flere som kveder til hverandre.) 

  h5 Ulik form – forskjellig innhold 
  p Hos setesdølene er det en oppfatning at gamlestevene er alvorlige og mer lyriske i formen. Nystevene er derimot friskere både i innhold og form og kan være mer direkte i uttrykksmåten. Her er det større rom for å improvisere. 

