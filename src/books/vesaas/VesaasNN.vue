  p Som i romanen 
    em Fuglane 
    | møter vi også her ein omfattande symbolikk: Den eine jenta går inn i ein labyrint av is og døyr. Den andre bergar seg så vidt etter å ha frigjort seg frå sin eigen dødsangst.

  p Boka kan tolkast som ei skildring av destruktive krefter i eit ungt menneskesinn, men ho kan òg lesast som ei forteljing om det einsame mennesket i eit vennskapsforhold. Eit anna viktig emne er korleis unge menneske leitar etter sin eigen identitet. Her er eit utdrag av boka:

