  p Mykje av det Vesaas skreiv, handlar om mennesket i eksistensielle kriser, situasjonar der mennesket møter dei destruktive kreftene inni og utanfor seg sjølv. Jamvel om tematikken i bøkene hans kan vere alvorleg og språket ikkje alltid er like tilgjengeleg, har novellene og romanane hans nådd fram til mange unge lesarar dei siste tiåra.

  p Tarjei Vesaas har fått fleire prisar for bøkene sine, mellom anna Nordisk Råds litteraturpris i 1964 for romanen 
    em Is-slottet
    | . Denne boka og romanen 
    em Fuglane 
    | (1957) har vorte ståande som dei mest kjende verka i forfattarskapen.

  h3 Fuglane
  p Romanen 
    a(href='https://www.nb.no/items/14ac565f836ab1fee0cfaf9e59bc3396?page=7&searchText=fuglane' target='_blank') 
      em Fuglane
    | &nbsp;(1957) handlar om raringen Mattis, som bur saman med syster si, Hege, og som ikkje er som andre. Folk i bygda kallar han Tusten. Mattis er ikkje i stand til å gjere vanleg arbeid, men han er god til å ro og vil prøve seg som ferjemann.

  p Mattis er det vi i dag kallar psykisk utviklingshemma, men han har eit kjenslevart sinn og kan sanse mange ting som vanlege folk ikkje oppfattar. Mattis sansar og tolkar dei fleste naturfenomena. Han er redd i torevêr, men ser han for eksempel eit fugletrekk, slepper angsten taket, og dagen blir god for han.

  p Mattis oppdagar mellom anna at det går eit rugdetrekk over huset. Dette rugdetrekket er eit under, noko gåtefullt og tiltrekkjande, synest Mattis. Han trur at livet skal ta ei ny vending no som rugda har byrja å trekkje rett over huset der han og Hege bur. Og endringar blir det.

  p Vesaas har sjølv sagt at romanen om Mattis også er eit sjølvportrett og eit bilete på kunstnaren, som kjenner lengten etter det useielege – etter 
