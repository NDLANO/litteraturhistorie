  p Tarjei Vesaas var ein diktar som i stor grad brukte 
    em symbolet 
    | som verkemiddel. Symbola har både ei konkret og ei overført tyding. Vi kjem eit stykke på veg når vi bruker fornuft og logikk for å tolke dei, men det ligg gjerne att ein rest i tillegg som ikkje lèt seg forklare.

  p Det kan derfor vere vanskeleg å finne ei konkret og avgrensa meining i symbola som Vesaas bruker. Gjennom Mattis skildrar han ei undring over livet som skjerpar sansane mykje meir enn ei rasjonell forståing av verda. Det er denne konkrete undringa, den direkte linja mellom sansar og kjensler, som Mattis er åleine om å ha.

  h3 Is-slottet

  figure.col.col6.right-image.mt5
    img(src='./Vesaas_Is-slottet.jpg' alt='Frose fossefall med snøkledd skog rundt. Foto')
    figcaption Foto: Sverre A. Børretzen / Aktuell / Scanpix

  p Romanen 
    a(href='https://www.nb.no/items/131e8f3a9f34883cff1258b103df307a?page=0&searchText=is-slottet' target='_blank') 
      em Is-slottet
    | &nbsp;kom i 1963. Her finn vi på mange måtar ei vidareføring av den eksperimenterande skrivestilen som hadde gjort Vesaas til ein stor forfattar.

  p Boka handlar om to jenter i pubertetsalderen som utviklar ein nær vennskap. Dei mister vennskapen på ein brutal måte, og den eine må kjempe for å kome over tapet og leve vidare. 
    em Is-slottet 
    | er ei utforsking av pubertetens psykologi, og det er ei forteljing om at mennesket er grunnleggjande einsamt, trass i lengten etter å overvinne kjensla av å stå åleine.

