  p Romanen 
    a(href='https://www.nb.no/items/131e8f3a9f34883cff1258b103df307a?page=0&searchText=is-slottet' target='_blank') 
      em Is-slottet
    | &nbsp;kom i 1963. Her finner vi på mange måter en videreføring av den eksperimenterende skrivestilen som hadde gjort Vesaas til en stor forfatter. 

  p Boka handler om to jenter i pubertetsalderen som utvikler et nært vennskap. De mister vennskapet på en brutal måte, og den ene må kjempe for å komme over tapet og leve videre. 
    em Is-slottet 
    | er en utforskning av pubertetens psykologi, og det er en fortelling om at mennesket er grunnleggende ensomt, til tross for lengselen etter å overvinne følelsen av å stå aleine. 

