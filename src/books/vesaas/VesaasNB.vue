  p Mye av det Vesaas skrev, handler om  mennesket i eksistensielle kriser, situasjoner der mennesket møter de destruktive kreftene inni og utenfor seg selv. Selv om tematikken i bøkene hans kan være alvorlig og språket ikke alltid er like tilgjengelig, har novellene og romanene hans nådd fram til mange unge lesere de siste tiårene. 

  p Tarjei Vesaas har fått flere priser for bøkene sine, blant annet Nordisk Råds litteraturpris i 1964 for romanen 
    em Is-slottet
    | . Denne boka og romanen 
    em Fuglane 
    | (1957) er blitt stående som de mest berømte verkene i forfatterskapet. 
  
  h3 Fuglane 
  p Romanen 
    a(href='https://www.nb.no/items/14ac565f836ab1fee0cfaf9e59bc3396?page=7&searchText=fuglane' target='_blank') 
      em Fuglane
    | &nbsp;(1957) handler om raringen Mattis, som bor sammen med søstera si, Hege, og som ikke er som andre. Folk i bygda kaller han Tusten. Mattis er ikke i stand til å gjøre vanlig arbeid, men han er god til å ro og vil prøve seg som ferjemann. 

  p Mattis er det vi i dag kaller psykisk utviklingshemmet, men han har et følsomt sinn og kan sanse mange ting som vanlige folk ikke oppfatter. Mattis sanser og tolker de fleste naturfenomener. Han er redd i tordenvær, men ser han for eksempel et fugletrekk, slipper angsten taket, og dagen blir god for han.  

  p Mattis oppdager blant annet at det går et rugdetrekk over huset. Dette rugdetrekket er et under, noe gåtefullt og tiltrekkende, synes Mattis. Han tror at livet skal ta en ny retning nå som rugda har begynt å trekke like over huset der han og Hege bor. Og endringer blir det.

  p Vesaas har selv sagt at romanen om Mattis også er et selvportrett og et bilde på kunstneren, som kjenner lengselen etter det usigelige – etter 
