  h3 Vesaas som lyriker
  p Vesaas gav ut seks diktsamlinger, som alle er modernistiske i form og uttrykk. Gjennomgangstemaet i diktene hans er angst, ondskap og destruktive krefter. Symbolbruken er rik, og språket er knapt og fortettet, akkurat som i prosadiktningen. 

  p Diktet 
    a(href='https://www.nb.no/items/e6a765a02cfa293c75ffe5eb66dc3db8?page=81&searchText=regn i hiroshima' target='_blank') «Regn i Hiroshima»
    | &nbsp;er fra samlingen 
    em Leiken og lynet 
    | (1947). Diktet handler om atombomba som ble sluppet over Hiroshima 6. august 1945. 150 000 mennesker ble rammet, og nesten hele byen Hiroshima lå i ruiner etterpå. 

  p Diktformen var utradisjonell i 1947. I første delen av diktet er setningene stykket opp og spredd over flere korte linjer, som for å understreke hvordan menneskene ble sprengt i filler under dette bombeangrepet. 

  p «Regn i Hiroshima» kan stå som illustrasjon på det de modernistiske dikterne hevdet: Når verden er kaotisk, må også språket og diktformen gjenspeile dette. Da holder det ikke med tradisjonelle strofeformer med rim og fast rytme. 

  p Hendingen som Vesaas prøver å si noe om, er så uhørt og så utenfor all fatteevne at ingen ord strekker til. Derfor må dikteren appellere til følelsene våre mer enn til forstanden, og det er nesten helt nødvendig at ordene kan tolkes på nokså forskjellig måte.

