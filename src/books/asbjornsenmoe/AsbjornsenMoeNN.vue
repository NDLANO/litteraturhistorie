  h3 Draumen om  det norske

  p I 1814 får Noreg sjølvråderett og eiga grunnlov. Samtidig veks interessa for alt som kan kallast opphavleg norsk. Men Noreg har vore under Danmark i fleire hundre år, og i heile denne perioden har norske forfattarar skrive på dansk. Ved inngangen til 1800talet finst det derfor ingen norskspråkleg litteratur bortsett frå eventyr, viser og segner som var over­leverte munnleg frå generasjon til genera­sjon. 

  h3 Større interesse for folkelitteraturen i Europa

  p Den dominerande kulturelle og åndelege retninga i Europa i første del av 1800­talet er romantikken. Romantikara­ne gjer opprør mot fornuftsdyrkinga som har prega 1700­talet. Vitskap og sivilisa­sjon har øydelagt menneskesinnet, hevdar filosofen JeanJacques Rousseau, det gjeld å finne tilbake til det naturlege og ekte. Derfor byrjar romantikarane mellom anna å interessere seg for den folkelege kulturen.

  p I tillegg meiner romantikarane at folke litteraturen er eit uttrykk for folke­sjela, den kulturelle og historiske bak­grunnen som er felles for ein nasjon. Utover på 1800­talet blir det samla inn og skrive ned eventyr, segner og folkeviser over heile Europa. Allereie i 1812 gir brør­ne Jacob og Wilhelm Grimm ut den første eventyrsamlinga si i Tyskland.

  h3 Noreg følgjer etter

  p I 1830­åra slår den romantiske bølgja inn over Noreg for fullt. Koplinga mel­lom den nyvekte interessa for det norske og dei romantiske førestellingane om det folkelege som noko opphavleg og ekte, blir i Noreg omtalt som nasjonalromantikk. Prosten Andreas Faye gir ut den første samlinga av norske segner i 1833. I 1840­åra kjem så Peter Christen Asbjørnsen og Jørgen Moe med si første samling av nor­ske eventyr og segner. I tillegg er det fleire andre som samlar inn eventyr, segner og viser i Noreg.

