        <strong>Novelleeventyr</strong> som «Prinsessa som ingen kunne målbinde»: I novelleeventyra spelar det overnaturlege lita eller inga rolle.
  
  h3 Eventyrsjangeren

  p <em>Eventyr</em> er forteljingar med oppdikta innhald som er overleverte munnleg frå generasjon til generasjon. Dei har verken tid eller stadfesting, men handlinga er like vel plassert i ein kulturhistorisk saman heng som vi kan kjenne att. Vi møter det gamle norske bondesamfunnet med kongar og prinsesser, prestar, futar, klokkarar, bønder og tenestefolk.

  p Dei fleste eventyra følgjer eit enkelt grunnmønster: Helten møter ei utfordring eller eit problem som skal løysast. Slutten er aldri noko å lure på: Helten lykkast, og motstandarane går det dårleg med. Spenninga ligg i kva helten må gjere for å vinne over motstandarane. Handlinga blir alltid fortald kronologisk, utan frampeik eller tilbakeblikk. 

  p Karakterane i eventyret er eindimensjonale og statiske. Dei er anten gode eller slemme, og dei endrar seg aldri. Eit anna typisk sjangertrekk ved eventyra er bruken av faste formlar som «det var en gang» og «snipp, snapp, snute». Gjen taking blir gjerne brukt for å auke spenninga: Det er ikkje første eller andre  gongen, men tredje gongen som gjeld. 

  p Eventyr finst i alle kulturar, og ofte går dei same historiene att i litt ulik form i ulike land. Eventyret om «Kvitebjørn Kong Valamon» er ein norsk versjon av det eventyret som Disney-filmen «Skjønnheten og udyret» byggjer på. 

