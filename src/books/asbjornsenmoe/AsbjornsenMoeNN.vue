  h3 Folkedikting

  p Samlingane til Asbjørnsen og Moe inneheld ikkje berre eventyr, men også fleire segner, for eksempel «Mannen som gikk i vargham» og «Tuftefolket på Sandflesa». 

  img(src="./asbjornsenmoe_06.jpg" alt="Illustrasjon fra eventyr")

  p Segna skil seg frå eventyret ved at segna gjerne er knytt til ein bestemt stad og ei viss tid. Segna gir ofte overnaturlege forklaringar på naturfenomen eller historiske hendingar.

  img(src="./asbjornsenmoe_07.jpg" alt="Danser i skogen")

  p Somme gonger lagar Asbjørnsen og Moe ei såkalla rammeforteljing rundt eventyra eller segnene. Rammeforteljinga handlar om korleis dei to folkeminnegranskarane reiser rundt på Austlandet og møter ulike forteljarar. På den måten kan dei også skildre kva slags personar forteljarane er, og korleis dei fortel historiene sine. Eksempel på slike rammeforteljingar er «En natt i Nordmarka» og «En sommernatt på Krokskogen».

  h3 Sjangertrekk og dramaturgi i  «De tre mostrene»

  p Eventyret om dei tre mostrene inneheld fleire sjangertrekk som er typiske for eventyret:
    ul
      li Forteljinga er ikkje tid- eller stadfesta, og personane er eindimensjonale og karikerte. Spenninga knyter seg til om hovudpersonen klarer å løyse det oppdraget ho får. I dette tilfelle får jenta hjelp av tre kvinner med overnaturlege evner. Som lønn må ho love å invitere dei i bryllaupet sitt.

      li Eventyret opnar med formelen «Det var en gang», som straks vekkjer heilt klare sjangerforventningar hos lesaren. Dramaturgien utnyttar den såkalla tretalslova: Jenta får tre oppdrag ho må løyse, og møter tre hjelparar. Samtalane mellom henne og hjelparane inneheld dei same replikkane tre gonger på rad. Gjentakingane skaper ein eigen rytme i teksten.

      li Og som dei fleste eventyra gjer, endar også dette godt til slutt. Jenta slepp unna dei arbeidsoppgåvene dei misunnelege kollegaene ville prakke på henne, og lever lykkeleg med prinsen alle sine dagar.
  
  h3 Språket hos Asbjørnsen og Moe

  p Eventyr og segner er munnleg littera-tur fortald frå generasjon til generasjon. Dei som tek på seg å skrive ned desse tekstane, står derfor overfor ei utfordring: Korleis kan ein ta vare på det munnlege i skriftleg form? Resultatet er at eventyr-språket har fått ein munnleg setnings-bygnad med mange jamstilte setningar og eit stort innslag av replikkar.

  p Når Asbjørnsen og Moe går i gang med å skrive ned norske folkeeventyr og segner, finst det framleis ikkje noko eige norsk skriftspråk. Eventyra blir derfor skrivne ned på dansk. Men Asbjørnsen og Moe prøver å ta omsyn til den munnlege originalteksten og nyttar mange norske dialektord.

  p Gjennom det språklege arbeidet med eventyra er Asbjørnsen og Moe med på å rydde grunnen for fornorskingsprogram-met til språkmannen Knud Knudsen, og for så vidt også for arbeidet til Ivar Aasen med å reise eit nytt norsk skriftspråk med utgangspunkt i norske dialektar.

