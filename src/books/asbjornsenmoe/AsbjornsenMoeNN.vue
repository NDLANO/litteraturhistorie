  h3 Norske folkeeventyr

  p Den første eventyrsamlinga til Asbjørnsen og Moe, <em>Norske Folkeeventyr</em>, blir utgitt som fire hefte. Det første heftet kjem i 1841, det siste i 1844. Seinare gir Asbjørnsen ut fleire eventyrsamlingar på eiga hand, mellom anna <em>Norske Huldreeventyr</em> og <em>Folkesagn</em> (1845–1847).

  img(src="./asbjornsenmoe_04.jpg" alt="Illustrasjon fra eventyr")

  p Eventyra kan delast inn i grupper etter innhaldet. I samlinga Norske Folkeeventyr finn vi for eksempel desse undersjangrane:
