  p Falkberget gjorde grundige studiar i lokalsamfunnet før han skreiv trilogien, og han sette seg godt inn i alle historiske detaljar. «Ikkje noko i miljøet må vere gale», sa Falkberget, «ikkje noko må hende som ikkje kunne vere hendt.» 
    em Christianus Sextus 
    | er ein romanserie om fattigdom, svolt og nød i ei krisetid, og ein viktig del av norsk arbeidarhistorie.

  p Det andre store romanverket til Falkberget, 
    em Nattens brød
    | , omfattar fire bind som kom ut i tidsrommet 1940 til 1959. Også desse bøkene byggjer på grundige historiske studiar. Forfattaren går her heilt tilbake til 1600-talet for å vise korleis gruvearbeidarane på Røros har vore med på å skape tidleg arbeidarhistorie.

  p I 
    em Nattens brød 
    | er det først og fremst kvinnene som er slitarane, og handlinga er lagd til Gauldalen nord for Røros, der den første gruvedrifta tok til. Gjennomgangsfiguren i bøkene er ungjenta An-Magritt, som køyrer malm med okse og slede over vidda fram til smeltehytta. Det er ei sterk historie om ei jente som er komen til verda som eit resultat av valdtekt, og der mora tok sitt eige liv etter å ha fått kyrkjetukt og måtta stå i gapestokken.

