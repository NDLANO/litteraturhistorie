  p Falkberget skreiv mykje gjennom eit langt liv, og han sa sjølv på sine eldre dagar at han hadde produsert meir som journalist enn som skjønnlitterær forfattar.

  p Han starta som journalist i Rørosavisa Fjell-Ljom. Seinare vart han redaktør for sosialistiske aviser, først Nybrot i Ålesund, deretter Smaalenenes SocialDemokrat i Fredrikstad. Falkberget var også aktiv skribent i fleire tidsskrift som hadde tilknyting til arbeidarrørsla.

  p Etter at han flytte heim til Røros i 1922, heldt han fram med ein omfattande journalistisk produksjon, kom med i lokalpolitikken og var varaordførar i to periodar. I 1930-åra var Falkberget også stortingsrepresentant for Arbeidarpartiet i SørTrøndelag, men han sa sjølv at han likte seg betre som skribent enn som rikspolitikar.

  p Falkberget hadde eit kriste livssyn, og dette verdisynet kom tydeleg fram i det han skreiv. Her forfekta han ein kristen etikk kombinert med eit sosialistisk samfunnssyn.

  p Mange meinte at Falkberget fortente nobelprisen i litteratur, og i 1949 fekk han overrekt «Det norske folks fredspris». 165 000 kr hadde vorte samla inn, ein svært stor sum den gongen.

