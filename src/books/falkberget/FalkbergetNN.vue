  p Falkberget blir rekna for ein av dei store historisk orienterte forfattarane frå 1920- og 1930-åra, og som nyrealist prøvde han å forstå samtida ved å gjenskape fortida. _ 1920-åra var tiåret for den historiske romanen. Tida kravde ei anna skildring av verda enn den nyromantikarane hadde stått for. Bakgrunnen for den endra haldninga og den nye skrivemåten blant forfattarane var fleire sentrale hendingar, som unionsoppløysinga i 1905, den første verdskrigen (1914–1918), revolusjonen i Russland i 1917 og ikkje minst den aukande industrialiseringa og framveksten av ein radikal arbeidarklasse. _ På same tid som Sigrid Undset skreiv mellomalderromanar og Olav Duun skreiv om ætter fire hundre år attende i tid, sette Johan Falkberget søkjelyset på norsk arbeidarhistorie. 

