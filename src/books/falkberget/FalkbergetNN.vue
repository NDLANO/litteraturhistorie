  p Bør Børson er stormannsgalen og ein naiv oppkomling med ufortent mykje flaks, men vi får samstundes sympati med han. Den dumsnille ungkaren frå Olderdalen driv landhandel og reiser ofte på handelsturar til Trondheim. Her blir han lokka både av sjarmerande jomfruer og av innpåslitne aksjespekulantar.

  p Forteljinga om Bør Børson er framleis ein populær folkekomedie. Alt i 1929 vart forteljinga dramatisert, og i 1938 vart det laga norsk film med Toralf Sandø i rolla som Bør. I 1972 kom musikalversjonen på Det Norske Teatret med Harald Tusberg som regissør og musikk av Egil Monn-Iversen. Rolv Wesenlund spela Bør Børson, og stykket hadde ein enorm suksess. Det førte til at det vart laga film med dei same skodespelarane i 1974. Seinare har fleire amatørteaterlag rundt om i landet sett opp Bør Børson på lokale scener. 

  

