  p Med trebindsverket 
    em Christianus Sextus 
    | går Falkberget tilbake til 1700-talet. Det strøymer arbeidarar til Røros, mange av dei svenske, for å arbeide i gruva som har fått namn etter kongen: Christian den sjette (Christianus Sextus på latin). Dei fleste har vandra over fjellet til Røros for å søkje arbeid. Her er sterke skildringar av hardt kroppsarbeid i gruvene, fattigdom, svolt og undertrykking.

  
