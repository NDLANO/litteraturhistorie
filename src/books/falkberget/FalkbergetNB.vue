  p Med trebindsverket 
    em Christianus Sextus 
    | går Falkberget tilbake til 1700-tallet. Det strømmer arbeidere til Røros, mange av dem svenske, for å arbeide i gruva som har fått navn etter kongen: Christian den sjette (Christianus Sextus på latin). De fleste har vandret over fjellet til Røros for å søke arbeid. Her er sterke skildringer av hardt kroppsarbeid i gruvene, av fattigdom, sult og undertrykking.  

