  p Falkberget gjorde grundige studier i lokalsamfunnet før han skrev trilogien, og han satte seg godt inn i alle historiske detaljer. «Ikke noe i miljøet må være galt», sa Falkberget, «ikke noe må hende som ikke kunne være hendt». 
    em Christianus Sextus 
    | er en romanserie om fattigdom, sult og nød i en krisetid, og en viktig del av norsk arbeiderhistorie. 

  p Det andre store romanverket til Falkberget, 
    em Nattens brød
    | , omfatter fire bind og kom ut i tidsrommet 1940 til 1959. Også disse bøkene bygger på grundige historiske undersøkelser. Forfatteren går her helt tilbake til 1600-tallet for å vise hvordan gruvearbeiderne på Røros har vært med på å skape tidlig arbeiderhistorie. 

  p I 
    em Nattens brød 
    | er det først og fremst kvinnene som er sliterne, og handlingen er lagt til Gauldalen nord for Røros, der den første gruvedrifta tok til. Gjennomgangsfiguren i bøkene er ungjenta An-Magritt, som kjører malm med okse og slede over vidda fram til smeltehytta. Det er en sterk historie om ei jente som er kommet til verden som et resultat av voldtekt, og der mora tok sitt eget liv etter å ha fått kirketukt og måttet stå i gapestokk. 

