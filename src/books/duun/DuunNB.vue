  p Hovedtemaet i 
    em Medmenneske 
    | er ondskapen i mennesket og hva som kan stilles opp mot det onde. Motsetningene i romanen er den gode Ragnhild og svigerfaren Didrik, som representerer ondskapen selv. Det er en lang og ondsinnet konflikt mellom Ragnhilds mann Håkon og svigerfaren, og denne konflikten ender med en katastrofe, for Ragnhild dreper svigerfaren. 

  p Drapet er ikke planlagt og overlagt, heller en spontan handling som kan tolkes som et selvforsvar. Ragnhild er et  godt menneske som i utgangspunktet aldri vil gjøre andre noe ondt. Men under ligger også angsten for at Håkon kan komme til å drepe faren. Hun vil ikke at mannen hennes skal bli morder, og gjør en slags plikthandling da hun tar livet av svigerfaren med øks. 

  p Ragnhild må sone fengselsstraff, og hun sier selv: «Det vonde drep du ikkje med øks.» Hun vet at hun har handlet galt og må ta sin straff for det, men hun kjenner seg også sveket av Håkon da hun må sone. Først mange år seinere blir de to  forsont, og da er det Håkon som har problemer med å reise seg når han innser at han har sveket kona. 

  h3 Menneske og maktene 
  p 
    a(href='https://www.nb.no/items/3ecb9574b82aad7759491cff624e34fa?page=7&searchText=menneskene%20og%20maktene' target='_blank') 
      em Menneske og maktene
    | &nbsp;(1938) er den siste boka i Duuns forfatterskap. Boka er en kollektivroman uten fast hovedperson, og handlingen utspiller seg i et fiktivt øysamfunn som Duun kaller Øyvære. Menneskene som lever på denne øya, må måle krefter med maktene, det vil si kreftene som styrer livet deres. Vi møter forskjellige mennesker som lar seg styre av ulike makter. Noen gir tapt for maktene, andre frykter maktene og lever i angst, og atter andre grubler over maktene og mister livslysten.

  p Første og siste kapittel i boka utgjør en rammefortelling. Det finnes en gammel spådom om at øysamfunnet skal gå under ei uværsnatt med storflom og storm, og at mange skal drukne, men enkelte skal overleve. Siste kapittel skildrer nettopp denne natta med uvær og flom. Midtpartiet i boka består av fem fortellinger. Hver fortelling gir oss forhistorien til en av hovedpersonene som stilles overfor kriser og påkjenninger i uværsnatta som kommer. 

  p Vi blir kjent med menneskene i øysamfunnet, blant annet Helmer, som tar ansvar for folk rundt seg, og den mistroiske Ludvig, som hater livet og er negativ til det meste. Når katastrofen kommer, får vi se hvem som klarer seg og overlever av alle dem som bor på øya: Det er de som har tro på at det går an å leve i fellesskap, og som står solidarisk med hverandre. De som mangler denne solidariteten og har mistro til hverandre, går under. 
  
  h3 Det gode i mennesket 
  p Hovedgrunnen til at Olav Duun var høyt verdsatt i samtida, er nok at han fokuserte på et grunnleggende mellommenneskelig tema: Hva er det som gjør oss til gode mennesker? Det menneskesynet Duun forfekter, går igjen som en rød tråd i hele forfatterskapet. 

  p Kampen mellom gode og onde makter er på mange måter hovedmotivet i Duuns forfatterskap: På hvilken side står vi i kampen mellom det gode og det onde? Utnytter vi de onde maktene for å skaffe oss makt over andre? Eller kjemper vi imot det onde og med det gode for å stå ved siden av dem som trenger hjelp? 

  p Første verdenskrig hadde gjort slike spørsmål svært aktuelle. Samtidig kom menneskelig ondskap på nytt i fokus utover i 1930-årene, da nazismen og fascismen var på frammarsj og krigstrusselen nok en gang hang over Europa. 

  p Det blir sagt om Duun at han ikke fortolker det onde som en autonom motmakt til det gode. I Duuns bøker har ikke ondskapen og hatet en kjerne som er ond. Vi kan si at hatet i sin natur er en skade, 
    em et menneske på vranga
    | . 

  p “En skyldfølelse, en opplevelse eller en ugjerning i ungdommen kan forskanse seg i et slags selvhat, og dette kan avspeile seg gjennom hele livet og hele væremåten”, skal Duun ha uttalt.  

  p Det onde er altså ingen universell makt, men snarere noe historisk og konkret. Derfor kan ondskapen også overvinnes. I uværsnatta ute på Øyvære er det livskreftene som seirer, og de tvinger menneskene sammen i et nødvendig skjebnefellesskap.

