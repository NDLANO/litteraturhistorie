      em Juvikingar
    | , der handlingen er knyttet til hjemstedet Jøa i Namdalen. Fram til 1923 skrev han ei bok i året om 
    em Juvikfolke
    | , til sammen seks romaner. 

  p Det er øyene, havet og folket i dette kystlandskapet som utgjør ramma om bokserien. Vi følger Juvik-slekta gjennom seks generasjoner, fra 1600-tallet og fram mot 1800-tallet. Slektas medlemmer kjemper en kamp mot hedenskap og villskap. De har lett for å gripe til vold og kommer stadig i konflikt med hverandre. 

  p Juvikfolke regnes som et storverk i norsk litteratur. Det har episk kraft og mangfold, og Duun viser stor bredde og innsikt gjennom sine skildringer av folk, miljø og kulturhistoriske utviklingslinjer. Det er sagt om romanserien at den kan tolkes som et møte mellom gammel tid og moderne samfunnsforståelse. 
  
  h3 Medmenneske 
