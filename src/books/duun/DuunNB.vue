  p Den nye forfattergenerasjonen kom fra ulike deler av landet og fra ulike samfunnslag. De var bergmenn og bønder, fiskere og industriarbeidere. Et felles tema i bøkene er at de handler om konflikten mellom enkeltmennesket og fellesskapet. 

  p Olav Duun kom fra Namdalen i Trøndelag og var fisker fram til han som 25-åring begynte med lærerutdanning. Han arbeidet først noen år som lærer i Trøndelag og flyttet deretter til Holmestrand i Vestfold, der han underviste fram til 1927. 

  p Studietida gav inspirasjon til å skrive, og han brukte et landsmål med sterke innslag av namdalsdialekt. Duun mente han måtte bruke den lokale dialekten, språket som folket hans talte, dersom han skulle dikte med troverdighet. 

  p Duun regnes som en stor språkkunstner og hører til blant de fremste forfatterne i norsk litteratur på 1900-tallet. Han var svært produktiv og utgav i alt 25 romaner, fire novellesamlinger og to barnebøker. 
  
  h3 Juvikfolke 
  p Gjennombruddet for Duun som forfatter kom i 1918 med slektsromanen 
