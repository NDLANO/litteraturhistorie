  h3 Nyrealismen og den historiske romanen
  p Rundt 1910 vokste det fram en ny generasjon romanforfattere her i landet. 
    em Nyrealistene 
    | hentet inspirasjon fra både realismen og nyromantikken, men hovedformålet deres var å vise at mennesket har ansvar for eget liv. 

  p Nyrealistene skrev gjerne om enkeltmennesket i moralsk eller etisk konflikt med omverdenen. Derfor blir denne litterære retningen også kalt etisk realisme. Sterke følelser som kjærlighet og hat får romanpersonene til å bryte sine egne etiske normer og grenser. Det fører til at de opplever samvittighetsnag og angst – og har et behov for å sone. Ønsket om å gjøre bot er et sentralt tema hos de nyrealistiske forfatterne. 
    
  p Den historiske romanen, ofte i flere bind, er ramme når nyrealistene skildrer hvordan menneskene gjennom generasjoner blir formet av ulike konflikter i det sosiale miljøet. Dette brede romanformatet gjør at forfatterne også kan skifte synsvinkel slik at vi ser de ulike hendingene gjennom øynene til flere av personene i romanen. 

  p Både her i landet og i de fleste andre land i Europa var det stor interesse for historisk, episk diktning. Olav Duun gav ut bokserien om 
    em Juvikfolke
    | . Samtidig kom Sigrid Undset med trilogien om 
    em Kristin Lavransdatter
    | , og Johan Falkberget lanserte sine historiske romaner med handling fra Røros. 
  
  h3 Folkelivsskildring og talespråk 

