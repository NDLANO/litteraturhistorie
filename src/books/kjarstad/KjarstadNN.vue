  p Nemninga postmoderne metafysikar kan verke som ei sjølvmotseiing. Postmodernisten hevdar at «dei store forteljingane» er daude – det finst ingen absolutte sanningar. Metafysikaren leitar derimot etter evige og allmenne sanningar.

  p Jan Kjærstad stiller dei heilt store spørsmåla i forfattarskapen sin: Kva er eit menneske? Kva får eit menneskeliv til å hengje saman? Kva er røyndom? Samstundes viser han seg som postmodernist når han insisterer på makta i språket og skaparkrafta i fantasien. Gjennom leik med språket og utforsking av romanen som sjanger granskar han tilhøvet mellom språket og røyndommen.
  
  h3 Kva er eit menneske?
  p Eit grunnleggjande spørsmål i heile forfattarskapen til Kjærstad er spørsmålet om kva det vil seie å vere menneske. Kjærstad bruker språket og forma på forteljinga til å formidle innsikt og gi moglege svar på dette spørsmålet. I botnen ligg ein tanke om at forteljingar kan skape identitet. Forteljingar kan skape meining og samanheng i eit liv som elles kan verke fragmentert og styrt av ulike krefter i eit globalt medie- og informasjonssamfunn. 
  
