    em Vinduet
    | , i perioden 1985–89, og 
    em Bøk
    | , saman med Jon Fosse, frå 1993–1996. Han har på denne måten vore med på å prege norsk litterær debatt. 

  h3 Død og kjærleik

  figure.col.col6.right-image.mt5
    img(src='./kjarstad_Portrett4.jpg' alt='Mann står framfor ein kvit kakkelomn. Han er omkransa av bøker. Foto')
    figcaption Foto: Jan Petter Lynau / VG / Scanpix

  p 
    em Homo Falsus eller Det perfekte mord 
    | (1984) og 
    em Rand 
    | (1990) er eksempel på romanar som på mange måtar er typiske for Jan Kjærstad. Forfattaren leikar med ulike sjangrar, og han leikar med lesaren. I begge romanane drep hovudpersonane, men romanane er ikkje realistiske psykologiske portrett eller krimromanar. Kjærstad utnyttar element frå krimromansjangeren til å formidle noko om kva det vil seie å vere menneske i ein postmoderne røyndom.

  p 
    em Homo Falsus eller Det perfekte mord 
    | er ein såkalla metaroman. Romanen tematiserer korleis han blir til gjennom at forteljaren skriv om sin egen skriveprosess. Utover i romanen mistar forteljaren kontrollen over si eiga forteljing, og mot slutten blir det uklart om forteljaren også er oppdikta – av hovudpersonen han har dikta opp. Det heile er både forvirrande og forførande. Kjærstad leikar med forholdet mellom fiksjon og røyndom i ein roman som går i kritisk dialog med ulike postmoderne førestillingar om sanning, røyndom og identitet.

  p I 
    em Rand 
    | blir hovudpersonen sett til å etterforske drap han sjølv har gjennomført. Nok ein gong leikar Kjærstad med element frå krimsjangeren, men prosjektet hans er ikkje å lage eit portrett av ein uetisk drapsmann. Hovudpersonen er ein representant for den ekstreme postmoderne karakteren – ein karakter som er så fristilt frå moralen og samanhengen i dei store forteljingane at han ikkje kjenner skuld eller ansvar for morda han har utført. Han er eit menneske utan moralsk kjerne eller etisk orienteringspunkt.

  p Kjærstad utnyttar her krimromanstrukturen til å formidle noko om korleis eit menneske freistar å skape orden og meining i ei verd truga av kaos og meiningsløyse. Det eigentlege prosjektet, i denne på fleire måtar utfordrande forteljinga, er å seie noko om makta som ligg i språket, om skaparkrafta til mennesket – om fantasiens fantastiske eigenskapar. Og nok ein gong er Kjærstad oppteken av spørsmålet: Kva er eit menneske?

  p Romanen 
    em Tegn til kjærlighet 
    | frå 2002 er kanskje noko mindre typisk for Kjærstads forfattarskap. Romanen fekk også noko meir blanda kritikkar. Romanen har ikkje, som 
    em Homo Falsus eller Det perfekte mord 
    | eller 
    em Rand
    | , noko kritisk prosjekt. Romanen er meir ein hyllest til språket, til fantasien, til forteljinga – og til kjærleiken. Kjærleiken til forteljinga, til livet og til dei andre – det vil seie medmenneska.

  p Andre kjende bøker av Kjærstad er mellom anna romanane 
    em Det store eventyret 
    | frå 1987 og 
    em Kongen av Europa 
    | frå 2005. Kjærstad har også gitt ut biletbøker for barn i samarbeid med ulike illustratørar.
  
  h3 Forførar, erobrar, oppdagar – Jonas Wergeland

  h5 Det samansette individet i eit komplekst mediesamfunn
  p Med 
    em Forføreren 
    | (1993), 
    em Erobreren 
    | (1996) og 
    em Oppdageren 
    | (1999) nådde Kjærstad ut til ei stor gruppe lesarar, både i og utanfor Noreg. I 2001 fekk han Nordisk råds litteraturpris for Oppdageren. Trilogien er ein fiktiv biografi om den oppdikta tv-personlegdommen Jonas Wergeland og kan reknast som ein klassikar i norsk postmodernistisk litteratur. Verket eksperimenterer med form, forteljeteknikk, komposisjon og karakterbygging. Kjærstad leikar med ulike sjangrar, som mellom anna biografi, danningsroman, erotisk forteljing og politisk satire.

  p Dei tre romanane har ulike forteljarar. På den måten får vi sjå hovudpersonen Jonas Wergeland frå fleire perspektiv. Romanserien formidlar historia om eit oppstykka og fragmentert individ, noko som kan spegle samtida han lever i.

