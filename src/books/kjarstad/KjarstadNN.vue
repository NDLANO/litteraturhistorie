  p Kjærstad er på mange måtar ein forfattar med eit litterært program. I tillegg til å skrive romanar og noveller har han skrive ulike essay og artiklar om litteratur. I 
    em Menneskets matrise 
    | (1989), 
    em Menneskets felt 
