  p I tillegg til å vere ein leik med biografisjangeren, er trilogien om Jonas Wergeland eit stykke noregshistorie. Tv-programmet Jonas får suksess med, «Å tenke stort», er portrett av norske heltar, eller rettare sagt – nordmenn som også har vorte heltar i utlandet. Programma er laga som tv-portrett av nordmenn med ambisjonar. Ulike personlegdommar, som for eksempel Sonja Hennie, Gro Harlem Brundtland og Henrik Ibsen, speglar Noreg og den norske «folkesjela», noko Kjærstad leikar med, undersøkjer og ironiserer over i eit forsøk på å spegle kva det vil seie å vere norsk. At Jonas heiter Wergeland til etternamn, må også sjåast som ein del av Kjærstads leik med store nordmenn – og kva det vil seie å vere norsk.
  
  h3 Eksperiment med forteljarperspektiv
  p Jan Kjærstad er svært oppteken av kva slags forteljemåte og forteljarperspektiv den mediale verda vi lever i, bringar med seg. Massemedias hurtige glimt, dei vekslande perspektiva i zappekulturen, blir trekte inn i litteraturen. Det same gjer samtidas enorme kjelde til kunnskap og erkjenning. Korleis orientere seg i dette mylderet av informasjon? Korleis skape identitet i denne vrimmelen av inntrykk?

  p Romanane er komponerte som mange små forteljingar, som ikkje er bundne saman av kronologi og kausalitet. Ofte kan årsak komme før verknad, slik at hendingar som skjer i ettertida, på merkverdige måtar kan få innverknad på hendingar som har gått føre. Og i og med at dei tre romanane har ulike forteljarar, som formidlar ganske ulike bilete av Jonas Wergeland, får vi mange ulike sanningar, eller sannsynlege versjonar, av forteljinga om tv-helten.

