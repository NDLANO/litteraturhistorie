  p Etter andre verdskrigen gjekk talet på nynorskbrukarar nedover. Det tok mellom anna mykje lengre tid for Noregs Mållag enn for Riksmålsforbundet å kome i gang att med arbeidet etter krigen. Krigen førte òg til at nynorskfolket miste eit viktig argument, det nasjonale. Det nytta ikkje lenger å argumentere med at nynorsken var meir norsk enn bokmål. Nasjonal innstilling hadde synt seg gjennom å leggje for dagen den rette haldninga overfor tyskarar og NS, slik mellom anna riksmålsforkjemparen Øverland hadde gjort.
  
  h3 Språknormering etter 1950
  p I 1951 vedtok Stortinget å skipe ei ny nemnd som skulle utarbeide ein ny læreboknormal. Lærebøkene skulle få ei moderat språkform som verken var heilt konservativ eller heilt radikal. Norsk språknemnd byrja arbeidet sitt i 1952, og i 1959 var den nye læreboknormalen klar.

  p Etter 1959 har skulebøkene på bokmål stort sett halde seg til dei tradisjonelle, moderate formene, og språkstriden har stilna av. I andre halvdelen av 1900-talet har ikkje tilnærming mellom målformene vore noko uttalt mål for språkpolitikken.

  p Språknormering er likevel framleis ei statleg oppgåve i Noreg. I 1972 vart Norsk språkråd skipa som eit rådgivande organ. Rådet endra namn til Språkrådet i 2005.
  
  h5 1981
  p I 1981 kom det ei rettskrivingsreform i bokmål der ein tok inn att fleire tradisjonelle riksmålsformer. No kan vi for eksempel på nytt skrive furuen, kuen, jenten.
  
  h5 2005
  p I 2005 vart sideformene avskaffa i bokmål. No er alle former jamstilte og kan brukast også i lærebøker og i statstenesta.
  
  h5 2012
  p Språkrådet arbeider med ein ny rettskrivingsnormal for nynorsk som skal takast i bruk i 2012. Norma skal vere «tydeleg, enkel og stram og utan sideformer», slik at det blir lettare for alle å skrive korrekt på nynorsk.

