  h5 Andre prinsipp
  p Også nasjonalisme kan styre normeringa. Dei fleste som var med i språkdebatten i 1830-åra, ønskte seg eit skriftspråk som var genuint norsk, jamvel om dei kunne vere usamde om korleis dette norske språket skulle skapast.

  p Purisme, det å finne eller lage heimlege avløysarord i staden for å ta inn framandord i språket, kan også plasserast i denne kategorien. Nokre døme på ferske avløysarord i norsk er vevsansvarleg, verdsveven, nettstad og nettside.

  p Elles kan vi for eksempel ta omsyn til kva former fleirtalet bruker, vi kan velje dei formene som har høgast sosial prestisje, vi kan gå inn for forenkling for å skape eit logisk system, eller vi kan fremje valfridom. Ofte kan fleire prinsipp vere like aktuelle, slik at vi må prioritere eitt av dei. 
  
  h3 Tilnærming mellom riksmål og landsmål

  figure.col.col4.left-image.mt5
