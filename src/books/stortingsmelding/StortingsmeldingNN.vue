  p I engelsk og ein del andre språk er det stor skilnad mellom talemål og skriftspråk. Det kjem av at desse skriftspråka sjeldan eller aldri har vorte normerte. Det norske skriftspråket har derimot gjennomgått mange normeringar på vel hundre år. Dagens bokmål og nynorsk ligg derfor nokså langt unna dansk-norsken og landsmålet som folk skreiv for hundre år sidan.
  
  h3 Normgrunnlag og normeringsprinsipp
  p Når vi skal skape eit skriftspråk, må vi bestemme oss for kva som skal vere normgrunnlaget. Det vil seie at vi må velje om skriftspråket skal byggje på ein konkret dialekt, på fleire dialektar eller på eit standardisert talemål.

  p Da dei på 1800-talet diskuterte korleis dei kunne skape eit norsk skriftspråk, var nettopp valet av normgrunnlag eit viktig spørsmål. Ivar Aasen (1813–1896) ville lage eit skriftspråk basert på talemålet i bygdedialektane, mens Knud Knudsen (1812–1895) ville tilpasse det danske skriftspråket til den såkalla danna daglegtalen, eit standardisert talemål som vart snakka av ei viss gruppe i samfunnet. 
  
  h5 Ortofoni 
  p Når vi gjer om lyd til skrift, må vi avgjere kva slags system vi skal gå etter. I Noreg har vi sterk tradisjon for det ortofone prinsippet, det vil seie at kvar lyd i talen skal svare til eitt og same teikn i skrifta.

  p Men slik har det ikkje alltid vore. Så lenge skriftspråket i Noreg var dansk, sleit dei fleste med stumme bokstavar, dansk bøyingsmønster og særdanske ord når dei skulle skrive. Særleg Knud Knudsen, som var pedagog, var svært oppteken av at skriftspråket måtte spegle norsk uttale slik at det vart lettare å lære. 
  
  h5 Etymologi 
  p Det finst fleire prinsipp enn ortofoni som vi kan ta omsyn til. Eitt av dei er etymologiprinsippet, det vil seie at ordforma speglar korleis ordet vart uttalt ein gong i fortida. For eksempel har somme stadnamn i dag ein historisk skrivemåte som skil seg frå den uttalen som har utvikla seg. 
  
