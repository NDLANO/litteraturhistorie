  p Språkpolitikken i første halvdel av 1900-talet hadde som overordna mål å få landsmålet og riksmålet til å nærme seg kvarandre. Håpet var at dei to målformene på sikt kunne smelte saman til eit felles norsk skriftspråk, eit samnorsk.

  p Men dei skjønte òg at denne prosessen ville ta tid. Ein av grunnane var at språkdebatten vekte så sterke kjensler. Både dei som gjekk inn for ei tilnærming mellom målformene, og dei som var imot, oppfatta nemleg skriftspråket sitt som ein viktig del av identiteten. 
  
  h5 Mange frontar 
  p Landsmålsfolket hadde tradisjonelt si politiske forankring i Venstre og Bondepartiet, mens riksmålstilhengarane stort sett var å finne i partiet Høgre. I tillegg var det innbyrdes usemje både i landsmåls- og riksmålsleiren om kva retning utviklinga skulle ta.

  p Med 1917-reforma hadde ein del folkelege austlandske former vorte valfrie både i riksmål og i landsmål. Men i riksmålsleiren var det mange som ikkje ville ha meir fornorsking no. Dei hadde fått ei skrivenorm som var såpass oppnorska at ho kunne spegle den såkalla danna daglegtalen. Folkelege former som sola, kasta, stein og fram såg dei på som vulgære. Sjølve skreiv dei solen, kastet, sten og frem.

  p Heller ikkje alle i landsmålsleiren ønskte å gi større rom for dei austlandske dialektane, mange ville halde fast ved dei tradisjonelle formene.
  
