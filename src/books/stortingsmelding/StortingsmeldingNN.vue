  h3 Skriftspråknormering
  p Å setje opp ei norm for skriftspråket vil seie å lage reglar for korleis vi skal stave og bøye ord. Ordet norm tyder rettesnor, og språknormering blir altså eit omgrep for arbeidet med å utvikle og endre skriftspråket
  p I Noreg er det Språkrådet som er normerande organ for både bokmål og nynorsk. I Frankrike har Det franske akademiet ansvaret for å redigere den offisielle ordboka over det franske språket. Ei tilsvarande offisiell normering av engelsk finst ikkje. Her er det i staden Oxford English Dictionar(britisk engelsk) og Webster’s Dictionary (amerikansk engelsk) som blir rekna for rettesnorer.

