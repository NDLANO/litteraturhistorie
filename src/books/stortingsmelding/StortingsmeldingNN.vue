  h5 Ny reform
  p I 1934 vedtok stortingsfleirtalet å setje ned ei nemnd med todelt mandat. Nemnda skulle for det første kome med framlegg om endringar i rettskriving, ordformer og bøyingsformer som kunne føre dei to målformene nærare saman. For det andre skulle nemnda prøve å redusere talet på dobbelformer i begge skriftspråka.

  p Medlemmene i nemnda meinte at språket slik det vart snakka dei fleste stadene i Noreg, skulle vere utgangspunktet for normeringa. Framlegget dei la fram, vekte sterke reaksjonar både i bokmåls- og riksmålsleiren, men det vart vedteke med nokre endringar i 1937. Den nye rettskrivinga tok til å gjelde frå hausten 1938.
  
  h3 Utviklinga i bokmål
  p I første halvdel av 1900-talet skjedde det omfattande endringar i begge skriftnormalane. Vi skal her sjå på nokre sentrale punkt, og vi byrjar med bokmål.
  
  h5 1907
  p 1907-reforma gjorde at riksmålet ikkje lenger var dansk, men spegla den såkalla danna daglegtalen. Det trongst ikkje særleg omfattande endringar i skriftnormalen for å få til denne tilpassinga, for den danna daglegtalen var i utgangspunktet kraftig påverka av dansk. Dessutan var det dei øvre samfunnslaga som brukte dette standardtalemålet, og dei var sjølvsagt nøgde med å få eit skriftspråk som fungerte godt for dei sjølve.
  
  h5 1917
  p Med 1917-reforma vart det opna for fleire folkelege former i skriftnormalen. Mens ein før berre kunne skrive solen og boken, vart det no valfritt å bruke a-ending i hokjønnsord: sola, boka. Det vart òg lovleg å skrive kasta og sparka i staden for kastet og sparket. Og mens ein før måtte skrive sten, røk og løs, kunne ein no velje former med diftong i somme ord: stein, røik, laus. Gamle og nye former vart jamstilte, men dei fleste heldt likevel fram med å skrive solen, kastet og sten.
  
  h5 1938
  p 1938-reforma var mykje meir radikal. Den danna daglegtalen vart ikkje lenger rekna som rettesnor for normering av bokmålet. I staden tok dei utgangspunkt i talemålet til fleirtalet av folket.

  p Frå 1938 fekk om lag 900 hokjønnsord obligatorisk a-ending i bokmål, særleg ord frå plante- og dyrelivet og frå arbeidslivet: bjørka, høna, nåla, øksa osv. Former som bjerken, hønen, nålen og øksen forsvann ut av skriftnormalen. Også diftong vart påboden i ei rad ord, for eksempel i stein, røyk, eik, geit, øy. Dermed kunne ein ikkje lenger skrive sten, røk, ek, gjet, ø.
  
  h3 Utviklinga i nynorsk

  figure.col.col5.right-image.mt5
   img(src='./Stortingsmelding_Utviklingen_i_nynorsk.jpg' alt='')
   figcaption Ver gild ikkje røyk her. Foto: Bjørn E. Larsen / VG / Scanpix

  p I motsetning til riksmålet bygde landsmålet frå starten av på norsk talemål i delar av landet. Men for dei som budde på det sentrale Austlandet og snakka ulike by- og bygdedialektar, var heller ikkje landsmålet eit skriftspråk dei kjende seg heime i.
  
  h5 1910
  p I 1910 kom det ei mindre landsmålsreform – eit lite første steg i retning av ei skriftspråkleg samling.
  
  h5 1917
  p Fram til 1917 var bøyingsformer som soli og husi eineformer. No vart det òg mogleg å skrive sola og husa. Og der det før måtte heite ei visa – fleire visor – alle visone, kunne ein no også velje ei vise – fleire viser – alle visene. Infinitivsformer på -e vart jamstilte med dei tradisjonelle formene på -a: å leve eller å leva. Dessutan vart presensformer som biter og ligger tillatne ved sida av bit og ligg. Til og med forma je kom med i 1917-normalen, i tillegg til eg. Gjennom desse endringane vart landsmålet eit aktuelt alternativ også for dei som snakka austlandsdialektar.
  
  h5 1938
  p Endringane som kom i 1938, gjekk stort sett i same lei som bokmålsreforma: Mange av dei eldre, konservative formene måtte vike for former som samsvara med talemålet til fleirtalet.

  p No vart -a innført som hovudform i bunden form eintal av hokjønnsord: sola, døra, og i bunden form fleirtal av inkjekjønnsord: husa, fjella. I-formene (soli, fjelli) vart no sideformer, det vil seie at dei ikkje kunne nyttast i lærebøker og i administrasjonen. Også fleirtalsendinga -or i ord som gator og visor vart klammeform frå 1938. Pronomenforma je fall derimot ut att, mens infinitiv på -e vart sideform.

  p Mange nynorskfolk var i førstninga sterkt misnøgde med den nye rettskrivinga fordi dei tradisjonelle bøyingsformene no var fjerna. Men motstanden la seg etter kvart.
  
  h5 Talet på dobbelformer

  figure.col.col6.right-image.mt20
   img(src='./Stortingsmelding_Skrivemaskin.jpg' alt='')
   figcaption Oslo ca. 1935: Moderne skrivemaskin fotografert i NTB. Foto: NTB / Scanpix

  p Nemnda lykkast ikkje i å få ned talet på dobbelformer, snarare tvert imot. Men det var i grunnen uråd å fjerne dobbelformene om ein framleis ville ha ei gradvis og smidig tilnærming mellom målformene, og i tillegg gi rom for austlandske former.

  p Etter 1938-reforma fanst det framleis mange jamstilte former, for eksempel melk og mjølk (i bokmål). I tillegg kom det enda ein type dobbelformer, der den eine forma var hovudform og skulle brukast i lærebøker og i statstenesta, mens den andre var sideform (klammeform) og kunne brukast av elevar og privatpersonar: hva [ka] i bokmål, gjekk [gikk] i nynorsk.
  
  h3 Perioden 1938–1945
  p 1938-reforma møtte sterk motstand frå den konservative delen av bokmålsleiren, som utgjorde ei ressurssterk samfunnsgruppe. Ein av talsmennene deira var forfattaren Arnulf Øverland. Han meinte det var heilt uakseptabelt at former med lange tradisjonar i norsk litteratur no skulle bort frå skriftspråket. Reforma ville på sikt føre til at begge skriftspråka våre ville bli raserte og avskaffa, hevda Øverland.

  p Men andre i bokmålsleiren var samde om at tida no var inne for verkeleg å ta i bruk tilnærmingsformene. Mellom anna gjorde Oslo skolestyre i 1939 eit vedtak om at lærebøkene skulle bruke dei bokmålsformene som var felles med nynorsk.

  p Da krigen kom, vart innarbeidinga av den nye skriftnorma forstyrra. I tillegg kom det ei ny rettskrivingsnorm under Quisling i 1941, men ho vart for det meste sabotert i skulane.
  
  h3 Bokbål og samnorskdyret

  figure.col.col6.right-image.mt5
   img(src='./Stortingsmelding_Samnorskdyret.jpg' alt='')
   figcaption Latterleggjering var eitt av verkemidla til riksmålsrørsla i kampen mot den nye bokmålsnormalen. Karikaturteikninga med «samnorskdyret» utan hovud skulle vise kva slags resultat ein fekk om ein prøvde å manipulere den «naturlege» språkutviklinga.

  p Etter krigen kom Riksmålsforbundet raskt på banen igjen, med diktaren Arnulf Øverland som leiar. Riksmålsforbundet var godt organisert og hadde sterke støttespelarar, mellom anna dei store konservative avisene Morgenbladet og Aftenposten.

  p Det var først og fremst dei folkelege formene i bokmålet Øverland og tilhengarane hans ville til livs. Dei argumenterte mellom anna med at språket måtte få utvikle seg fritt og utan statleg innblanding, og at ein måtte ta vare på ein lang skrifttradisjon.

  p Men opprøret kom nok like mykje av at ei privilegert samfunnsgruppe no risikerte å miste den skriftspråknormalen dei identifiserte seg med. Mange syntest det var hårreisande at språket til arbeidarar og bønder skulle fortrengje det språket dei gjennom heile livet hadde lært var pent og danna.
  
  h5 Kampen om lærebokspråket
  p Kampen gjaldt i første omgang språket i lærebøkene. I Oslo starta dei foreldreaksjonar med organisert retting av språket i lærebøkene, det vart sett i gang underskriftslister, opprop og demonstrasjonar. I 1954 gav Oslo skolestyre etter for presset, og dei moderate bokmålsformene kom på nytt inn i lærebøkene. Alt i 1952 gav Riksmålsforbundet i tillegg ut si eiga ordliste, og Morgenbladet og Aftenposten gjekk raskt over til å bruke henne. Språknorma i denne lista vart kalla riksmål, til skilnad frå bokmålet.

  figure.col.col6.right-image.mt5
    img(src='./Stortingsmelding_Foreldreaksjonen.jpg' alt='')
    figcaption Foreldreaksjonen mot samnorsk framfor Stortinget i Oslo i 1951. Foto: NTB / Scanpix

  p Øverland og dei andre riksmålstilhengarane brukte omgrepet samnorsk om den radikale bokmålsvarianten. Eit omgrep som før hadde gitt uttrykk for eit optimistisk framtidshåp, vart no eit nedsetjande uttrykk. Kampen var intens, og dei fleste midla vart tekne i bruk, ikkje minst konsekvent latterleggjering. Det fanst tilfelle der riksmålstilhengarar laga bokbål av bøker med radikal språkdrakt, og mange riksmålsfolk var også svært aktive i kampen for å få røysta ut nynorsken når skulekrinsane skulle avgjere opplæringsmålet.

  p Harseleringa var så kraftig og gjekk så langt at Arbeidarpartiet, som i heile mellomkrigstida hadde vore positiv til samnorsktanken, fann det klokast å tone ned språkspørsmålet. Dermed fekk riksmålsleiren dominere arenaen nesten åleine.

  
  h3 Nynorsken etter andre verdskrigen
  p Fram til 1940 auka talet på nynorskbrukarar jamt, og mot slutten av andre verdskrigen hadde kvar tredje elev i Noreg nynorsk som hovudmål. NRK hadde gjort nynorsk til ein del av kvardagen for radiolyttarane, mykje av reklamen i avisene var på nynorsk, og «Smørbukk» var ein typisk nynorsk teikneserie.

