  h5 Ny reform 
  p I 1934 vedtok stortingsflertallet å nedsette ei nemnd med todelt mandat. Nemnda skulle for det første komme med forslag om endringer i rettskriving, ordformer og bøyningsformer som kunne føre de to målformene nærmere sammen. For det andre skulle nemnda prøve å redusere antall dobbeltformer i begge skriftspråk. 

  p Medlemmene i nemnda mente at språket slik det ble snakket de fleste stedene i Norge, skulle være utgangspunkt for normeringen. Forslaget de la fram, vakte sterke reaksjoner både i bokmåls- og riksmålsleiren, men det ble vedtatt med enkelte endringer i 1937. Den nye rettskrivingen begynte å gjelde fra høsten 1938.  
  
  h3 Utviklingen i bokmål 
  p I første halvdel av 1900-tallet skjedde det omfattende endringer i begge skriftnormalene. Vi skal her se på noen sentrale punkter, og vi begynner med bokmål.  
  
  h5 1907 
  p 1907-reformen førte til at riksmålet ikke lenger var dansk, men gjenspeilte den såkalte dannede dagligtale. Det var ikke nødvendig med særlig omfattende endringer i skriftnormalen for å få til denne tilpasningen, for den dannede dagligtale var i utgangspunktet kraftig påvirket av dansk. Dessuten var det de øvre samfunnslagene som brukte dette standardtalemålet, og de var selvsagt fornøyd med å få et skriftspråk som fungerte godt for dem selv. 
  
  h5 1917 
  p Med 1917-reformen ble det åpnet for flere folkelige former i skriftnormalen. Mens en før bare kunne skrive solen og boken, ble det nå valgfritt å bruke a-ending i hunkjønnsord: sola, boka. Det ble også lov til å skrive kasta og sparka i stedet for kastet og sparket. Og mens en før måtte skrive sten, røk og løs, kunne en nå velge former med diftong i enkelte ord: stein, røik, laus. Gamle og nye former ble sidestilt, men de fleste fortsatte likevel å skrive solen, kastet og sten. 
  
  h5 1938 
  p 1938-reformen var langt mer radikal. Den dannede dagligtale ble ikke lenger regnet som rettesnor for normering av bokmålet. I stedet tok en utgangspunkt i talemålet til flertallet av folket. 

  p Fra 1938 fikk om lag 900 hunkjønnsord obligatorisk a-ending i bokmål, særlig ord fra plante- og dyrelivet og fra arbeidslivet: bjørka, høna, nåla, øksa osv. Former som bjerken, hønen, nålen og øksen forsvant ut av skriftnormalen. Også diftong ble påbudt i en rekke ord, for eksempel i stein, røyk, eik, geit, øy. Dermed kunne en ikke lenger skrive sten, røk, ek, gjet, ø. 

