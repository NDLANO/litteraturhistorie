  p Etter andre verdenskrig gikk tallet på nynorskbrukere nedover. Det tok blant annet mye lengre tid for Noregs Mållag enn for Riksmålsforbundet å komme i gang igjen med arbeidet etter krigen. Krigen førte òg til at nynorskfolket mistet et viktig argument, det nasjonale. Det nyttet ikke lenger å argumentere med at nynorsken var mer norsk enn bokmål. Nasjonal innstilling hadde vist seg gjennom å legge for dagen den rette holdningen overfor tyskere og NS, slik blant annet riksmålsforkjemperen Øverland hadde gjort. 
  
  h3 Språknormering etter 1950 
  p I 1951 vedtok Stortinget å opprette ei ny nemnd som skulle utarbeide en ny læreboknormal. Lærebøkene skulle få en moderat språkform som verken var helt konservativ eller helt radikal. Norsk språknemnd begynte arbeidet sitt i 1952, og i 1959 var den nye læreboknormalen klar. 

  p Etter 1959 har skolebøkene på bokmål stort sett holdt seg til de tradisjonelle, moderate formene, og språkstriden har stilnet. I andre halvdel av 1900-tallet har ikke tilnærming mellom målformene vært noe uttalt mål for språkpolitikken. 

  p Språknormering er likevel fortsatt en statlig oppgave i Norge. I 1972 ble Norsk språkråd stiftet som et rådgivende organ. Rådet endret navn til Språkrådet i 2005. 
  
  h5 1981 
  p I 1981 kom det en rettskrivingsreform i bokmål der en tok inn igjen flere tradisjonelle riksmålsformer. Nå kan vi for eksempel på nytt skrive furuen, kuen, jenten. 
  
  h5 2005 
  p I 2005 ble sideformene avskaffet i bokmål. Nå er alle former likestilt og kan brukes også i lærebøker og i statstjenesten. 
  
  h5 2012 
  p Språkrådet arbeider med en ny rettskrivingsnormal for nynorsk som skal tas i bruk i 2012. Normen skal være «tydeleg, enkel og stram og utan sideformer», slik at det blir lettere for alle å skrive korrekt på nynorsk. 

