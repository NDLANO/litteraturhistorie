  h5 Frigjering – også for kvinner?
  p I samsvar med kravet om frigjering og utvida demokratiske rettar for alle byrjar kvinnene no å krevje rett til deltaking i samfunnet. Alt på slutten av 1700-talet problematiserte Mary Wollstonecraft manglande rettar for kvinnene. Ho var ei opplysningskvinne som ville ha eit samfunn basert på fornuft. Ho meinte at menn og kvinner var like rasjonelle, derfor burde kvinna ha dei same økonomiske, politiske og sosiale rettane som mannen. John Stuart Mill førte desse tankane vidare tidleg på 1800-talet. 

  p I Noreg synleggjer industrialiseringa og urbaniseringa skilnadene mellom kvinner og menn. Mange flytter no inn til byane og får arbeid i fabrikkar, men det er ikkje like vilkår for kvinner og menn. Kvinnene får lågare lønn enn mennene, og arbeidstilhøva er ofte elendige. Mange unge jenter blir grovt utnytta. Kvinner av embetsstanden tek no til å krevje likestilling. Dei vil mellom anna ha lovendringar som sikrar at ei gift kvinne kan vere myndig og ha råderett over det ho sjølv eig. 

  p Desse kvinnene grunnlegg Norsk Kvinnesaksforening i 1884. Dei krev retten til utdanning og skikkeleg betaling for lønnsarbeid. Det er framleis uvanleg at kvinner tek utdanning, og det vekkjer derfor oppsikt når Cecilie Thoresen går opp til studenteksamen (examen artium) i 1882. Allmenn stemmerett for kvinner får vi likevel ikkje før i 1913.

