  h5 Arkitektur
  p I arkitekturen stod drakestilen og sveitserstilen sterkt. Begge retningane henta inspirasjon frå tradisjonelle norske formuttrykk, som utskorne ornament og listverk. Sveitserstilen kom opphavleg frå Sveits og Tyskland, men vart tilpassa norske forhold. Sveitserstilen var med på å setje tydeleg preg på norske byar og bygder. Sentrale bygg som jernbanestasjonar, skysstasjonar og hotell var ofte å sjå i sveitserstil. 

  p Stavkyrkjene våre og norrøn mytologi la grunnlaget for drakestilen. Drakestilen er ei vidareføring av sveitserstilen, og han dukka opp mot slutten av hundreåret. Frognerseteren restaurant og Holmenkollen hotell er bygde i drakestil. 

  h5 Industri og design 
  p Industrialiseringa førte til eit skilje mellom det å skape eit produkt og det å produsere det. I det førindustrielle samfunnet vart produkta designa og produserte i den same prosessen. Det var same handverkaren som tenkte ut korleis ein stol skulle sjå ut, og som laga stolen. Med industrialiseringa voks design fram som eige fagområde. Ingeniørane teikna produkta, som så vart masseproduserte av arbeidarane på fabrikkane. Krava til funksjon og effektivitet var høge. 

  p Den industrielle produksjonen skulle sikre framgang i samfunnet – symaskinen skulle gjere det enklare og raskare å sy, skrivemaskinen skulle effektivisere skriving og lesing. Det var funksjonen til produktet som var viktig, all pynt og unødig utforming vart fjerna. 

  p Ikkje alle var like positive til denne utviklinga. Den såkalla 
    em Arts and Crafts Movement 
    | meinte at berre handverket kunne sikre framsteg og fridom for menneska. Dei såg på ”brølet” frå fabrikkane som eit kulturelt forfall. 

