    strong Edvard Grieg (1843–1907)
    |. Han blir rekna som den største klassiske komponisten her i landet nokon gong. Grieg var først og fremst nasjonalromantisk i stilen, og han fekk mykje å seie for identitets- og nasjonsbygginga mot slutten av hundreåret. I 1876 fekk Grieg i oppdrag av Henrik Ibsen å komponere musikken til ei teateroppsetjing av 
    em Peer Gynt
    | . 

  p
    <strong>Rikard Nordraak (1842–1866)</strong> er best kjend for å ha komponert melodien til ”Ja, vi elsker”. Han var fetter av Bjørnstjerne Bjørnson, og Nordraak tonesette fleire av tekstane hans.

  p
    <strong>Johan Svendsen (1840–1911)</strong> levde storparten av livet i København. Han er mest kjend for romansar for fiolin og orkester. På same måten som Edvard Grieg var Svendsen inspirert av den norske folkemusikken. Han gjorde internasjonal karriere både som dirigent og som komponist.

  p
    <strong>Agathe Backer Grøndahl (1847–1907)</strong> studerte både i Tyskland og i Italia og var ein framifrå konsertpianist. Backer Grøndahl komponerte i alt 70 verk for piano. Ho var ein nær venn av Edvard Grieg og er kjend for å ha framført musikken hans ute i Europa. 

  p Europeisk musikk elles var dominert av komponistar som Franz Liszt (1811–1886), Richard Wagner (1813–1883), Johann Strauss d.y. (1825–1899), Johannes Brahms (1833–1897), Georges Bizet (1838–1875) og Pjotr Tsjaikovskij (1840–1893). 


