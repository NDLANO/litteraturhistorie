  p Den franske forfatteren Émile Zola (1840–1902) regnes som den første og største naturalisten. Idealet hans var den nye naturvitenskapen og darwinismen, og diktningen skulle være så dokumentarisk og objektiv som mulig. Zola mente at mennesket bare i liten grad hadde noen fri vilje, det var mer et produkt av arv og miljø. Derfor var det blant annet vanskelig å bryte ut av den klassen eller standen en var født inn i. 

  p Naturalismen har mye til felles med realismen. Det som skiller de to retningene, kan vi skissere slik: 
  ul
    li Realistene skrev om folk fra borgerskapet, naturalistene skildret også folk fra lavere samfunnslag, for eksempel enslige mødre og prostituerte. 
    li Naturalistene var påvirket av positivismen: Dikteren skulle samle inn fakta og detaljer som til sammen ville utgjøre sannheten om et menneske. 
    li Naturalistene ville vise livets skyggesider, og det skulle framgå tydelig av tekstene de skrev at mennesket er et resultat av arv og miljø. 
    li Det vil si at naturalistene var determinister: De mente at alt som skjer, er årsaksbestemt, og trodde ikke på menneskelig frigjøring. Naturalismen er med andre ord pessimistisk i sitt grunnsyn.
    li En konsekvens av dette var at naturalistene ikke trodde på den endringen og det framskrittet som realistene var drevet av. De blottla de nakne kjensgjerningene og kom ikke med ideer om endringer eller reformer. 
    li Troen på fornuft og vitenskapelig sannhet var særlig sterk hos naturalistene. Her var de i pakt med modernitetens ideer. Men samtidig brøt naturalistene nokså grunnleggende med noen av ideene fra det moderne prosjektet. Den manglende troen på frihet og den sviktende troen på framskrittet pekte framover mot modernismen, som kom til Norge i 1890-årene.

  h5 Naturalister i norsk litteratur

  figure.col.col4.left-image
    img.mt10(src="./Realisme_Naturalismen_Skram.jpg" alt="Portrettbilde av ei kvinne med blikket til siden. Foto.")
  p <strong>Amalie Skram (1847–1905)</strong> regnes som en reindyrket naturalist. Romanserien 
    em Hellemyrsfolket 
    | (1887–1898) er hovedverket hennes og det fremste naturalistiske verket i norsk litteratur. Her skildrer hun en familie gjennom flere generasjoner, og hun får fram sammenhengen mellom arv, miljø og utvikling. Grunntonen er pessimistisk, og det ender dårlig for familien på Hellemyren. Amalie Skram setter søkelyset på miljøet som former menneskene, og hun vil vise at fattigdom og nød har sine årsaker. 

  p I novellen ”Karens Jul” (1885) opplever vi hvordan ei fattigjente dør med et lite barn på fanget fordi hun mangler det sosiale nettverket som trengs for å klare seg i samfunnet. I ekteskapsromanene 
    em Constance Ring 
    | (1885) og 
    em Forraadt 
    | (1892) ender det også tragisk. Her møter vi unge jenter som blir tvunget inn i ekteskap med eldre menn. 

