  h5 Arkitektur
  p I arkitekturen stod drakestilen og sveitserstilen sterkt. Begge retningene hentet inspirasjon fra tradisjonelle norske formuttrykk, som utskårne ornamenter og listverk. Sveitserstilen kom opprinnelig fra Sveits og Tyskland, men ble tilpasset norske forhold. Sveitserstilen bidro til å sette tydelig preg på norske byer og bygder. Sentrale bygg som jernbanestasjoner, skysstasjoner og hotell ble ofte bygd i sveitserstil.

  p Stavkirkene våre og norrøn mytologi la grunnlaget for drakestilen. Drakestilen er en videreføring av sveitserstilen, og den dukket opp mot slutten av århundret. Frognerseteren restaurant og Holmenkollen hotell er bygd i drakestil. 

  h5 Industri og design 
  p Industrialiseringen førte til et skille mellom det å skape et produkt og det å produsere det. I det førindustrielle samfunnet ble produktene designet og produsert i den samme prosessen. Det var samme håndverker som tenkte ut hvordan en stol skulle se ut, og som laget stolen. Med industrialiseringen vokste design fram som eget fagområde. Ingeniørene tegnet produktene, som så ble masseprodusert av arbeiderne på fabrikkene. Kravene til funksjon og effektivitet var høye. 

  p Den industrielle produksjonen skulle sikre framgang i samfunnet – symaskinen skulle gjøre det enklere og raskere å sy, skrivemaskinen skulle effektivisere skriving og lesing. Det var produktets funksjon som var viktig, all pynt og unødig utforming ble fjernet. 

  p Ikke alle var like positive til denne utviklingen. Den såkalte 
    em Arts and Crafts Movement 
    | mente at bare håndverket kunne sikre framskritt og frihet for menneskene. De så på ”brølet” fra fabrikkene som et kulturelt forfall. 


