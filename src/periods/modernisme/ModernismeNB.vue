  h5 Nyrealismen 
  p
    | Den realistiske, tradisjonelle romankunsten fortsetter også i mellom- og etterkrigstida. De realistiske romanene fra denne perioden forteller sammenhengende historier med en tydelig og meningsbærende avslutning. Estetisk kjennetegnes de blant annet ved et lett tilgjengelig språk.  
  p
