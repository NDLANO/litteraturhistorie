  h5 Høymodernismen 
  p
    | Høymodernismen fikk sitt gjennombrudd i Norge i denne epoken. Flere av forfatterne som debuterer, skriver seg inn i den modernistiske romantradisjonen. Vi får romaner der indre bevissthetsstrømmer, altså ”stream of consciousness”-teknikken, symbolikk og subjektive ”virkeligheter” erstatter den sammenhengende historien.  
  p
    | Den realistiske og den modernistiske tradisjonen karakteriseres gjerne som to parallelle strømninger, men det er litt misvisende. Mange tekster har både realistiske og modernistiske trekk, og flere forfattere skrev snart innenfor den realistiske tradisjonen, snart innenfor den modernistiske. Dette er ikke minst tydelig i disse årene fra 1930 til 1960.  

