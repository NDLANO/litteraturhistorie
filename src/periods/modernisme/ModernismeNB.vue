  h5 Kunst som propaganda 
  p
    | I mellomkrigstida og under andre verdenskrig var store deler av kunstverdenen dominert av politiske retninger eller kontrollert av politiske regimer. I Sovjetunionen forbød Stalin kunstnere å male nonfigurativt fordi han mente at slik kunst ikke bidro til å fremme det sosialistiske prosjektet.  
  p
    | Hitler knyttet også til seg kunstnere. Filmskapere, kunstmalere og komponister medvirket til økt popularitet og oppslutning rundt naziregimet. Under andre verdenskrig blomstret propagandaen: Plakater og filmsnutter viste sunne, ariske kvinner og menn som på hver sin måte bidro i kampen. Kvinnene skulle ta seg av barn og hjem, mens mennene skulle nedkjempe ”den sosialistiske og jødiske fare”.  

