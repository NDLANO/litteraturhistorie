  h3 Lyrikken i perioden 1930–1960 

  p
    | I perioden 1930–1960 hadde lyrikken en sentral plass i litteraturen og i samfunnslivet. Fram til andre verdenskrig dominerte den tradisjonelle lyrikken, men etter krigen kom den modernistiske lyrikken for fullt også i Norge, og det nye formspråket skapte stor debatt i 1950-årene. 

  h5 Krigslyrikken 
  p
    | Under krigen var sensuren streng – tekster som oppfordret til samhold og kamp mot tysk okkupasjon, var strengt forbudt. Likevel sirkulerte det flere illegale tekster der forfatterne spilte på nasjonalt samhold og oppfordret til motstand. Disse tekstene – oftest dikt – hadde gjerne en ukjent avsender, men etter krigen stod Inger Hagerup, Nordahl Grieg og Arnulf Øverland igjen som de mest kjente krigslyrikerne. Felles for diktningen deres var en tradisjonell form. Diktene skulle nå ut til så mange som mulig, og da var eksperiment med form lite hensiktsmessig. I tillegg ble ofte faktiske situasjoner og hendinger brukt som motiv. 

  h5 Nullpunktslyrikken 
  p
    | De voldsomme ødeleggelsene, overgrepene mot sivilbefolkningen og Hitlers forsøk på å utrydde det jødiske folket og andre minoritetsgrupper, satte Europa i en slags nullpunktssituasjon. Var det i det hele tatt mulig å sette ord på det som hadde skjedd? Mange diktere opplevde at det tradisjonelle lyriske formspråket ikke lenger strakk til når de prøvde å formidle erfaringene og refleksjonene sine. 

