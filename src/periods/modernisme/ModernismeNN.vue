  h5 Høgmodernismen 
  p
    | Høgmodernismen fekk sitt gjennombrot i Noreg i denne epoken. Fleire forfattarar som debuterer, skriv seg inn i den modernistiske romantradisjonen. Vi får romanar der indre minnestraumar, altså ”stream of consciousness”-teknikken, symbolikk og subjektive ”røyndommar” avløyser den samanhengande historia.  
  p
    | Den realistiske og den modernistiske tradisjonen blir gjerne karakteriserte som to parallelle retningar, men det er litt misvisande. Mange tekstar har både realistiske og modernistiske drag, og fleire forfattarar skreiv snart innanfor den realistiske tradisjonen, snart innanfor den modernistiske. Dette er ikkje minst tydeleg i desse åra frå 1930 til 1960. 

