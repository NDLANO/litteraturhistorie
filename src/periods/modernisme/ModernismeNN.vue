  h3 Lyrikken i perioden 1930–1960  

  p
    | I perioden 1930–1960 hadde lyrikken ein sentral plass i litteraturen og i samfunnslivet. Fram til andre verdskrigen dominerte den tradisjonelle lyrikken, men etter krigen kom den modernistiske lyrikken for fullt også i Noreg, og det nye formspråket skapte stor debatt i 1950-åra. 

  h5 Krigslyrikken 
  p
    | Under krigen var sensuren streng – tekstar som oppmoda til samhald og kamp mot tysk okkupasjon, var strengt forbodne. Likevel sirkulerte det fleire illegale tekstar der forfattarane spela på nasjonalt samhald og oppmoda til motstand. Desse tekstane – oftast dikt – hadde gjerne ein ukjend avsendar, men etter krigen stod Inger Hagerup, Nordahl Grieg og Arnulf Øverland fram som dei mest kjende krigslyrikarane. Felles for diktinga deira var ei tradisjonell form. Dikta skulle nå ut til så mange som råd, og da var eksperiment med form lite tenleg. I tillegg vart ofte faktiske situasjonar og hendingar nytta som motiv. 

  h5 Nullpunktslyrikken 
  p
