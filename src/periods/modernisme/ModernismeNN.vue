  h5 Nyrealismen 
  p
    | Den realistiske, tradisjonelle romankunsten held fram også i mellom- og etterkrigstida. Dei realistiske romanane frå denne perioden fortel samanhengande historier med ein tydeleg og meiningsberande slutt. Estetisk kan vi mellom anna kjenne dei att på eit lett tilgjengeleg språk.  
  p
