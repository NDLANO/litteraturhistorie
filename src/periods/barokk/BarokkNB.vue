  h5 Barokken som stilart 
  p
    | Barokken er betegnelsen på en stilart som oppstår i Roma på slutten av 1500-tallet. I tillegg er barokken navnet på en kulturhistorisk epoke som fyller hele 1600-tallet. Barokken omfatter europeisk kunst, arkitektur, musikk og litteratur. I Norge ser vi påvirkningen noe seinere enn i resten av Europa, og her i landet regner vi derfor den barokke perioden fra ca. 1650 til 1750.  

  h5 Den katolske kirken som pådriver  
  p
    | Kirken bruker den barokke stilarten til å manifestere sin makt. Vi kan derfor se barokken som en motreaksjon til renessansen. Det aller meste av barokk kunst er kirkekunst, og det meste av litteraturen er salmer og diktning knyttet til kirken.

  h5 En prangende stil 
  p
    | I barokken møter vi en pompøs og prangende livsutfoldelse, med vekt på det overdådige. Skulptur- og malerkunst, barokk arkitektur og møbelstil er preget av en slik overveldende utsmykking. Det spares ikke på verken materialer eller effekter.  

