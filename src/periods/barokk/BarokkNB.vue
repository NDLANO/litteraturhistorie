  h5 Merkantilisme  
  p
    | Begrepet merkantilisme stammer fra opplysningstida. De store sjøfartsnasjonene driver utstrakt handelsvirksomhet, og ideen er å eksportere flere varer enn de importerer. På den måten legger en til rette for større statsmakt, der kongen styrer all handel og skjermer produksjonen. Dette fører i mange tilfeller til konflikt mellom ulike stater, og mellom kongen og det nye handelsborgerskapet.

