  h5 Eneveldet  
  p
    | Samfunnsbildet (verdensbildet) kan sammenliknes med en pyramide der alle mennesker har sin faste plass. På toppen står kongen, og over kongen står bare Gud. Under kommer de adelige, deretter borgerskapet med handelsmenn og håndverkere. Bønder, leilendinger og husmenn står nederst i hierarkiet.  

  h5 Grip dagen, men husk at du skal dø  
  p
    | Menneskenes livsholdning på 1600-tallet kan karakteriseres med de to latinske uttrykkene Carpe diem, som betyr ”Grip dagen”, og Memento mori, som betyr ”Husk at du skal dø”. Livet er kort, alt er forgjengelig, så grip dagen og lev mens du kan. Døden venter, og den kommer vi ikke utenom. Disse to uttrykkene sier noe om den motsetningsfylte livsholdningen som ofte trer fram i barokk kunst.  

  h5 Idealisme  
  p
    | En del tenkere på 1600-tallet mener at tilværelsen er av sjelelig eller åndelig karakter. En slik holdning kalles idealisme. I motsetning til materialismen ser idealismen verden som noe som utgår fra bevisstheten. En idealist i moderne språkbruk er en som handler ut fra visse idealer. 

  h5 Materialisme 
  p
    | Andre mener at alt kan tilbakeføres til fysiske størrelser. En slik holdning kaller vi materialisme. Den engelske legen William Harvey oppdaget i 1628 at hjertet bare var en muskel som pumpet blodet rundt i menneskekroppen. En kollega av Harvey uttrykte: Video sed non credo – ”Jeg ser det, men jeg tror det ikke”. Denne konflikten mellom gamle, fastgrodde forestillinger og vitenskapelige bevis preger barokken. Materialismen avviser med andre ord at det eksisterer noe som er overnaturlig, og hevder at alt lar seg forklare vitenskapelig. 

