  h5 Konflikt og dramatikk  
  p
    | Heller ikke forfatterne er beskjedne i bruk av virkemidler. De tar i bruk svulstige uttrykk og sterke metaforer. Overdrivelser, motsetninger og et sterkt og dramatisk bildespråk er kjennetegn på barokk kunst av alle slag. Operaen som scenekunst vokste fram i den barokke perioden, og store, påkostede operaforestillinger kom på mote.  

  h5 Det åndelige og det verdslige  
  p
    | Gud står i sentrum i barokken, men kunsten er likevel dualistisk. Det vil si at kunstneren fokuserer både på mennesket (det verdslige) og på Gud (det åndelige). Kunstneren fokuserer altså både på dette livet og på livet etter døden.  

