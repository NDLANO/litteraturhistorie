  h5 Materialisme 
  p 
    | Andre meiner at alt kan førast attende til fysiske storleikar. Ei slik haldning kallar vi materialisme. Den engelske legen William Harvey oppdaga i 1628 at hjartet berre var ein muskel som pumpa blodet rundt i menneskekroppen. Ein kollega av Harvey uttrykte: Video sed non credo – ”Eg ser det, men eg trur det ikkje”. Denne konflikten mellom gamle, fastgrodde førestillingar og vitskaplege prov pregar barokken. Materialismen avviser med andre ord at det eksisterer noko som er overnaturleg, og hevdar at alt lét seg forklare vitskapleg. 

