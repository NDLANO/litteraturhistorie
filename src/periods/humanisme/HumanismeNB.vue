  h5 Dansk overtar som kirkespråk 
  p
    | Før reformasjonen forelå ikke Bibelen på de ulike nasjonalspråkene. Den var bare oversatt til gresk og latin. Med Luther og reformasjonen kom Bibelen først på tysk i 1522 og seinere på de fleste germanske språk. Den danske oversettelsen skjedde i 1550. Også her i Norge ble den danske oversettelsen brukt.   

