  h5 Film 
  p I 1904 vart den første norske kinoen opna i Kristiania, og det kom straks fleire. I 1911 var om lag 150 kinoar i drift på landsbasis. Filmen hadde høg status i førstninga, mellom anna fordi innreisa og kroningsferda til kong Haakon 7. vart filma og vist som dokumentarfilm på kinoane. Folk over heile landet kunne da følgje kongen og familien hans. 

  p Men den høge statusen var kortvarig. Snart utvikla det seg eit skilje mellom høg- og lågkultur. Det strøymde på med utanlandske filmar med dristig innhald – lett underhaldning for folk frå arbeidarklassen som ønskte å slappe av etter lange arbeidsdagar. I 1913 kom derfor kinolova. Seinare har filmar i Noreg vore underlagde sensur. Filmar med umoralsk, valdeleg eller støytande innhald blir klipte eller ikkje viste på norske kinoar. 

  h5 Aviser 
  img.col.col6.right-image.mt20(src="./Nyrealisme_Massemedier3.jpg" alt="To personar bruker ei eldre rotasjonspresse. Foto.")
  p I 1890 fanst det 100 aviser i Noreg, femten år seinare var talet dobla. Ny teknologi og aukande politisk innsikt førte til framveksten av nye aviser. Avisene vart talerør for dei politiske partia. Høgre og Venstre hadde sine aviser i alle dei store byane, og også rundt om i landet elles. Etter kvart kom også Arbeidarpartiet med eigne aviser. Ofte sat avisredaktørane i kommunestyret, ja til og med på Stortinget – det var tette band mellom pressa og politikken. 

  p Den første verdskrigen var ikkje berre den første teknologiske krigen – det var òg den første mediekrigen. No kunne folk sjå bilete og lese artiklar om kvardagen til soldatane. Inntrykka frå skyttargravene kom rett inn i stovene. Det var dermed klart at massane kunne påverkast gjennom media, og media vart nytta aktivt i kampen om sanninga og i framstillinga av kjensgjerningane. 

