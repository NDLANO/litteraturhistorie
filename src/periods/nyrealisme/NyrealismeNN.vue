  h5 Radio 
  p I 1920-åra vart det sett opp radiosendarar i landet. Teknologien fanst, men det tok tid før ein greidde å skaffe kapital til å etablere sendarar over heile landet. Med etableringa av Norsk Rikskringkasting i 1933 skaut utviklinga fart. Radiodrift vart eit offentleg monopol, styrt av staten og regulert av kringkastingslova.

