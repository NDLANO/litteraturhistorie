  h5 Herman Wildenvey
  p Herman Wildenvey (1885–1959) var ein folkekjær lyrikar som har skrive mange sentrallyriske dikt om våren, kjærleiken og andre allmennmenneskelege tema. Han brukte stort sett tradisjonelle strofeformer i dikta sine og handterte emna på ein lett og ofte morosam måte. Men somme gonger finn vi også ein undertone av alvor og livsvisdom i dikta hans. Wildenvey er ein av dei mest siterte norske lyrikarane. Han var også populær i samtida og er den lyrikaren nest etter Alf Prøysen som har selt best i Noreg.

