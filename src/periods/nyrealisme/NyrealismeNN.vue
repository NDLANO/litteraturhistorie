  h5 Slektsromanar
  p Forfattarane var ofte opptekne av å vise at val kan få konsekvensar i lang tid framover, ja over fleire generasjonar. Det kom derfor mange fleirbindsverk, der vi følgjer ein familie over lengre tid og gjennom fleire generasjonar.

  h5 Historiske romanar
  p Ein annan typisk tendens er dei mange historiske romanane som no dukka opp. Etter at landet på nytt var sjølvstendig, trong folk å bli minte om si eiga historie. Forfattarane la handlinga i tekstane sine til ulike tider, til ulike stader av landet og til ulike miljø og samfunnsklassar.

