  h5 Røysterett for kvinner
  p Ei anna gruppe som gradvis fekk meir makt i samfunnet, var kvinnene. Dei hadde vore ei nokså usynleg gruppe, men i 1913 fekk dei allmenn røysterett. Også i dei andre nordiske landa fekk kvinnene røysterett rundt dette tidspunktet. Berre nokre få land i verda var enda tidlegare ute, mellom anna New Zealand og Australia. Dei to første tiåra av 1900-talet fekk kvinnene røysterett både i USA og i dei fleste europeiske landa. 

  h5 Europa: ”La Belle Époque” 
  p Byrjinga av 1900-talet var merkt av ein sterk optimisme i heile Europa, og epoken blir omtalt som ”La Belle Époque”, det vil seie ”den gode tida”. I Noreg heldt denne optimismen seg, i alle fall i ein viss grad, men i Europa elles skjedde det ei radikal endring i mentalitet og livssyn etter 1918. Under den første verdskrigen vart om lag ti millionar soldatar drepne. I tillegg omkom rundt sju millionar sivile. I somme land vart ein heil generasjon unge menn skadde og uføre, om dei overlevde skyttargravene. Den moderne teknologien som skulle skape fridom og framsteg, hadde ført til død og granatsjokk, stress og angst. 

  h5 Krise i verdsøkonomien 
  p Framtidsoptimismen fekk seg òg ein alvorleg knekk på grunn av ei krise i verdsøkonomien. Børskrakket på Wall Street i 1929 førte mange land ut i ein økonomisk depresjon. Resultatet var stor arbeidsløyse, fattigdom og nød – og misnøye med styresmaktene. Det liberalistiske idealet om den frie marknaden vart etter dette revurdert, og også i USA greip staten sterkare inn i økonomi og næringsliv. 
  
