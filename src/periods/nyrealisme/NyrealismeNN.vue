  h5 Språksituasjonen rundt 1900
  p I 1887 vedtok Stortinget at dansk-norsk og landsmålet til Ivar Aasen skulle vere jamstilte målformer. Det innebar at begge skriftspråka skulle brukast i statsadministrasjonen. I 1907 vart dessutan sidemålsstilen innført. No skulle elevane ikkje berre lese, men også skrive på begge målformene. 

  p Det dansk-norske skriftspråket var framleis så godt som dansk. Rettskrivinga låg så langt unna talemålet til dei fleste at det var vanskeleg å lære seg skrivemåten. Det hasta derfor med å fornorske dansk-norsken. 

  p Landsmålet vart teke i bruk i stadig fleire skulekrinsar. I 1909 hadde 20 prosent av alle skulekrinsane gått over til landsmål. Landsmålet hadde behov for fastare rettskrivingsreglar, mellom anna fordi forfattarane som skreiv på landsmål, ikkje heldt seg til den norma Ivar Aasen hadde foreslått. 

  p Men den overordna utfordringa låg i at vi hadde fått to konkurrerande skriftspråk, for det var ein situasjon ingen oppfatta som ideell. Alle ønskte eit norsk skriftspråk, men berre eitt. Politikarane var nøydde til å ta tak i situasjonen. I første halvdel av 1900-talet handla derfor språkpolitikken i Noreg om korleis ein kunne få dei to målformene til å møtast. 

  p Heile perioden fram mot andre verdskrigen var prega av kampen for eitt skriftspråk – samnorsk. I første halvdel av hundreåret vedtok politikarane fleire språkreformer. Meininga var å fornorske dansk-norsken, å modernisere nynorsken og framfor alt å føre dei to målformene nærare kvarandre. 
  h5 1907-reforma 
  p Den første offisielle reforma kom i 1907. Ho gjaldt dansk-norsk og var det første steget mot det vi i dag kallar bokmål. Først no vart forslaga til språkmannen Knud Knudsen gjennomførte, og skriftspråket vart betre tilpassa til norsk uttale. 

