  h5 Ein renessanse for kristendommen 
  p Fleire filosofar og tenkjarar på 1800-talet hadde problematisert kristendommen både som livssyn og som maktfaktor. Charles Darwin (1809–1882) og Friedrich Nietzsche (1844–1900) hadde på ulike vis utfordra trua på at jorda og menneska er skapte av Gud. Også dei realistiske forfattarane i 1880-åra var sterkt kritiske til den makta kyrkja hadde i samfunnet. 

  p Men etter fleire tiår i krise fekk kristendommen ein slags renessanse tidleg på 1900-talet, i alle fall i visse krinsar. Dei raske omveltingane i samfunnet førte til at fleire var på leit etter meir varige verdiar. Mange av dei nyrealistiske tekstane tok opp tema som tru og tvil, skuld og forsoning. Vi ser i tillegg ei vriding mot kristen tru som eit individuelt val. Litteraturen handla no om verdiane og vala til kvar enkelt. 

