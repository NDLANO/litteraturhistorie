  h5 Rudolf Nilsen
  p Rudolf Nilsen (1901–1929) var ein representant for arbeidardiktinga i førstninga av hundreåret. Nilsen vart med i Norges Socialdemokratiske Ungdomsforbund i 1919, og i 1923 melde han seg inn i Norges Kommunistiske Parti. Mange av dikta hans handlar om arbeidarkamp og arbeidarrettar, og dei blir lesne den dag i dag ved ulike markeringar for arbeidarrørsla. Nilsen skreiv også kjærleiksdikt og dikt om heimbyen sin, Oslo.

