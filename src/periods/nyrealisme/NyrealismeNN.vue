  h3 Nyrealismens skrivestil

  figure.col.col6.right-image.mt5
    img(src="./Nyrealisme_Skrivestil1.jpg" alt="Seremoni med menneske i kapper. Foto.")
    figcaption Foto: Gisle Oddstad / Scanpix

  p I denne perioden får det realistiske uttrykket sin renessanse. Målet var at kunstuttrykka skulle vere forståelege og lette å kjenne att. Framstillinga av personar og miljø skulle vere mest mogleg jordnær. Det ser vi særleg i mellomalderromanane til Sigrid Undset. Ho brukte mykje tid på bakgrunnsundersøkingar fordi ho ville skildre samfunnet i Noreg på 1300-talet så nøyaktig og korrekt som råd. 

  p Bruken av talemål var eit viktig verkemiddel når forfattarane skulle teikne realistiske miljø og karakterar. Særleg Olav Duun er kjend for å skrive dialogane i bøkene sine på namdalsmålet. 

  p Nyrealistane skreiv om psykologiske mekanismar og om etiske dilemma som enkeltmennesket sleit med i møte med samtida. Karakterane blir skildra på ein truverdig og nyansert måte, og den indre kampen dei må kjempe, gjer at dei ikkje verkar statiske eller flate. 

  p Nyrealismen er epoken for dei store og lange forteljingane. Etter lausrivinga frå Sverige hadde ein behov for forteljingar om fortida og om utviklinga som landet hadde gått gjennom. I fleirbindsverka kunne forfattarane få fram nyansar og eit samla bilete av karakterar og miljø. Også på den måten vart forteljingane meir røyndomsnære. 


  h3 Lyrikken 1900–1930: Tradisjonalisme og modernisme 

  figure.col.col12
    img(src="./Nyrealisme_Lyrikken_Tradisjonalisme_og_modernisme1.jpg" alt="Snøtoppar på nattestid. Måleri.")
    figcaption Harald Sohlberg: «Vinternatt» (1914)

  p Etter eit kort modernistisk mellomspel på slutten av 1800-talet kom det p tradisjonelle formspråket tilbake også i lyrikken. Forfattarane skreiv stort sett dikt med rim og fast rytme. Nokre få heldt likevel fram med å eksperimentere med språk og form. Tematisk stod sentrallyrikken sterkt i denne perioden. Mange forfattarar skreiv tidlause dikt om dei evige spørsmåla – livet, døden og kjærleiken. Andre hadde meir politiske formål med diktinga si, og arbeidarlyrikken voks seg no sterk. 

  h5 Olav Nygard 
  p Olav Nygard (1884–1924) skreiv sentrallyriske dikt om dei store spørsmåla i livet. Han kan plasserast i ein romantisk diktartradisjon. Mange av dikta til Nygard formidlar eit romantisk og naturmytisk verdsbilete, og han var tydeleg inspirert av romantiske lyrikarar som Shelley, Keats og Wergeland. Nygard blir òg omtalt som ”dødens diktar” fordi fleire av dikta hans handlar om nettopp døden. Nygards liv var prega av sjukdom, og han rakk berre å gi ut fire diktsamlingar før han døydde av tuberkulose, 40 år gammal.

  h5 Olav Aukrust
  p Olav Aukrust (1883–1929) var ein forfattar som skreiv tradisjonelle, sentrallyriske dikt. Dikta hans var prega av romantikk og naturmystikk. Aukrust var orientert mot antroposofien, og i mange av dikta finn vi ein religiøs lengt. Menneskesynet til Aukrust botnar i at vi er skapte i Guds bilete, og i fleire dikt tematiserer han korleis mennesket kan finne den guddommelege gneisten i sitt indre.

