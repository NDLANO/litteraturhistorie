  h5 1960-årene: Sentrallyrikk i fokus
  p I 1960 er tungetaledebatten, det vil si debatten om den modernistiske lyrikken, lagt død. Det er lite omstridt å skrive dikt uten taktfast rytme eller enderim. Men det skrives fortsatt tradisjonell lyrikk, selv om den frie formen nå dominerer. 

  p En lyriker som debuterte i 1960, var 
    strong Stein Mehren 
    | (1935–2017). Mehren skriver sentrallyriske dikt om død og kjærlighet, og han skriver modernistisk om forholdet mellom språk og erkjennelse. Mehrens poesi er intellektuell og filosofisk, med røtter tilbake til romantikken, men med et blikk fram mot moderniteten og postmoderniteten – som han ofte er veldig kritisk til. 

  p 
    strong Kolbein Falkeid 
    | (1933–2021) debuterte i 1962. Falkeid er kjent for å skrive en kombinasjon av sentrallyrikk og hverdagspoesi, og han er en folkekjær og mye lest poet. Samarbeidet med gruppa Vamp har også ført til at tekstene hans er formidlet til mange. 

