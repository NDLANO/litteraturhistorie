  p Fra og med 1885 hadde Norge to offisielle skriftspråk. Gjennom det såkalte jamstillingsvedtaket ble de to målformene landsmål og riksmål likestilt. 

  p Det var særlig skolen som var den sentrale arenaen for språkutviklingen i denne perioden. I 1892 kom Nordahl Rolfsens 
    em Læsebog for Folkeskolen 
    | ut. Boka var i bruk i skolen helt fram til 1950-årene og ble trykt i over 8 millioner eksemplarer. Den har vært med på å prege generasjoner av skolebarn. 1892-utgaven kom på riksmål, og Knut Knudsens ortofone (lydrette) prinsipp ble fulgt: Den danske skrivemåten var fornorsket, slik at det skulle bli lettere for barna å lese. I 1893 ble det vedtatt at rettskrivingen i leseboka skulle være skrivenorm for riksmålet. 

  p Den såkalte målparagrafen i den nye folkeskoleloven fra 1892 stadfestet at undervisningen skulle ”foregaa i det norske Sprog”. Skolen fikk også selv avgjøre hvilken målform elevene skulle ta i bruk. Men det var et krav at elevene skulle kunne lese på ”begge Maal”. Jamstillingsvedtaket fikk nå praktiske konsekvenser for den norske skolen. I 1893 kom den første landsmålsutgaven av Nordahl Rolfsens lesebok. 

  p I 1895 døde Knut Knudsen, og Ivar Aasen døde i 1896. Mot slutten av 1800-tallet ser vi at arbeidet til de to har gitt resultater – men kanskje ikke helt det resultatet de hadde ventet. Ingen av dem hadde vel sett for seg at landet skulle få to offisielle skriftspråk. Fra 1893 hadde en også kunnet bruke såkalte 
    em valgfrie former
    | , så språkmangfoldet var nå på godt og vondt en realitet. Denne valgfriheten skulle vise seg å være kimen til flere språkkonflikter på 1900-tallet. 

  h3 Massemedier

  figure.col.col7.left-image
    img(src="./Nyromantikk_Massemedier_Grammofon.jpg" alt="En eldre grammofonspiller. Foto.")
    figcaption En Victor grammofon fra omkring 1907
  p Mot slutten av århundret ser vi hvordan den teknologiske utviklingen fører til at gamle medier blir forbedret, mens nye kommer til. Utviklingen av fotoapparatet var nå kommet så langt at det ble mer og mer vanlig med portrettbilder og illustrasjoner i aviser og magasiner. I 1904 kom grammofonen og dermed grammofonplata. Det gjorde at en nå kunne lagre musikk. 

  p Tidsskriftene var sentrale medier for formidling av nye tanker, og nå kom det flere nye. Et av dem var Samtiden, som ble utgitt første gang i 1890. Her kom mange av de nye tankene i tida på trykk. For eksempel var det gjennom Samtiden at folk ble kjent med tankene til den tyske filosofen Nietzsche, og artikkelutgaven av Hamsuns foredrag, ”Fra det ubevidste sjæleliv”, stod også på trykk i Samtiden i 1890. 

  p De fleste bøkene i landet ble gitt ut i Danmark, for Norge hadde fram til slutten av århundret bare ett forlag, Cappelen. I 1888 etablerte Aschehoug seg med egen forlagsdel. Samme år gav de ut den første barneboka si og den første skjønnlitterære boka for voksne. I 1890 ble også Olaf Norlis Forlag etablert. Forlaget gav ut både skjønnlitteratur og lærebøker. Gyldendal forlag eksisterte også på den tida, men var fortsatt et dansk foretak. 

  p Den mest spektakulære nyvinningen var nok filmen. #[a(href='https://www.youtube.com/watch?v=4nj0vEO4Q6s' target="_blank") Den første filmframvisningen i verden skjedde 28. desember 1895 i Paris.] Brødrene Auguste (1862–1954) og Louis Lumière (1864–1948) var ansvarlige for visningen. Titlene ”Arbeiderne forlater fabrikken” og ”Toget kommer til stasjonen” sier litt om at flyktige hendinger fra dagliglivet ble festet til lerretet. 

  h3 Andre kunstformer

  h5 Nyromantiske stemningsbilder
  p I 1886 inviterte Christian Skredsvig (1854–1924) blant annet Harriet Backer (1845–1932), Kitty Kielland (1843–1914) og Erik Werenskiold (1855–1938) til gården sin, Fleskum i Bærum. Hele sommeren bodde de på gården og malte sammen. For disse kunstnerne var skumringstida viktig. På samme måte som de mer kjente Skagen-malerne var den lyse skandinaviske sommernatta inspirasjonskilde for motiv og fargevalg. Denne orienteringen mot naturen delte disse Fleskum-malerne med de nyromantiske forfatterne. 

  p Oda Krohg (1860–1935) er en annen representant for nyromantikken i malerkunsten. Krohg brukte raffinerte farger og spilte på det underforståtte. Hun lot seg her inspirere av symbolismen i malerkunsten.

  img.col.col4.mt10.right-image(src="./Nyromantikk_Andre_kunstformer_Munch.jpg" alt="Portrett av mann med bart og hatt. Foto.")
  h5 Symbolisme 
  p 
    a(href='https://www.munchmuseet.no/edvard-munch/' target='_blank') Edvard Munch
    | &nbsp;(1863–1944) er den norske maleren som i ettertid er best kjent og verdsatt. Han var en representant for symbolismen i malerkunsten. Han ville synliggjøre sterke følelser og sjelelige kamper gjennom bildene sine og brukte derfor ofte kontraster, både i motiv og i farger. Kjærlighet, angst, død, smerte og ensomhet blir framstilt i enkle linjer i klare og sterke farger. Munch er ikke opptatt av å male pent, men å male sant. Og det sanneste en kunne male, var følelser og drivkrefter hos menneskene. Det mest kjente maleriet hans, ”Skrik” (1893), framstår som selve symbolet på den eksistensielle angsten hos det siviliserte mennesket. 

  h5 Modernismen i bildekunsten 
  p De modernistiske bildekunstnerne brydde seg ikke lenger om sentralperspektiv og fotografisk etterlikning av motivene. De eksperimenterte i stedet med farger, form og perspektiv. I tillegg hevdet de at kunsten ikke trengte noe formål eller budskap utenfor seg selv. Valget av motiv endret seg også. Kunstnerne samlet seg i storbyer – særlig Paris – og malte det moderne bylivet.

