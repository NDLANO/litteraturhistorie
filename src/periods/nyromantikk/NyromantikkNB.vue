  p På slutten av 1800-tallet var Norge et samfunn i blomstring. En stadig større del av folket fikk skolegang og utdanning, og med industrien kom nye arbeidsplasser. Arbeiderklassen var i vekst og organiserte seg politisk, det samme gjorde kvinnene. Den teknologiske framgangen førte menneskene sammen – jernbane, bedre veier, telegraf og telefon gjorde det lettere å holde kontakt. 

  p Med den industrielle revolusjonen vokste det fram en helt ny samfunnsklasse, arbeiderklassen. Etter hvert ble dette en sterk samfunnsgruppe. Landsorganisasjonen (LO) ble grunnlagt i 1899, og fra nå av var det lettere for arbeiderne å stå samlet og støtte hverandre i kampen for rettigheter i arbeidslivet. I perioden 1884–1887 ble også de tre store politiske partiene Venstre, Høyre og Arbeiderpartiet stiftet. De to siste tiårene av 1800-tallet var i det hele en aktiv tid for utviklingen av norske organisasjoner.

  p Folketallet i Norge økte veldig på 1800-tallet, fra 800 000 tidlig i århundret til 2 millioner i 1890. Samtidig gjennomgikk landet en økende 
    em urbanisering
    | . I 1890 bodde 30 prosent av befolkningen i byer, mot bare 10 prosent i begynnelsen av århundret. 

  p Den industrielle revolusjonen skapte mange arbeidsplasser, men folketallet steg så mye at det likevel utviklet seg en ressurskrise. Mange flyttet til andre steder i landet, og mange emigrerte. I perioden 1825–1930 utvandret over 800 000 nordmenn, de fleste til USA. 

  p Fortsatt bodde storparten utenfor byene. Men også på bygdene skjedde det en radikal 
    em modernisering
    | . Ny teknologi og vitenskap førte til et mer effektivt husdyrhold, og jordbruket ble mekanisert. Hovedmålet var ikke lenger selvberging. Jordbruksproduktene ble nå varer på et marked, og bonden ble et ledd i en markedsøkonomi. 

  p Kommunikasjonsveier ble utbygd i høyt tempo mot slutten av århundret. Jernbanen mellom Kristiania og Trondheim var ferdig i 1880, banen mellom Kristiania og Bergen i 1900. Også skipsfarten var i vekst, transporten til sjøs ble mer effektiv da dampskip overtok for seilskipene. Utbygging av telekommunikasjoner gjorde det enklere å kommunisere med mennesker andre steder i landet og verden. 

