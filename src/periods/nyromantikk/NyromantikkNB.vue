    h5 Friedrich Nietzsche 
    p Den tyske filosofen Friedrich Wilhelm Nietzsche (1844–1900) var en sentral inspirator for forfattere som August Strindberg, Henrik Ibsen, Arne Garborg og Knut Hamsun. Det var særlig Nietzsches fokus på det han kalte livskraft og vitalitet, og på de mer irrasjonelle sidene ved menneskene, som inspirerte forfatterne i 1890-årene. 

  h5 Symbolisme
  p 
    em Symbolismen 
    | er en retning innenfor litteratur og bildekunst som oppstod i Frankrike og Belgia i andre halvdel av 1800-tallet. Symbolistene ville ikke etterlikne den ytre virkeligheten eller sette problemer under debatt. Kunsten fulgte sine egne lover, mente de, og den estetiske opplevelsen var derfor et mål i seg selv. Denne opplevelsen skulle skapes indirekte, og symbolet var derfor et viktig virkemiddel, i og med at symbolet har en underliggende tolkning. De symbolistiske forfatterne utnyttet også ordenes klang og rytme, og de fjernet seg fra den bundne verseformen.

  h5 Heimstaddikting
  p Heimstaddiktning er en litterær retning der forfatterne bruker stedet og miljøet de selv kommer fra, i sin diktning. Mot slutten av 1800-tallet var heimstaddiktningen i Europa preget av en klar reaksjon mot urbaniseringen av samfunnet. Storbyen ble skildret som noe usunt, et sted der folk kjente seg rotløse. Det var livet på landet eller i småbyen som var idealet. Det var hit folk lengtet, det var her de hadde røttene sine. Norske forfattere som Gabriel Scott (1874–1958) og Hans E. Kinck (1865–1926) dyrket det lokale framfor livet i storbyen. Også Knut Hamsun (1859–1952) lot seg inspirere av denne retningen. 

