  p I 1890 holdt Knut Hamsun et foredrag med tittelen ”Fra det ubevidste sjæleliv”. Her kritiserte han realismen og kom med et programskrift for en ny type litteratur. Hamsun mente at realistene fokuserte for mye på ”forlovelser og ball og landturer”, det vil si den ytre handlingen, og at karakterene i de realistiske verkene var typer, ikke individer. Han hevdet at en litteratur for samtida heller burde interessere seg for sjelelige tilstander hos enkeltmennesket. 

  p Både Hamsun selv og Arne Garborg, Sigbjørn Obstfelder og Hans E. Kinck begynte nå å skildre stemninger og følelser, sjeleliv og spiritisme. De utforsket ofte de dunkle, irrasjonelle og mystiske sidene ved mennesket, og de hentet inspirasjon fra folkediktning, myter og religiøs tenkning. Denne nye retningen i litteraturen har fått navnet nyromantikken. 

  p Som romantikken seksti år tidligere orienterer også nyromantikken seg mot følelser og sjeleliv – og mot natur. Naturen får på nytt en sentral funksjon som miljø og motiv, blant annet som kontrast til kulturen og sivilisasjonen. Også lyrikken kommer tilbake for alvor. Lyriske stemninger, naturskildringer og subjektive følelser lar seg fint formidle i denne sjangeren. På den måten kan vi si at århundret biter seg selv i halen. 1800-tallet starter med romantiske drag i litteraturen og et fokus på lyrikk, og ender på samme måte.

  h5 Nyromantisk litteratur
  p Da diktet ”Fandango” av Vilhelm Krag (1871–1933) ble lest opp i Studentersamfundet i 1890, varslet det noe helt nytt i norsk lyrikk. Diktet, som var med på å markere innledningen til nyromantikken i Norge, ble straks populært. Forfatteren traff her en tidsånd. Diktet tematiserer, både i form og innhold, at andre rytmer nå skal overta – mer sanselige, vitale og livskraftige. 

  p Diktsyklusen 
    em Haugtussa 
    | (1895) av Arne Garborg (1851–1924) er også eksempel på et nyromantisk verk. 
    em Haugtussa 
    | handler om ungjenta Gislaug, med kallenavnet Veslemøy. Vi kan lese 
    em Haugtussa 
    | som et portrett av ei ungjente i sorg – Veslemøy blir sviktet av han hun elsker. Vi kan også lese teksten som en hyllest til naturen, som en dannelsesfortelling og som en kamp mellom gode og vonde krefter i menneskene, personifisert gjennom skikkelser fra folketroen. 

  p Knut Hamsuns roman 
    em Pan 
    | (1894) er et av de mest sentrale prosaverkene i nyromantikken. Skildringen av hovedpersonen i romanen, løytnant Glahn, er et typisk eksempel på dypdykk i et individ. Romanen er også en fortelling om kjærlighet, både til kvinna og til naturen. I tillegg er 
    em Pan 
    | en fortelling om konflikten mellom kultur og natur, mellom det ”opprinnelige” mennesket og det siviliserte. Også 
    em Sult 
    | (1890) og 
    em Mysterier 
    | (1892) er eksempler på romaner av Hamsun med nyromantiske drag. 

  h5 Skrivemåten i nyromantikken 
  p De nyromantiske prosatekstene er preget av at det nå var individets indre sjeleliv som dikterne interesserte seg for. Det var derfor vanlig med jeg-romaner, der hovedpersonen også er fortelleren. Den ytre handlingen i romanen er gjerne mindre viktig. Romanene er mer dominert av tankereferater, som blant annet kan gjenspeile de skiftende sinnsstemningene hos jeg-personen. 

  p I den nyromantiske lyrikken handler det også om det subjektive, det irrasjonelle og det sanselige. Her kan det være vanskelig å skille nyromantikken fra andre litterære strømdrag i tida, for eksempel symbolisme, modernisme og dekadanse. Men felles for alle disse retningene er en orientering bort fra det rasjonelle og fra samfunnsproblemer – mot skildringer av sanseopplevelser og subjektive stemninger. 

  p Under nyromantikken blomstrer også en blanding av fortellende og lyrisk skrivemåte som vi kaller 
    em prosalyrisk
    | . Vi finner denne skrivemåten både i romaner og dikt. Hamsun har for eksempel flere prosalyriske partier i romanen 
    em Pan
    | . Særlig skriver han lyrisk i avsnittene der han skildrer naturen og lar seg inspirere av myter. Ellers er diktet ”Byen” (utgitt 1903) av Sigbjørn Obstfelder (1866–1900) et godt eksempel på prosadikt, det vil si en tekst som signaliserer at den er et dikt, men som er skrevet i et mer fortellende språk. 

  h3 Modernismen 

