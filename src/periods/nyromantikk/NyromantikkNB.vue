  h5 ”Fin de siècle”
  p ”Fin de siècle” er fransk og betyr slutten av århundret. Begrepet blir også brukt i betydningen slutten på en æra, og sammenfatter en grunnstemning som vi finner over hele Europa i perioden fra 1890 fram til første verdenskrig. Forfattere og andre kunstnere var opptatt av at århundret snart var slutt, og at en epoke var over. Men hva var det som skulle komme? Tida var preget av motstridende tankestrømmer og følelser: frykt for at det kjente samfunnet skulle gå under, men også en følelse av at nye og bedre tider var i vente. 

  p Litteraturen på slutten av 1800-tallet gjenspeiler denne kulturelle og åndelige uroen, for vi ser at ulike litterære retninger eksisterer side om side. I tillegg til 
    em nyromantikk 
    | finner vi 
    em symbolisme
    | , 
    em modernisme 
    | og 
    em heimstaddiktning
    | . Realismen lever også videre, og kunstnere og forfattere var gjerne inspirert av flere retninger og ideer. 

  h5 Dekadanse 
  p Et begrep som gjerne nevnes i sammenheng med ”fin de siècle”, er 
    em dekadanse 
    | (av fransk ”décadance”, forfall). Adjektivet 
    em dekadent 
    | blir vanligvis brukt i negativ betydning og viser til en dannet og forfinet, men også utagerende livsstil, som gjerne tolkes som symptom på at et samfunn er i ferd med å miste livskrafta. Mot slutten av 1800-tallet var det flere tenkere og forfattere som mente at samtida bar preg av nettopp et slikt moralsk og kulturelt forfall. 

  p Samtidig finner vi også en gruppe forfattere som helt bevisst tok avstand fra det borgerlige samfunnet, og som insisterte på at kunsten ikke hadde noen nytteverdi, den var til for egen del. Forbildet for disse kunstnerne var den franske dikteren Charles Baudelaire (1821–1867), og en typisk representant for gruppa var den irske forfatteren Oscar Wilde (1854–1900). Han er også kjent for et omflakkende og utagerende liv. 

