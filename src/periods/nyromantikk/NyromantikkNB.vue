  p ”Make it new” kan stå som slagord for modernismen. Felles for alle de modernistiske tekstene er at de 
    em bryter 
    | med tradisjonelle kunstidealer. Gjennom eksperimenter med form og språk utfordres publikum til å tenke nytt. 

  p Modernistene tok opp hva det ville si å være menneske i den moderne verden. Men i motsetning til realistene var de ikke så opptatt av å sette problemer under debatt. De ville heller skildre sjelelivet til det moderne mennesket. 

  p Den nye verden skapte nemlig et nytt menneske, mente modernistene. Nye tanker innenfor religion, politikk og psykologi førte til at verden ikke lenger var like enkel å orientere seg i. Hva skjer med mennesket når det en tidligere trodde på, ikke lenger er en fastlagt sannhet? Det moderne mennesket opplevde en verden som på mange måter smuldret opp i små biter, og det var i større grad enn før opp til hver enkelt å gi livet sitt mening. Dette prøvde modernistene å sette ord på. Det er derfor viktig å se modernismen i tilknytning til moderniteten og de nye forholdene for menneskene. 

  p Målet for flere modernistiske kunstnere, særlig på 1900-tallet, var sjokk og provokasjon. Kunstnerne var ofte i opposisjon til sin egen samtid, og de ønsket å vekke reaksjoner hos sitt publikum. Kunstformen var ny og krevende, og det kunne være vanskelig å forstå hva forfatteren eller kunstneren ville få fram med verket sitt. Det er kanskje ikke så rart at de modernistiske uttrykkene ikke alltid var like populære i samtida. 

  h5 Modernistisk litteratur i Norge 
  p Romanen 
    em Sult 
    | (1890) av Knut Hamsun er en av de første modernistiske romanene i verdenslitteraturen. Hovedpersonen, en navnløs jeg-person, vandrer rundt i gatene i Kristiania. I 
    em Sult 
    | skildrer Hamsun den såkalte modernistiske livsfølelsen. Sentrale stikkord er fremmedfølelse, dekadanse og meningstap. 

  p Henrik Ibsen ble kritisert av Hamsun for å skildre litterære ”typer” i stedet for individer. Men i ettertid ser vi at deler av denne kritikken ikke er rettferdig. I skuespillene fra 1890-årene finner vi sterke psykologiske skildringer av enkeltmennesker. I dramaet 
    em Hedda Gabler 
    | (1890) tar Ibsen i tillegg opp temaer som står sentralt i seinere modernistisk litteratur: Hovedpersonen Hedda Gabler er et eksempel på en dekadent, fremmedgjort kvinne som lider under at livet hennes er tomt og meningsløst. 

  p Sigbjørn Obstfelder er omtalt som den første norske modernistiske lyrikeren. Han er modernist i form og innhold. Ingen har vel skildret den moderne livsfølelsen bedre enn Obstfelder i diktet ”Jeg ser” (1893). Diktet er en subjektiv skildring av sinnsstemningen hos en mann som ikke føler seg hjemme i verden. Også i form bryter Obstfelder med de tradisjonelle kravene til struktur, rim og rytme. 

  h5 Skrivemåten i modernismen 
  p Modernistene mente at det ikke passet å bruke tradisjonelle motiver og høystemt formspråk til å skildre mennesket i den moderne sivilisasjonen. Dikterne hentet nå motiv og symboler fra hverdagen og bylivet, i stedet for å dikte vakre vers om ”havet, døden og kjærligheten”. 
  p De modernistiske lyrikerne er mest kjent for at de eksperimenterte med form. Men selv om diktene er uten fast rim og rytme, gjenspeiles ofte formen i innholdet. I Obstfelders dikt ”Jeg ser” registrerer det lyriske jeg-et omverdenen på en undrende og ”hypersensitiv” måte. Rytmen og gjentakelsene i diktet gjør at teksten får et drømmeaktig preg over seg som understreker temaet.

  p I teatrene fjernet en seg også fra tradisjonelle uttrykk og realistisk scenografi. Målet var å få fram det sammensatte sjelelivet til det moderne mennesket gjennom nye virkemidler. Dette ser vi særlig i de såkalte symboldramaene til Henrik Ibsen, der drømmer, minner og fantasier løftes fram. I 
    em Vildanden 
    | (1884) fungerer den skadeskutte villanda på mørkeloftet som et symbol på familien Ekdal – en familie som på mange måter er like merket og fanget som fuglen.

  h3 Språket i perioden

  figure.col.col6.right-image.mt5
