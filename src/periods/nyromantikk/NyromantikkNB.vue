  h5 Modernisme
  p På slutten av århundret ser vi også framveksten av det som skulle bli en dominerende retning i kunst og litteratur framover – 
    em modernismen
    | . Mens de realistiske forfatterne hadde hatt et positivt syn på den teknologiske utviklingen, var stemningen annerledes mot slutten av århundret. Troen på framskrittet var avløst av en følelse av å ikke høre hjemme. Menneskene var rotløse, ensomme og fremmede for hverandre og for seg selv. Forfattere og kunstnere eksperimenterte med form og sjanger: Hvordan skulle de skildre mennesker som forstod at det ikke var noen vei tilbake til fortida, men som heller ikke følte seg hjemme i den moderne sivilisasjonen? 

