  p På slutten av 1800-talet var Noreg eit samfunn i blomstring. Ein stadig større del av folket fekk skulegang og utdanning, og med industrien kom nye arbeidsplassar. Arbeidarklassen var i vekst og organiserte seg politisk, det same gjorde kvinnene. Dei teknologiske framstega førte menneska saman – jernbane, betre vegar, telegraf og telefon gjorde det lettare å halde kontakt. 

  p Med den industrielle revolusjonen voks det fram ein heilt ny samfunnsklasse, arbeidarklassen. Etter kvart vart dette ei sterk samfunnsgruppe. Landsorganisasjonen (LO) vart skipa i 1899, og frå no av var det lettare for arbeidarane å stå samla og støtte kvarandre i kampen for rettar i arbeidslivet. I perioden 1884–1887 vart også dei tre store politiske partia Venstre, Høgre og Arbeidarpartiet stifta. Dei to siste tiåra av 1800-talet var i det heile ei aktiv tid for utviklinga av norske organisasjonar. 

  p Folketalet i Noreg auka veldig på 1800-talet, frå 800 000 tidleg i hundreåret til 2 millionar i 1890. På same tid gjekk landet gjennom ei aukande 
    em urbanisering
    | . I 1890 budde 30 prosent av innbyggjarane i byar, mot berre 10 prosent i førstninga av hundreåret. 

  p Den industrielle revolusjonen skapte mange arbeidsplassar, men folketalet auka så mykje at det likevel utvikla seg ei ressurskrise. Mange flytte til andre stader i landet, og mange emigrerte. I perioden 1825–1930 utvandra over 800 000 nordmenn, dei fleste til USA. 

  p Framleis budde storparten utanfor byane. Men også på bygdene skjedde det ei radikal 
    em modernisering
    | . Ny teknologi og vitskap førte til eit meir effektivt husdyrhald, og jordbruket vart mekanisert. Hovudmålet var ikkje lenger sjølvberging. Jordbruksprodukta vart no varer på ein marknad, og bonden var eit ledd i ein marknadsøkonomi. Kommunikasjonsvegar vart bygde ut i høgt tempo mot slutten av hundreåret. Jernbanen mellom Kristiania og Trondheim var ferdig i 1880, banen mellom Kristiania og Bergen i 1900. Også skipsfarten var i vekst, transporten sjøvegs vart meir effektiv da dampskipa tok over for seglskipa. Utbygging av telekommunikasjonar gjorde det enklare å kommunisere med menneske andre stader i landet og verda.

