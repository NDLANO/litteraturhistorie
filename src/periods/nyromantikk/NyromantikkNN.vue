  p I 1890 heldt Knut Hamsun eit foredrag med tittelen ”Fra det ubevidste sjæleliv”. Her kritiserte han realismen og kom med eit programskrift for ein ny type litteratur. Hamsun meinte at realistane fokuserte for mykje på ”forlovelser og ball og landturer”, det vil seie den ytre handlinga, og at karakterane i dei realistiske verka var typar, ikkje individ. Han hevda at ein litteratur for samtida heller burde interessere seg for sjelelege tilstandar hos enkeltmennesket. 

  p Både Hamsun sjølv og Arne Garborg, Sigbjørn Obstfelder og Hans E. Kinck byrja no å skildre stemningar og kjensler, sjeleliv og spiritisme. Dei utforska ofte dei dunkle, irrasjonelle og mystiske sidene ved mennesket, og dei henta inspirasjon frå folkedikting, mytar og religiøs tenking. Denne nye retninga i litteraturen har fått namnet nyromantikken. 

  p Som romantikken seksti år tidlegare orienterer også nyromantikken seg mot kjensler og sjeleliv – og mot natur. Naturen får på nytt ein sentral funksjon som miljø og motiv, mellom anna som kontrast til kulturen og sivilisasjonen. Også lyrikken kjem tilbake for alvor. Lyriske stemningar, naturskildringar og subjektive kjensler lèt seg fint formidle i denne sjangeren. På den måten kan vi seie at hundreåret bit seg sjølv i halen. 1800-talet startar med romantiske drag i litteraturen og eit fokus på lyrikk, og endar på same måten.

  h5 Nyromantisk litteratur
  p Da diktet ”Fandango” av Vilhelm Krag (1871–1933) vart lese opp i Studentersamfundet i 1890, varsla dette noko heilt nytt i norsk lyrikk. Diktet, som var med på å markere innleiinga til nyromantikken i Noreg, vart straks populært. Forfattaren råka her ei tidsånd. Diktet tematiserer, både i form og innhald, at andre rytmar no skal overta – meir sanselege, vitale og livskraftige. Diktsyklusen 
    em Haugtussa 
    | (1895) av Arne Garborg (1851–1924) er òg eksempel på eit nyromantisk verk. 
    em Haugtussa 
    | handlar om ungjenta Gislaug, med kallenamnet Veslemøy. Vi kan lese 
    em Haugtussa 
    | som eit portrett av ei ungjente i sorg – Veslemøy blir svikta av han ho elskar. Vi kan òg lese teksten som ei hylling til naturen, som ei danningsforteljing og som ein kamp mellom gode og vonde krefter i menneska, personifisert gjennom skapningar frå folketrua. 

  p Romanen 
    em Pan 
    | (1894) av Knut Hamsun er eit av dei mest sentrale prosaverka i nyromantikken. Skildringa av hovudpersonen i romanen, løytnant Glahn, er eit typisk eksempel på djupdykk i eit individ. Romanen er òg ei forteljing om kjærleik, både til kvinna og til naturen. I tillegg er 
    em Pan 
    | ei forteljing om konflikten mellom kultur og natur, mellom det ”opphavlege” mennesket og det siviliserte. Også 
    em Sult 
    | (1890) og 
    em Mysterier 
    | (1892) er eksempel på romanar av Hamsun med nyromantiske drag. 

  h5 Skrivemåten i nyromantikken 
  p Dei nyromantiske prosatekstane er prega av at det no var det indre sjelelivet til individet som diktarane interesserte seg for. Det var derfor vanleg med eg-romanar, der hovudpersonen også er forteljaren. Den ytre handlinga i romanen er gjerne mindre viktig. Romanane er meir dominerte av tankereferat, som mellom anna kan spegle av dei skiftande sinnsstemningane hos eg-personen. 

  p I den nyromantiske lyrikken handlar det også om det subjektive, det irrasjonelle og det sanselege. Her kan det vere vanskeleg å skilje nyromantikken frå andre litterære straumdrag i tida, for eksempel symbolisme, modernisme og dekadanse. Men felles for alle desse retningane er ei orientering bort frå det rasjonelle og frå samfunnsproblem – mot skildringar av sanseopplevingar og subjektive stemningar. 

  p Under nyromantikken blomstrar også ei blanding av forteljande og lyrisk skrivemåte som vi kallar 
    em prosalyrisk
    | . Vi finn denne skrivemåten både i romanar og dikt. Hamsun har for eksempel fleire prosalyriske parti i romanen 
    em Pan
    | . Særleg skriv han lyrisk i avsnitta der han skildrar naturen og lèt seg inspirere av mytar. Elles er diktet ”Byen” (utgitt 1903) av Sigbjørn Obstfelder (1866–1900) eit godt eksempel på prosadikt, det vil seie ein tekst som signaliserer at han er eit dikt, men som er skriven i eit meir forteljande språk. 


  h3 Modernismen 

  figure.col.col7.right-image
    img(src="./Nyromantikk_Modernismen1.jpg" alt="Konsentrert kunstnar med penslar og lerret. Måleri.")
    figcaption
      em Selvportrett 
      | (1889). Vincent van Gogh
  p ”Make it new” kan stå som slagord for modernismen. Felles for alle dei modernistiske tekstane er at dei 
    em bryt 
    | med dei tradisjonelle kunstideala. Gjennom eksperiment med form og språk blir publikum utfordra til å tenkje nytt. 

  p Modernistane tok opp kva det ville seie å vere menneske i den moderne verda. Men i motsetning til realistane var dei ikkje så opptekne av å setje problem under debatt. Dei ville heller skildre sjelelivet til det moderne mennesket. 

  p Den nye verda skapte nemleg eit nytt menneske, meinte modernistane. Nye tankar innanfor religion, politikk og psykologi førte til at verda ikkje lenger var like enkel å orientere seg i. Kva skjer med mennesket når det ein tidlegare trudde på, ikkje lenger er ei fastlagd sanning? Det moderne mennesket opplevde ei verd som på mange måtar smuldra opp i små bitar, og det var i større monn enn før opp til kvar enkelt å gi livet sitt meining. Dette prøvde modernistane å setje ord på. Det er derfor viktig å sjå modernismen i tilknyting til moderniteten og dei endra vilkåra for menneska. 

  p Målet for fleire modernistiske kunstnarar, særleg på 1900-talet, var sjokk og provokasjon. Kunstnarane var ofte i opposisjon til si eiga samtid, og dei ønskte å vekkje reaksjonar hos publikumet sitt. Kunstforma var ny og krevjande, og det kunne vere vanskeleg å forstå kva forfattaren eller kunstnaren ville få fram med verket sitt. Det er kanskje ikkje så rart at dei modernistiske uttrykka ikkje alltid var like populære i samtida. 

  h5 Modernistisk litteratur i Noreg
  p Romanen 
    em Sult 
    | (1890) av Knut Hamsun er ein av dei første modernistiske romanane i verdslitteraturen. Hovudpersonen, ein namnlaus eg-person, vandrar rundt i gatene i Kristiania. I 
    em Sult 
    | skildrar Hamsun den såkalla modernistiske livskjensla. Sentrale stikkord er framandkjensle, dekadanse og meiningstap. 

  p Henrik Ibsen vart kritisert av Hamsun for å skildre litterære ”typar” i staden for individ. Men i ettertid ser vi at delar av denne kritikken ikkje er rettvis. I skodespela frå 1890-åra finn vi sterke psykologiske skildringar av enkeltmenneske. I dramaet 
    em Hedda Gabler 
    | (1890) tek Ibsen i tillegg opp tema som står sentralt i seinare modernistisk litteratur: Hovudpersonen Hedda Gabler er eit eksempel på ei dekadent, framandgjord kvinne som lir under at livet hennar er tomt og meiningslaust. 

  p Sigbjørn Obstfelder er omtalt som den første norske modernistiske lyrikaren. Han er modernist i form og innhald. Ingen har vel skildra den moderne livskjensla betre enn Obstfelder i diktet ”Jeg ser” (1893). Diktet er ei subjektiv skildring av sinnsstemninga hos ein mann som ikkje kjenner seg heime i verda. Også i form bryt Obstfelder med dei tradisjonelle krava til struktur, rim og rytme.

  h5 Skrivemåten i modernismen
  p Modernistane meinte at det ikkje passa å bruke tradisjonelle motiv og høgstemt formspråk til å skildre mennesket i den moderne sivilisasjonen. Diktarane henta no motiv og symbol frå kvardagen og bylivet, i staden for å dikte vakre vers om ”havet, døden og kjærleiken”. Dei modernistiske lyrikarane er mest kjende for at dei eksperimenterte med form. Men trass i at dikta er utan fast rim og rytme, speglar forma ofte av innhaldet. I Obstfelders dikt ”Jeg ser” registrerer det lyriske eg-et omverda på ein undrande og ”hypersensitiv” måte. Rytmen og gjentakingane i diktet gjer at teksten får eit drøymande preg over seg som understrekar temaet. 

  p I teatera fjerna ein seg òg frå tradisjonelle uttrykk og realistisk scenografi. Målet var å få fram det samansette sjelelivet til det moderne mennesket gjennom nye verkemiddel. Dette ser vi særleg i dei såkalla symboldrama til Henrik Ibsen, der draumar, minne og fantasiar blir lyfte fram. I 
    em Vildanden 
    | (1884) fungerer den skadeskotne villanda på mørkeloftet som eit symbol på familien Ekdal – ein familie som på mange måtar er like merkt og fanga som fuglen. 

  h3 Språket i perioden 

  figure.col.col6.right-image.mt5
