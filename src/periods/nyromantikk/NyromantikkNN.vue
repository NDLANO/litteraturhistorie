    h5 Friedrich Nietzsche 
    p Den tyske filosofen Friedrich Wilhelm Nietzsche (1844–1900) var ein sentral inspirator for forfattarar som August Strindberg, Henrik Ibsen, Arne Garborg og Knut Hamsun. Det var særleg fokuset som Nietzsche hadde på det han kalla livskraft og vitalitet, og på dei meir irrasjonelle sidene ved menneska, som inspirerte forfattarane i 1890-åra. 

  h5 Symbolisme 
  p 
    em Symbolismen 
    | er ei retning innanfor litteratur og biletkunst som voks fram i Frankrike og Belgia i andre halvdel av 1800-talet. Symbolistane ville ikkje etterlikne den ytre røyndommen eller setje problem under debatt. Kunsten følgde sine eigne lover, meinte dei, og den estetiske opplevinga var derfor eit mål i seg sjølv. Denne opplevinga skulle skapast indirekte, og symbolet var derfor eit viktig verkemiddel, i og med at symbolet har ei underliggjande tolking. Dei symbolistiske forfattarane utnytta også klangane og rytmane i orda, og dei fjerna seg frå den bundne verseforma. 

  h5 Heimstaddikting
  p Heimstaddikting er ei litterær retning der forfattarane bruker staden og miljøet dei sjølve kjem frå, i diktinga si. Mot slutten av 1800-talet var heimstaddiktinga i Europa prega av ein klar reaksjon mot urbaniseringa av samfunnet. Storbyen vart skildra som noko usunt, ein stad der folk kjende seg rotlause. Det var livet på landet eller i småbyen som var idealet. Det var hit folk lengta, det var her dei hadde røtene sine. Norske forfattarar som Gabriel Scott (1874–1958) og Hans E. Kinck (1865–1926) dyrka det lokale framfor livet i storbyen. Også Knut Hamsun (1859–1952) lét seg inspirere av denne retninga. 

