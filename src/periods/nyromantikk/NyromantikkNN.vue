  h5 ”Fin de siècle”
  p ”Fin de siècle” er fransk og tyder slutten av hundreåret. Omgrepet blir òg brukt i tydinga slutten på ein æra, og summerer opp ei grunnstemning som vi finn over heile Europa i perioden frå 1890 fram til første verdskrigen. Forfattarar og andre kunstnarar var opptekne av at hundreåret snart var slutt, og at ein epoke var over. Men kva var det som skulle kome? Tida var prega av motstridande tankestraumar og kjensler: frykt for at det kjende samfunnet skulle gå under, men òg ei kjensle av at nye og betre tider var i vente. 

  p Litteraturen på slutten av 1800-talet speglar denne kulturelle og åndelege uroa, for vi ser at ulike litterære retningar eksisterer side om side. I tillegg til 
    em nyromantikk 
    | finn vi 
    em symbolisme
    | , 
    em modernisme 
    | og 
    em heimstaddikting
    | . Realismen lever òg vidare, og kunstnarar og forfattarar var gjerne inspirerte av fleire retningar og idear. 

  h5 Dekadanse 
  p Eit omgrep vi gjerne nemner i samanheng med ”fin de siècle”, er 
    em dekadanse 
    | (av fransk ”décadance”, forfall). Adjektivet 
    em dekadent 
    | blir til vanleg brukt i negativ tyding og viser til ein danna og forfina, men også utagerande levemåte, som gjerne blir tolka som symptom på at eit samfunn er i ferd med å miste livskrafta. Mot slutten av 1800-talet var det fleire tenkjarar og forfattarar som meinte at samtida bar preg av nettopp eit slikt moralsk og kulturelt forfall.

  p Samstundes finn vi òg ei gruppe forfattarar som heilt medvite tok avstand frå det borgarlege samfunnet, og som insisterte på at kunsten ikkje hadde nokon nytteverdi, han var til for eigen del. Førebiletet for desse kunstnarane var den franske diktaren Charles Baudelaire (1821–1867), og ein typisk representant for gruppa var den irske forfattaren Oscar Wilde (1854–1900). Han er òg kjend for eit omflakkande og utagerande liv. 

