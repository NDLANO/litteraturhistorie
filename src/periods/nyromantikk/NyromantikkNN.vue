  h5 Modernisme 
  p På slutten av hundreåret ser vi òg framveksten av det som skulle bli ei dominerande retning i kunst og litteratur framover – 
    em modernismen
    | . Mens dei realistiske forfattarane hadde hatt eit positivt syn på den teknologiske utviklinga, var stemninga annleis mot slutten av hundreåret. Trua på framsteget var avløyst av ei kjensle av å ikkje høyre heime. Menneska var rotlause, einsame og framande for kvarandre og for seg sjølve. Forfattarar og kunstnarar eksperimenterte med form og sjanger: Korleis skulle dei skildre menneske som skjønte at det ikkje var nokon veg tilbake til fortida, men som heller ikkje kjende seg heime i den moderne sivilisasjonen? 


