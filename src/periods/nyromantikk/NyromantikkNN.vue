  p Frå og med 1885 hadde Noreg to offisielle skriftspråk. Gjennom det såkalla jamstillingsvedtaket vart dei to målformene landsmål og riksmål jamstilte. 

  p Det var særleg skulen som var den sentrale arenaen for språkutviklinga i denne perioden. I 1892 kom Nordahl Rolfsens 
    em Læsebog for Folkeskolen 
    | ut. Boka var i bruk i skulen heilt fram til 1950-åra og vart trykt i over 8 millionar eksemplar. Ho har vore med på å prege generasjonar av skulebarn. 1892-utgåva kom på riksmål, og Knut Knudsens ortofone (lydrette) prinsipp vart følgt: Den danske skrivemåten var fornorska, slik at det skulle bli lettare for barna å lese. I 1893 vart det vedteke at rettskrivinga i leseboka skulle vere skrivenorm for riksmålet. 

  p Den såkalla målparagrafen i den nye folkeskulelova frå 1892 stadfesta at undervisninga skulle ”foregaa i det norske Sprog”. Skulen fekk også sjølv avgjere kva målform elevane skulle ta i bruk. Men det var eit krav at elevane skulle kunne lese på ”begge Maal”. Jamstillingsvedtaket fekk no praktiske konsekvensar for den norske skulen. I 1893 kom den første landsmålsutgåva av leseboka til Nordahl Rolfsen. 

  p I 1895 døydde Knut Knudsen, og Ivar Aasen døydde i 1896. Mot slutten av 1800-talet ser vi at arbeidet til dei to har gitt resultat – men kanskje ikkje heilt det resultatet dei hadde venta. Ingen av dei hadde vel sett for seg at landet skulle få to offisielle skriftspråk. Frå 1893 hadde ein òg kunna bruke såkalla 
    em valfrie former
    | , så språkmangfaldet var no på godt og vondt ein realitet. Denne valfridomen skulle syne seg å vere kimen til fleire språkkonfliktar på 1900-talet. 

  h3 Massemedium 

  figure.col.col7.left-image
    img(src="./Nyromantikk_Massemedier_Grammofon.jpg" alt="Ein eldre grammofonspelar. Foto.")
    figcaption Ein Victor grammofon frå omkring 1907
  p Mot slutten av hundreåret ser vi korleis den teknologiske utviklinga fører til at gamle medium blir forbetra, mens nye kjem til. Utviklinga av fotoapparatet var no komen så langt at det vart meir og meir vanleg med portrettbilete og illustrasjonar i aviser og magasin. I 1904 kom grammofonen og dermed grammofonplata. Det gjorde at ein no kunne lagre musikk. 

  p Tidsskrifta var sentrale medium for formidling av nye tankar, og no kom det fleire nye. Eit av dei var Samtiden, som vart gitt ut første gongen i 1890. Her kom mange av dei nye tankane i tida på trykk. For eksempel var det gjennom Samtiden at folk vart kjende med tankane til den tyske filosofen Nietzsche, og artikkelutgåva av foredraget til Hamsun, ”Fra det ubevidste sjæleliv”, stod òg på trykk i Samtiden i 1890. 

